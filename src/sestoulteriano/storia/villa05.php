    <td align="left" valign="top" width="75%"> 
      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>La 
        tradizione</b><br>
        Galvano Fiamma fu il primo che raccolse e consacr&ograve; la tradizione, 
        mista a leggenda, del rinvenimento del Corpo dell'eremita Matroniano de' 
        Boccardi ad opera del giovane Guglielmo pure della famiglia de' Boccardi, 
        e la deposizione fatta da Ambrogio presso l'altare di santa Margherita 
        nella Basilica da lui costruita e dedicata agli Apostoli.<br>
        Della leggenda do la versione stesa dal Latuada che non dubita affatto 
        della traslazione compiuta da sant'Ambrogio, anzi fa appello alla costante 
        tradizione: &#147;la quale sussisteva vigorosa insino ne' tempi di Gualvaneo 
        Fiamma pi&ugrave; volte citato, che mentovando nella sua Cronaca Maggiore 
        al capo 119 le Reliquie depositate dal mentovato Pastore in questa Basilica 
        cos&igrave; scrisse: &#148;Trov&ograve; ancora il Corpo di san Matroniano 
        Eremita de' Boccardi in questa maniera. Era di quel tempo nella Citt&agrave; 
        di Milano un Nobil'Uomo per nome Guglielmo de' Boccardi, molto inclinato 
        alla caccia degli uccelli, a cui disse santo Ambrogio inspirato da Divina 
        rivelazione: Figliuol mio Guglielmo oggi mi darai parte della tua Caccia; 
        la quale inchiesta fu di buona voglia accettata. In quel giorno nel Bosco 
        al rilasciarsi dello Sparviere, i cani ed i cavalli rimasero immobili. 
        In tal sito fece scavare la terra, e ritrov&ograve; il Corpo di san Matroniano; 
        e trasportandolo alla Citt&agrave;, tutte le Campane suonarono da loro 
        stesse, ed il Beato Ambrogio seppell&igrave; quel Corpo santissimo nella 
        Chiesa degli Apostoli, ossia di san Nazzaro nella Cappella di santa Margarita&#148;.<br>
        Fu certo molto generoso il Latuada fidandosi completamente del Fiamma.<br>
        Sant'Ambrogio nei suoi scritti, il biografo di questi Paolino, gli storici 
        e studiosi della figura e dell'et&agrave; di Ambrogio non mai alludono 
        ad un eremita, santo, nominato Matroniano. Alludono bens&igrave; al magnifico 
        movimento, sviluppatosi in quel secolo IV di piena libert&agrave; religiosa: 
        il monachismo occidentale. La permanenza di san Martino a Milano, la testimonianza 
        di sant'Agostino che ammir&ograve; la vita dei cenobiti raccolti in un 
        Monastero fuori le mura sotto la cura di Ambrogio, confermano l'esistenza 
        e l'influsso di bene irradiato sui barbari invasori dai consacrati alla 
        solitudine.<br>
        Ascoltiamo Agostino: &#147;Persino a Milano esisteva un monastero pieno 
        di pii fratelli, fuori delle mura, tenuto in vita da Ambrogio; e noi non 
        si aveva sentore&#148;.<br>
        Di san Matroniano sappiamo che fu eremita: la leggenda aggiunge che ritrovarono 
        il suo corpo, incorrotto, seppellito dagli Angioli, fra le mani una scritta: 
        &#147;Vita eius&#148;.<br>
        Quale poi la sua vita? Nessuno lo sapr&agrave;; eppure la sua santit&agrave; 
        &egrave; certa, compie miracoli; dalla chiesa ottiene gli onori liturgici 
        propri dei Santi. Non si richiede di pi&ugrave; perch&egrave; il popolo 
        fantasioso possa crearsi le gesta dell'eremita santo.<br>
        E' sintomatico che nel catalogo dei codici agiografici latini della Biblioteca 
        Ambrosiana, compilato dai Bollandisti nel 1892, non si accenni a san Matroniano.<br>
        Comunque tre fatti stanno a base della tradizione:<br>
        l'esistenza di un individuo che conducendo vita di anacoreta raggiunse 
        il vertice della santit&agrave;, si da divenire il tipo di quello stuolo 
        di anacoreti che pullularono nei dintorni boscosi di Milano, e nel monastero 
        fuori le mura;<br>
        gli onori liturgici a lui tributati dalla Chiesa e dal popolo;<br>
        la sepoltura nella Basilica degli Apostoli.<br>
        Questi tre fatti certi nella essenza e non nei dettagli, si prestarono 
        ad essere coloriti da leggenda, dando origine a discussioni che si trascinano 
        ancora.<br>
        Il primo fatto porter&agrave; alla discussione circa il luogo della terrena 
        dimora, palestra della sua santit&agrave;.<br>
        V'&egrave; chi pensa alla regione boscosa fuori di Porta Romana vicino 
        a Sesto Ulteriano. Carlo Massimo Rota in uno dei suoi ultimi scritti storici 
        allude ad un villaggio nominato Maderniano, sito nel territorio dell'attuale 
        Comune di Sesto Ulteriano, villaggio scomparso intorno al 1200.<br>
        Suppone, il Rota: &#147;che il nome di Matroniano sia nato proprio sul 
        luogo, che intorno al 1200 si fece deserto e che accolse il solitario 
        eremita che a noi tramand&ograve; il nome del paese distrutto con quello 
        delle sue proprie virt&ugrave;&#148;.<br>
        La tesi del Rota non regge, nonostante la documentazione preziosissima 
        che porta: san Matroniano visse molto innanzi al 1200.<br>
        I contatti della Basilica degli Apostoli con Sesto Ulteriano furono strettissimi: 
        in pergamene del 1222 e 23 la Chiesa di S.Nazaro e il suo Capitolo sono 
        nominati a proposito della Chiesa di S.Marziano in Sesto Ulteriano, sulla 
        quale la Basilica esercitava l'jus patronato per l'elezione del parroco. 
        Gli ottimi canonici di S.Nazaro vi debbono avere portato il culto di san 
        Matroniano.<br>
        Comunque la qualifica di eremita fa pensare ad un luogo aspro e boscoso 
        dei dintorni di Milano lungo le tortuose rive del fiume Lambro, e non 
        &egrave; possibile allontanarsi dalla direttrice costituita dalla via 
        Romana, n&eacute; si esclude che il santo abbia avuto contatti col monastero 
        ricordato da sant'Agostino, dove pu&ograve; aver attinto le norme della 
        vita monastica, che poi condurr&agrave; in un eremo.<br>
        Il secondo fatto (gli onori liturgici) serv&igrave; a circondarlo di avvenimenti 
        in tutto simili a quelli attribuiti ai pi&ugrave; popolari santi anacoreti, 
        cos&igrave; santo Alessio.<br>
        Il terzo (la sepolture nella Basilica degli Apostoli accanto al corpo 
        di Nazaro martire, storicamente rinvenuto &#150; in hortis=brolo &#150; 
        e riposto da Ambrogio nella Basilica, a lui cara, accanto alle Reliquie 
        degli Apostoli), fece nascere la leggenda, che quanto Ambrogio aveva antecedentemente 
        compiuto per il corpo di Nazaro, lo aveva pure ripetuto per il corpo di 
        Matroniano.<br>
        Pertanto si pu&ograve; ritenere che la tradizione, fortemente colorita 
        da leggenda, conduce ad affermare, sia l'esistenza di Matroniano in un 
        periodo che oscilla dal VI a VII secolo come si vedr&agrave;, sia la santit&agrave; 
        raggiunta, e la definitiva sepoltura nella Basilica degli Apostoli.<br>
        Non parr&agrave; ozioso chiederci ove era sito il monastero abitato dai 
        monaci ricordati da sant'Agostino, tanto pi&ugrave; che alcuni lo collocarono 
        accanto alla distrutta chiesa di sant'Agata, che sorgeva lungo la piazza 
        di san Nazaro in Brolo di fianco alla canonica con l'asse longitudinale 
        parallelo a quello della Basilica degli Apostoli.<br>
        Il Latuada nel darci la descrizione della Chiesa di sant'Agata afferma 
        che: &#147;al lato sinistro della Basilica (degli Apostoli) su la medesima 
        Piazza, e contigua alla Canonica &egrave; posta un'altra Chiesa col titolo 
        di sant'Agata Oratorio di Scuolari Disciplini&#148;, e dopo aver negata 
        l'affermazione del Castiglioni che la chiesa di sant'Agata venne eretta 
        prima della Basilica degli Apostoli ne adduce le prove: &#147;quando per 
        prova contraria si sa, che in quel sito vi era il Brolio, ove poi Sant'Ambrogio 
        eresse il Templo, che, sussistendo allora tal Chiesa, sarebbe stato pi&ugrave; 
        facilmente contrassegnato dagli antichi scrittori, coll'additarlo vicino 
        alla Chiesa di sant'Agata; e tanto pi&ugrave; se fosse vero quanto altri 
        soggiungono, che in quei tempi vi soggiornassero alcuni Religiosi, cosa 
        che al solo nominarla viene riprovata dalle persone versate nell'antichit&agrave;; 
        essendovi la sola testimonianza di santo Agostino, che erat Mediolani 
        Monasterium plenum bonis Fratribus extra Urbis Mediolani su Ambrosio nutritore, 
        et non noveramus etc., ma chi pu&ograve; dedurre che fosse in questo luogo, 
        a parlare rigorosamente n&eacute; del tutto dentro, n&eacute; del tutto 
        fuori della Citt&agrave;, perch&egrave; posto tra le Mura, e la porta 
        del Fossato, come pi&ugrave; volte si &egrave; detto?<br>
        Sopra l'addotto testo di santo Agostino non si sono sin'ora accordati 
        i moderni scrittori; n&eacute; mai alcuno di essi, per quanto sappiamo, 
        ha n&eacute; pure sognato, che l'accennato Monastero fosse in questo sito, 
        che facilmente poteva essere noto ad Agostino, onde non avrebbe con verit&agrave; 
        scritto di non averlo saputo: et non noveramus; essendo cos&igrave; vicino 
        ad una basilica fondata da Ambrogio, e frequentata tanto da' milanesi 
        in que' primi tempi della di lei fondazione; per conseguenza in luogo 
        di doverne avere certezza ancora Agostino&#148;.<br>
        All'esistenza della chiesa di sant'Agata ai tempi di Ambrogio non si deve 
        certo prestar fede , cade quindi anche quanto sostennero quegli &#147;altri&#148; 
        ricordati dal Latuada. Chi sono e su quali fonti si basavano non &egrave; 
        dato sapere.<br>
        Se in un primo tempo pu&ograve; sembrare allettante, per spiegare la sepoltura 
        di Matroniano eremita nella Basilica degli Apostoli, tosto per&ograve; 
        se ne vede la falsit&agrave;.<br>
        Innanzi tutto la zona ove sorse la Basilica non era boscosa poich&eacute; 
        era zona cemeteriale, poi come mai i monaci non seppellirono un loro santo 
        nella propria chiesa, quella di sant'Agata?<br>
        Se gli scrittori contemporanei del Latuada non sciolsero la questione 
        dell'ubicazione del monastero ricordato da sant'Agostino, neanche i nostri 
        contemporanei sciolsero ogni dubbio.<br>
        Ci fu il Turazza che dopo un attento esame arriva a queste conclusioni: 
        &#147;1&deg; che nel 357 san Martino introduceva in Milano la professione 
        monastica fra gli eremiti che gi&agrave; vivevano in solitudine nelle 
        vicinanze della citt&agrave;; 2&deg; che il luogo di questa adunanza non 
        pu&ograve; essere se non quello che lungo i secoli e fino a noi si chiama 
        ad Nemus, quasi il bosco sacro dei cristiani; 3&deg; che ai tempi di s. 
        Ambrogio e di s.Agostino, cio&egrave; oltre trent'anni dopo l'erezione, 
        vi aveva questo solo monastero, del quale aveva cura s.Ambrogio stesso&#148;.<br>
        Il Turazza sostiene che il monastero fosse stato annesso alla chiesa di 
        sant'Ambrogio ad Nemus; mancano tuttavia documenti convincenti.</font></p>
