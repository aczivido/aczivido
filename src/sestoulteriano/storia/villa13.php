        al Pellegrini. Infatti da questa pianta risultano gi&agrave; studiate 
        e risolte le numerose ordinanze del mecenate.<br>
        Parecchie di queste ordinanze riguardano l'altare e la cappella di san 
        Matroniano.<br>
        Nella XX, XL, XXXXVII, fa levare gli altari di san Michele, di san Nazariolo, 
        di santa Mariola il titolo dei quali coi loro emolumenti ed obblighi fa 
        trasferire ex nunc all'altare di san Matroniano.<br>
        Nell'ordinanza III: &laquo;...A presso si ricorda che nella Cappella di 
        San Matroniano accanto al Corpo di esso Santo qual non &egrave; pi&ugrave; 
        di due brazze sotto terra (per quel che ho inteso) vi stanno hinc inde 
        due sepolture molto contigue, tal che non so se sii bene tollerarl&raquo;.<br>
        Nella XXXXIV: &laquo;La feradella che &egrave; sulla preda (pietra) di 
        sopra al Corpo di San Matroniano, si faccia pi&ugrave; grande et si accomodi 
        che resti pi&ugrave; elevata da terra a ci&ograve; non se gli calpesti 
        sopra et se intagli in detta preda il nome di San Matroniano&raquo;.<br>
        Nella XXXXV: &laquo;Alla sodetta capella se gli provveda dal Capitolo 
