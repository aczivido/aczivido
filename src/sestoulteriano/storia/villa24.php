      <p align="JUSTIFY"> <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>La 
        nuova cappella di san Matroniano</b><br>
        Come sar&agrave; la nuova cappella di san Matroniano? Si deve restaurare 
        la cappella attuale con la cupoletta ottagona, costruita nel 1653 ad imitazione 
        della cappella detta del Corpus Domini, o meglio ricostruirla a forma 
        di &quot;scodella&quot;, quale la troviamo segnata nella pianta della 
        Raccolta Bianconi e costruita innanzi il 940, perci&ograve; strettamente 
        connessa alla primitiva Basilica del sec.IV dall'architettura paleocristiana?<br>
        Non &egrave; compito mio il decidere; chi con perizia e dedizione dirige 
        e segue i lavori di restauro decider&agrave;; comunque mi sia lecito il 
        fantasticare.<br>
        Portiamoci nella Basilica al centro, ov'era l'altare eretto da Ambrogio 
        per elevare una preghiera a Dio, non fra le consuete, sospinti come gi&agrave; 
        il salmista. &quot;Quoniam zelus domus tuae comedit me&quot; ps.68, &quot;perch&egrave; 
        mi divora lo zelo per la tua casa&quot; e ripetere da ultimo: &quot;Domine 
        dilexi decorem domus tuae, et locum habitationis gloriae tuae. Ne perdideris 
        cum impiis animam meam et cum viris sanguinum vitam mea&quot; ps.25 &quot;o 
        Signore, amo il decoro della tua casa, e il luogo ove si posa la tua maest&agrave;: 
        non togliere in un fascio coi l'anima mia n&eacute; la mia vita coi sanguinari&quot;.<br>
        Poi lo sguardo lo rivolga ovunque, parecchie e non poche constatazioni 
        sorgono spontanee.<br>
        Fessure e crepe che percorrono archi, volte e cupola indicano che la statica 
        non &egrave; nelle migliori condizioni. Le mutilazioni recate ai pilastri, 
        ai muri di sostegno per ricavare armadi o scale o locali, nidi di topi, 
        hanno indebolito di molto le strutture portanti.<br>
        L'intonaco o cade per infiltrazione di acqua o porta muffa ed abbondante, 
        per una umidit&agrave; quasi congenita, cos&igrave; che molti accusano 
        mancanza di manutenzione ove &egrave; solo necessit&agrave; urgente di 
        restauro.<br>
        Alla basilica - sempre per le pareti interne - si &egrave; fatto indossare 
        una casacca da burla, la si &egrave; uniformata creando con la decorazione 
        una linea classica stiracchiata e stucchevole.<br>
        Nell'intento di togliere la monotonia creata dalle ampie pareti sviluppatesi 
        sotto il gioco degli archi a largo diametro e di fornire luce ed aria 
        per eliminare luoghi oscuri e focolai di umidit&agrave;, si aprirono finestroni 
        da per tutto.<br>
        Allo scopo di abbellire e rendere preziosa la Basilica, agli altari della 
        Madonna della Florana, di santo Ulderico, del Corpus Domini e di san Matroniano 
        si diede un esagerato volume in contrasto con quello interno della Basilica.<br>
        Infatti, come si possa ricavare da un edificio romanico, costruito sia 
        pure su di una pianta non romanica, usufruendo delle strutture portanti 
        di una preesistente costruzione paleocristiana, (quella di Ambrogio), 
        una costruzione dalle linee classiche &egrave; compito non facile.<br>
        Cos&igrave; la sagomatura, che gira in forte aggetto lungo le navate all'imposta 
        degli archi, appesantisce di molto il volume interno da far credere ad 
        un presunto pavimento della Basilica alla profondit&agrave; di qualche 
        metro dell'attuale, mentre non lo pu&ograve; essere.<br>
        I due altari poi, siti a capo dell'abside di levante e di ponente sono 
        simili a giganti che a male pena percorrono una bassa galleria da curvare 
        la testa.<br>
        Le due aperture ricavate nei fianchi delle navate di levante e di ponente 
        per mostrare le cappelle del Corpus Domini e di san Matroniano, distruggono 
        di colpo la vastit&agrave; riposante delle navate; caratteristica della 
        Basilica.<br>
        Dalle cinque constatazioni sopra esposte, ne viene che tra l'elemento 
        strutturale e quello decorativo si impone un nuovo connubio, se si vuole 
        raggiungere l'armonia di una architettura qualsiasi ma vera. Non si auspica 
        ricchezza o suntuosit&agrave;, che la Basilica degli Apostoli mai ebbe, 
        bens&igrave; il rapporto perfetto offerto dai tre elementi architettonici, 
        che sino dall'origine ebbe.<br>
        N&eacute; alcuno pensi che si caldeggi il restauro, anche dell'interno, 
        sospinti da una estetica raffinata, ovvero per creare un museo di pi&ugrave; 
        in citt&agrave;.<br>
        Lo esige la Basilica degli Apostoli che vuole ritornare il santuario della 
        fede Ambrosiana, dandoci la visione di quelle stesse mura contemplate 
        da Ambrogio e dal fratello Satiro e divenire centro di vita parrocchiale 
        sentita.<br>
        Conclusione: balza evidente che l'attuale cappella di san Matroniano non 
        pu&ograve; sussistere.<br>
        Potr&agrave; sembrare ardita questa affermazione e forse pi&ugrave; quanto 
        si sta per esporre.<br>
        Cio&egrave;: si demolisca l'attuale cappella rettangolare con la cupoletta 
        ottagona, si ritrovi il perimetro dell'abside distrutta nel 1563, torner&agrave; 
        poi facile e legittimo agire la ricostruzione dell'abside e della calotta.<br>
        Scomparir&agrave; il vuoto grande dell'attuale apertura, poich&eacute; 
        si ricostruir&agrave; la parete con la piccola porticina che dalla corte 
        della canonica introdurr&agrave; nella basilica, come &egrave; dato dalla 
        descrizione della Visita del 1567.<br>
        Lo sconcio offerto dalla cappella di san Matroniano adibita a locale di 
        servizio - un passaggio - scomparir&agrave;.<br>
        I documenti che rimangono, quale la pianta della Raccolta Bianconi, le 
        descrizioni delle visite pastorali di san Carlo, del Card. Federico Borromeo, 
        danno gli elementi primi per una ricostruzione della cappella ad emiciclo 
        di san Matroniano.<br>
        E non basta: al centro dell'abside, un po' discosto dal muro sorger&agrave; 
        l'altare come lo vuole la liturgia nostra rivolto al popolo, nel sepolcreto 
        delle Reliquie l'urna contenente le Ossa di Matroniano; lo sviluppo dell'ampia 
        parete absidale e della calotta sar&agrave; affrescato, la guida non manca, 
        il Puricelli in &quot;Dissertatio Nazariana&quot;, come si &egrave; visto, 
        d&agrave; il ciclo completo delle vicende dell'Eremita.<br>
        Sette quadri nell'ordine superiore e solo sei nell'inferiore, tredici 
        quindi e distrutti, il mancante dell'ordine inferiore, il settimo che 
        doveva essere al centro dei sei e costituisce l'icona dell'altare, fortunatamente 
        si conserv&ograve; in Basilica e riscuote la nostra venerazione.<br>
        E' il grazioso affresco della Madonnina che nutre il Divino Infante con 
        ai piedi San Matroniano, che si ammira sulla parete sinistra della navata 
        di entrata a fianco della statua di san Pietro alla sua destra.<br>
        Prova ne &egrave;, oltre al Torre gi&agrave; citato la didascalia postavi 
        sopra:<br>
        VENERANDA HAEC IMAGO<br>
        A TEMPORE D.AMBROSII IN SACELLO S.MATRONIANI<br>
        PRO ICONE ALTARIS STETERAT<br>
        HUC EX DIVOT.NE TRANSLATA AN. MDCLIV<br>
        QUO NOVU SACELLU ELEGANTIORI FORMA CONSTRUEBATUR<br>
        &quot;Questa veneranda pittura, che fino dal tempo di Sant'Ambrogio era 
        stata posta come icona dell'altare nella Cappella di S.Matroniano, fu 
        qui trasportata per devozione nell'anno 1654 quando si costru&igrave; 
        una nuova cappella di forma pi&ugrave; elegante&quot;.<br>
        Falsa nell'affermazione di sant'Ambrogio, veritiera per il trasporto, 
        infatti la parete dipinta non &egrave; una superficie piana, bens&igrave; 
        curva con un raggio uguale al raggio della distrutta cappella ad emiciclo.<br>
        L'epoca dell'affresco, va ricercata nel sec.XIV e non oltre.<br>
        Il grazioso affresco della Madonnina dopo tre secoli dal primo trasporto, 
        ne dovr&agrave; subire un secondo, vorremmo essere i protagonisti, e torner&agrave; 
        ad essere l'icona della cappella di Matroniano.<br>
        Ricostruita questa, torner&agrave; facile il ripristino della basilicula 
        di san Lino e se la tomba dell'Arciv. Arderico verr&agrave; alla luce 
        anche lui riposer&agrave; e ci benedir&agrave; da dove volle essere sepolto. 
        Accanto ad Arderico vorremmo riporre, sotto l'altare dell'absidiola, le 
        spoglie sacre dei quattro Arcivescovi milanesi santi - Venerio, Marolo, 
        Glicerio e Lazzaro - attualmente custodite in modesti reliquiari, esposti 
        nelle solennit&agrave; all'altare maggiore.<br>
        Quanto saggiamente si &egrave; verificato per l'Oratorio di san Sigismondo, 
        gi&agrave; santa Maria Greca nel restauro condotto tra il 1930 ed il 1940, 
        perch&egrave; non &egrave; possibile per la cappella di Matroniano eremita 
        e santo?</font></p>
