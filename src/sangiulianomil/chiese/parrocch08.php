              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>la 
                visita pastorale di San Carlo Borromeo</u><br>
                <br>
                Era l'anno 1573 quando l'Arcivescovo di Milano San Carlo Borromeo 
                giunse a San Giuliano in visita pastorale accolto dal Prevosto 
                Giorgio Rocchi, 1567-1591. Di questo importante evento si conserva 
                nell'archivio parrocchiale un volume di ben 200 pagine.<br>
                San Carlo era un vescovo severo quanto giusto e voleva che le 
                sue disposizioni fossero eseguite alla lettera. Interessante a 
                tal proposito una annotazione: &quot;Fu condannato il Prevosto 
                di San Giuliano, commutando la pena in trenta scudi&quot;. I tempi 
                lo esigevano, poichè il clero milanese aveva perso di vista la 
                propria missione e per il nostro Prevosto evitare un soggiorno 
                nelle celle di San Carlo sotto l'Arcivescovado era già una fortuna.&nbsp;&nbsp;&nbsp; 
                Agli occhi di San Carlo la chiesa parrocchiale sangiulianese si 
                presentava con la facciata rivolta verso Milano.<br>
                L'intero edificio sacro era composto dalle attuali cappelle di 
                San Giuseppe e della Madonna, (in quest'ultima si trovava l'altare 
                maggiore) e dallo spazio fra loro compreso. Di fianco alla cappella 
                della Madonna, ora in parte giardinetto, vi era l'angusta sacrestia, 
                mentre il campanile manteneva inalterata la sua posizione.<br>
                Da alcune fotografie, scattate durante i lavori di demolizione 
                della vecchia chiesa, si può desumere la fisionomia della parrocchiale 
                al tempo di San Carlo.<br>
                Tutta la costruzione che va dalla cappella della Madonna fino 
                al campanile presenta una certa omogeneità con parete laterale 
                scandita da costoloni di rinforzo e sormontata da una semplice 
                modanatura, con chiara funzione di perimetro esterno.<br>
                Tutti questi elementi vengono a mancare se si prende in esame 
                il lato fra il campanile e la cappella di San Giuseppe, anzi il 
                sottotetto è ben visibile. Ciò spiega come al tempo di San Carlo 
                in questo punto preciso vi fosse la facciata, della quale però 
                fino ad oggi non conosciamo l'aspetto.<br>
                Non appena San Carlo visitò la nostra parrocchiale rimase amareggiato 
                per lo stato di abbandono e ordinò che venissero al più presto 
                intonacate le pareti interne ed abbellite con dipinti. Dispose 
                la soppressione della vecchia sacrestia e l'abbattimento della 
                parete frontale che andava sistemata ad arco in modo che il battistero, 
                fino allora ubicato nella vicinissima chiesetta di Santa Maria, 
                potesse trovarvi degna sistemazione. La nuova sacrestia doveva 
                essere costruita sul lato destro dell'altare maggiore, ora cappella 
                della Madonna.<br>
                Il Santo Arcivescovo ordinò pure che l'attigua ed antichissima 
                chiesetta di Santa Maria fosse privata, oltre al battistero, anche 
                dei due altari laterali ed utilizzata unicamente per la sepoltura 
                dei defunti e per tutte le cerimonie di suffragio, con il divieto 
                di celebrare la Messa.<br>
                A tal proposito fece spianare il cimitero che la circondava quasi 
                completamente e con il materiale di riporto fu rialzato il pavimento 
                della chiesetta che si era venuto a trovare vistosamente più in 
                basso rispetto al terreno circostante.<br>
                Sappiamo che le disposizioni di San Carlo furono negli anni seguenti 
                attuate, ma nel 1665 si pose mano ad una radicale trasformazione 
                della chiesa parrocchiale. La parete verso l'attuale piazza fu 
                abbattuta per far posto alla nuova facciata mentre la vecchia 
                venne demolita e sostituita con la cappella di San Giuseppe.<br>
                Il lato est fu modificato aprendo degli archi fra i costoloni 
                di sostegno. Si ricavò una apertura più ampia ed alta per la navata 
                centrale ed il tutto fu prolungato verso la via Don Bosco, edificandovi 
                una modesta abside per il nuovo altare maggiore.<br>
                A lavori ultimati sappiamo che la chiesa, di forma pressochè quadrata, 
                misurava 25 braccia in lunghezza e 24 in larghezza e che era composta 
                da una navata principale e due laterali.<br>
                L'aspetto più curioso ed ancora misterioso è il motivo per cui 
                fu in modo così singolare spostato il suo asse di novanta gradi.</font></p>
