              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>don 
                Luigi Carcano e la nuova chiesa</u><br>
                <br>
                L'auspicio che il cardinale Schuster aveva espresso durante la 
                sua visita pastorale del 1935 ebbe in don Luigi Carcano il suo 
                artefice.<br>
                Nominato Prevosto di San Giuliano il 31 maggio 1959, l'attuale 
                parroco si rese ben presto consapevole della trasformazione che 
                il paese stava iniziando sotto la spinta dell'immigrazione interna 
                e per la vicinanza a Milano.<br>
                Sia la Chiesa che le opere parrocchiale, già insufficienti e vetuste, 
                non avrebbero retto la situazione quindi occorreva studiare una 
                soluzione. Scartata decisamente l'idea di restaurare i fabbricati 
                esistenti si arrivò alla determinazione di ridisegnare ex novo 
                la planimetria dell'area parrocchiale e questo avrebbe permesso 
                il sorgere di un centro moderno e funzionale, comprendente anche 
                la nuova Chiesa.<br>
                Il progetto fu attuato gradualmente per non intralciare il normale 
                funzionamento dell'attività parrocchiale, perciò si iniziò dal 
                grande cinema-teatro. Si proseguì con i fabbricati dell'oratorio 
                maschile e femminile divisi fra loro dalla nuova e più grande 
                casa parrocchiale. Mancava solo la Chiesa.<br>
                L'incarico di stendere il progetto fu affidato all'architetto 
                Giuseppe Mariotti il quale dovette subito tener presente due vincoli 
                rappresentati dalla facciata, comprendente le due cappelle laterali, 
                dichiarata monumento nazionale, e dallo spazio disponibile relativamente 
                poco esteso.<br>
                Uno dei primi progetti prevedeva la classica forma a croce latina, 
                ma risultò anacronistica e non garantiva una capienza maggiore. 
                poichè dietro la nuova Chiesa doveva trovare posto il cortile 
                per l'oratorio femminile, occorreva sviluppare il progetto anche 
                in larghezza.<br>
                L'architetto Mariotti mise allo studio un edificio che pur nelle 
                sue linee moderne risultasse in sintonia con la facciata e seguisse 
                le direttive emerse dal Concilio Vaticano II. Non fu facile ed 
                i progetti presentati furono numerosi, così come numerose le modifiche 
                man mano apportate anche grazie al contributo di don Carcano.<br>
                Finalmente si raggiunse il risultato ritenuto soddisfacente e, 
                approvato dalla Curia ambrosiana, nel 1964 venne dato il via ai 
                lavori.<br>
                La spesa complessiva della nuova Chiesa fu preventivata in 106 
                milioni con l'esclusione però del pavimento e dell'altare maggiore. 
                Per coprire l'importo necessario alla sistemazione del pavimento, 
                che occupa un'area di 750 mq, si invitò la cittadinanza ad acquistare 
                simbolicamente uno o più metri quadrati dello stesso pagando per 
                ognuno 10.000 lire. In breve la risposta generosa dei sangiulianesi 
                non mancò.<br>
                La chiesa parrocchiale di San Giuliano, ormai ultimata, venne 
                solennemente consacrata dall'Arcivescovo di Milano Cardinale Giovanni 
                Colombo il 2 giugno 1966 e a testimonianza dell'evento fu murata 
                al suo interno una lapide.</font></p>
