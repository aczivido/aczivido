          <td align="left" valign="top" width="98%">
            <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">Se 
              gi&agrave; all'inizio del VII secolo datano alcuni ribattini, ritrovati 
              vicino a questa zona, come spiegato nei primi capitoli di questo 
              volume, e possibile indizio di un'antica presenza militare, nel 
              Duecento, nel &quot;Liber Notitiae Sanctorum Mediolani&quot; il 
              sacerdote Goffredo da Bussero ricordava &quot;in plebe Sancti Iuliani&quot; 
              sia una &quot;ecclesia de Sancta Brigida item in loco Sancte Brigide&quot;, 
              nonch&egrave; &quot;in loco Sancta Brigida ecclesia Sancti Petri&quot;. 
              Se di quest'ultima chiesa mancano, in seguito, notizie dirette, 
              l'altra fu quella che diede il nome alla casciana e alle case circostanti 
              di San Brigida, antica denominazione di Santa Brera.<br>
              Ancora nel Duecento, verso la fine del secolo, in data 29 agosto 
              1299, la cascina di S. Brigida &egrave; nominata in un atto notarile, 
              relativo a un'investitura livellaria, effettuata da Paolo Capra, 
              proprietario di un mulino sul Lambro, a favore di beltramino de 
              Inzago. Come i Brivio, anche i Capra erano un importante famiglia 
              milanese che invest&igrave; parte dei suoi capitali ugualmente nella 
              campagna attorno a Zivido. Infatti, verso la met&agrave; del secolo 
              seguente, Franciscolo Capra acquistava, l'1 settembre 1354 il &quot;castello&quot; 
              di Santa Brigida, e dopo oltre un trentennio, i fratelli Luigi e 
              Giovannolo Capra stipulavano, il 17 novembre 1389, un contratto 
              d'affitto livellario con fratelli Gio.Pedrolo e Francescolo Pioltini, 
              per beni &quot;situati in localit&agrave; Santa Brigida&quot;: &quot;casa 
              con camera, solaio, portito, porticato di cinque arcate, pozzo otturato, 
              altro porticato di quattro arcate; campo detto Campo della Strada; 
              un bosco oltre il Lambro detto Isola... per il fitto livellario 
              di 46 fiorini d'oro e patto liberabile anche previo scambio&quot;.<br>
              Di conseguenza, nel 1384 e nel 1398 Giovannolo Capra era menzionato 
              &quot;quale soggetto tenuto a versare la Decima alla Prepositurale 
              di San Giuliano... per i beni siti in loci de Sancta Bri(g)ida&quot;: 
              quindi, nel Quattrocento, saranno Luigi e Beltramiino Capra a prendere 
              in affitto un &quot;campo a vigna detta di San Pietro&quot;, cio&egrave; 
              quel terreno dove si trovava, probabilmente, la seconda chiesa gi&agrave; 
              ricordata, come detto, nel Liber Notitiae.<br>
              Il fatto che, nel Cinquecento, la battaglia dei Giganti sia menzionata 
              da alcuni cronisti contemporanei, al seguito del re di Francia - 
              quali il maresciallo di Fleuranges, il Guiffrey e Bourgeois - con 
              il nome di &quot;Santa Brigida&quot;, sta altres&igrave; a confermare 
              l'importanza del ruolo svoltovi dalla cascina. Se quest'ultima fu 
              saccheggiata dai Guasconi la mattina del 12 settembre 1515, nel 
              pomeriggio dello stesso giorno, il &quot;cristianissimo&quot; re 
              Francesco I stabil&igrave; di installarvi il suo quartiere generale, 
              alloggiandovi, con i suoi gentiluomini. E sar&agrave; nella stessa 
              cascina che, la sera del seguente 14 settembre, il giovane re scriver&agrave; 
              alla madre, Luisa di Savoia, sul felice esito della battaglia e 
              che, la mattina del giorno dopo, ricever&agrave; la visita del duca 
              D'Alviano e dell'ambasciatore veneto Contarini, il quale scriver&agrave;: 
              &quot;Poi svegliato il Cristianissimo, intrassemo in camera dove 
              era Sua Majest&agrave; in lecto. La qual levatasi a sedere in camisa, 
              domesticamente ne prese la mano, et dipoi molte facezie, venissemo 
              a parlamento de quanto se haveva a fare...&quot;<br>
              Indi, &egrave; altres&igrave; a Santa Brigida che, in quello stesso 
              giorno si riunirono trecento cavalieri milanesi, guidati dal giureconsulto 
              Gerolamo da Castiglione, per rendere atto di sottomissione e fedelt&agrave; 
              al re, e che, come scrisse nel suo diario Jean Barillon, &quot;Le 
              Roy partit du camp de Saincte Brigide le XVI jour de septembre&quot;.<br>
              Ancora nel 1515, il re di Francia volle decretare l'esenzione da 
              tutte le tasse, ordinarie e straordinarie, gravanti su Santa Brigida 
              - il cui territorio era stato gravemente danneggiato a causa della 
              battaglia, e di cui il re non mancava di ricordare come determinante 
              alla vittoria fosse stata la scelta di porvi gli accampamenti - 
              a favore di un altro illustre appartenente alla casata dei capra: 
              quel magistrato Paolo Capra, che il medesimo re nominer&agrave;, 
              in seguito, conte palatino.<br>
              Nel suo testamento del 10 ottobre 1516, questi si dichiarava &quot;doctor 
              domin Paulino de capris, Comes Palatino, filius quondam Magnifici 
              Domini Baldassarre, habitante in loco Sancta brigida plebis Sancti 
              Juliani&quot;, trasmettendo al figlio Francesco i benefici gi&agrave; 
              assegnatigli sia dal re che dal papa. Quindici anni dopo, il 10 
              agosto 1531 don Carlo e don Cesare, fratelli Brivio, confessano 
              di aver ricevuto dai fratelli Marini lire 200, a saldo di canoni 
              livellari sopra i beni di Santa Brera, a scarico dei fratelli Capra, 
              ossia Andrea, arcidiacono del Duomo e di Francesco, sacerdote.<br>
              All'anno seguente, cio&egrave; al 13 marzo 1432 data l'inventario 
              dei beni lasciati da Andrea Capra, in cui appare finalmente anche 
              quella che sar&agrave; la denominazione moderna della cascina, quando 
              vi &egrave; menzionata &quot;una possessione posta nel loco e territorio 
              di Santa Brigida chiamata Santa Brera&quot;, consistente in: &quot;Casa 
              da gentiluomo, cinque casa da massari, tre vicine e due di l&agrave; 
              del lambro. Diritti di giurisdizione di pesca e mulini. Torchiera 
              e Torchio con colombara, forni et altre edifici, stalla grande in 
              volta, mulino da tre omboni o ruote, che per&ograve; &egrave; rovinato 
              a causa della passata guerra. 2200 pertiche di terra affittata per 
              1300 lire annue a Francesco Marino Bresciano, altre terre in Vizzolo 
              e due case in Melegnano&quot;.<br>
              Pi&ugrave; di un trentennio dopo, sar&agrave; il fratello dell'arcidiacono 
              Andrea, il sacerdote Francesco, a suddividere i suoi beni - assommanti, 
              in totale a 2357 pertiche di terra, fra cui 1099 a vigna - in tre 
              parti, destinandole, con testamento del 27 novembre 1565, ai figli 
              delle due sorelle, landrisio Visconti ed Enrico Panigada, come anche 
              all'Ospedale della Piet&agrave;: il sacerdote non manc&ograve; di 
              stabilirvi, in perpetuo, sia la celebrazione di messe alla festivit&agrave; 
              di S.Brigida, &quot;calendis&quot; di febbraio, sia elemosine ai 
              poveri del luogo di Santa Brigida, per le riparazioni della cui 
              cappelletta, il medesimo testatore stabil&igrave; un'offerta.<br>
              Tuttavia, queste opere non furono attuate, se nel 1570 lo stesso 
              oratorio di Santa Brigida risulter&agrave; in rovina, e S.Carlo 
              Borromeo ne ordiner&agrave; la demolizione. ma, alla visita pastorale 
              effettuata in luogo nel 1609, ormai all'epoca del cardinal Federigo 
              Borromeo, appare menzionato anche un &quot;Oratorio ricostruito 
              recentemente, vi celebra il prete Ferrario Cappelli, e vi sono 45 
              anime&quot;.<br>
              Un secolo e mezzo pi&ugrave; tardi, nel 1756, risulta che il podere 
              di Santa Brera, nella porzione in propriet&agrave; dell'Ospedale 
              della Piet&agrave; e anche all'epoca irrigata dalla Roggia Nuova, 
              aveva aumentato la sua quota, che ammontava, allora, a 1196 pertiche. 
              Se al 5 settembre 1794 &egrave; documentata un'assemblea degli estimati 
              appartenenti al comune di Santa Brera per l'elezione dei suoi deputati 
              - riunitisi a Melegnano, nell'abitazione del Regio Cancelliere della 
              pieve di San Giuliano Milanese - attorno alla met&agrave; dell'Ottocento, 
              nel 1844, lo stesso comune sar&agrave; sciolto, confluendo in quello 
              di Zivido. Dai Brivio saranno acquistate le propriet&agrave; gi&agrave; 
              appartenenti sia ai Capra, che all'Ospedale della Piet&agrave; (1885).<br>
              A tutt'oggi operosa - anche se abbandonata in alcune sue zone - 
              la cascina di Santa Brera appare catalogata, abbastanza recentemente, 
              come cinquecentesca, presentando quella conformazione caratteristica 
              alla tipica cascina lombarda: innalzata su impianto planimetrico 
              quadrangolare, la cascina &egrave;, in sostanza costituita da corpi 
              di fabbrica disposti attorno a una grande corte centrale. Sulla 
              destra, la casa padronale - in cui dovette dimorare il &quot;cristianissimo&quot; 
              re di Francia - comprende, a sua volta, una corte minore: in corrispondenza 
              a questa dimora si innalza la torretta, che si trova, cos&igrave;, 
              disposta a lato, nonch&egrave; quasi a sorveglianza, come di consueto, 
              del grande ingresso principale.<br>
              Anche al castello di Zivido si possono accostare caratteri architettonici 
              - pertanto di tipologia convenzionale - altres&igrave; riscontrabili 
              nella cascina di Santa Brera: ad esempio, la regolare scansione 
              di alcune aperture, specialmente sui prospetti della casa padronale, 
              o la generale costituzione della torretta, dalle superfici lisce 
              superiormente decorate da sottile cornicetta, nonch&egrave; i due 
              portichetti sostenuti da colonnine di pietra chiara, in entrambe 
              le corti, e, infine, fra queste ultime, la sagomatura decorante 
              l'androne di collegamento.<br>
              Qualche traccia dell'oratorio &egrave; ancora percepibile sul lato 
              esterno, opposto all'ingresso principale: situata, come di consueto 
              anche nelle ville lombarde, sulla strada, cos&igrave; da risultare 
              facilmente accessibile anche a chi non abitava nella cascina, e 
              a quota rialzata ripsetto alla medesima via, questa cappelletta 
              doveva prospettare su piccolo sagrato, con scaletta centralizzata 
              d'ingresso e, sino a non molto tempo fa, delimitato da parapetto 
              su balaustrina.<br>
              <br>
              (da Giovanni Battista Sannazzaro in &quot;Zivido, mille anni di 
              storia. Dall'alto medioevo alla Battaglia dei Giganti&quot;, edizione 
              Associazione Culturale Zivido, San Giuliano Milanese, 1994</font></p>
