            <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
              Il 1920 fu un anno assai procelloso, fu un anno di decadimento morale 
              e religioso. Cause ne furono la propaganda socialista, sovversiva, 
              che in seguito alla vittoria riportata nelle elezioni politiche, 
              si fece tracotante e con una propaganda continua, insistente, con 
              conferenze all'aperto. Conseguenza era&nbsp; la lotta di classe 
              fra i lavoratori ed i padroni, scioperi incessanti imposti con la 
              violenza da turbe di forsennati, che battevano la campagna armati 
              di randelli.<br>
              Di mezzo a tanta violenza, che la R. Autorità non poteva o non voleva 
              reprimare, forse per non inasprire maggiormente gli animi, nasceva 
              il fascismo a farsi vindice dei diritti cancellati, della libertà 
              oltraggiata e distrutta. Ma avendo accolto nelle proprie file soggetti 
              violenti pronti a tutto anzicchè porre d'ordine, si ebbero un inasprimento 
              delle lotte civili. Causa ancora del deperimento morale fu la sete 
              insaziabile dei piaceri, dei godimenti, dei divertimenti. Si formarono 
              delle compagnie di danzatori e ogni osteria, ogni taverna aveva 
              la propria.<br>
              Il paese era stato inondato da un esercito d'organetti, che tutti 
              i giorni, tutte le ore assordavano. Il tutto era diventato una follia. 
              Le donne e le ragazze pazzescamente vi accorrevano nè v'era modo 
              di frenarle. I disordini, che a Rogoredo avevano funestato il ballo 
              indusse l'autorità di R. Sicurezza a proibirlo colà. Male per San 
              Giuliano, perchè vi si accorreva da Rogoredo, e frequenti succedevano 
              le risse, i ferimenti. Stato si deplorevole la durò tutto l'anno 
              e parte ancora nel susseguente 1921. <br>
              <br>
              (da &quot;Liber Chronicus&quot; 1895-1923, Vol.I, archivio storico 
              Parrocchia di San Giuliano Martire, San Giuliano Milanese)</font></p>
