      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Introduzione</b><br>
        Noi abbiamo il privilegio ma anche il dovere di misurarci con la tradizione, 
        l'arte, la bellezza e la storia e quindi di dover anche difendere questi 
        valori. Il punto oggi in questione &egrave; proprio come difendere questi 
        valori. Il titolo di questo convegno promosso dall'Associazione per la 
        Salvaguardia e la Valorizzazione di Viboldone nasce da queste considerazioni. 
        Dato per scontato che tutti siamo d'accordo che questo patrimonio vada 
        difeso, bisogna decidere tutti quanti insieme - sia dal punto di vista 
        tecnico (gli esperti) sia dal punto di vista istituzionale (gli enti preposti, 
        Comune, Parco agricolo, Sovrintendenza), coinvolgendo anche le associazioni 
        che si sono adoperate nel corso di questi anni per la difesa del patrimonio 
        culturale del nostro paese - con quali modalit&agrave; un patrimonio religioso 
        e artistico vada difeso.<br>
        L'Associazione per la Salvaguardia di Viboldone &egrave; nata nel 1992 
        perch&egrave; la situazione preesistente agli anni Ottanta poneva su Viboldone 
        un vincolo monumentale per circa 150.000 metri quadri. Con la variante 
        al Piano regolatore del '90 questo vincolo &egrave; sceso a 13.000 metri 
        quadri, quindi limitato solo all'abbazia e al monastero, con la possibilit&agrave; 
        di edificare quasi 90.000 metri cubi. Il resto della zona monumentale 
        &egrave; stato suddiviso in area cascinale e area di completamento edilizio, 
        quindi &egrave; stata consentita la possibilit&agrave; di realizzare circa 
        90.000 metri cubi stravolgendo la struttura originaria del borgo.<br>
        Partendo dall'idea che il borgo appartiene alla cultura non solo di San 
        Giuliano ma anche di Milano e della Lombardia &egrave; importante ribadire 
        che avere radici salde nel passato aiuta a guardare fiduciosi al futuro 
        non disconoscendo il passato. Quella scelta urbanistica disconosceva il 
        passato perch&egrave; avrebbe modificato sostanzialmente il quadro urbanistico 
        esistente. <br>
        L'Associazione in questi anni ha tenuto sempre vivo il problema del recupero 
        del borgo, organizzando delle feste agresti in Cascina Grande e delle 
        visite all'Abbazia, nonch&egrave; delle gare podistiche e ciclistiche 
        non competitive lungo i percorsi rurali che costituiscono il reticolo 
        di comunicazione tra i vari nuclei cascinali (Mulino Torretta, Cascina 
        Selmo, Occhi&ograve;, Montone, Mezzano, Castelletto, Rancate, Cantalupo, 
        Videserto, ecc.); nel '98 ha promosso e organizzato una giornata dei Beni 
        culturali e ambientali nel territorio (la Rocca Brivio, il Castello di 
        Zivido, il borgo di Mezzano, la Cascina carlotta, con visita guidata dei 
        luoghi e pranzo convenzionato presso i ristoranti locali, coinvolgendo 
        anche le altre associazioni del territorio. noi confidiamo che la presenza 
        qui degli Enti Istituzionali competenti e le sollecitazioni degli oratori 
        invitati con i loro contributi diano un significativo indirizzo per la 
        soluzione e per l'avvio del recupero di Viboldone.<br>
