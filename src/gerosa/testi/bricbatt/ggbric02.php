          <td align="left" valign="top"><p><br>
              <strong>Il testamento di Gian Galeazzo</strong><br>
              <br>
Sempre nel castello di Melegnano il 25 agosto, sentendosi prossimo alla fine, Gian Galeazzo fece chiamare il notaio Giovanni Oliva pavese e rimaneggi&ograve; i precedenti testamenti, con una nuova spartizione delle sue citt&agrave; fra i figli, senza per&ograve; un certo codicillo, ossia una disposizione in favore della figlia Valentina che era sicuramente contenuta nel primo testamento, quello originario del 1397, e ci&ograve; che pi&ugrave; conta era contenuta in un contratto matrimoniale formalmente sottoscritto.<br>
Era Valentina l'unica figlia sopravvissuta dal primo matrimonio di Gian Galeazzo con Isabella di Valois, quindi gi&agrave; sangue reale di Francia nelle vene, e suo padre la diede in sposa giovanissima a Luigi duca di Turenna conte di Valois, fratello del Re.<br>
La bellissima principessa portava oltralpe, oltre che una educazione raffinata, una elegante cultura ed amore per le lettere e le arti, la grossa dote di 400.000 fiorini, la contea d'Asti e quella di Vertus appartenente alla madre; ma il peggio fu che il Visconti, allora preoccupato di non avere discendenza maschile, dispose che in caso di morte senza eredi, il ducato di Milano sarebbe andato a Valentina od ai suoi discendenti.<br>
Di qui la grave iattura, che per una disposizione di interesse dinastico di famiglia, rimase sulla nostra citt&agrave; come una piovra secolare che la poneva a determinate scadenze alla merc&egrave; dello straniero. Vennero i figli maschi dal secondo matrimonio di Giangaleazzo con la Caterina figlia di Bernab&ograve; Visconti e furono Giovanni Maria e Filippo maria, il secondo successo al primo e come il primo morto senza eredi nel 1447; meno quella illegittima Bianca Maria che l'ultimo duce Visconti ebbe da Agnese del maino e che andata sposa a Francesco Sforza, immise costui in un diritto sullo stato di Milano che per&ograve; gli altri pretendenti ritennero una usurpazione.<br>
Francesco Sforza, facendosi consegnare quel testamento tent&ograve; di farlo scomparire <strong>(1)</strong>, ma la sua preoccupazione era inutile: gli Orl&eacute;ans ne tenevano ben certamente una copia, ed aspettavano solo i momenti a loro propizi per intervenire nelle cose d'Italia.<br>
Valentina Visconti, o Valentina di Milano come la chiamano gli storici francesi, aveva avuto dal marito, divenuto duca d'Orl&eacute;ans cinque figli, fra cui carlo, ossia il padre del Re Luigi XII, e Giovanni conte d'Angoul&egrave;me da cui discese il Re Francesco I.<br>
Essa ebbe una vita assai infelice al fianco di un marito libertino fratello di un Re pazzo, alla cui corte fra feste, balli ed intrighi, avvenivano anche truci delitti; Valentina era sospettata di magia e di sortilegi in danno del folle cognato e quando le trucidarono il marito, venne allontanata coi figli.</p></td>
