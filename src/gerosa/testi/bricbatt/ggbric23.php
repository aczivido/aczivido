          <td align="left" valign="top"><p><br><strong>La meravigliosa sfilata</strong><br>
              <br>
              Il Re sostava ancora a Lione ed accoglieva con giubilo le notizie della buona riuscita dell&#146;impresa; gli affari del regno erano stati ben sistemati, poich&eacute; prima di muoversi da Parigi, prevedendo le difficolt&agrave; ed i pericoli della spedizione che poteva necessariamente non essere rapida, aveva ceduto la reggenza alla madre Luisa di Savoia. Per essere inoltre pi&ugrave; tranquillo sulla validit&agrave; delle sue pretese dinastiche sul Milanese, oltrech&egrave; sui propri diritti di famiglia come discendente di Valentina Visconti, il 28 giugno 1515 si era fatto fare dalla regina Claudia sua moglie una donazione regolare del ducato, in quanto essa ne era l&#146;erede come figlia del Re Luigi XII suo predecessore.<br>
              Pertanto Francesco I, percorrendo la stessa via tenuta dall&#146;armata, seguito da tutta la sua corte discese in Italia e si congiunse alle truppe.<br>
              Lo spettacolo della visione di insieme di tutto quell&#146;esercito deve essere stato per tali tempi imponente. Passavano le schiere baldanzose con divise ed armature diverse per ogni corpo a seconda della loro provenienza: ecco i fantaccini di Normandia, i Piccardi ed i Guasconi, soldati di leva improvvisati, designati col nome di &quot;aventuriers&quot;, tanto valorosi quanto privi di disciplina. Fra tutti, i Guasconi sono considerati migliori degli altri, e come li descrive il Giovio, &quot;patientissimi della fatica, animosi, espediti et molto destri&quot;.<br>
              Il grosso dei soldati regolari o di carriera &egrave; costituito dai Lanzichenecchi che vengono di Germania: usano ancora lo scudo, portano la picca, l&#146;alabarda o l&#146;archibugio, la daga od il pugnale; i capitani ed i graduati sono forniti di corazza.<br>
              Anche i Lanzichenecchi sono considerati soldati non pi&ugrave; apprezzabili degli avventurieri di Francia (ci fu chi scrisse che le due piaghe storiche della Germania furono il &quot;mal franzese&quot; ed i Lanzichenecchi). Essi per&ograve; hanno un corpo scelto detto la Banda Nera: &quot;se chiama banda negra per essere de una montagna appresso Fribourg in confin de Sguizzari chiamata Montenegro, etiam perch&eacute; per ditto de ognuno sono vestiti de negro&quot;.<br>
              L&#146;artiglieria, che sta affermando la sua superiorit&agrave; sulla fanteria e sta divenendo la migliore d&#146;Europa, sfila coi suoi nuovi cannoni a palle di ferro; le carrette che li portano sono trainate da vigorosi cavalli e gli ufficiali del corpo sono valentissimi. Precedono e seguono le artiglierie i tremila pionieri che hanno fatto &quot;mirabilia&quot; nel passare i cannoni dai gioghi scoscesi e dalle forre impraticabili di Saint Paul e dell&#146;Argentiera <strong>(1)</strong>.<br>
            Le genti d&#146;arme nelle loro complete armature passano sui loro focosi cavalli con una selva di lance e pennacchi al vento; uno stuolo di gentiluomini, principi e baroni occupano i ranghi della cavalleria che &egrave; rimasta il privilegio degli aristocratici, i quali disprezzano &quot;la pietaille&quot;, ossia la gente a piedi; seguono le &quot;impedimenta&quot;, cio&egrave; un&#146;orda di vivandieri e di osti che foraggiano l&#146;esercito ed hanno ordine di non acquistare nulla senza pagare. Ma i marescialli coi propri arcieri addetti al servizio di polizia sono incapaci di matenere l&#146;ordine e quelli fanno man bassa di polleria, di granaglie e vino dove ne trovano, sicch&egrave; le citt&agrave; ed i paesi gareggiano nel tenersi fuori per quanto &egrave; possibile dal passaggio e dall&#146;alloggiamento delle truppe. </p></td>
