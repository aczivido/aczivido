              <td align="left" valign="top"><p>Coinvolti comune e costruttori, la proposta: &quot;Ideale per farci del turismo culturale&quot;<br>
                  <strong>Un polmone verde per i Giganti</strong><br>
  Alle battute finali il progetto di un parco a Zivido<br>
  <br>
  Un'area di 480 mila metri quadrati &egrave; destinata a diventare il polmone verde della frazione di Zivido. Parte dell'appezzamento al momento &egrave; in mano alle imprese costruttrici che stanno edificando due nuovi quartieri residenziali nella zona e che a scomputo degli oneri di urbanizzazione realizzeranno un'arena naturale di circa 40 mila metri quadrati (pari a 4 campi da calcio regolamentari), proprio in quella porzione di territorio dove si tenne nel 1515 la Battaglia dei Giganti.<br>
&quot;Il progetto prevede un anfiteatro naturale scosceso nel terreno con sedili in legno, che sar&agrave; gi&agrave; pronto per la rievocazione storica che si terr&agrave; il settembre prossimo - spiega il sindaco marco Toni -. Il costo per la realizzazione dell'arena e del parco si aggira sui 600 milioni&quot;.<br>
  E per la gestione di quest'area posta tra i nuovi complessi (la Piana dei Giganti e i Girdini di Milano), la vecchia chiesetta di Santa Maria in via di restauro e la nuova struttura religiosa in progetto, viene avanzata ora qualche proposta da parte dell'Associazione Culturale Zivido, impegnata da tempo nella rivalutazione del patrimonio storico locale.<br>
&quot;Si tratta di una zona che acquisisce un particolare pregio, dal momento che &egrave; ubicata tra il borgo e la chiesa antica proprio in quel che &egrave; rimasto dei luoghi della Battaglia - spiega il presidente del sodalizio Pierino Esposti -. Per questo ritengo che occorra che il parco sia gestito da una societ&agrave; in cui dovr&agrave; essere coinvolta l'amministrazione comunale, ma anche alcuni cittadini, visto l'interesse pubblico. E proprio da l&igrave; potrebbe partire un percorso turistico culturale a cui noi puntiamo da tempo, dal momento che il nostro &egrave; un territorio ricco di cascine, di reperti archeologici e di molte altre strutture del passato. Per questo occorre puntare ad un recupero ambientale con vegetazione autoctona, oltrech&egrave; storico attraverso gruppi scultorei che ricordano i trascorsi della nostra zona. Un percorso in cui occorrerebbe comunque coinvolgere anche il Consolato svizzero e quello francese&quot;.<br>
  E l'intenzione di Esposti &egrave; proprio quella di sottoporre all'amministrazione l'iniziativa in cui confluiscono una serie di proposte volte a tutelare questo spazio, come la chiusura notturna dei cancelli e la recinzione dell'area, al fine di giungere ad un progetto concordato. &quot;Ho gi&agrave; parlato in passato con il sindaco di queste iniziative a mi piacerebbe arrivare ad un documento protocollato dal comune da sottoporre poi ai consolati&quot;, prosegue Esposti.<br>
  Intanto le imprese predisporranno il terreno per questo parco, che dovrebbe comunque essere fruibile a tutti con arredi come panchine e giochi, e su cui confluiscono gi&agrave; numerose aspettative, in vista anche di altri progetti in fase di realizzazione volti a dare un nuovo aspetto all'antica frazione. Gli obiettivi punterebbero infatti ad evitare che l'edilizia e le nuove strutture possano cancellare l'impronta del passato.<br>
  <br>
  (di Giulia Cerboni, &quot;Il Cittadino&quot;, 19.10.2001)</p></td>
