              <td><p>A Zivido sull'area della storica battaglia<br>
                  <strong>Un parco dei Giganti per difendere il verde</strong><br>
  <br>
  Un parco che rechi il nome dell'evento che ha reso famosa l'intera frazione di Zivido, la battaglia dei Giganti tra svizzeri e francesi, avvenuta nel settembre del 1515 per il possesso del Ducato di Milano, e che sia facilmente identificabile all'interno di un'area dalla duplice vocazione residenziale e industriale. E' la proposta che lancia l'Associazione Culturale Zivido per salvaguardare dall'avanzare del mattone l'ultima vestigia di un avvenimento che, oltre ad un grosso rilievo internazionale, ha lasciato tracce di s&egrave; in tutta San Giuliano, dalla vicina chiesetta dei Brivio-Sforza all'ossario di Civesio. Si tratta di una vasta zona verde, destinata secondo il piano regolatore a diventare un parco pubblico come tanti altri. Ma il sodalizio culturale la pensa diversamente.<br>
&quot;A prima vista - spiega Pierino Esposti, presidente dell'associazione - potrebbe sembrare uno spazio come tanti altri dove, collocate alla rinfusa piante, arbusti, e qualche panchina, permettere ai cittadini, e purtroppo anche ai soliti vandali, di prendere una boccata d'aria. Ma la persona sensibile ed attenta avverte che tale spazio &egrave; ci&ograve; che rimane del famoso campo di battaglia dove si svolsero le azioni pi&ugrave; cruente e dove, nella vicina chiesetta, riposano da cinque secoli i corpi dei contendenti di quel tragico scontro. Occorre dunque rammentare tutto ci&ograve;, dare un'identit&agrave; a questi spazi e per questo abbiamo elaborato il progetto denominato Parco dei Giganti&quot;. Si tratta, nelle intenzioni di Esposti, di realizzare un parco storico, che sia nel contempo habitat naturale lombardo e percorso culturale, con la battaglia e i suoi personaggi come tema portante. &quot;E' sconfortante - aggiunge il presidente - passeggiare in certe aree a verde pubblico, disseminate ovunque, dove le piante, gli arbusti e le panchine sembrano piovute casualmente da cielo, collegate da percorsi a volte irrazionali; parchi senza identit&agrave; e senza rispetto per l'habitat naturale del luogo. Il Parco dei Giganti dovr&agrave; invece valorizzare, con il sostegno dell'associazione Vivai Pro Natura, quattro temi importanti: il recupero dell'antica strada campestre che collegava Zivido a cascina Carlotta; il giardino all'italiana; l'ambientazione lombarda con vegetazione autoctona; un ampio spazio verde a cavea con spalti in terra dove proporre iniziative pubbliche di qualit&agrave;. Come parco storico, poi, dovr&agrave; riproporre e ricordare l'evento che che gli d&agrave; il nome, collocando gruppi scultorei, raffiguranti personaggi o momenti della battaglia, in angoli particolarmente curati e scenografici&quot;. L'Associazione propone inoltre una forma peculiare di gestione della struttura, che potrebbe essere affidata a un ente creato ad hoc, con capitale pubblico e privato.<br>
  <br>
  (di Marco Ostoni, &quot;Il Cittadino&quot;, 20.01.2001)</p></td>
