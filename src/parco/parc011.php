                    La volont&agrave; degli svizzeri di acquistare una parte del territorio di San Giuliano Milanese &egrave; la notizia che pi&ugrave; ha fatto scalpore in questi ultimi tempi. Essa &egrave; stata ripresa ampiamente dalla stampa e dalla televisione italiana e svizzera.<br>
                    L'area interessata &egrave; quella sulla quale si &egrave; svolta la battaglia di Marignano, detta la battaglia dei Giganti per l'imponenza delle forze in campo (francesi e mercenari svizzeri) e per il carattere cruento dello scontro.<br>
