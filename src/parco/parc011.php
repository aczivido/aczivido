                    L'Amministrazione di San Giuliano intende favorire i propositi degli svizzeri compatibilmente con le scelte urbanistiche fatte e nell'intento di conservare e allargare l'ambiente e il territorio gi&agrave; vincolato a verde agricolo e a verde pubblico. Ci si deve quindi augurare che l'operazione possa essere un contributo svizzero positivo, seppur parziale, allo sviluppo culturale, ambientale e sociale di San Giuliano Milanese.<br>
