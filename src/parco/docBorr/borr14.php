              <td align="left" valign="top"><p>Il progetto prevede l&#146;utilizzo degli elementi che hanno caratterizzato il paesaggio agricolo della bassa pianura e che sono stati cancellati dalle trasformazioni industriali: il sistema della piantata padana con filari di aceri campestri, gelsi, olmi, la scansione regolare e fitta della trama agricola delle marcite e delle risaie, i frutteti, i filari di pioppi cipressini etc.<br>
                    La parte degli orti urbani e dei giardini di quartiere viene strutturata attraverso una trama quadrata regolare di filari di alberi da frutto e da specie storicamente utilizzate nella &#147;piantata&#148;.<br>
                    Il paesaggio agrario &egrave; ancora richiamato nella zona dei giochi attraverso la piantagione in una maglia regolare di 10x10 m di pioppi, salici e ontani alternati che riprendono i &#147;blocchi&#148; dei pioppeti da arboricoltura che interrompono il piatto skyline della pianura.<br>
                Elementi del paesaggio agrario sono infine i filari di pioppi cipressini (Populus nigra &#147;italica&#148; L.), il filare di salici da vimini (Salix viminalis L.)lungo la roggia e le siepi informali polispecifiche. </p></td>
