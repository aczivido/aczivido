    <td width="78%" valign="top" align="left"><font FACE="Verdana" SIZE="1">
      <p ALIGN="JUSTIFY"><br>
        <strong><font face="Verdana, Arial, Helvetica, sans-serif">La formazione 
        delle Pievi<br>
        </font></strong><font face="Verdana, Arial, Helvetica, sans-serif">La 
        formazione delle Pievi nell&#146;antichità è strettamente legata al processo 
        di diffusione del Cristianesimo nelle campagne e al conseguente estinguersi 
        del paganesimo. Come il processo di diffusione del messaggio di Cristo 
        non deve essere considerato quale fenomeno omogeneo e continuo e forza 
        che coinvolse automaticamente e nella stessa misura il popolo, così la 
        formazione delle Parrocchie Rurali deve essere vista come processo graduale, 
        diverso da zona a zona.<br>
        Fino al III secolo non conosciamo niente di preciso a proposito della 
        diffusione del Cristianesimo. Possiamo solamente dire che si era propagato 
        con maggior facilità nei Paesi Orientali, mentre per quanto riguarda i 
        Paesi Occidentali ed in particolare l&#146;Italia, molte furono le difficoltà 
        che esso aveva incontrato nella sua espansione. Due furono gli ostacoli 
        più duri da superare nel nostro Paese:<br>
        1) le classi colte che vedevano nel Cristianesimo un avversario all&#146;egemonia 
        politica di Roma;<br>
        2) le plebi che erano attaccatissime ai loro culti locali.<br>
        Per quanto riguarda il primo ostacolo possiamo dire che le classi colte, 
        dopo qualche periodo di resistenza, avevano accettato il Cristianesimo 
        come religione ufficiale.<br>
        Il secondo ostacolo fu molto più arduo da superare e, se nelle città, 
        la nuova religione si diffuse pienamente nel IV secolo, nelle campagne 
        ancora alla fine del VI-VII esistevano nuclei pagani. La frequente presenza 
        degli imperatori cristiani da Costantino in poi con i loro eserciti, la 
        creazione delle prime diocesi nei centri più importanti e più popolosi, 
        l&#146;influsso di Roma per l&#146;Italia Meridionale e di Milano per 
        il Settentrione, furono certo elementi di grande importanza per la diffusione 
        del Cristianesimo tra le popolazioni cittadine.<br>
        Nelle campagne questi influssi si fecero sentire in maniera decisiva molto 
        più tardi rispetto alle città e, il messaggio cristiano, anche se arrivò 
        alla popolazione agreste prima della formazione delle Parrocchie Rurali 
        mediante i missionari e gli stessi abitanti della città, fu frammentario 
        e dispersivo e non poteva certo competere con l&#146;attaccamento delle 
        plebi alle loro divinità locali.<br>
        In campagna la vita quotidiana era, anche nell&#146;antichità, più impregnata 
        di religione di quanto non si verificasse nell&#146;ambiente cittadino. 
        La popolazione rurale conservava tenacemente culti preromani, romani, 
        gallici e orientali che si erano sovrapposti e mescolati con il passare 
        del tempo.<br>
        Accanto a questo aspetto non dobbiamo dimenticare che il culto di una 
        specifica divinità era tipico di un intero gruppo sociale. La religione 
        veniva così ad assumere un aspetto sociale e comunitario per cui un dio 
        locale diventava un elemento molto importante per garantire l&#146;unità 
        di un certo numero di individui.<br>
        La diffusione del Cristianesimo tra le popolazioni rurali dunque, fu compito 
        difficoltoso per la Chiesa. E se il Sud, grazie alla vicinanza di Roma, 
        potè avere alla fine del IV secolo numerose diocesi, il Nord cristiano, 
        nonostante la notevole influenza di Milano, non riuscì ad assorbire facilmente 
        la popolazione rurale.<br>
        L&#146;opera dei Vescovi di Milano fu molto ardua soprattutto perché proprio 
        nel Settentrione l&#146;attaccamento agli dei pagani era molto forte e 
        non bastavano certo le piccole comunità cristiane esistenti nel territorio 
        agreste e gli intraprendenti missionari a debellare i culti idolatrici.<br>
        Occorreva, se mai, da parte della Chiesa una organizzazione capillare 
        nelle campagne, formata di piccole cellule di valore basilare che svolgessero 
        anche nel territorio del pagus tutte le funzioni che le diocesi avevano 
        già nelle città grandi per convertire, prima attraverso la predicazione 
        e poi attraverso il Battesimo, la popolazione agreste alla parola di Cristo.<br>
        Proprio per adempiere a questo compito la Chiesa decentrò la parrocchialità, 
        un tempo raccolta nella chiesa cattedrale, creò delle &quot;ramificaziooni&quot;: 
        le cosiddette Parrocchie Rurali che nell&#146;Italia Centro-Settentrionale 
        furono chiamate Pievi. In altre parole le Pievi e più in generale le Parrocchie 
        Rurali, costituirono le cellule basilari della Chiesa e furono create 
        proprio in quei luoghi che, dal punto di vista geografico, sociale, economico, 
        giuridico, potevano meglio irradiare il messaggio cristiano. Nacquero 
        insomma dove era stabilito il popolo e si può dire, a ragione, che la 
        storia della Parrocchia Rurale sia la storia del popolo rurale.<br>
        Lo stesso nome &quot;Pieve&quot; non fu usato a caso: esso deriva dal 
        latino &quot;plebs&quot; (plebe) secondo la definizione data da Tacito 
        e da Ammiano Marcellino e stava ad indicare la più modesta classe sociale 
        degli uomini liberi, sia presso i Romani sia presso i Germani. Nel nostro 
        caso questo termine indica i villici, cioè gli uomini delle campagne che 
        si raggruppavano nelle Parrocchie Rurali come entità sociali e poi necessariamente 
        come entità territoriali. E' facile capire che, tenendo conto della tendenza 
        della filosofia cristiana a spiritualizzare ogni cosa, anche nella vita 
        concreta, attraverso un processo mentale di astrazione, &quot;plebs&quot; 
        fu estesa al tempio ove il popolo si riuniva, così come il nome &quot;ecclesia&quot;, 
        non solo indicò la riunione dei fedeli, ma il luogo di adunanza dei fedeli 
        stessi. Inoltre il termine &quot;plebs&quot; indicava talvolta anche il 
        distretto civile, raccogliendo in sé tutti gli aspetti della vita delle 
        campagne.<br>
        Così la diffusione del Cristianesimo nelle campagne e quindi la corrispettiva 
        nascita delle Parrocchie Rurali, si erano realizzate in modo che la comunità 
        cristiana si compenetrasse nell&#146;aggregato sociale, economico e giuridico 
        entro cui essa viveva partecipando alla sua stessa vita.<br>
        Fu certo una decisone sapiente della Chiesa quella di avvalersi dei preesistenti 
        ordinamenti sociali delle campagne per creare le Pievi, ma ciò non bastò, 
        almeno agli inizi, ad assicurare al Cristianesimo una facile diffusione 
        nei territori agresti.<br>
        Non dobbiamo dimenticare che la popolazione rurale è lenta e restia a 
        compiere il &quot;primo passo&quot;, proprio perché troppo spesso non 
        ha coscienza di una evoluzione sociale.<br>
        Quando si pensa alla formazione di una Pieve, viene spontaneo immaginare 
        che il Vescovo mandasse uno dei suoi Sacerdoti in una località adatta 
        all&#146;organizzazione di una Parrocchia Rurale che raccogliesse gli 
        abitanti di un distretto. In realtà quest&#146;opera di organizzazione 
        non si effettuò grazie all&#146;attività del singolo, né immediatamente, 
        ma fu la risultante del lavoro di un gruppo di Sacerdoti e si sviluppò 
        con gradualità. I ministri di Dio, che si dedicavano alla creazione di 
        una Pieve, erano numerosi e avevano una gerarchia che si modellava sull&#146;esempio 
        di quella della Cattedrale, naturalmente in maniera ridotta. Abbiamo a 
        questo proposito, diverse testimonianze che parlano di tale gerarchia, 
        composta di Accoliti, Suddiaconi, Diaconi e Presbiteri.<br>
        Il problema si fa complesso quando si tenta di stabilire come viveva il 
        clero della Pieve, perché non abbiamo documenti che ci diano notizie in 
        proposito e dobbiamo affidarci esclusivamente alle interpretazioni, diverse 
        e contrastanti, degli storici che si interessarono di tale questione. 
        Noi ci limiteremo a citare due posizioni tra di loro contrarie: quella 
        del Forchielli e quella del Palestra.<br>
        Il Forchielli ci parla di vita in comune del clero e quindi di collettività 
        di beni nelle Pievi, perché la Chiesa aveva avuto organi molteplici che, 
        per tutto il Medio Evo, furono collegiali: a questa regolamentazione non 
        fecero eccezioni le Parrocchie Rurali. Se la costituzione della Chiesa, 
        sempre secondo il Forchielli, fu per molto tempo corporativa e se la proprietà 
        ecclesiastica fu collettiva, i più antichi patrimoni parrocchiali non 
        furono i benefizi, bensì beni comuni le cui rendite servivano al sostentamento 
        dei Chierici.<br>
        Lo stesso concetto di &quot;titolare&quot;, che designa l&#146;amministratore 
        dei beni plebani, figura molto importante nella dottrina canonistica, 
        dimostra che il patrimonio delle Parrocchie Rurali non poteva essere la 
        concessione in precario del beneficio. Quest&#146;ultimo fu se mai il 
        prodotto della successiva individualizzazione del patrimonio, prima comune 
        e collettivo, e non una forma di reddito a sé stante. In altre parole, 
        come nella storia della proprietà civile, per il Forchielli, si passa 
        da forme collettive a forme individuali di possesso, così nella storia 
        della proprietà ecclesiastica si passa da forme collettive, collegiali, 
        corporative, a forme individuali e beneficiarie.<br>
        Il Palestra è su posizioni molto diverse nel momento in cui afferma che 
        la Pieve non include necessariamente il concetto di vita comunitaria e 
        che, anzi, proprio perché il clero della Parrocchia Rurale era un &quot;clero 
        itinerante&quot;, tutto intento a portare tra la popolazione rurale il 
        messaggio cristiano, la vita in comune era pressochè impossibile.<br>
        Partendo da questo presupposto il Palestra arriva a dire che l&#146;unica 
        fonte di reddito del clero plebano era costituito dal beneficio e dalla 
        libera elemosina.</font><br>
