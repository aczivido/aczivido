      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Nascita 
        e fondamento della Confederazione</b><br>
        E' motto dell'antica saggezza politica quello per cui gli Stati si conservano 
        fintanto che sanno rimanere fedeli ai loro principi; non diversamente, 
        il Machiavelli afferma nei &quot;Discorsi&#133;&quot; (libro III, capitolo 
        1): &quot;A volere che una setta o una repubblica viva lungamente &egrave; 
        necessario ritirarla spesso verso il suo principio&quot;.<br>
        La Confederazione degli Svizzeri cominci&ograve; come lega di comunit&agrave; 
        rurali lungo il passo del San Gottardo, aperto verso la fine del XII&deg; 
        secolo, cio&egrave; da quando un oscuro artigiano della Val d'Orsera o 
        pi&ugrave; probabilmente una confraternita di penitenti gett&ograve; il 
        &quot;ponte gocciolante&quot; lungo le rocce della Reuss, congiungendo 
        cos&igrave; due sentieri montani. L'Europa traversava allora uno dei periodi 
        pi&ugrave; torbidi della sua storia; le lotte tra Papato e Impero, quelle 
        tra i Principi del nord e quelle ancora tra i Comuni o, dentro lo stesso 
        Comune, tra fazioni e consorterie rendevano l'esistenza cos&igrave; insicura, 
        la giustizia tanto precaria che citt&agrave; e vallate si videro costrette 
        ad agire indipendentemente e di proprio diritto, affinch&egrave; assassini, 
        incendi, ruberie e altrettanti misfatti non andassero impuniti; questo 
        bisogno di difesa e di ordine fece incontrare i montanari dei Paesi forestali. 
        Essi avevano gi&agrave; dato vita, nei loro paesi, a forme economiche 
        di collaborazione: utilizzazione comunitaria dei pascoli, costruzione 
        di strade alpine, opere di premunizione contro alluvioni e valanghe &#133; 
        e avevano creato talune comunanze di mercato; dalla collaborazione economica 
        si trattava ora di passare a quella politica.<br>
        Gli storiografi non sono ancora riusciti a stabilire quando sia stato 
        concluso il primo patto tra Uri, Svitto e Nidwalden (Sottoselva); forse 
        nei primi decenni del Duecento, dato che il Patto &quot;perpetuo&quot; 
        del 1291 dice espressamente di confermare &quot;l'antiqua confoederationis 
        forma&quot;; la pergamena del 1291, conservata negli archivi federali 
        di Svitto, venne redatta i primi giorni d'agosto, cio&egrave; due settimane 
        dopo la morte dell'Imperatore Rodolfo d'Asburgo, il primo dello storico 
        casato argoviese che raggiunse la dignit&agrave; imperiale; quel patto, 
        in quel preciso momento, rivela la volont&agrave; di profittare della 
        momentanea carenza dell'autorit&agrave; imperiale per risolvere certe 
        questioni essenziali nella vita delle tre comunit&agrave; rurali. Gi&agrave; 
        prima, queste ultime si erano fatte concedere delle franchigie o carte 
        di dipendenza immediata dall'Imperatore; cos&igrave; gli Urani nel 1251, 
        gli Svittesi nel 1240 e personalmente dall'Imperatore Federico II&deg; 
        di Svevia, sotto le mura di Faenza che stava assediando. Quest'ultima 
        carta afferma la dipendenza immediata di quelle genti dall'Imperatore, 
        senza cio&egrave; gli intermediari o feudatari che erano gli odiati governatori 
        della famiglia Asburgo; si trattava di bloccare la politica asburgica 
        che tendeva a trasformare una funzione amministrativa, ricevuta teoricamente 
        dall'Impero, in un diritto di propriet&agrave; sui Paesi forestali. L'oggetto 
        - anche se non espresso - della contesa era evidentemente il nuovo passaggio 
        delle Alpi, il San Gottardo, ultimo entrato nella storia, ma che subito 
        aveva assunto importanza particolare e che ancora oggi ha per gli Svizzeri 
        la risonanza di un simbolo. Se non che, l'ascesa di Rodolfo alla dignit&agrave; 
        imperiale (1275) rendeva vani d'un tratto gli sforzi dei Paesi forestali 
        e riconduceva tra di essi i governatori e i funzionari degli Asburgo; 
        si capisce come, non appena giunta la notizia della morte di Rodolfo, 
        gli uomini liberi delle tre comunit&agrave; passassero all'azione.<br>
        Essi non erano contro l'Impero, n&eacute; pensavano di creare uno Stato 
        autonomo, ch'erano concetti per allora impossibili; pur riconoscendo l'autorit&agrave; 
        imperiale, intendevano amministrare da soli il loro paese e aiutarsi contro 
        ogni minaccia. Nel patto del 1291 i Cantoni primitivi s'impegnavano perci&ograve; 
        a non accettare pi&ugrave; governatori e giudici stranieri alle loro vallate 
        e ai loro costumi - &egrave; il principio stesso dell'indipendenza; ad 
        aiutarsi nei pericoli, considerando l'aggressore di una comunit&agrave; 
        quale nemico di tutte - &egrave; il principio della &quot;sicurezza collettiva&quot;; 
        a lasciar decidere dai pi&ugrave; saggi nelle vallate le loro divergenze 
        e le loro liti - ed &egrave; il principio dell'arbitrato. Indipendenza, 
        sicurezza collettiva, arbitrato: sono le &quot;idee-forza&quot; che hanno 
        creato la Confederazione e che ancora oggi ispirano la sua politica all'interno 
        e verso l'estero; notevole continuit&agrave;. Due altri rilievi: il Patto 
        veniva concluso sulla parola, non prevedeva sanzioni; gente, dunque, per 
        la quale la parola data &egrave; sacrosanta, come costume tra galantuomini; 
        inoltre, gl'impegni reciproci erano audacemente fissati &quot;per l'eternit&agrave;&quot;, 
        e anche questo particolare non cessa di stupire, specialmente se si pone 
        mente alla &#133; durata dei patti stipulati solennemente oggigiorno &#133;</font></p>
