          </td>
          <td width="95%" align="left" valign="top"> 
            <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">Dal 
              sesto secolo la cittadina di Sion, capoluogo del Vallese, &egrave; 
              sede vescovile, e tale rimase fino al 1798. La figura del cardinal 
              di Sion, Matteo Schinner &egrave; naturalmente pi&ugrave; nota nella 
              storiografia svizzera che in quella francese ed italiana. Per&ograve;, 
              come dicemmo nel testo, parecchi lati dell'azione politica dello 
              Schinner devono ancora approfondirsi, almeno nel settore delle giustificazioni 
              e dei motivi determinanti. Del resto, lo Schinner &egrave; una delle 
              tante figure storiche che si prestano ad essere travisate secondo 
              la piega partigiana, se non si tiene conto della complessit&agrave; 
              dei tempi in cui visse. Anche qui la nostra informazione &egrave; 
              ricca; basti per&ograve; ricordare &quot;Sion&quot; (Sitten), in 
              &quot;Dict. histor. et biograph. de la Suisse&quot;, VII, Neuchatel, 
              1933. I valori dell'animo e della mente di Schinner furono per&ograve; 
              sempre valutati positivamente, soprattutto da Giulio II: l'ascesa 
              sulla carriera ecclesiastica fu rapida: fin dal marzo 1511 il pontefice 
              lo aveva eletto alla dignit&agrave; di cardinale; con Bolla del 
              9 gennaio 1512 lo creava Legato Pontificio presso i Confederati, 
              in Germania e nell'Italia Settentrionale.<br>
              E con una nuova Bolla del 6 febbraio 1512, diretta al capitolo di 
              Novara, lo designava vescovo di quella citt&agrave;, al posto del 
              destituito cardinal Federico di Sanseverino, uno dei cardinali scismatici 
              che avevano convocato contro Giulio II il Concilio da tenersi in 
              Pisa.</font></p>
