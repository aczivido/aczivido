              Une bande &eacute;tait une petit arm&eacute;e. De l&agrave; l'origine 
              du premier uniforme qui fut port&eacute; comme signe de reconnaissance 
              par tous les hommes de la meme bande. La garde du Roi portait ses 
              couleurs et ses armes; les bandes mercenaires se ralliaient &agrave; 
              un drapeau de leur chef, comme, par exemple, les bandes noires de 
              Marignan au drapeau noir de Florange&quot;.</font></p>
          </td>
