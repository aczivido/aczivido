        Culturale Zivido, Pierino Esposti, da una ventina d&#146;anni in prima 
        linea per la salvaguardia dei gioielli semi sconosciuti del territorio. 
        &laquo;Voglio portare l&#146;attenzione - dice - sui sarcofaghi romani, 
        che a Mezzano vengono utilizzati come abbeveratoi. Ma sempre dell&#146;epoca 
        romana abbiamo anche le lapidi ritrovate a Zivido su cui abbiamo concentrato 
        una ricerca. E poi, da non tralasciare la chiesetta di Occhi&ograve;, 
        con i suoi pregevoli affreschi esposti alle intemperie a causa di un tetto 
        che non ha tenuto di fronte alla mancanza totale di manutenzione. Ormai 
        diroccato, pure il Molino Torretta, che con sue linee semplici ed essenziali 
        rappresenta uno stupendo esempio di architettura del passato. Un patrimonio 
        di cui nessuno si &egrave; mai preso cura, che i cittadini quasi non conoscono 
        e che le istituzioni hanno dimenticato&raquo;. Proprio da queste considerazioni, 
        ha preso forma l&#146;idea promossa dal sodalizio di dotare il futuro 
        Parco dei Giganti di un museo che possa accogliere le antiche testimonianze. 
        Ma intanto, viene alzata l&#146;attenzione sulla citt&agrave; del passato. 
        Ed Esposti, non nascondendo una certa amarezza, si chiede: &laquo;Perch&eacute; 
        la citt&agrave; di San Donato ha trovato un antico cippo e lo ha rivalutato, 
        mentre sul nostro territorio &egrave; stato tutto dimenticato? Abbiamo 
        cascine stupende, semi diroccate. Abbiamo un percorso ciclabile in pratica 
        gi&agrave; pronto che unisce Cascina Carlotta a Rocca Brivio e non vengono 
        fatti neanche quei pochi lavori di manutenzione per rendere il tratto 
        agibile&raquo;. Vengono cos&igrave; rimarcate una serie di &#147;dimenticanze&#148; 
        da parte di tutti coloro che hanno avuto in mano la gestione del territorio. 
        Ma non viene risparmiata neanche una punzecchiatura nei confronti degli 
        abitanti di quest&#146;area dell&#146;hinterland sudmilanese. &laquo;Mi 
        chiedo anche come mai - evidenzia il presidente del sodalizio culturale 
