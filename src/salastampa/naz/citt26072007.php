e consultabile all&rsquo;indirizzo www.aczivido.net. Si condisce cos&igrave; di nuovi spunti l&rsquo;impegno di tenere alta la tradizione riproponendo ogni anno una rassegna di eventi che punta ad arricchirsi ulteriormente di nuove iniziative. E per chi volesse saperne di pi&ugrave;, al rientro delle vacanze sul sito dell&rsquo;associazione potr&agrave; trovare il programma completo.</p>
