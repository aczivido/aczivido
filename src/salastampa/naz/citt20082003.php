        tradizione locale. Quest'anno infatti dai balconi dei palazzi sbandiereranno 
        bandiere gialle e blu con gli stemmi significativi della manifestazione 
        locale. Un'idea realizzata per la prima volta grazie alla collaborazione 
        della cooperativa Garibaldina che ha sponsorizzato la realizzazione di 
        questi nuovi simboli. I cittadini che fossero quindi interessati, potranno 
        reperire una bandiera presso la sede del Centro donna di Sesto Ulteriano, 
        o all'ufficio cultura del comune, piuttosto che presso la sede dell'associazione. 
