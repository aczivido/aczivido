      <p align="JUSTIFY"><img src="../leco.jpg" width="145" height="70"><br>
        <font face="Verdana, Arial, Helvetica, sans-serif" size="1">n.43 - dal 
        28.12.2001 al 07.01.2002 - di Giuseppe Torregrossa<br>
        <br>
        Con quasi un anno di anticipo si prepara la 12&deg; edizione della storica 
        manifestazione di Zivido<br>
        <b><font size="2">Al via la nuova Battaglia dei Giganti</font></b><br>
        Incontro fra organizzatori, insegnanti e rappresentanti dei quartieri 
        per inserire l'evento fra gli itinerari turistici<br>
        <br>
        Le idee sono tante e la voglia di realizzarle pure. Per questo, lo scorso 
        11 dicembre, con largo anticipo rispetto al settembre del prossimo anno 
        quando si proporr&agrave; la 12&deg; edizione della Battaglia dei Giganti, 
        gli organizzatori hanno chiamato a raccolta per l'organizzazione di un 
        evento che si annuncia davvero speciale.<br>
        Comunione d'intenti tra il presidente dell'Associazione Culturale Zivido, 
        Pierino Esposti, patron della storica rievocazione, e l'assessore alla 
        Cultura del Comune di San Giuliano Milanese, Giovanna Bugada.<br>
        Invitati alla riunione organizzativa preliminare gli insegnanti delle 
        scuole sangiulianesi e i rappresentanti dei quartieri cittadini. Dopo 
        il positivo esperimento del coinvolgimento dei commercianti nell'edizione 
        2001, l'attenzione si sposta adesso ai ragazzi delle scuole.<br>
        &quot;E' importante recuperare la memoria storica e trasmetterla ai ragazzi&quot;, 
        ha detto l'assessore Bugada, &quot;bisogna fare opera di sensibilizzazione&quot;.<br>
        Il senso d'appartenenza al territorio &egrave;, invece, quanto auspica 
        Pierino Esposti per i ragazzi che &quot;... devono imparare a conoscere 
        il territorio sul quale crescono e vivono. Il sanguinoso e luttuoso episodio 
        della guerra non &egrave; che il punto di partenza, lo spunto da cui ha 
        inizio una rievocazione che va oltre l'atto cruento&quot;.<br>
        In quel particolare momento storico il territorio ferveva di vita. Se 
        il fatto d'arme, la cui rievocazione &egrave; il clou della manifestazione 
        annuale, &egrave; avvenuto a Zivido, e qui ovviamente deve ripetersi, 
        il resto del paese pu&ograve; vantare un altrettanto importante trascorso 
        storico che deve essere rivalutato. La proposta lanciata da Esposti, di 
        estendere la manifestazione ai quartieri, ognuno dei quali potrebbe autonomamente 
        organizzare una rievocazione storica tematica da far confluire nell'evento 
        catalizatore della rievocazione della battaglia, ha trovato favorevoli 
        i rappresentanti delle associazioni dei quartieri.<br>
        &quot;San Giuliano &egrave; un territorio con un patrimonio storico notevole&quot;, 
        ha affermato il presidente dell'Associazione Culturale Zivido, &quot;il 
        Castello, il sito archeologico di Zivido che risale all'epoca preromana, 
        la chiesetta di Occhi&ograve;, che si &egrave; scoperta molto pi&ugrave; 
        antica giacch&egrave; per la sua costruzione &egrave; stato utilizzato 
        materiale di recupero proveniente da una villa patrizia romana, rendono 
        San Giuliano una citt&agrave; con una storia propria. Chi dice che questa 
        &egrave; una citt&agrave; dormitorio, probabilmente &egrave; lui che dorme&quot;.<br>
        Alla fine della riunione, i partecipanti hanno convenuto di ritrovarsi 
        gi&agrave; nel prossimo mese di gennaio, affinch&egrave; tutti gli aspetti 
        organizzativi possano essere vagliati attentamente per far funzionare 
        senza intoppi la macchina organizzativa, e quindi la manifestazione. Se 
        si riuscir&agrave; veramente a coinvolgere l'intera citt&agrave;, con 
        i suoi quartieri, i commercianti e le scuole, c'&egrave; da credere che 
        la rievocazione finir&agrave; nell'arco di qualche anno con il collocare 
        San Giuliano Milanese tra le tappe degli itinerari storico-culturali pi&ugrave; 
        importanti del nostro Paese.</font></p>
