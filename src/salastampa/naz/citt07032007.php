            <strong>Parco dei Giganti, prende quota il progetto della frazione di Zivido</strong><br><br>
          Si aprono spiragli positivi per concretizzare il sogno di un parco tematico tra il nucleo di complessi edilizi della storica frazione.
Proprio al fine di rilanciare questa opportunit&agrave; accarezzata da anni, il presidente dell&rsquo;associazione culturale Zivido, Pierino Esposti, nei giorni scorsi ha incontrato il primo cittadino Marco Toni, a cui ha sollecitato l&rsquo;avvio dell&rsquo;atteso da iter. &laquo;Il sindaco - spiega Esposti, promotore dell&rsquo;idea -, mi ha annunciato l&rsquo;intenzione di procedere quanto prima all&rsquo;acquisizione del progetto realizzato dall&rsquo;architetto Daniela Borroni. Dopodich&eacute; il lavoro verr&agrave; suddiviso in lotti. Un&rsquo;opera che in base alle prospettive non si concretizzer&agrave; in un parco qualsiasi, bens&igrave; si tratta di un&rsquo;iniziativa curata nei particolari, che ha tutte le carte in regola per tradursi in un&rsquo;importante opportunit&agrave; di valorizzare una zona ricca di riferimenti storici. Del resto nei mesi scorsi mi hanno contattato alcuni cittadini particolarmente interessati da questa iniziativa&raquo;. Viene cos&igrave; riportato in primo piano l&rsquo;ambizioso mosaico in cui dovrebbero confluire elementi legati alla storia, insieme ad un punto ristoro, un piccolo museo, in un circuito nel verde, tra le piantumazioni autoctone e un corso d&rsquo;acqua.Questi gli ingredienti principali contenuti nel disegno da tempo a disposizione sul sito Internet del sodalizio locale all&rsquo;indirizzo www.aczivido.net, con tanto di illustrazione e riferimenti. Proprio rispetto a questa ipotesi che ha collezionato ampi consensi tra la cittadinanza, l&rsquo;associazione questa volta sembra cos&igrave; aver collezionato certezze. Nei prossimi mesi dovrebbe cos&igrave; prendere quota un lavoro che partir&agrave; dall&rsquo;atto formale del comune di acquisire il progetto e farlo quindi proprio, con un piano di investimenti da concentrare nell&rsquo;ampio appezzamento. L&rsquo;idea &egrave; stata infatti messa in campo alcuni anni fa, in una fase in cui iniziava a profilarsi l&rsquo;occasione di un ampio puzzle di terreni passati al comune dalle imprese costruttrici che hanno realizzato comparti residenziali nel tratto di Zivido. La frazione che a fronte di consistenti interventi edilizi &egrave; man mano cresciuta in termini di residenti, potr&agrave; cos&igrave; iniziare a vedere i primi accenni di un&rsquo;opera. Un armonico quadro le cui linee di sviluppo sono state pensate ad un architetto al Parco dei Giganti ha dedicato la sua tesi di laurea.</p>
