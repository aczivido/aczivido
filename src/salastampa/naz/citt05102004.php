        iniziativa di creare un polmone verde dedicato ai &quot;Giganti&quot; 
        della battaglia di Marignano, nonostante le difficolt&agrave; e soprattutto 
        i rallentamenti, possa andare in porto. In base ad una valutazione effettuata 
