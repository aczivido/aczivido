      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">San Giuliano 
        Milanese, 13.07.2001<br>
        <br>
        <b>Zivido: un futuro consapevole</b><br>
        <br>
        Da qualche tempo sul futuro della cascina Invernizzi, situata proprio 
        nel bel mezzo del centro storico dell'antica frazione di Zivido, 
        è in atto un contendere che impegna positivamente le realtà politiche 
        e sociali sangiulianesi.<br>
        Cessata la sua attività agricola, la vasta area è oggetto di un progetto 
        di riconversione ad edilizia residenziale che pone molti quesiti delicati, 
        sebbene la proposta avanzata sia di gran lunga più consona e rispettosa 
        dell'impianto urbanistico storico rispetto alla precedente ventilata 
        edificazione di 24 villette a schiera.<br>
        Tutto ciò non può che far piacere a coloro i quali da anni lavorano incessantemente 
        e con successo al recupero e valorizzazione di questo centro storico frazionato 
        in proprietà private, ma che è anche, se non soprattutto, patrimonio di 
        tutti i cittadini.<br>
        Gli interventi che si susseguono nelle sedi istituzionali e non, tralasciando 
        quelli polemici utili solo a rendere più difficoltoso il confronto delle 
        idee, porteranno sicuramente ad una maggiore sensibilità ed attenzione, 
        ma appaiono ancora limitati al particolare contingente.<br>
        Il futuro della cascina Invernizzi, a nostro parere, non può essere condizionato 
        dall'inserimento di particolari modifiche se prima non si è esaminato 
        attentamente l'intero progetto e non si è valutato: l'impatto 
        storico-ambientale sull'intero impianto urbano; il rispetto del preesistente 
        da parte del nuovo da edificare; la funzione o destinazione dell'ala 
        sud del castello Brivio, utilizzata impropriamente per decenni per le 
        attività agricole, che è compresa nel progetto ma della quale nulla si 
        sa.<br>
        Per non incorrere negli errori precedentemente fatti, come pare giustamente 
        emergere da alcuni interventi sull'argomento, occorre da subito un 
        opportuno confronto di idee e proposte per chiarire, e decidere, quale 
        Zivido si vuole realizzare.<br>
        Riteniamo che sia imprescindibile avere ben presente l'iter storico 
        ed architettonico del borgo bimillenario; gli eventi di rilievo anche 
        internazionali che lo hanno visto coinvolto; quali edifici possano subire 
        interventi radicali senza compromettere il tutto; quale &quot;nuovo&quot; 
        si vuole inserire e la sua funzione privata o pubblica; quali benefici 
        devono ricadere sulla collettività e perché no, quale immagine finale 
        si intende dare dell'operato.<br>
        La nostra proposta di progettualità è: partire dall'esistente, che 
        si presenta con una stratificazione urbanistica omogenea e ricca di patrimonio 
        culturale ed artistico, aggiungendo il nuovo necessario, tenendo conto 
        delle varie esigenze, non come una somma matematica di singoli interventi 
        ma come un unico intervento suddiviso in particolari momenti operativi.<br>
        Ben venga quindi il progetto sulla cascina Invernizzi, se questo però 
        si inserisce compiutamente nel tessuto urbano e non risulti alla fine 
        un'altra &quot;isola&quot;, accompagnato dalla chiara determinazione 
        di un percorso operativo più ampio: la realizzazione del &quot;Parco dei 
        Giganti&quot; che si inserisce senza soluzione di continuità con il borgo; 
        la limitazione dell'accesso veicolare ai soli residenti spostando 
        all'esterno il movimento di solo transito; la previsione di eventuali 
        esercizi commerciali che abbiano una offerta merceologica adeguata alle 
        necessità effettive o magari utili ad una ulteriore valorizzazione del 
        luogo evitando che divengano zone d'ombra; i servizi per la comunità; 
        l'individuazione del patrimonio storico, architettonico, artistico 
        sul quale predisporre progetti concreti di intervento e recupero; la promozione 
        del turismo culturale, elemento questo non trascurabile per i benefici 
        derivanti.<br>
        Si può fare, ma solo coinvolgendo tutti i cittadini sangiulianesi sollecitando 
        idee e proposte e delegando a loro, con opportune strutture, la gestione 
        di alcune realtà.</font></p>
