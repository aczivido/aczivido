        <font size="2"><b>Le Guardie del Papa tornano a Zivido</b></font><br>
        <br>
        Le Guardie Svizzere Pontificie tornano a Zivido. Il 10 aprile, nel 500&deg; 
        anniversario dalla sua nascita, il corpo papale far&agrave; di nuovo tappa 
        nella vivace frazione di San Giuliano. Nel 1515 quest'area fu teatro di 
        un cruento combattimento che i libri ricordano come la &quot;Battaglia 
        dei Giganti&quot;. Per difendere il Ducato di Milano, nello scontro con 
        l'esercito francese guidato da Francesco I&deg;, vi persero la vita migliaia 
        di soldati. Su queste terre sono sepolti gli antichi discendenti dei militari 
        svizzeri. I loro resti si trovano proprio accanto alla vecchia chiesetta, 
        in corrispondenza del monumento dello scultore elevetico Josef Bisa. Luned&igrave; 
        prossimo le guardie del Papa renderanno omaggio ai loro avi ripercorrendo 
        un cammino gi&agrave; intrapreso alcuni secoli fa. La giornata sar&agrave; 
        densa di appuntamenti. I militari in uniforme si daranno appuntamento 
        alle ore 14,00 in via Corridoni. Guideranno il corteo insieme al Corpo 
        Forestale dello Stato per poi raggiungere il monumento dei caduti. Le 
        autorit&agrave; locali e quelle svizzere renderanno omaggio a tutte le 
        vittime dello scontro; verr&agrave; deposta anche una targa celebrativa. 
        Alle ore 15,00 si sposteranno tutti a Rocca Brivio. Un'ambientazione tipicamente 
        medievale far&agrave; da sfondo agli eventi. Ad accogliere gli ospiti 
        ci saranno infatti il duce di Milano, Massimiliano Sforza, la cugina Isabella 
        d'Aragona e tutta la corte. &quot;L'arrivo delle guardie svizzere a San 
        Giuliano costituisce per noi un grande motivo di orgoglio&quot;, afferma 
        Pierino Esposti, presidente dell'AC Zivido, da anni impegnato nell'organizzazione 
        di eventi, primo fra tutti la rievocazione storica, che tengano vivo il 
        ricordo di questi fatti. &quot;Saranno circa 70 le ex guardie svizzere 
        che il 7 aprile partiranno da Bellinzona per fare tappa nella nostra citt&agrave;. 
        La loro marcia durer&agrave; in tutto 27 giorni. Un itinerario di circa 
        700 chilometri che finir&agrave; con l'entrata in Vaticano. L'arrivo a 
        Roma &egrave; previsto per il 3 maggio, mentre i festeggiamenti culmineranno 
        qualche giorno dopo col giuramento delle nuove reclute&quot;</font></p>
