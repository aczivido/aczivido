      <p align="JUSTIFY"><img src="../ilgiorno.gif" width="280" height="37"><br>
        <font face="Verdana, Arial, Helvetica, sans-serif" size="1">12.08.2005 
        - di Patrizia Tossi<br>
        <br>
        Il progetto ambizioso di un museo medioevale a cielo aperto<br>
        <font size="2"><b>La storia torner&agrave; a vivere nel Parco dei Giganti</b></font><br>
        <br>
        Un'opera faraonica da otto milionoi di euro per aprire un museo a cielo 
        aperto, trasformare la citt&agrave; in una meta turistica internazionale 
        e convincere i cittadini a diventare &quot;azionisti&quot; del parco.<br>
        E' il progetto del Parco dei Giganti, studiato per recuperare l'antico 
        campo di battaglia di Francesco Primo e costruire quello che potrebbe 
        diventare il pi&ugrave; importante parco storico d'Europa. Ci sar&agrave; 
        la ricostruzione in miniatura di San Giuliano medioevale, un museo di 
        storia locale, percorsi didattici e una galleria all'aperto per ospitare 
        le sculture degli artisti europei.<br>
        Ma, tra le novit&agrave; del progetto, c'&egrave; l'idea di mettere sul 
        mercato delle quote simboliche del parco, cos&igrave; da rendere protagonisti 
        del progetto tutti i sangiulianesi. &quot;Il parco deve diventare un parimonio 
        di propriet&agrave; comune, per questo vorrei che i cittadini diventassero 
        azionisti, acquistando delle quote dell'ente di gestione che stiamo per 
        fondare&quot; spiega Pierino Esposti, presidente dell'Associazione Culturale 
        Zivido e promotore del Parco dei Giganti.<br>
        Se ne parla da anni, ma con la realizzazione dell'arena dei Giganti e 
        la conclusione del progetto i tempi si stannoaccorciando. &quot;Entro 
        un anno si potranno iniziare i lavori - conferma Esposti - esiste gi&agrave; 
        il primo abbozzo della societ&agrave; che dovr&agrave; gestire il Parco 
        ed &egrave; gi&agrave; iniziato l'iter per realizzare le opere&quot;.<br>
        Inventato da Esposti alcuni anni fa, l'idea del Parco dei Giganti ha riscosso 
        cos&igrave; tanto successo da essere utilizzato come tesi per un master 
        universitario. &quot;Si tratta di un grande parco ambientale da 180 mila 
        metri quadrati - entra nel dettaglio Esposti - dove verr&agrave; ricostruito 
        l'ambiente palustre padano con la piantumazione di essenze e arbusti autoctoni.<br>
        Verr&agrave; valorizzato l'elemento dell'acqua, da sempre colonna portante 
        dell'economia locale, con la creazione di una rete di fontanili in cui 
        verr&agrave; canalizzata l'acquadella prima falda, pulita ma non potabile&quot;.<br>
        Un tempo San Giuliano aveva duemila fontanili naturali, marcite e sistemi 
        di irragazione messi a punto dai frati degli Umiliati, che nel dodicesimo 
        secolo bonificarono il territorio dalle paludi. &quot;Un fontanile artificiale 
        far&agrave; girare l'acqua intorno al parco - dice il presidente - ed 
        andr&agrave; ad alimentare un laghetto che ricorder&agrave; le vecchie 
        paludi, ma non ci saranno insetti perch&egrave; l'acqua non sar&agrave; 
        stagnante&quot;. <br>
        Oltre all'aspetto naturalistico, il parco avr&agrave; anche un'area dedicata 
        alla storia locale. &quot;Abbiamo pensato a un percorso artistico con 
        l'allestimento di un'esposizione permanente di sculture e bassorilievi. 
        Il soggetto comune sar&agrave; la Battaglia dei Giganti&quot;. L'obiettivo 
        &egrave; quello di valorizzare il terreno di battaglia dove nel Cinquecento 
        si consum&ograve; la pi&ugrave; cruenta e sanguinosa guerra della storia.<br>
        &quot;Ci vorranno 8 milioni di euro per realizzare il Parco - aggiunge 
        Esposti - , soldi che cercher&ograve; di trovare con i finanziamenti pubblici. 
        Il Parco si autoalimenter&agrave; con le risorse del turismo cuturale: 
        ci sar&agrave; un museo di storia locale con le antiche testimonianze 
        archeologiche rinvenute a Zivido (oggi conservate in Soprintendenza), 
        una biblioteche, un bar e ristorante e gli uffici dell'ente di gestione. 
        il Parco - assicura Esposti - sar&agrave; il motore propulsivo di un nuovo 
        turismo internazionale&quot;.</font></p>
