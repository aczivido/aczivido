        San Giuliano, Battaglia dei Giganti fra tradizione e progetti futuri a 
        Zivido, mentre i cinquecento anni dal grande evento europeo si avvicinano 
        sempre pi&ugrave;. Ancora una volta ieri nella frazione di San Giuliano 
        Milanese il 491esimo anniversario dello scontro ha messo in moto iniziative, 
        a cominciare dalla rievocazione storica in costume curata dall&#146;Associazione 
        Zivido, riflessioni e nuovi percorsi per arricchire la centralit&agrave; 
        della battaglia nella storia locale. La commemorazione ha rinnovato i 
        gemellaggi e i legami di amicizia fra San Giuliano e diversi comuni d&#146;Europa. 
        In municipio, all&#146;apertura della giornata, non sono mancate le presenze 
        di David Vogelsanger, console svizzero in Milano; dei rappresentanti istituzionali 
        di Boussy Saint-George, la localit&agrave; francese vicina a Parigi gemellata 
        da tempo con San Giuliano, a partire dal vicesindaco Fran&ccedil;ois Barnier; 
        di Roland Haudenschild dell&#146;associazione Pro Marignano di Berna. 
        Insomma, voci per le tre principali nazioni coinvolte nell&#146;evento 
        bellico, chiamate a spiegare che cosa cinque secoli (quasi esatti..) dopo 
        quell&#146;evento significa per la memoria collettiva. Vogelsanger ha 
        sottolineato ancora una volta come gli svizzeri che conoscono la loro 
        storia individuano in Marignano, o Zivido che dir si voglia, &laquo;il 
        pi&ugrave; sanguinoso giorno del nostro Paese&raquo;. La stima dei soldati 
        confederati presenti allora nelle campagne del Lambro, infatti, &egrave; 
        di trentamila di cui ottomila caduti:&laquo;La pi&ugrave; grande unit&agrave; 
        di battaglia mai messa in campo dalla mia nazione&raquo;. La tremenda 
        sconfitta, mise per&ograve; in moto, sottolinea il console &laquo;quella 
        scelta di neutralit&agrave; che da allora &egrave; diventata fondamento 
        del nostro Stato&raquo;. Negli stessi anni di Marignano, peraltro, nasceva 
        un&#146;altra grande tradizione elvetica: la Guardia pontificia. E il 
        diplomatico d&#146;oltralpe non ha dimenticato come proprio nel 2006 i 
        cinquecento anni dell&#146;esercito del papa (fondato nel 1506) abbiano 
        rinsaldato i legami fra la Svizzera e la Bassa, dato che le ex guardie 
        papali in marcia pochi mesi fa verso Roma sono passate proprio dalla via 
        Emilia. &laquo;Io stesso le ho accompagnate a piedi sino al passo della 
        Cisa&raquo;, ha precisato l&#146;ospite elvetico. Anche Fran&ccedil;ois 
        Barnier spiega come nella battaglia persa da svizzeri e sforzeschi nel 
        settembre 1515 c&#146;&egrave; da un lato &laquo;la prefigurazione delle 
        ecatombi della guerra moderna&raquo;, ma dall&#146;altro &laquo;l&#146;inizio 
