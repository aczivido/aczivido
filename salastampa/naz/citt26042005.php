        Nuova serie di interventi per il rilancio della chiesetta di Cascina Cantalupo. 
        Nel settecentesco complesso sangiulianese dislocato lungo la strada per 
        Locate Triulzi ha gi&agrave; infatti preso il via un nuovo ciclo di lavori 
        concentrati sulla storica chiesetta valorizzata da pareti affrescate con 
        pregevoli dipinti. Una fase di cantieri, a seguito del recente restauro 
        della pala d&#146;altare, che si articoler&agrave; su un duplice obiettivo. 
        Oltre infatti alla ristrutturazione edile con sostanziali ritocchi alla 
        sagrestia insieme ad altre parti che necessitano di una rimessa a punto, 
        verranno infatti restaurati anche dipinti situati all&#146;interno dell&#146;antico 
        edificio. In particolare, verr&agrave; dato nuovo lustro ai noti medaglioni 
        a forma di scudo che decorano le pareti interne. A fare decollare un piano 
        che ha anche ricevuto il sostegno della regione Lombardia, mediante un 
        cofinanziamento, &egrave; la propriet&agrave; della struttura, l&#146;ente 
        &#147;Ipab ex Eca (Istituzioni pubbliche di assistenza e beneficenza)&#148; 
        , che mediante un accordo con la soprintendenza, sta dando esecuzione 
        ad un progetto pilota stilato da un dipartimento del Politecnico di Milano 
        per il recupero di 6 antichi oratori lombardi. Un piano che a Cantalupo 
        tra circa 4 mesi dovrebbe concludersi lasciando un&#146;impronta di perfetta 
        conservazione in una nicchia locale candidata a diventare tappa di un 
        percorso culturale - artistico che tocca i gioielli storici locali. Al 
        rilancio, parteciper&agrave; anche l&#146;associazione culturale Zivido, 
        che gi&agrave; da tempo collabora con la propriet&agrave; per promuovere 
        eventi tesi a far conoscere ai sangiulianesi un angolo del proprio territorio 
        che per il momento ha aperto i battenti solo in particolari occasioni. 
        &laquo;Insieme con la propriet&agrave; - spiega il presidente Pierino 
        Esposti -, promuoveremo occasioni per far s&igrave; che Cantalupo diventi 
