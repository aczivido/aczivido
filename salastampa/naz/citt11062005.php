      <p align="JUSTIFY"><img src="../cittadinotestata.gif" width="272" height="69"><br>
        <font size="1" face="Verdana, Arial, Helvetica, sans-serif">11.06.2005 
        - di Eugenio Lombardo<br>
        <br>
        tesi di laurea del lodigiano - La scoperta del territorio attraverso la 
        ricerca dei giovani dell&#146;Universit&agrave; <b><br>
        <font size="2">Il borgo di Zivido nella piana dei Giganti </font><br>
        </b>Daniela Borroni e lo scenario della battaglia di Marignano (1515) 
        <br>
        <br>
        La frazione Zivido di San Giuliano Milanese &egrave; stata apprezzata 
        dall&#146;Universit&agrave; degli Studi di Firenze, per via di una tesi 
        di specializzazione, compiuta dall&#146;architetto Daniela Borroni, che 
        ha frequentato, nell&#146;anno accademico 2003/&#145;04, un master in 
        paesaggistica, successivamente al conseguimento della laurea in Architettura 
        presso il Politecnico di Milano.La scelta di questo argomento la si deve 
        al signor Pierino Esposti, presidente dell&#146; &#147;Associazione Zivido&#148;, 
        che da anni si batte per la valorizzazione della zona agricola della frazione, 
        destinata dal piano regolatore di San Giuliano Milanese a parco pubblico. 
        E&#146; stato Esposti a mandare avvisi agli studenti universitari affinch&eacute; 
        si facessero venire delle idee per abbellire la realt&agrave; di Zivido; 
        e dall&#146;Universit&agrave; di Firenze &egrave; arrivata una risposta: 
        Daniela Borroni, di Saronno, ma attualmente residente a Como, &egrave; 
        rimasta incuriosita, ha proposto l&#146;iniziativa alla sua tutor, l&#146;architetto 
        Tessa Matteini, &egrave; arrivata a San Giuliano, e ha scoperto il sud 
        Lodigiano: &#147;Di questa zona, come della confinante Provincia di Lodi, 
        - mi racconta in un bar di Milano - non conoscevo assolutamente nulla: 
        non &egrave; retorica dire che anche vicino casa possono trovarsi realt&agrave; 
        interessantissime&#148;.Daniela Borroni &egrave; una ragazza diretta, 
        trasparente, come i suoi occhi azzurri e intensi: &#147;Ho chiesto al 
        Comune di San Giuliano Milanese che riconoscessero il mio impegno sotto 
        forma di stage; ho trovato amministratori attenti, competenti, sono stata 
        accontentata e spero che adesso l&#146;Associazione Culturale Zivido e 
        l&#146;Amministrazione realizzino i loro progetti&#148;.La tesi di specializzazione 
        ha il seguente titolo: &#147;Il Parco dei Giganti in Zivido &#150; San 
        Giuliano Milanese -&#148;; e poi il seguente sottotitolo: &#147;Paesaggio 
        in memoria della battaglia di Marignano (1515)&#148;; relatori: il professor 
        architetto Biagio Guccione, il professor dottore Forestale Fabio Salbitano, 
        e, come tutor, la gi&agrave; citata architetto Tessa Matteini.Il parco 
        &egrave; molto ampio, suscettibile di iniziative ambientali, paesaggistiche, 
        sociali, culturali: &#147;L&#146;area ha una superficie di 175.000 mq&sup2;; 
        15 ettari dell&#146;area del nuovo parco hanno destinazione urbanistica 
        prevista a standard come area verde, attrezzature sportive e ricreative, 
        e risultano attualmente quasi totalmente acquisiti dal Comune attraverso 
        la cessione delle aree a scomputo degli oneri di urbanizzazione dei nuovi 
        quartieri residenziali. Invece 2,5 sono di propriet&agrave; della Curia 
        Vescovile &#150; Arcidiocesi di Milano e sono destinati cone area per 
        attrezzature religiose, civili e sociali: su questa superficie vicino 
        ad una piccola chiesa quattrocentesca verr&agrave; realizzata una nuova 
        chiesa&#148;. L&#146;area del futuro &#147;Parco dei Giganti&#148;, &egrave; 
        stata inclusa nel perimetro del Parco Agricolo Sud di Milano, istituito 
        quale Parco Regionale di Cintura Metropolitana della Legge Regionale n. 
        24 del 23 Aprile 1983: &#147;L&#146;ente Parco, in accordo con la Regione 
        Lombardia, ha promosso una serie di interventi di riqualificazione ambientale, 
        le cui finalit&agrave; principali sono: la forestazione d&#146;aree incolte, 
        la riqualificazione flogistica, la creazione di poli per la didattica 
        ambientale, la fruizione naturalistica, il rafforzamento dei corridoi 
        ecologici; per questo, ad esempio, si &egrave; pensato a rimodellare le 
        scarpate per il recupero dei fiumi e dei fontanili, oppure a creare e 
        riqualificare zone umide e superfici a canneto, o a porre in essere capanni 
        per l&#146;osservazione degli uccelli acquatici, e&#133;&#148;; Daniela 
        Borroni non accenna a fermarsi immaginando il parco che verr&agrave;: 
        &#147;Mi faccia sottolineare un ultimo aspetto: l&#146;area del Parco 
        dei Giganti assume, per cos&igrave; dire, il ruolo di appoggio per la 
        ricostruzione di una trama ecologica complessa, tesa a configurare un 
        continuum ambientale nor-sud, che comprenda la sistemazione dei parchi 
        rivieraschi della zona e delle sue oasi naturalistiche&#148;.&#147;Ha 
        presente i corridoi verdi?&#148;, chiede l&#146;architetto Borroni, proseguendo 
        senza ascoltare la mia risposta: &#147;Ecco, si tratta di concretizzare 
        a livello locale il grande progetto delle greenways, appunto quella rete 
        di percorsi verdi, che si pongono l&#146;obiettivo di migliorare il rapporto 
        tra uomo e territorio, contribuendo a ridare identit&agrave; e riconoscibilit&agrave; 
        a luoghi troppo spesso compromessi dalla crescita disordinata delle citt&agrave;. 
        Ora questi percorsi rappresentano un sistema di circolazione dolce, che 
        bene pu&ograve; sposarsi con le esigenza di dinamicit&agrave;: si tratta, 
        in definitiva, di realizzare vie di movimento per pedoni, ciclisti, persone 
        a cavallo, e anche corridoio di movimento per la fauna&#148;.Ma questo 
        progetto, a cui bene si presterebbe il Parco dei Giganti, va inquadrato 
        in un&#146;idea ancora pi&ugrave; grandiosa: &#147;Potrebbe crearsi un 
        collegamento con il nuovo parco agricolo della Vettabia (135 ettari di 
        cui 35 a foresta e 100 a parco che si estende fino alla foce della roggia 
        omonima a Melegnano, e comprende al suo interno la storica abbazia di 
        Chiaravalle e il depuratore di Nosedo). Un progetto di percorso verde 
        per l&#146;area agricola che si trova fra la via Emilia e il Lambro potrebbe 
        partire dal castello nel centro di Melegnano passando per il bosco di 
        Montorfano, Rocca Brivio, cascina Santa Brera, costeggiando il Lambro, 
        per Cascina Carlotta e per il nuovo parco di Zivido lungo l&#146;attuale 
        strada campestre, per la chiesa della Nativit&agrave; di Maria, il borgo 
        e il castello, per le altre cascine fino al borgo di Carpianello per riconnettersi 
        al sistema delle piste ciclabili comunali&#148;.Daniela Borroni ha proprio 
        una passione professionale per lo studio e la realizzazione di parchi, 
        e gi&agrave; per la sua tesi di laurea, al Politecnico di Milano, aveva 
        scelto di investire sul recupero di una struttura a verde dell&#146;Ospedale 
        psichiatrico &#147;Belle-Id&egrave;e&#148; di Ginevra: &#147;Dove ho compreso 
        quanto il limite tra ragione e follia sia davvero molto labile; ma, scherzi 
        a parte, ho avuto cos&igrave; modo di avvicinarmi alla progettazione e 
        alla storia dei giardini; da qui &egrave; scaturito il desiderio di realizzare 
        un progetto per la realizzazione di un grande parco urbano. Io l&#146;ho 
        immaginato molto articolato, non unitario, perch&eacute; le dimensioni 
        dell&#146;aerea interessata, per il &#147;Parco dei Giganti&#148;, sono 
        davvero notevoli.e. Penso, ad esempio, che possa essere utilizzato all&#146;interno 
        di un sistema di riqualificazione, nella pi&ugrave; ampia cornice del 
        Parco Sud, delle strade campestri e delle cascine, insomma del mondo agricolo; 
        le &laquo;stanze verdi&raquo; racchiudono anche spazi organizzati suddivisi 
        in orti familiari in affitto prendendo atto dell&#146;esigenza espressa, 
        e gi&agrave; presente nell&#146;aerea in forma abusiva e disordinata, 
        del desiderio di spazi semiprivati per attivit&agrave; orticole e di giardinaggio; 
        possono inoltre essere inserite attrezzature per feste e pic-nic, oltre 
        ad un&#146;area dedicata appositamente ai giochi dei bambini; ma pu&ograve; 
        essere pure un giardino di quartiere, perch&eacute; nelle zone adiacenti 
        del Parco vi &egrave; stata una notevole espansione edilizia, la quale 
        sarebbe avvantaggiata da questo straordinario polmone verde. Non trascurerei, 
        di conseguenza, la fruizione culturale e ricreativa dell&#146;ambiente 
        da parte dei cittadini&#148;. Oltre al progetto e all&#146;immaginazione, 
        non va dimenticato che Daniela Borroni ha scritto una tesi per un collegio 
        di docenti che &#150; si suppone &#150; del Sud Milano non conoscesse 
        quasi nulla, e cos&igrave; alcune parti del suo scritto sono evocative 
        della genesi della bassa pianura lombarda: &#147;E&#146; vero: ho effettuato 
        uno studio morfologico dell&#146;ambiente; ma non solo: ho voluto anche 
        tracciare un breve profilo storico architettonico del territorio; non 
        dimentichiamo che la struttura del borgo di Zivido ha origini medievali 
        e si &egrave; consolidata attestandosi al di sopra di un terrazzamento 
        del fiume Lambro e seguendone l&#146;andamento curvilineo. Permangono 
        a Zivido monumenti storici importanti: primo fra tutti il castello dei 
        marchesi Brivio con la torre viscontea. La strada che attraversa il borgo 
        va ad attestarsi sulla chiesa quattrocentesca di Santa Maria della Nativit&agrave;, 
        costruita su una precedente cappella&#148;.E&#146; in questa parte di 
        rivisitazione storica, che Daniela Borroni, anche servendosi di articoli 
        di stampa, ha spiegato ai docenti la battaglia del 1515: &#147;Il 13-14 
        settembre 1515 il ventenne Francesco I, discendente dei Visconti e da 
        poco sovrani di Francia, scaten&ograve; i 12.000 cavalieri e i 18.000 
        fanti della sua armata contro le truppe confederate: ventimila milanesi, 
        spagnoli, veneziani e papalini unitisi per contrastare, invano, la conquista 
        francese della Lombardia. I Giganti che 474 anni fa si affrontarono sul 
        campo di Melegnano erano da una parte i migliori cavalieri di Francesco 
        I, dall&#146;altra i mercenari svizzeri, alleati ai confederati, che avanzavano 
        impavidi facendosi scudo con le loro lunghe lance. Melegnano (denominata 
        dai transalpini anche Marignano) rappresenta per i francesi una localit&agrave; 
        importante perch&eacute; indica di fatto l&#146;inizio della loro supremazia 
        in Europa occidentale, che si prolungher&agrave; fino all&#146;Ottocento. 
        Per gli svizzeri &egrave; ancora pi&ugrave; importante perch&eacute; segna 
        l&#146;inizio della loro celebre neutralit&agrave;. Fu l&#146;ultima volta 
        che soldati regolari svizzeri, inviati da vari Cantoni, scesero in campo 
        dietro la bandiera rossocrociata. Dopo la sconfitta di Melegnano, infatti, 
        i mercenari continuarono a combattere ma solo sotto altri vessilli: quello 
        del Re di Francia, quello del Papa. Il sogno di un&#146;espansione elvetica 
        era tramontato per sempre&#148;.I segni della storia, anche simbolici, 
        sono tuttavia rimasti, nel territorio di Zivido, e l&#146;idea &egrave; 
        quella di valorizzarli anche all&#146;interno del Parco: &#147;Il valore 
        simbolico di questo luogo deriva dalla sua storia, dalla feroce battaglia 
        che qui avvenne secoli fa. Ma il conflitto oggi &egrave; soprattutto locale, 
        tra la nuova espansione urbana che segue dinamiche estraneo al territorio 
        storicizzato in cui va ad inserirsi e questo paesaggio agricolo su cui 
        permangono i segni e i progetti che la collettivit&agrave; nella sia evoluzione 
        ha impresso sul territorio&#148;.Da qui la genialit&agrave; di realizzare 
        una planimetria del parco che sapesse rievocare la sfida: &#147;Elemento 
        centrale o &egrave; il grande semicerchio di prato di due ettari e mezzo. 
        Il luogo evoca lo scontro tra due parti in opposizione che si fronteggiano, 
        l&#146;accerchiamento contro un ordinato schieramento. Le parti vengono 
        simboleggiate da un semicerchio di bosco coi suoi avamposti regolari di 
        carpino e da un filare di farnie sull&#146;opposta postazione. Al centro 
        il campo di battaglia: come lance, simbolo dello scontro, in uno spazio 
        privo di centro, sulla superficie a prato si incrociano e sovrappongono 
        fasce di pavimentazione diversa. L&#146;ampio semicerchio &egrave; racchiuso 
        dal bosco, che isola il cuore del parco dagli impianti visivi, acustici 
        e inquinanti.&#148; Proprio sull&#146;implementazione, arricchimento e 
        gestione del bosco, l&#146;architetto Borroni dedica alcune pagine della 
        sua tesi, rivelandosi quale coraggiosa botanica: &#147;Nello strato arboreo, 
        vi sono numerosissime specie. E&#146; inutile elencare le funzioni, che 
        sono tantissime, cui un impianto vegetale deve assolvere. Certo, il rimboschimento 
        impone anche degli obblighi: quello di tipo estensivo, che prevede operazioni 
        semplici e poco costose, anche per la manutenzione, costringerebbe ad 
        un accesso sul suolo da parte del pubblico dopo 10 anni dall&#146;intervento. 
        D&#146;altra parte il bosco &egrave; l&#146;elemento pi&ugrave; complesso 
        e raro nella &laquo;foresta urbana&raquo;, e anche questo va sottolineato&#148;.L&#146;augurio 
        &egrave;, dunque, che oltre al Comune di San Giuliano Milanese, e all&#146;Associazione 
        Culturale Zivido, gli stessi abitanti del territorio e gli amanti della 
        natura sappiano proteggere questo bosco, sapendo sempre coniugare storia, 
        tradizioni, e paesaggio.</font></p>
