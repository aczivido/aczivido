        letterine dove per avere la risposta dovranno inserire nome, cognome, 
        data, ed et&agrave;. Per i giovanissimi insomma, &egrave; tempo di prendere 
        carta e penna ed iniziare a mettere nero su bianco. Un occhio quindi al 
        programma. A partire dalle 15 l&#146;appuntamento &egrave; in via della 
        Vittoria e nel cortile Rota Bragherio, dove all&#146;esposizione di prodotti, 
        si alterneranno la fiera degli animali da cortile, e la riproduzione di 
        alcuni mestieri dell&#146;epoca. Mentre i bambini potranno cimentarsi 
        in giochi rinascimentali presso la nuova piazza dell&#146;oratorio, E 
        spostandosi di qualche centinaia di metri, sempre alle 15, presso il campo 
