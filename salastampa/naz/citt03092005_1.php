        le novit&agrave;, con la fiera degli animali da cortile e la dimostrazione 
        di mungitura. Inoltre, per la prima volta entrer&agrave; in scena la fata 
        dei boschi: un personaggio protagonista delle vecchie credenze popolari, 
        che prender&agrave; parte alla festa per ascoltare i pi&ugrave; piccoli. 
