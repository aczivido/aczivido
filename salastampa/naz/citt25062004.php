        che settimana scorsa l'iniziativa &egrave; stata illustrata all'amministrazione 
        comunale uscente. Un lavoro che &egrave; proseguito ininterrottamente, 
        nonostante il periodo elettorale abbia distolto l'attenzione da tutto 
        ci&ograve; che non fossero programmi e comizi.Ma in questo caso il circuito 
        &egrave; scattato anni fa. E ora sembra avere le carte in regola per trasformarsi 
        in realt&agrave;, anche se per l'inaugurazione del lavoro completo 
        occorrer&agrave; attendere alcuni anni. Il primo tassello a disposizione 
        della cittadinanza sar&agrave; l'arena, che dovrebbe ospitare la 
        rievocazione storica, ma anche eventi di grande richiamo. In attesa che 
        il puzzle inizi a diventare concreto, a disposizione di tutti gli interessati, 
        sul sito dell'associazione (www.aczivido.net) &egrave; possibile 
        iniziare ad addentrarsi in questo creativo circuito, dove anche l'acqua 
        avr&agrave; il suo ruolo ornamentale, con un laghetto, una fontana e una 
