        <font face="Verdana, Arial, Helvetica, sans-serif" size="1">04.05.2005 
        - di Domenico Palumbo<br>
        <br>
        <b><font size="2">I Giganti preparano la Battaglia</font></b><br>
        <br>
        Sono finalmente iniziati a Zivido i lavori di costruzione del Parco dei 
        Giganti, un'opera da otto milioni di euro che trasformer&agrave;, nel 
        giro di poco tempo, un'area di 175mila mq in un'ampia arena dove verr&agrave; 
        rievocata la storica Battaglia dei Giganti, combattuta nel 1515 fra francesi 
        e svizzeri. La rievocazione a San Giuliano Milanese avviene gi&agrave; 
        da molti anni a settembre con una sfilata di personaggi in costume e armature 
        dell'epoca medievale, che lungo le strade della frazione attirano sempre 
        moltissima gente. &quot;Finalmente si avvera un sogno&quot;, ha esclamato 
        Pierino Esposti quando ha visto arrivare nell'area designata le prime 
        ruspe e i primi cantieri di lavoro.<br>
        Pierino Esposti &egrave; il principale promotore della rievocazione della 
        Battaglia, l'uomo che pi&ugrave; di tutti si &egrave; battuto, insieme 
        all'Associazione Culturale Zivido da lui stesso creata, per la realizzazione 
        a Zivido del Parco dei Giganti. L'incarico dei lavori &egrave; stato affidato 
        dall'Amministrazione comunale all'Arch. Daniela Borroni, che recentemente 
        &egrave; stata all'Universit&agrave; di Firenze per il suo progetto davvero 
        innovativo riguardante il Parco dei Giganti. Lungo la via Corridoni, a 
        fianco dell'antica chiesetta, si vede gi&agrave; l'impresa lavorare con 
        una certa alacrit&agrave; per terminare i lavori al pi&ugrave; presto 
        possibile. L'idea &egrave; quella di costruire subito l'arena dove verr&agrave; 
        rievocata a settembre prossimo la Battaglia trasferendo in questo posto 
        i personaggi in costume. L'arena ha un'area di 50mila mq. e potr&agrave; 
        ospitare servi, punti di ristoro, alberi e vialetti. Il progetto dell'Architetto 
        Borroni prevede che l'opera completa del Parco dei Giganti possa essere 
        sede culturale e turistica di primo piano, non solo per San Giuliano ma 
        anche per i visitatori e turisti.<br>
        Per settembre, durante la manifestazione storica, sono attese importanti 
        personalit&agrave; francesi e svizzere che terranno anche dei convegni 
        molto interessanti presso la sala Previato. Da qualche anno anche Sesto 
        Ulteriano e Borgolombardo sono coinvolte nella rievocazione cinquecentesca. 
        A Sesto sar&agrave; propsta la Fiera dei Giganti, giunta alla quinta edizione, 
        con il compito di ricostruire l'ambiente popolare proprio del '500 quando 
        i contadini erano soliti ritrovarsi alla fiera per l'acquisto delle merci. 
        La famosa &quot;Strega&quot; sar&agrave; quest'anno sostituita dalla &quot;Fata 
        dei boschi&quot; con la partecipazione dei ragazzi delle scuole che leggeranno 
        alcuni &quot;pensierini&quot; da loro stessi ideati e premiati da un'apposita 
        commissioe. A Borgolombardo in giugno, durante la festa patronale di SS. 
        Pietro e Paolo, verr&agrave; riproposta, come gi&agrave; l'anno scorso, 
        la manifestazione &quot;Alla corte ducale&quot; allo scopo di rievocare 
        l'epoca vissuta dagli Sforza a Milano.<br>
        Insomma, fervono i preparativi per la Battaglia dei Giganti. L'Associazione 
        Culturale Zivido, unitamente al suo presidente, Pierino Esposti, non tralascia 
        niente per completare al meglio i preparativi anche con la partecipazione 
        delle frazioni e dei comuni cittadini. Sar&agrave; uno spettacolo tutto 
        da vedere.</font></p>
