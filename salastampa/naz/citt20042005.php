        <br>
        Il Parco dei Giganti &egrave; decollato con un segnale concreto. Delimitata 
        l'area dalle cesate del cantiere, dove sono in circolazione ruspe e macchinari 
        al lavoro, l'arena di circa 40 mila quadrati si appresta infatti a diventare 
        realt&agrave;. Nel mese di giugno le opere dovrebbero infatti concludersi. 
        E in settembre il tratto locale inserito nell'appezzamento di 175 mila 
        quadrati che ospiter&agrave; il parco tematico, sar&agrave; pronto per 
        ospitare la rievocazione storica della Battaglia dei Giganti. Un evento 
        che taglier&agrave; il nastro al teatro naturale pensato per diventare 
        un palcoscenico anche per altre manifestazioni all'aperto.&laquo;Con questa 
        fase - spiega il presidente dell'associazione culturale Zivido, Pierino 
        Esposti - inizia a concretizzarsi un piano a cui lavoriamo da anni. Il 
        prossimo passaggio prevede il conferimento dell'incarico all'architetto 
        Daniela Borroni per la stesura del progetto esecutivo. Poi &egrave; prevista 
        una massiccia fase di piantumazioni per la creazione di un'area boscosa, 
        con piante autoctone&raquo;.Si tratta infatti del primo step di un disegno 
        vasto, che oltre ad una serie di riferimenti storici dedicati alla Battaglia 
        di Marignano, in cui svizzeri e francesi si fronteggiarono proprio in 
        quella piana di Zivido, prevede anche la valorizzazione di elementi naturali 
        come l'acqua, accanto a un percorso culturale per rendere l'area fruibile 
        al massimo, come ad esempio un punto di ristoro, piuttosto che una recinzione 
        che garantisca un parco protetto dai vandali nelle ore notturne. Tutti 
        aspetti che il sodalizio affronter&agrave; nei prossimi incontri con l'amministrazione 
        comunale. Ma intanto &egrave; gi&agrave; scattata la stesura di un calendario 
        per il grande appuntamento che si terr&agrave; in settembre, con un' anticipazione 
        a Borgolombardo nell'ultimo fine settimana di giugno.&laquo;Quest'anno 
        - spiega Esposti - sar&agrave; un evento particolare. Presenteremo una 
        rievocazione storica che funger&agrave; anche da taglio del nastro per 
        l'arena. Mentre a Borgolombardo, che si trasformer&agrave; in una corte 
        rinascimentale, &egrave; prevista la ricostruzione di un matrimonio dell'epoca 
