        ansa con una depressione naturale. Mentre i gradoni saranno in terra battuta 
        delimitati da travetti in legno. Un primo tassello intorno al quale verr&agrave; 
        realizzato un enorme polmone verde che dar&agrave; ampio respiro all'antica 
