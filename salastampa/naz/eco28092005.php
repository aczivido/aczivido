        <br>
        &quot;Tornano i Giganti&quot; ma questa volta sotto una pioggerellina 
        che non promette nulla di buono e la tradizionale rievocazione storica 
        si &egrave; svolta in un &quot;Parco dei Giganti&quot; ridotto un po' 
        a palude.<br>
        Insomma il tempo non è certo stato clemente e non ha favorito il classico 
        appuntamento con la storia che è giunto così alla sua quindicesima edizione 
        nel 490° anniversario della Battaglia dei Giganti svoltasi a Marignano 
        nel settembre del 1515. Nonostante questo per&ograve;, i personaggi in 
        costume, la sfilata e gli sbandieratori hanno permesso un pomeriggio diverso 
        e sono stati momenti di incontro fra i cittadini sangiulianesi e le autorit&agrave; 
        svizzere e francesi. Tutti conoscono la storia della storica battaglia: 
        il 13 e il 14 settembre 1515, nella piana di Zivido si scontrarono le 
        truppe francesi di Francesco I&deg; con i mercenari svizzeri; la disfatta 
        degli svizzeri fu grande, il numero di vittime altissimo. Fu per questo 
        che la battaglia di Marignano venne denominata &quot;dei Giganti&quot;, 
        per il numero di morti che si dovette pagare alla storia.<br>
        !Per gli svizzeri - ha ricordato il console generale del Paese elvetico 
        - quella fu l'ultima competizione, l'ultima battaglia sullo scacchiere 
        europeo a cui parteciparono gli svizzeri. Si andava delineando allora 
        la politica di neutralit&agrave;, dalla sconfitta gli svizzeri trassero 
        la salvezza. E' una linea che ancora oggi &egrave; un aspetto caratterizzante 
        della politica estera del nostro Paese&quot;.<br>
        Ma come hanno ricordato sia il sindaco Marco Toni, che l'assessore alla 
        cultura, Giovanna Bugada, &quot;questa &egrave; un'occasione di festa 
        non per ricordare la battaglia in s&egrave;. E' divenuta una prassi abituale 
        per educare alla tolleranza, alla pace, al dialogo che, seppure aspro, 
        serve a risolvere i problemi&quot;.<br>
