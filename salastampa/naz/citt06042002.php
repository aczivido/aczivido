        <b><font size="2">San Giuliano si gemella con Bussy Saint-George</font></b><br>
        <br>
        Un patto all'insegna della cultura tra San Giuliano e la cittadina francese 
        Bussy Saint-George. &Egrave; arrivato infatti il momento ufficiale del 
        gemellaggio tra queste due realt&agrave; industriali, poste entrambe nell'hinterland 
        di una grande metropoli. La manifestazione che sancir&agrave; l'intesa 
        volta allo scambio di iniziative che coinvolgeranno anche le scuole, si 
        terr&agrave; oggi durante una cerimonia in programma alle 12 nella sala 
        consiliare del comune. Tra i presenti ci sar&agrave; il sindaco Hugues 
        Ronedau, e altri rappresentanti dell'amministrazione di questa cittadina 
        posta ad una trentina di chilometri da Parigi, che conta 12 mila abitanti.<br>
        Un appuntamento che culminer&agrave; con la firma di un trattato bilaterale, 
        volto a creare un ponte di collaborazione tra le due citt&agrave;, accomunate 
        da vari elementi. Le iniziative proseguiranno poi alle 16.30 al cinema 
        Ariston, con la proiezione del film in lingua originale &quot;La bille 
        en tete&quot;, presentata dal regista sangiulianese Carlo Cotti.<br>
        Un commento per questa occasione arriva dal sindaco Marco Toni, il quale 
        spiega: &laquo;&Egrave; un'iniziativa di grande significato che vuole 
        unire ed avvicinare realt&agrave; tra loro apparentemente distanti. Solamente 
        valorizzando e promuovendo il rapporto, lo scambio e il confronto tra 
        culture e tradizioni differenti si interpreta meglio il processo di unificazione 
        europea che &egrave;, in primo luogo, unione di cittadini, popoli e comunit&agrave;&raquo;. 
        La delegazione francese era gi&agrave; stata ospitata a San Giuliano nel 
        settembre scorso in occasione del programma della Battaglia dei Giganti. 
        Una tour di alcuni giorni in cui erano state inserite alcune tappe significative, 
        che comprendevano sia alcuni servizi erogati dal comune, sia realt&agrave; 
        produttive che rappresentano ormai una tradizione per il settore locale, 
        come l'industria dolciaria Bindi. Nuove sinergie quindi destinate a coinvolgere 
        tutta la cittadinanza con proposte volte a spaziare dai confini territoriali.</font></p>
