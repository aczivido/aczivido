        <br>
        <b><font size="2">Il Duca di Milano anticipa i Giganti</font></b><br>
        <br>
        Tutto &egrave; pronto per domenica prossima, 26 giugno, a Borgolombardo 
        per la seconda edizione de &quot;Alla Corte Ducale&quot;, manifestazione 
        storica rinascimentale organizzata in occasione della Festa patronale 
        dei Santi Pietro e Paolo di mercoled&igrave; 29 che anticipa di un paio 
        di mesi la rappresentazione dell'ormai tradizionale &quot;Battaglia dei 
        Giganti&quot; in programma a Zivido a met&agrave; settembre prossimo.<br>
        Come al solito, Pierino Esposti, &quot;patron&quot; della Battaglie e 
        Presidente dell'Associazione Culturale Zivido che da oltre un decennio 
        cura con particolare dedizione la sontuosa rievocazione dei Giganti del 
        1515, ha posto ogni attenzione affinch&egrave; la Corte Ducale di Borgo 
        venga attuata in modo altamente spettacolare con una sceneggiatura mozzafiato.<br>
        Tutto il paese &egrave; stato mobilitato per questo avvenimento grazie 
        anche alla collaborazione dei giovani della parrocchia e dell'Amministrazione 
        comunale di San Giuliano Milanese.<br>
        Ci saranno re e regine, principi e principesse, paggi e paggetti, rullo 
        di tamburi preceduti dallo sventolio degli sbandieratori e, dulcis in 
        fundo, una ricca Dama Vivente che nella passata edizione sul sacrato della 
        chiesa impegn&ograve; moltissimo il sindaco Marco Toni contro un principe 
        della Corte Ducale. Ci sar&agrave; anche il Duca di Milano, Massimiliano 
        Sforza, accompagnato dalla cugina Isabella d'Aragona.<br>
        Il corteo prender&agrave; il via alle ore 16,30 e si snoder&agrave; lungo 
        le vie del paese soffermandosi brevemente davanti alle vetrine dei negozi 
        opportunamente addobbati con allestimenti scenici dell'epoca, cos&igrave; 
        come tutte le facciate degli stabili e delle case sono state trasformate 
        in palazzi rinascimentali.<br>
        Un'ora dopo ci sar&agrave; lo spettacolo &quot;Sponsalia&quot; in cui 
        teatro e musica si fondono insieme a danze dell'epoca con la celebrazione 
        di un matrimonio cos&igrave; come avveniva nel '500 con danzatori, intrattenimenti 
        poetici e contrasti.<br>
        Alle ore 18,30 l'evento pi&ugrave; atteso con lo svolgimento della Dama 
        Vivente in cui la sfida viene riprodotta su una grande scacchiera animata 
        dai giovani del paese con nuovissimi costumi nei colori ducali ideati 
        dalle sarte Anna e maria Baldon.<br>
        Alle ore 21,00 &egrave; prevista una serata danzante nel cortile dell'oratorio 
        in cui i cittadini di Borgolombardo, nei loro costumi non rinascimentali 
        ma quelli solitamente indossati nel giorno di festa, potranno dare libero 
        sfogo alle loro virt&ugrave; di ballerini al ritmo di un valzer o di una 
        mazurca, cos&igrave; come avviene in una comune balera.</font></p>
