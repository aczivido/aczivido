        <font face="Verdana, Arial, Helvetica, sans-serif" size="1">14.09.2005 
        - di Domenico Palumbo<br>
        <br>
        Personalit&agrave; estere, due consoli e autorit&agrave; civili assisteranno 
        domenica 18 all'evento<br>
        <b><font size="2">La Battaglia dei Giganti entra nell'Arena</font></b><br>
        <br>
        Dopo Borgolombardo, domenica 26 giugno, e Sesto Ulteriano, domenica scorsa, 
        11 settembre, &egrave; la volta di Zivido, domenica prossima, 18 settembre. 
        La Battaglia dei Giganti entra nel cuore della sua storia, meglio sarebbe 
        dire che i Giganti francesi e svizzeri quest'anno, per la prima volta, 
        si affronteranno nel luogo giusto, la Piana di Zivido, molto simile a 
        un'arena, di cui verr&agrave; effettuata l'inaugurazione, in cui si combatterono 
        tra di loro 490 anni fa, nel 1515, provocando migliaia di morti rimasti 
        nell'ignoto. Per quasi quattro secoli questo epico scontro militare &egrave; 
        stato completamente ignorato dagli storici fino a quando, alla fine dell'800, 
        un monaco, scartabellando le scritture d'archivio del suo convento, non 
        s'accorse di questo terribile episodio informando subito le autorit&agrave; 
        competenti. La Battaglia dei Giganti, combattuta fra i soldati francesi 
        di Francesco I e i mercenari svizzeri assunti dal Duca di Milano, venne 
        alla luce in tutta la sua crudelt&agrave;. Lo scontro fu vinto dai francesi 
        ma la Svizzera, da allora, non partecip&ograve; pi&ugrave; a nessuna guerra 
        dichiarandosi neutrale a qualsiasi conflitto.<br>
        Se un monaco ebbe il merito di &quot;scoprire&quot; questo funesto episodio 
        del '500, si deve all'Associazione Culturale Zivido e al dinamismo del 
        suo presidente, Pierino Esposti, se la Battaglia dei Giganti, detta anche 
        la Battaglia di Marignano, non &egrave; caduta nuovamente nel dimenticatoio 
        organizzando tutti gli anni a settembre, quest'anno &egrave; la 15&deg; 
        edizione, una ricostruzione fedele dello storico avvenimento. Domenica 
        prossima un centinaio di figuranti, soldati armati, cannoni, sbandieratori, 
        popolani ecc., tutti rigorosamente vestiti con costumi dell'epoca, sfileranno 
        nel pomeriggio per le strade antiche di Zivido per poi entrare nell'arena 
        costruita apposta per questa ricorrenza e di cui si spera nel futuro possa 
        diventare, sotto il nome di Parco dei Giganti, un luogo d'attrazione anche 
        per il turismo. Due gruppi teatrali, &quot;Reno Castello d'Argile&quot; 
        e &quot;Compagnia del Lupo passante di Monselice&quot;, si affronteranno 
        nell'arena con fucili e colpi di cannone sparati a salve davanti al pubblico 
        e alla presenza delle autorit&agrave; civili francesi, svizzere e italiane 
        (saranno presenti i Consoli di Francia e Svizzera). Il sindaco di San 
        Giuliano Marco Toni, insieme ai rappresentanti ospiti, fra cui alcuni 
        assessori regionali, inaugurer&agrave; la nuova struttura e dar&agrave; 
        il via alla manifestazione. Per ricordare i 40 anni dell'inaugurazione 
        del monumento ai caduti svizzeri situato nei pressi della chiesetta di 
        Zivido, un'opera di Josef Bisa, il Consolato Generale di Svizzera a Milano, 
        l'Agenzia della Confederazione elvetica &quot;Presenza Svizzera&quot; 
        e l'Associazione Culturale Zivido, hanno realizzato una serie di quattro 
        suggestive cartoline commemorative raccolte in un contenitore. Sabato 
        17, alle ore 17,00, nella Sala Previato, ci sar&agrave; una conferenza 
        internazionale dal titolo &quot;Il Signore di Bayard, l'ultimo Cavaliere&quot; 
        con tre temi diversi illustrati dalla Dott.ssa Giuliana Fantoni, da Andr&eacute; 
        Heurtaut e da Marcel Fakhoury. In serata, alle ore 21,00, &egrave; previsto 
        un concerto di musiche rinascimentali eseguite dal gruppo internazionale 
        &quot;Le Rosignol&quot; dal titolo &quot;Musiche de diversi autori nella 
        magnifica excellentissima Corte di Massimiliano Sforza&quot;<br>
        &quot;Quest'anno abbiamo allestito un programma davvero eccezionale&quot;, 
        sorride Pierino Esposti, &quot;patron&quot; della manifestazione, &quot;con 
        molti ospiti e tanto spettacolo che speriamo attiri molta gente a Zivido&quot;.</font></p>
