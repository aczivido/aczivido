        <br>
        Pierino Esposti, presidente dell'associazione di Zivido, sprona a 
        incentivare il turismo<br>
        <b><font size="2">Rocca Brivio pu&ograve; diventare una miniera d'oro</font></b><br>
        <br>
        C'&egrave; modo di valorizzare Rocca Brivio? A chiederselo &egrave; Pierino 
        Esposti, presidente dell'Associazione Culturale Zivido, da sempre in prima 
        linea per difendere la cultura e la storia della citt&agrave;. &laquo;Piuttosto 
        isolata nella campagna, un tempo come oggi - afferma - Rocca Brivio fatica 
        ad essere percepita come parte di un tessuto vivo e comune&raquo;. L'acquisizione 
        del complesso monumentale da parte dei tre Comuni San Donato, San Giuliano, 
        Melegnano e l'associazione Roccabrivio, avvenuta nel 1997, non fu adeguatamente 
        motivata ai cittadini. &laquo;L'indifferenza regna sovrana -continua Esposti 
        - e non mancano dubbi sui propositi che si presentano. Coloro che si accingono 
        a visitare la Rocca, lo vedono come un luogo &quot;spento&quot;, senza 
        vita, quasi abbandonato. Un complesso monumentale di tali dimensioni, 
        com'&egrave; Rocca Brivio comporta necessariamente un sensibile impegno 
        economico per la sua manutenzione e gestione&raquo;. Il dilemma &egrave;: 
        uso appropriato o pi&ugrave; comodamente un condominio? Come si possono 
        scoprire le differenze significative tra i luoghi, soprattutto in quest'epoca, 
        in cui la globalizzazione appiana le distinzioni geografiche?<br>
        Secondo Esposti, &laquo;Rocca Brivio, cos&igrave; come possiamo ammirarla 
        oggi, &egrave; il risultato di un intervento materiale e culturale del 
        XVII secolo: ha una identit&agrave; precisa. Proprio su tale specifica, 
        ed unica, identit&agrave; occorre lavorare individuando il suo pi&ugrave; 
        idoneo uso, promozione e ricavo economico. Agendo insieme alle forze economiche 
        di attrazione e repulsione, le selezioni culturali sono sempre all'opera, 
        e si riflettono sui risultati economici. Al giorno d'oggi il turismo, 
        e quello culturale in maggior misura, &egrave; la pi&ugrave; grande industria 
        del mondo: il turismo condiziona i beni durevoli, e tale condizionamento 
        ovviamente dipende da fattori locali. Rocca Brivio possiede, oltre all'edificio 
        monumentale centrale, anche gli spazi e le costruzioni compresi nelle 
        cosiddette &quot;pertinenze&quot; possono divenire un unicum culturalmente 
        ed economicamente interessanti per le attivit&agrave; future. Ecco che 
        nelle pertinenze avranno collocazione bar, ristorante, punto vendita di 
        oggettistica e pubblicazioni cartacee o informatiche, foresteria, magazzini. 
        L'esperienza consolidata dei pi&ugrave; famosi ed illustri musei francesi, 
        che vengono copiati ora anche in Italia, fanno testo. L'ambiente circostante, 
        l'arte e la storia locale, le tradizioni, le progettualit&agrave; della 
        vita futura, i cittadini di ogni ordine e grado, devono trovare in Rocca 
        Brivio il punto naturale di convergenza e, perch&eacute; no, anche di 
        svago, il motore propulsore per contribuire a costruire un futuro migliore, 
        valorizzando il passato, dove i cittadini si sentano orgogliosi della 
        propria identit&agrave; e pronti a divenire consapevoli cittadini del 
        mondo. Ci&ograve; &egrave; possibile? Ne sono convinto, basta volerlo&raquo;.</font></p>
