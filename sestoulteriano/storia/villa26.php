      <p align="JUSTIFY"> <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>APPENDICI<br>
        I Visite di S.E. Il Cardinale Ildefonso Schuster agli scavi in corso 
        nella Basilica degli Apostoli</b><br>
        Una improvvisa visita compiva S.E. Il Cardinale A.I. Schuster, Arcivescovo 
        di Milano, il pomeriggio del 7 ottobre 1941.<br>
        Non inaspettata, poich&eacute; da tempo aveva espresso il desiderio di 
        portarsi a constatare quanto si stava compiendo in san Nazaro.<br>
        Lesto, come sempre, volle osservare ogni cosa, chiese informazioni, notizie, 
        il perch&egrave; di taluni lavori; il signor Prevosto lo soddisfece.<br>
        Avrebbe voluto scendere nelle umide e strette gallerie praticate nel sottosuolo 
        della Basilica, ma la tarda ora ed i timori del segretario non lo permisero.<br>
        Accomiatandosi disse: torner&ograve; presto, nel frattempo trovatemi il 
        Corpo di san Matroniano.<br>
        Una seconda visita, desiderata, la comp&igrave; il pomeriggio del 28 ottobre 
        1841. Erano ad attenderlo in piazza san Nazaro il signor Prevosto ed i 
        suoi Coadiutori, il prof. Aristide Calderini ed il dott. Alberto De Capitani 
        d'Arzago, promotori della &quot;Forma urbis mediolani&quot;, e una rappresentanza 
        della Fabbriceria della Basilica.<br>
        Una prima sosta avvenne sulla stessa piazza di san Nazaro da tre mesi 
        sottoposta a sistematici assaggi e scavi con meraviglia e curiosit&agrave; 
        dei passanti, i quali sl sentire che si stavano cercando degli autentici 
        mattoni romani, pensavano ad uno scherzo, per&ograve; di ottimo gusto 
        se S.E. Il Cardinale volle pur lui, archeologo per istinto, rimirare mattoni, 
        porzioni di pavimento a rozzo mosaico, pezzi di intonaci affrescati - 
        raccolti siccome reliquie - cossi, monete, etc.<br>
        Il prof. Calderini illustr&ograve; a S.E. Il Cardinale gli avanzi, pochi 
        in vero, di alcune probabili edicole funerarie.<br>
        Nonostante la loro pochezza dovuta al pessimo sottosuolo milanese ed a 
        diverse precedenti manomissioni rivestono comunque per l'Archeologia Milanese 
        una importanza notevole.<br>
        La seconda sosta e pi&ugrave; prolungata si comp&igrave; agli scavi, eseguiti 
        dal Comitato Restauri Monumenti di Milano, della ridivenuta nota cappella 
        di san Lino - gi&agrave; da oggi non pi&ugrave; un comune e banale passaggio 
        - ed a quelli della vicina cappella di san Matroniano.<br>
        Il dott. Ferrari mostr&ograve; e segnal&ograve; a S.E. Il Cardinale la 
        curiosa pianta centrale della cappella di san Lino; gli affreschi rinvenuti; 
        le fondamenta e pochi ruderi in rialzato della vetusta cappella ad emiciclo 
        di san Matroniano, distrutta nel 1653.<br>
        L'importanza dei frammenti di queste mura non &egrave; indifferente; quando 
        se ne trovarono le prime tracce, coloro che assistevano all'opera di ritrovamento 
        erano soddisfatti, nonostante che la fitta e fine polvere coloriva di 
        un bianco candido gli abiti e la nera veste.<br>
        Successivamente Sua Eminenza, accompagnato dal Cavaliere del lavoro gr. 
        Uff. Ettore Moretti si portava ad ammirare il restauro quasi ultimato 
        dell'abside che d&agrave; su Largo Richini e gli scandagli compiuti all'abside 
        maggiore posta al capo della croce.<br>
        Ad ora tarda si accomiat&ograve; non prima di essersi congratulato per 
        il gi&agrave; fatto ed auspicando per il da farsi, rammentando che la 
        Basilica Apostolorum &egrave; tra gli edifici pi&ugrave; significativi 
        della Milano paleocristiana e non poche sorprese serber&agrave;.<br>
        Il 6 novembre alla solenne adunanza del Reali Istituto Lombardo di Scienze 
        e Lettere, l'Em.mo Cardinale Schuster tenne la prolusione per il nuovo 
        anno 1941-42.<br>
        Il tema: &quot;L'attivit&agrave; edilizia di sant'Ambrogio a Milano&quot;.<br>
        Non manc&ograve; il Cardinale di ricordare la Basilica degli Apostoli, 
        anzi annunci&ograve; le conclusioni delle due minuziose visite da lui 
        compiute agli scavi in corso.<br>
        Conclusione o meglio preannuncio di altre conclusioni che torna d'incoraggiamento 
        e stimolo a persistere nei lavori di studio, di ricerca, di restauro anche 
        se le difficolt&agrave; incontrate non furono poche e non sono tutte.<br>
        La certezza di rintracciare la costruzione di Ambrogio, che sino dagli 
        inizi arrise, non fu vana.<br>
        Il testo della prolusione venne integralmente pubblicato il 9 novembre 
        dal quotidiano cattolico &quot;L'Italia&quot;.<br>
        Nell'esordio cos&igrave; si espresse: &quot;L'aspetto sotto il quale voglio 
        presentare oggi sant'Ambrogio, &egrave; nuovo solo in parte. Infatti, 
        se a Milano domandate anche ad un ragazzo per via, chi ha costruito la 
        imponente mole della basilica ambrosiana, egli vi guarder&agrave; dapprima 
        come sorpreso dell'ingenua domanda, quindi vi risponder&agrave;. Sant'Ambroeus. 
        Per&ograve;, soltanto le pi&ugrave; recenti indagini nel campo dell'Archeologia, 
        come in quello della Patrologia Cristiana hanno potuto misurare la molteplice 
        attivit&agrave; edilizia del grande Vescovo, valutandone anche l'importanza 
        per la storia dell'arte.<br>
        In questo campo, dell'Archeologia milanese ancora in parte inesplorato, 
        le moderne ricerche sono ancora quasi agli inizi. Quella sfinge della 
        chiesa di san Lorenzo sulla via Ticinese, solo adesso si rassegna a svelarci 
        i suoi secreti.<br>
        Nell'area della Basilica Apostolorum sulla via Romana qualche recente 
        sondaggio ci ha dimostrato che l&igrave; uno studio sistematico del monumento 
        potrebbe essere coronato da liete sorprese archeologiche, sino magari 
        a ritrovare, almeno sino ad una certa altezza, le primitive mura della 
        pianta a croce equilatera della basilica paleocristiana&quot;.<br>
        Dopo aver descritto a lungo &quot;il nostro bel san Lorenzo&quot; passa 
        alla Basilica Apostolorum.: &quot;Mentre si stanno ancora ultimando i 
        restauri della Laurenziana, ecco che la Basilica degli Apostoli sulla 
        via Romana richiama nuovamente a s&eacute; l'attenzione dell'archeologo. 
        Dietro la maschera della Cappella trivulziana che le nasconde la faccia 
        e sotto le vesti variopinte con cui l'hanno camuffata, in questi ultimi 
        quattro secoli, essa cela ancora importanti avanzi della sua costruzione 
        primitiva. Non siamo ancora in grado di determinare quali e quante parti 
        dell'ossatura di san Nazaro risalgono ancora a sant'Ambrogio; ma &egrave; 
        gi&agrave; constatato che queste parti originarie con un rozzo &quot;opus 
        spicatum&quot; ancora esistano, soprattutto della parte dell'abside.<br>
        Quella che noi oggi chiamiamo semplicemente san Nazaro, sant'Ambrogio 
        intitola invece Basilica Romana, dalla via consolare lungo la quale sorgeva.<br>
        Precedentemente, quella vasta area era stata adibita ad uso funerario. 
