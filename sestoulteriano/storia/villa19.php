    <td align="left" valign="top" width="75%"> 
      <p align="JUSTIFY"> <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>La 
        cappella di san Lino</b><br>
        Nella &quot;Chronica Archiepiscopi Mediolanensis&quot; scritta nel 1318, 
        si legge dell'Arciv. Arderico - eletto nel 936 - che fece costruire la 
        cappella di san Lino nella chiesa di san Nazaro, nella quale cappella 
        fu sepolto.<br>
        &quot;Costui fece costruire la Cappella di S.Andrea Apostolo presso il 
        muro rotto e la cappella di S.Lino nella chiesa di san Nazaro nella quale 
        fu sepolto&quot;.<br>
        Il parrocchiano pi&ugrave; assiduo della Basilica non saprebbe indicarci 
        dove sia o fosse stata.<br>
        Arderico deve averla costruita dietro la cappella di san Matroniano a 
        fianco dell'Altare Maggiore.<br>
        Nella Visita del 25 luglio 1567 si indica come dall'Altare Maggiore attraverso 
        ad una porta si passava: &quot;In una certa cappella molto oscura, cos&igrave; 
        da sembrare pi&ugrave; un passaggio che una cappella. In questa cappella 
        vi &egrave; un altarino sotto l'invocazione di San Lino&quot;.<br>
        Una porta, continua la descrizione, metteva in comunicazione la cappella 
        con un porticato della canonica. Non basta l'averla umiliata s&igrave; 
        da paragonarla ad un passaggio, la parte superiore serv&igrave; per collocarvi 
        i mantici dell'organo posto nell'ampia parete del lato dell'Epistola dell'Altare 
        maggiore.<br>
        Il canonico Carlo Torre, in Ritratto di Milano nel tentativo di dimostrare 
        che la Basilica nello stato che egli la contemplava, siamo nel 1714, non 
        apparteneva alla costruzione voluta da Ambrogio ma: &quot;essere stata 
        eretta ne' tempi delle perdute buone Arte&quot;, cio&egrave; nel periodo 
        romanico, ricorre alla cappella di san Lino eretta nel 940, architettonicamente 
        simile per il gioco della volta, delle sagome e decorazioni alla Basilica, 
        per dimostrare che la Basilica, come egli la contemplava, non poteva essere 
        del periodo di sant'Ambrogio.<br>
        &quot;E se ne volete una veridica prova considerate entro del Verone, 
        che apre il passo dal Coro alla Calonica (canonica) la piccola Cappella 
        del manco lato situata, che fu da Orderico Cotta Arciv. Nostro fatta edificare 
        l'anno 940, ad onore di San Lino Papa, ch'ella trovasi sulla stessa architettura 
        della Chiesa con rozzi scarpellati marmi a figure; conchiudere adunque, 
        non poter essere quella stessa che fece Sant'Ambrogio edificare&quot;.<br>
        Ma a noi non resta che rimpiangere i rozzi scarpelati marmi a figure.<br>
        Una ordinanza di san Carlo, l'VIII, emessa il 1569, d&agrave; il colpo 
        di grazia: &quot;Si levi l'altare di San Lino, il titolo del quale con 
        suoi emolumenti e obblighi sin adesso, se trasferiscano ex nunc alla suddetta 
        Cappella Maggiore. La cappella di detto altare se imbianchi e resti solo 
        per transito&quot;.<br>
        L'aver ridotto a passaggio la cappella di san Lino, rese inutile la porticina 
        a fianco della cappella di san Matroniano, e permise che una pi&ugrave; 
        ampia costruzione della cappella di san Matroniano fosse realizzata.<br>
        L'architetto, che sappiamo fu Carlo Buzzi, non procedette a casaccio, 
        prese a modello la cappella del Corpus Domini sita nella parte destra 
        della navata di ponente per chi dal centro guardi l'altare di sant'Ulderico.<br>
        Il disegno attribuito al Pellegrini riproduce con chiarezza la planimetria 
        della cappella del Corpus Domini.<br>
        L'architetto della nuova cappella di san Matroniano lo si pu&ograve; accusare 
        di plagio, spiegabile; ripet&egrave; la cappella del Corpus Domini colla 
        sola variante d'occupare tutto lo spazio tra i due pilastri.<br>
        La simmetria perfetta sar&agrave; ottenuta col rifacimento dell'architetto 
        Pestagalli negli anni 1828-1830; la cappella del Corpus Domini a sua volta 
        sar&agrave; restaurata ed ampliata ad imitazione della cappella di san 
        Matroniano; scambio di paternit&agrave;.<br>
        Per procedere alla nuova costruzione, si rese necessario l'abbattimento 
        della vecchia cappella ad emiciclo che implic&ograve; la distruzione delle 
        preziose pitture descritte dal Puricelli; la centrale, come ho gi&agrave; 
        illustrato, venne salvata ed &egrave; la Madonnina sita sulla parete di 
        sinistra della navata di entrata.<br>
        Prezioso ricordo di ben pi&ugrave; preziosi affreschi, che ne testimonia 
        l'epoca.<br>
        Con le pitture si distrussero il muro perimetrale, la calotta e l'altare 
        consacrato. In questa demolizione il Corpo del santo Eremita sub&igrave; 
        un traslocco. La nuova cappella ebbe uno sviluppo a pianta rettangolare; 
        rettangolo formato da un quadrato e da due rettangoli laterali. Sul quadrato 
        si impost&ograve; snella la cupola ottagona con tamburo e lanternino, 
        sostenuta dalla parete di fondo e da due colonne di granito e sui due 
        rettangoli le volte a botte quale contrafforto alla cupola.<br>
        Sotto la cupola a ridosso della parete di fondo si costru&igrave; il nuovo 
        altare e sotto la mensa venne collocato il Corpo di san Matroniano.<br>
        Stando nel cortile della canonica e per chi transita lungo il viale alberato 
        congiungente Largo Richini con la via F.Sforza, l'esterno della cappella 
        con la cupola ottagona &egrave; facilmente visibile, perch&egrave; domina 
        sui basi tetti di casupole e catapecchie che le fanno corona.<br>
        Se si costru&igrave; una cappella ed un altare nuovo ad onore del santo 
        Eremita, lo fu per una maggior glorificazione e venerazione del prodigioso 
        Santo, anche se il lascito Spinola ne fu la causa. Infatti il &quot;Martyrologium 
        Mediolanensis Ecclesiae&quot;, utile catalogo compilato da Paolo Pietro 
        Bosca, ed edito il 1695 cos&igrave; si esprime: &quot;Negli anni precedenti, 
        reggendo la Chiesa Milanese l'Eminentissimo Card. Alfonso Litta fu restaurata 
        con costosi lavori la Cappella di San Matroniano e nell'Altare della stessa 
        Cappella - in eiusdem Sacelli - con magnifica parata del tempio e con 
        la venerazione di tutto il popolo milanese fu riposto il Corpo di San 
        Matroniano&quot;.<br>
        Alfonso Litta fu Arcivescovo di Milano dal 1652 al 1679, il restauro della 
        nostra cappella costitu&igrave; adunque la primizia del suo governo spirituale; 
        il Corpo di san Matroniano da lui riposto nell'altare nuovo non verr&agrave; 
        pi&ugrave; toccato, e qui si lo si deve ritrovare.</font></p>
