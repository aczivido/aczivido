                  <h2>Blitz a Rocca Brivio </h2>
                    <p>Mercoled&igrave; 21 novembre 2007 si &egrave; svolta l’Assemblea Straordinaria dei Soci della Rocca Brivio Sforza srl. Principale, ma non unico, argomento all’ordine del giorno &egrave; stata la modifica dell’art.8 dello Statuto della Societ&agrave; a seguito dell’entrata in vigore dell’art.1 c.729 Legge 296/06.<br>
  Erano presenti, oltre al Consiglio di Amministrazione, Tiziano Butturini, presidente di TASM spa Socio di maggioranza; Lucia Salvato, assessore di San Giuliano Milanese; Domp&egrave;, sindaco di San Donato Milanese; Tessarin, assessore della giunta Bellomo di Melegnano; il vice presidente dell’Associazione Roccabrivio.<br>
  Espletate le formalit&agrave; alla presenza del notaio si &egrave; passati al punto riguardante il nuovo assetto del Consiglio di Amministrazione della Rocca Brivio, anche se le nuove disposizioni di legge mantenevano il numero di cinque componenti compreso il presidente.<br>
  Nelle settimane precedenti si erano manifestate forti e continue pressioni da parte del presidente di TASM spa nei confronti dei propri rappresentanti eletti in Rocca affinch&eacute; presentassero le dimissioni. Fatto poi avvenuto.<br>
  Le consultazioni elettorali amministrative della scorsa primavera, con il passaggio dal centro sinistra al centro destra di numerosi e grossi comuni soci di Tasm spa, hanno certamente provocato qualche problema di equilibrio interno, situazione che ha coinvolto anche Rocca Brivio.<br>
  Le dimissioni dei Consiglieri e del Presidente, di nomina TASM spa, alle quali si sono aggiunte anche quelle del Consigliere di nomina dell’Associazione Roccabrivio, hanno provocato conseguentemente l’azzeramento dell’intero Consiglio di Amministrazione.<br>
  Come certamente concordato in precedenza, l’assessore Salvato, in rappresentanza di San Giuliano Milanese unica amministrazione del centrosinistra rimasta in Rocca, ha sussurrato al notaio il nome del nuovo presidente sangiulianese della Rocca Brivio Sforza srl.<br>
  Ceduta la presidenza della societ&agrave;, TASM spa ha voluto nominare, oltre ai suoi due nuovi Consiglieri, un suo candidato di fiducia nell’incarico di Direttore Generale.<br>
  San Donato Milanese e Melegnano, dopo lunga assenza hanno nuovamente un rappresentante in Consiglio di Amministrazione della Rocca sebbene entrambe le Amministrazioni si sono espresse chiaramente poco convinte della loro partecipazione nella societ&agrave; e nella gestione di un bene che non ha dato alcun vantaggio concreto alle rispettive cittadinanze.<br>
  Il Consiglio di Amministrazione uscente aveva iniziato il proprio lavoro nel novembre 2002 ereditando una Societ&agrave; “vittima di veti incrociati e di rivalse politiche che poco avevano a che fare con una sana gestione di un bene di propriet&agrave; pubblica” e con un bilancio pesantemente fallimentare. Inoltre si era trovato costretto a gestire onerose cause legali provocate da ben tre contenziosi in seguito alla “…malagestione e dell’insensatezza di alcune scelte messe in atto dal mio predecessore”.<br>
  Il duro lavoro del Consiglio di Amministrazione della Rocca Brivio Sforza srl in questi cinque anni ha portato a presentare il bilancio 2007 con un saldo attivo, fatto unico nella storia della Societ&agrave;, ma al Presidente non &egrave; stata concessa neppure la soddisfazione di firmarlo.<br>
  Obbligati alle dimissioni per giochi politici, i componenti del Consiglio di Amministrazione sono stati letteralmente ignorati dal socio di maggioranza TASM spa e dall’assessore rappresentante dell’Amministrazione sangiulianese, i quali, dimostrando cinismo e mancanza di buona educazione, non hanno proferito neppure parole formali di ringraziamento. </p></td>
