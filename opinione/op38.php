                  <h2>Il &quot;Duomo&quot; di Zivido</h2>
                    <p>Quasi tutte le parrocchie sangiulianesi hanno adeguate strutture per l'attivit&agrave; parrocchiale. Solo una, Zivido, ne era sprovvista.<br>
  Nella storica frazione, che da oltre un decennio si va popolando con ritmo sostenuto, le funzioni religiose dovevano essere celebrate nella piccola chiesa quattrocentesca. Le attivit&agrave; parrocchiali e quelle dedicate ai giovani dovevano accontentarsi di modeste e provvisorie strutture.<br>
  Con l'edificazione della nuova chiesa e del complesso parrocchiale, oggi anche Zivido pu&ograve; pensare al futuro con maggior serenit&agrave;.<br>
  Eppure pi&ugrave; di una voce di critica si è fatta sentire con riferimento a quest’opera necessaria per la storica frazione definendola, con malcelato livore denigratorio, il &quot;Duomo&quot; di Zivido.<br>
  E' opportuno ricordare che le amministrazioni sangiulianesi, succedutesi nel tempo, per lunghi anni hanno abbondantemente utilizzato le strutture parrocchiali per sopperire alla scandalosa mancanza di scuole e centri sportivi.<br>
  Ancora oggi queste amministrazioni fanno affidamento alle strutture parrocchiali per colmare la lacuna di spazi dedicati ai giovani ed alle loro attivit&agrave; visto il fallimento del Centro di Aggregazione Giovanile.<br>
  C'&egrave; un altro aspetto inquietante nella definizione: il &quot;Duomo&quot; di Zivido ed &egrave; la mediocrit&agrave; che domina la citt&agrave; di San Giuliano Milanese.<br>
  Basta osservare le piazze, il dozzinale arredo urbano, la sistemazione data ai tratti di copertura del Redefossi con opere dalle finalit&agrave; sconosciute e molte altre situazioni che sarebbe lungo elencare.<br>
  Il nuovo complesso parrocchiale di Zivido &egrave; il frutto dell’ingegno di due famosi e preparati architetti, Gabetti Roberto e Isola Aimaro, e si integra magistralmente con l’antico borgo rispettandone l’architettura e lo spirito.<br>
  La medesima cosa non si pu&ograve; dire riguardo il delittuoso sventramento ed abbattimento di antichissime abitazioni a pochi passi dalla quattrocentesca chiesetta per far posto a palazzine moderne con il beneplacito dell‘amministrazione comunale.<br>
  Ed ecco che ritorna con prepotenza la mediocrit&agrave;.<br>
  Si ha paura di tutto ci&ograve; che dimostra valore, di tutto ci&ograve; che emerge per la sua qualit&agrave;.<br>
  I nuovi barbari non smettono mai di essere fra noi.<br>
