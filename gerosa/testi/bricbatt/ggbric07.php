          <td align="left" valign="top"><p><br>
              <strong>La carovana di Venere</strong><br>
            <br>
Questo principe non sembra che avesse alcuna energia n&egrave; elevazione d'animo: egli spensieratamente portava il titolo di Duca, e in mezzo alla umiliazione propria ed alla miseria de' sudditi pensava a passar giocondamente il suo tempo. Donava feudi, donava regalie, regalava danaro e roba a tutti i suoi favoriti con profusione in guisa che aveva sempre l'erario esausto&quot;. (Verri)<br>
Poi divent&ograve; geloso del fratello duca di Bari, ed il Prato racconta che una volta vistolo pensoso appoggiato ad una finestra, lo apostrof&ograve; violentemente minacciandolo di morte: &quot;Io so che voi mirati de farvi duca de Milano, ma cavativelo dalla fantasia; ch&egrave; io vi prometto, alla fede di leal signore, che io vi far&ograve; morire&quot;.<br>
Peggio tocc&ograve; al vescovo di Lodi, Ottaviano Sforza suo zio: &quot;Unde a d&igrave; vigesimo primo di maggio, andando esso Vesco in compagnia del Duca de Barri in Castello a corteggiare il Duca, fu nel primo ponte da Sviceri preso et tosto da l&agrave; fu menato in rocca, dove, fu aspramente curlato&quot; (Prato), il che significa che con ammirevol rispetto all'abito ed all'et&agrave; gli fece dare degli strattoni di corda perch&egrave; cantasse su qualche macchinazione contro di lui.<br>
L'infelice prelato fu tenuto ben due settimane alla tortura, eppoi non risultando nulla a suo carico venne spedito in Germania: dietro questi provvedimanti c'era la mano pesante dell'infatuato cardinale di Sion che muoveva i fili di un fantoccio di duca &quot;sospettoso, ingiusto, depresso, timido, violento&quot; il quale non meritava certo di essere sovrano.<br>
Da tutti gli eventi storici e dagli episodi pi&ugrave; curiosi il Verri trae la morale, e siccome dicono che la storia si ripete, vale la pena di riportarne qualche pensiero: &quot;Gli uomini presso a poco sono sempre stati gli stessi; ma questo presso a poco &egrave; il vantaggio della generazione vivente. Invidii chi non sa la storia i tempi antichi. Benediciamo Dio noi di vivere in un secolo in cui le passioni ed i vizi degli uomini sono (almeno in apparenza) meno atroci, e meno sfacciatamente insultino la virt&ugrave;&quot;.<br>
Queste ed altre amabil cose si scrivevano circa duecento anni fa; se la morale possa calzare anche par i tempi nostri, confidiamo il giudizio alla arguzia del nostro lettore.</p></td>
