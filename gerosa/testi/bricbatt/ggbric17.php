          <td align="left" valign="top"><p><br><strong>Schermaglie diplomatiche</strong><br>
              <br>
&quot;Il buon Re di Francia Francesco I &#150; scrive il Verri &#150; radun&ograve; un&#146;armata formidabile e si prepar&ograve; a scendere egli stesso nell&#146;Italia&quot;. Pi&ugrave; che buono diremmo astuto come una volpe, bench&egrave; giovanissimo; fece credere a tutti che per quell&#146;anno 1515 non ci sarebbe stata guerra, ed intanto, pi&ugrave; o meno segretamente si dava &quot;ad apparecchiar le cose sue&quot;.<br>
Mentre in Italia si profilava una lega promossa dal cardinale Bibbiena fra il Papa, gli Svizzeri, il Duca di Milano, Spagna ed Impero, il Re di Francia stringeva amicizie ed alleanze con stati da considerarsi altrimenti pericolosi: l&#146;Inghilterra, le Fiandre, i Veneziani e perfino Genova, dove il doge Ottaviano Fregoso, nonostante di essere &quot; congiuntissimo di antica benivolenza&quot; al Papa, era passato tranquillamente dall&#146;altra parte.<br>
Gli Svizzeri erano particolarmente tenuti d&#146;occhio e la Francia, approfittando delle loro discordie interne, specie dell&#146;antagonismo capeggiato dalla frazione antivallese, iniziava tempestivamente delle segrete mene per staccarli dall&#146;alleanza col Papa e lo Sforza.<br>
La confederazione degli stati italiani aveva riservato al Pontefice &quot;luogo di entrarvi infino alla Domenica che si dice <span class="citazione">Laetare</span> della prossima Quadragesima&quot;; l&#146;anno prima il Papa de&#146; Medici aveva fatto discrete pressioni sul Re Luigi XII, il quale, pur avendo gi&agrave; un piede nella fossa, si sognava anche di notte del ducato di Milano. &quot;N&eacute; si dubitava per alcuno che il Re di Francia, cessato quasi in tutto il timore di essere molestato di l&agrave; dai monti, non havesse il consueto desiderio di ricuperare il Ducato di Milano, ma era incerto se havesse in animo di muover l&#146;armi subito, o differire all&#146;anno futuro, perch&eacute; la facilit&agrave; appariva presente, ma non apparivano i segni di preparatione: nella quale incertitudine il Pontefice, ancor che gli fusse molestissimo che il Re ricuperasse quello stato, lo confort&ograve; molto efficacemente che col differire non corrompesse le occasioni presenti&#133;&quot; (Guicciardini &#150; Libro XII)<br>
Di parere conforme era stato il Re Luigi, il quale pur essendo intento a pompe e spettacoli, come scrive il Guicciardini, &quot;desideroso di certificarsi dell&#146;animo del Pontefice, ma determinato qualunque &egrave; fusse di proseguire l&#146;impresa destinata, lo ricerc&ograve; che volesse dichiararsi in suo favore&quot;. Ma nonostante che tutto fosse pronto alla guerra, la morte del Re fece segnare il passo; Francesco I non desistette dal solleticare il Papa ed allora fu Leone stesso che gli mand&ograve; il vescovo di Tricarico a trattar segretamente l&#146;alleanza, pena la scomunica se il Re avesse fiatato. Entrambi i due volponi tenevano come si suol dire il piede in due scarpe; perfino con un matrimonio si tent&ograve; di unire il papato alla Francia, e fu quello di Giuliano de&#146; Medici fratello del Pontefice con Filiberta di Savoia, sorella della duchessa d&#146;Angoul&egrave;me, quindi zia di Francesco I.</p></td>
