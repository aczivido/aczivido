            <p><br>
              <strong>Gli Svizzeri in Milano</strong><br>
              <br>
              La prima settimana di settembre si pass&ograve; in citt&agrave; fra gran parate delle genti delle varie porte, le immancabili Messe de lo Spirito Santo, e suono di campane a festa e divozioni che culminarono il sabato, cio&egrave; il giorno &quot;de la Natividade di Nostra Donna&quot;. Il luned&igrave; invece giunsero gli Svizzeri, e si distribuirono nei borghi dapprima, ma ben presto si introdussero dentro le portee l'occuparon tutta, facendo dei gran conciliaboli, in San Marco, in Brera e nelle case dove alloggiavano. &quot;Parte aderiva a Maximiliano duca nostro, et parte al re di Franza, et parte voleano ritornare a casa&quot;.<strong> (1) </strong><br>
            In campo alleato tirava un po' di vento di fronda fra le truppe spagnole e quelle del Papa: dovevano in sostanza passare il Po e tagliare la strada ai Veneziani condotti dal duca d'Alviano, i quali stavano a Cremona con l'evidente proposito di congiungersi con l'armata dl Re di Francia.<br>
            Ognuno dei due voleva che passasse l'altro per primo, preferendo vedere inguaiato l'altro che s&egrave; stesso. &quot;Cominciarono dipoi il mezzo giorno a passare per lo ponte le genti Spagnuole, doppo le quali dovevano incontinente passare gli Ecclesiastici, ma avendo per lo sopravvenire della notte, differito necessariamente alla mattina seguente, non solamente non passarono, ma il Vicer&egrave; ritorn&ograve; con l'esercito di qua' dal fiume&quot;. Avevano odorato il vento infido: il giorno precedente, cento lancie dei Francesi erano riuscite a fare una puntata a Lodi e la sera stessa il duca d'Alviano vi giunse con il suo esercito e vi si stabil&igrave; saldamente.<br>
          In virt&ugrave; dell'accordo segnato a Gallarate e che per&ograve; era ancora assai nebuloso, il Re di Francia doveva versare subito agli Svizzeri la somma pattuita perch&egrave; abbandonassero l'impresa; perci&ograve; nel campo di Lacchiarella Francesco I ed il Connestabile decisero di fare dei prestiti sui principi, baroni e signori che partecipavano alla spedizione. In meno di dieci ore si trov&ograve; tutta la somma contante necessaria, <span class="citazione">&quot;qui est bien pour demontrer le grand amour que les Francis on a leur Prince&quot;</span> (Barillon).</p></td>
