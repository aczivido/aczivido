            <p><br>
              <strong>Verso Marignano</strong><br>
              <br>
              L'ambasceria che venne inviata a Gallarate era composta come abbiamo detto dal Duca di Savoia e da suo fratello il gran Bastardo, dal sire di Lautrec e suo fratello Tommaso di Foix signore di Lescun, al comando di 400 lance di scorta. Essi erano accompagnati inoltre da vari gentiluomini, alcuni arcieri e gente di finanza; perdurando col&agrave; le trattative, e per il successivo precipitare degli avvenimenti, tutti costoro non poterono essere presenti alla grande giornata di Marignano. <strong>(1)</strong><br>
  Partitesi da Lacchiarella il 9 di settembre, le forze del Re e del Connestabile si distribuirono fra Cavagnera e Vidigulfo, ed infine il giorno 10, per la via di Landriano si diressero su Melegnano.<br>
  Nei pressi del borgo il Re venne avvertito che alcuni avventurieri francesi avevano abbandonato i loro reparti e si apprestavano al saccheggio di un villaggio (forse Riozzo). Egli si stacc&ograve; dalla strada con la sua compagnia e cerc&ograve; di raggiungerli, ma quelli quando lo videro si diedero alla fuga, ed il Re appresso speronando a tutta forza il suo cavallo, che inciamp&ograve; in un ostacolo e cadde e sbalz&ograve; di sella il suo cavaliere.<br>
  Allora uno di quegli avventurieri, vedendo il principe a terra, accorse verso di lui e lo aiut&ograve; a rimontare in sella; poi fugg&igrave; di nuovo insieme agli altri. Il Re desistette dall'inseguirli e fece cercare quell'avventuriero per ricompensarlo ma non se ne seppe pi&ugrave; nulla. <strong>(2)</strong><br>
  A Melegnano gli intendenti dell'armata che precedevano l'arrivo delle truppe, avevano accapparrato per il Re l'albergo del Cappello rosso, ma il sovrano non volle fermarsi entro il borgo, e passato il ponte del lambro, fece distendere l'esercito lungo la strada Pandina, fra Casalmaiocco, Dresano e Mulazzano. Il Connestabile pose il suo quartiere a Sordio, mentre il Re, a detta di Pasquier Le Moyne, si stabil&igrave; in Mulazzano. <strong>(3)</strong></p></td>
