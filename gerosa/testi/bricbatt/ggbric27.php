            <p><br>
              <strong>Una puntata a Sant'Eustorgio</strong><br>
              <br>
             Tanto dubbio fu il risultato delle trattative dei messi del Re di Francia con gli Svizzeri a Gallarate, quanto infruttuoso il tentativo del Trivulzio che si era affacciato alle porte di Milano, ed a cui i suoi concittadini, come si suol dire, mostrarono i denti.<br>
             Le cose andarono cos&igrave;: mentre il Re sostava a Boffalora, ed erano i primi di settembre, il Trivulzio, con una parte delle sue genti, si port&ograve; a San Cristoforo alle porte di Milano, eppoi fece un passo pi&ugrave; in l&agrave; fino al borgo di Porta Ticinese; voleva indurre i Milanesi a darsi al Re di Francia senza combattere, ma questi, presi fra due fuochi, col nemico alle porte e con gli Svizzeri in casa, usarono la diplomazia di consigliare il Re a starsene al largo e gli mandarono ambasciatori a Boffalora, dichiarando che il popolo era devotissimo alla sua corona, e che gli avrebbero fornito vettovaglie, ma che ritardasse la sua venuta.<br>
             Il Duca di Milano se ne stava ricoverato in castello, ed il suo consigliere Gerolamo Morone, sempre abile nel tenere i fili della politica, per rinforzare le argomentazioni degli ambasciatori inviati al Re, aizz&ograve; la plebe a dare qualche unghiata alle soldatesche del Trivulzio; in piazza Sant'Eustorgio gli armati al comando di Camillo figlio del Maresciallo vennero assaliti e pi&ugrave; d'uno ci lasci&ograve; la pelle. Pertanto fecero marcia indietro a San Cristoforo e di qui rientrarono incontanente a Boffalora.<br>
&quot;Andasse, vincesse gli inimici - seguitavano gli ambasciatori - presupponendo che Milano, acquistata che gli avesse la campagna, fusse prontissimamente per riceverlo&quot;. Il buon Re di Francia, pensiamo non tanto commosso da quelle dichiarazioni, quanto dalla palese ostilit&agrave; e dall'ardire dei popolani di Porta Ticinese, mutando tattica, decise di mantenersi ancora al largo ed intanto far continuare le trattative di Gallarate. Ad una nuova deputazione di cittadini milanesi che lo supplicava di procrastinare il suo ingresso onde non sottoporli alle crudeli vendette che certamente gli Svizzeri non avrebbero risparmiate, il Re si arrese.<br>
  Stava tutta l'armata accampata nella valle del Ticino sotto Turbigo. Il giorno 5 settembre fece levare le tende, e presa la via di Magenta e Robecco, si port&ograve; ad Abbiategrasso e Gaggiano; qui fece sonare un falso allarme <span class="citazione">&quot;pour tenir la gendarmerie plus dilligente&quot;</span>. Da Abbiategrasso le truppe vennero a Binasco dove alloggi&ograve; il Connestabile, mentre il Re era stato accolto nella abbazia di Conigo, ed il d&igrave; seguente 8 settembre il campo fu posto a Lacchiarella. <strong>(1)</strong></p></td>
