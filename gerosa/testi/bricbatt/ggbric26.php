            <p><br>
              <strong>Dalle Alpi al Lambro</strong>              <br>
              <br>
                E' opinione degli storici militari che il gran numero di salmerie dell'esercito francese lo rendevano cos&igrave; pesante da ritardarne enormemente la marcia; cosicch&egrave; i vantaggi della prodigiosa calata dalle Alpi e della inopinata cattura del generalissimo Colonna andavano man mano annullandosi. Gli Svizzeri retrocessero rapidamente su Vercelli e Novara, non prima di aver messo a sacco ed incendiato Chivasso, i cui cittadini avevano opposto ostilit&agrave; rinchiudendosi nel borgo con le porte sbarrate.<br>
                Sbarcate a Genova, le forze al comando del signor Aymar de Prie avevano raggiunto rapidamente ed occupato Alessandria e Tortona, mentre l'armata di terra progred&igrave; sino a Saluzzo. Quivi il Re ricevette la notizia della nascita della sua prima figlia, ma non ne rimase molto soddisfatto: &quot;Soa Maest&agrave; ne ha sent&igrave; pocho allegrezza perch&egrave; sempre pensa di haver uno fiol maschio&quot; <strong>(1)</strong><br>
                Pass&ograve; l'armata francese da Torino mentre il Re alloggiava in castello ospite del duca di Savoia; frattanto in campo svizzero crescevano il malumore e le discordie che stavano per degenerare in aperta rivolta al punto che alcuni battaglioni presero la via dei monti per ritornare ai paesi loro. Senonch&egrave; ebbero sentore che altre forze calavano in Italia al comando del celebre e valoroso capitano Rostio, per cui si ridussero a pi&ugrave; miti consigli e convennero di riunirsi a Gallarate dove si accamparono.<br>
                Prese il destro il Re di Francia per proseguire e stringere le oramai lunghe trattative allo scopo di evitare il combattimento ed impadronirsi di Milano senza colpo ferire, epperci&ograve; invi&ograve; a Gallarate il gran Bastardo di Savoia col sire di Lautrec ed altri notabili; ad essi si aggiunse il Duca di Savoia in qualit&agrave; di intermediario ed il patto venne stipulato e ambe le parti: gli Svizzeri si sarebbero ritirati dietro un congruo compenso del Re di Francia.<br>
                In forza di quei patti Massimiliano Sforza, in cambio di Milano che cedevasi al Re di Francia, avrebbe avuto il ducato di Nemours con ricca pensione e per di pi&ugrave; una moglie di sangue reale; per non venir meno all'uso dei tempi, i baratti di terre e di genti venivano sempre suggellati con un bel matrimonio principesco.<br>
          L'armata francese proseguiva, e giunta in vista del Ticino si accamp&ograve; a Boffalora, dove si congiunsero anche le forze di Aymar de Prie che dopo Tortona avevano assoggettato Pavia. Le cose andavano intanto mutandosi nel campo opposto: giunto da Bellinzona il comandante generale Rostio con ventimila fanti quanto mai freschi e desiderosi di combattere ed impinguarsi di preda come due anni prima a Novara, i patti di Gallarate furono dimenticati, si chiese guerra anzich&egrave; pace, e tutti riunitisi sotto le loro bandiere, giurarono fedelt&agrave; al nuovo Capitano dei Vallesi.</p></td>
