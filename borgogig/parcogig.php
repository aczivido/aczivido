        <td align="left" valign="top"><p>di Pierino EspostiI - 15.11.2000<br>
<br>
A Zivido di San Giuliano Milanese, terminata l&#146;edificazione dei quartieri residenziali &quot;Piana dei Giganti&quot;, &quot;Il Giardino di Milano&quot;, &quot;Arcobaleno&quot; ed il nuovo complesso parrocchiale, avrà una precisa delimitazione l&#146;area destinata a verde pubblico che risulterà ben più ampia dell&#146;esistente Parco Nord sangiulianese.<br>
A prima vista potrebbe sembrare uno spazio come tanti altri dove, collocate alla rinfusa piante ed arbusti e qualche panchina, permettere ai cittadini, e purtroppo anche ai soliti ignoti vandali, di prendere una boccata d&#146;aria.<br>
La persona sensibile ed attenta però avverte immediatamente che tale spazio è ciò che rimane del famoso campo di battaglia dove si svolsero le azioni più cruente e dove, accanto nell&#146;antica chiesetta, riposano da cinque secoli i corpi dei contendenti che il 13 e 14 settembre 1515 si affrontarono con ferocia nello scontro passato alla storia con il nome di &quot;Battaglia dei Giganti&quot; o anche detta &quot;di Marignano&quot; per la conquista del ricco e prosperoso Ducato di Milano.<br>
Lo scontro vide vincitore il giovane re di Francia Francesco I e sconfitti gli invincibili mercenari Svizzeri che da allora cessarono di pensare ai sogni di espansione e ripudiarono la guerra come strumento nel rapporto con gli altri dando inizio alla loro secolare neutralità.<br>
Se questo è il prologo, l&#146;epilogo ne deve essere degno tant&#146;è che l&#146;Associazione Culturale Zivido da tempo andava elaborando un progetto denominato &quot;Parco dei Giganti&quot;.<br>
In sintesi: un parco storico che sia nel contempo habitat naturale lombardo e percorso culturale che abbia per tema la battaglia ed i suoi personaggi.<br>
E&#146; sconfortante passeggiare in certe aree a verde pubblico, disseminate ovunque, dove le piante e gli arbusti e le panchine sembra siano piovute casualmente dal cielo, collegate da percorsi a volte irrazionali; parchi senza identità e senza rispetto per l&#146;habitat naturale del luogo.<br>
Il &quot;Parco dei Giganti&quot; dovrà invece valorizzare quattro importanti temi: il recupero dell&#146;antica strada campestre che collegava Zivido alla cascina Carlotta; il giardino all&#146;italiana; l&#146;ambientazione lombarda con vegetazione autoctona e qui diviene necessario l&#146;apporto dell&#146;associazione dei Vivai Pro Natura, con sede a Rocca Brivio, sia sotto l&#146;aspetto professionale sia per quanto riguarda la fornitura delle specie arboree; un ampio spazio verde a cavea con spalti in terra dove proporre iniziative pubbliche di qualità.<br>
Il &quot;Parco dei Giganti&quot; anche come parco storico perché dovrà riproporre e ricordare l&#146;evento locale che tanto influì nella storia europea e questo collocando gruppi scultorei, raffiguranti i personaggi o i momenti della battaglia, in angoli particolarmente curati e scenografici. Promuovere dunque l&#146;attenzione di artisti nazionali ed internazionali che, con il supporto finanziario di singoli enti e delle nazioni interessate al famoso evento, cimentandosi e gareggiando apportino al &quot;Parco dei Giganti&quot; arte europea di qualità con una ricaduta d&#146;immagine, oltre che economica, non trascurabile per San Giuliano Milanese.<br>
L&#146;Associazione Culturale Zivido ha pensato pure ad un nuovo modo di gestire il parco: l'istituzione di una entità giuridicamente definita, con capitale di dotazione proprio ed organi deliberativi, composta dal pubblico, cioè l'Amministrazione cittadina, e dal privato, cioè il singolo cittadino. In tale modo si promuoverebbe una compartecipazione attiva ed interessata creando nel contempo una nuova cultura di attenzione e rispetto.</p></td>
