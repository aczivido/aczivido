        <td align="left" valign="top"><p>di Pierino Esposti - 27.01.2004<br>
<br>
Il grado di civilt&agrave; e lungimiranza di una comunit&agrave; &egrave; riscontrabile dalle azioni che compie. Azioni che talvolta sono positivamente promosse dal basso e successivamente accolte e realizzate dai propri rappresentanti istituzionali.<br>
A San Giuliano Milanese il &quot;Progetto Parco dei Giganti&quot;, per il quale all'Associazione Culturale Zivido va riconosciuto il merito di averlo ideato, viluppato e promosso da oltre un decennio, rappresenta l'esempio concreto dell'affermazione sopra detta.<br>
Tale progetto dimostra di essere un'azione di grande respiro e lungimiranza in ambito storico, culturale ed ambientale con la prospettiva concreta di favorire nel prossimo futuro anche il positivo sviluppo economico dell'intera comunit&agrave; sangiulianese tramite la promozione del Turismo Culturale.<br>
Con il Parco, soprattutto questo Parco, si riconoscono e si accolgono le sempre pi&ugrave; evidenti esigenze di nuovi spazi verdi, come confermato da una recente indagine commissionata dall'Amministrazione comunale, emerse da parte dei cittadini sangiulianesi, ma anche conestualmente di una accresciuta identit&agrave; comune alla quale l'ambiente scolastico locale ha dato un contributo non trascurabile.<br>
Per quanto attiene il processo di recupero e valorizzazione della storica frazione di Zivido, in atto da alcuni anni con apportuni strumenti e positivamente valutato anche in una recente e pregevole pubblicazione edita dalla Provincia di Milano Ente Gestore del Parco Agricolo Sud Milano, il &quot;Parco dei Giganti&quot; rappresenta la naturale conclusione di una visione complessiva ed omogenea di questa parte importante del territorio sangiulianese.<br>
Il carattere intrinseco e le finalit&agrave; del &quot;Parco dei Giganti&quot; fanno s&igrave; che si possa a ben ragione definire la sua realizzazione come una &quot;rivoluzione culturale&quot; sangiulianese.<br>
Certamente l'iter procedurale necessario perch&egrave; dall'enunciazione dei principi guida si passi agli atti concreti presenta alcune difficolt&agrave; in quanto il &quot;Progetto Parco dei Giganti&quot; coinvolge diversi ed importanti aspetti fra loro strettamente correlati. A titolo esemplificativo ne citiamo alcuni che riteniamo principali.<br>
Anzitutto un accorto recupero ambientale e naturale che valorizzi appieno i caratteri specifici del nostro territorio cos&igrave; come l'evoluzione secolare ci ha tramandato. Con ci&ograve; il cittadino avr&agrave; modo di gustare i positivi vantaggi di una grande area verde sotto casa carica anche di personalit&agrave; al contrario di altri esempi di parco anonimi e senza vita.<br>
Valorizzare e promuovere, contestualmente, la memoria storica del luogo che supera gli angusti confini locali per toccare buona parte dell'Europa, &egrave; il secondo aspetto qualificante del &quot;Parco dei Giganti&quot;. Perci&ograve; &egrave; prevista la realizzazione, naturalmente inserita nell'ambiente, di un percorso storico-artistico segnalato da pregevoli opere scultoree con il compito di richiamere l'evento famoso svoltosi cinque secoli orsono proprio su questa area e conosciuto come &quot;Battaglia dei Giganti&quot; (o, impropriamente, Battaglia di Marignano). Il tutto costituirebbe una inedita galleria d'arte internazionale all'aperto e, aspetto non trascurabile, motivo di attrazione europea.<br>
Nel &quot;Parco dei Giganti&quot; trova collocazione pure un museo storico-archeologico la cui giustificazione trova riscontro nei numerosi ed interessanti reperti venuti casualmente alla luce e raccolti dall'Associazione Culturale Zivido, ora presso la Soprintendenza Archeologica della Lombardia a Mlano, che propongono nuove e pi&ugrave; antiche testimonianze di vita sul nostro territorio. Il museo permetterebbe inoltre di conservare e rendere facilmente usufribili dai cittadini altri aspetti del patrimonio locale altrimenti a rischio di distruzione perch&egrave; dispersi e trascurati negli angoli remoti dell'area sangiulianese.<br>
Intrinseco nel concetto di Parco &egrave; lo spazio, e lo spazio opportunamente attrezzato lo completa necessariamente. Quest'ultimo si concretizza nell'arena naturale all'aperto e nell'auditorium al coperto. Entrambi favorirebbero una interessante aggregazione dei cittadini tramite iniziative di curata qualit&agrave; che tocchino la cultura, la musica, il teatro e lo svago. E' questa un'ulteriore esigenza emersa dalla collettivit&agrave; sangiulianese alla quale occorre dare una risposta concreta e razionale per colmare un vuoto ormai insopportabile.<br>
Nel suo complesso il &quot;Progetto Parco dei Giganti&quot;, illustrato in sintesi, testimonia dunque l'inizio di una azione che possiede indubbiamente le caratteristiche di una &quot;rivoluzione culturale&quot; e ci&ograve; in un momento particolare che vede la comunit&agrave; sangiulianese aver raggiunto la maturazione e la sensibilit&agrave; opportuna per accoglierlo ed apprezzarlo in giusta misura.<br>
In occasione di una cena tenutasi a Milano nel dicembre scorso, alla quale erano presenti oltre ai due attori principali del &quot;Progetto Parco dei Giganti&quot; anche importanti personalit&agrave; straniere, ha preso corpo la necessit&agrave;, gi&agrave; in precedenza pi&ugrave; volte esplicitata dall'Associazione Culturale Zivido, della contestuale istituzione di un Ente a cui affidare il compito di sovrintedere alla realizzazione, gestione e promozione del Parco. Questo Ente, la cui figura giuridica dev'essere ancora individuata, dovr&agrave; avere caratteristiche tali da poter operare con ampi strumenti per tutelare e valorizzare il patrimonio sangiulianese, senza disdegnare la visione sovracomunale, favorendo il benessere dei cittadini.<br>
L'azione, racchiusa nel &quot;Progetto Parco dei Giganti&quot;, si presta indubbiamente a divenire motore propulsivo per la citt&agrave; di San Giuliano Milanese, proiettando la comunit&agrave; in una dimensone futura pi&ugrave; consona alle esigenze emerse di identit&agrave; ed ambiente oltre a valorizzare degnamente un territorio, il nostro, che non ha pari per cultura e patrimonio nel sud milanese.</p></td>
