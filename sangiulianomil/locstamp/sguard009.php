          nuova chiesa</u></b><br>
          <br>
          <font size="1">Chi ha visto il plastico della nuova chiesa o meglio 
          (conoscendo il disegno) ha visto le tavole esposte in quella che era 
          la casa del sacrestano, si &egrave; domandato perch&egrave; si &egrave; 
          voluto fare una chiesa cos&igrave; strana ed originale. Certo che era 
          facile costruire dietro la facciata (che essendo monumento nazionale 
          &egrave; intoccabile e quindi da mantenersi) un capannone pureo e semplice. 
          Questo per&ograve; non poteva creare un clima di chiesa; a questo si 
          sarebbero opposti gli stessi sangiulianesi che vogliono per la dignit&agrave; 
          del loro paese &quot;una bella chiesa&quot;.<br>
          La forma data alla nuova chiesa parte da alcune situazioni di fatto. 
          La facciata infatti deve risultare senza alcune strutture che la disturbino, 
          questo chiede la Soprintendenza; la via XI Febbraio deve essere allargata 
          questo chiede il Piano Regolatore; la casa parrocchiale &egrave; costruita 
          da soli due anni; il cortile dell'Oratorio femminile deve essere pi&ugrave; 
          grande possibile e questo lo esige una sana pedagogia.<br>
          L'Architetto poi ha avuto le sue buone ragioni dopo una decina di progetti 
          e quasi due anni di studi. Certo la struttura interna risponde ai criteri 
          di una chiesa moderna. E' infatti semplice, funzionale e le stesse strutture 
          in cemento armato hanno assunto forme decorative. Sono messe in giusto 
          rilievo le parti pi&ugrave; valide. tali sono l'altare, il battistero, 
          i confessionali.<br>
          L'altare con la mensa rivolta al popolo &egrave; centro di tutta la 
          chiesa. Sar&agrave; l'ambiente messo pi&ugrave; in rilievo e come posizione 
          e come luce. Alla stessa quota sono stati posti gli amboni per le letture 
          bibliche, la mensa e il presbiterio, per significare l'importanza e 
          il valore che queste cose ricoprono per il cristiano. Il battistero 
          dove la vita cristiana ha avuto inizio, &egrave; ben visibile a chi 
          entra in chiesa. Quote diverse significano i vari passaggi dal non cristiano 
          prima del battesimo al cristiano dopo.<br>
          Cos&igrave; sono messi in ambienti discreti ma con struttura stabile, 
          i confessionali tanto per gli uomini quanto per le donne. L'aver abolito 
          poi tutte le colonne dar&agrave; alla chiesa un senso di respiro e di 
          vastit&agrave;. Da tutti i punti infatti l'altare sar&agrave; visibile 
          cosicch&egrave; le cerimonie saranno pi&ugrave; seguite o meglio comprese. 
          Sar&agrave; creato anche un vasto pronao che preparer&agrave; il fedele 
          all'incontro con Dio.<br>
          La travi di collegamento tra i vari pilastri daranno un movimento armonico 
          al soffitto. Completeranno la chiesa vera e propria due sacrestie, un 
          ufficio-archivio ed una sala per adunanze. la chiesa invernale che servir&agrave; 
          anche da cappella per l'oratorio femminile sar&agrave; utilizzata nei 
          giorni feriali. Il riscaldamento studiato dalla ditta Scolari sar&agrave; 
          tra i pi&ugrave; moderni oggi in commercio.<br>
          L'Architetto G. mariotti ideatore-progettista ha voluto dare alla &quot;sua&quot; 
          S. Giuliano una chiesa architettonicamente valida e perfettamente funzionante. 
          Sul nostro giornale ne illustrer&agrave; nei prossimi numeri le varie 
          parti. Attualmente i lavori procedono con ritmo celere. La generosit&agrave; 
          dei sangiulianesi ricchi e poveri &egrave; continua anche perch&egrave; 
          vedono nella nuova chiesa che sorge il simbolo della nuova S. Giuliano 
          cattolica. <br>
