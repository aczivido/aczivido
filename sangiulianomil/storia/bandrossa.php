        <td align="left" valign="top"><p><b>Inaugurazione della bandiera rossa</b><br><br>
          (1920) Anche i socialcomunisti di San Giuliano non vollero essere da meno degli altri. Anch'essi si procurarono la loro bandiera rossa. Essa era pronta per essere inaugurata il 10 ottobre ma dove avverrà la mascherata? Nella gran corte che circonda la cooperativa? Sarebbe stata miglior cosa, perchè si sarebbe evitata una clamorosa pubblicità che poteva sovreccitare gli animi già eccitati. ma i signori Zerbi, proprietari dei locali della cooperativa, del palazzo che vi sta di fronte del frapposto cortile, per quanto pregati, non vollero acconsentire, per non sembrare del numero dei rossi. D'altronde i socialisti di San Giuliano volevano far mostra di se stessi e dimostrare che anch'essi sono capaci di levare rumore.<br>
Due di essi vennero da me, domandando che permettessi loro d'innalzare il palco sulla piazza. Con bella maniera feci loro intendere che non potevo concedere loro simile permesso ed essi soggiungevano che ne facevano senza e che avrebbero ugualmente innalzato il palco. ma o fossero intimoriti dalle mie parole di risposta oppure fossero ridotti a migliore senno da qualche persona ben pensante, si accontentarono di innalzare il palco lungo la cancellata che chiude il cortile delle scuole sulla proprietà comunale.<br>
Ma a disturbare la festa pensò Dio. A mezza mattina incominciò a cadere una pioggia dirotta che durò sin verso le ore 13, impedendo così che accorressero a San Giuliano in gran numero i socialisti dai dintorni. La pagliacciata ebbe luogo l'ora della dottrina che io potei fare con discreto concorso di popolo. Ci voleva anche il comico e ci fu. Arrovellati i socialisti d'essere stati dal tempo impediti di fare la loro pubblicità, se la prendevano con il prevosto che per disturbarli aveva provocato da Dio la pioggia ed uno ebbe la prontezza di rispondere loro: se credete che Dio ascolta il prevosto, perchè non lo ascoltate voi?<br>
                    <br>
(da &quot;Liber Chronicus&quot; 1895-1923, Vol.I, archivio storico Parrocchia di San Giuliano Martire, San Giuliano Milanese)</p></td>
