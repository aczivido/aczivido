                <td width="98%" align="left" valign="top"> 
                  <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
                    Nel novembre del 1968 e precisamente il 19, un decreto presidenziale 
                    autorizzava l'amministrazione comunale a fregiarsi dello stemma 
                    e gonfalone, così come noi oggi lo conosciamo. Fu questo decreto 
                    l'epilogo di un lungo periodo di ricerche e formalità burocratiche 
                    durato 23 anni.<br>
                    Ci risulta che un decreto legge del 1896 
                    richiedeva uno stemma ad ogni comune, vietando l'uso dello 
                    stemma dello Stato (R.D. 234 del 23 aprile 1905), pertanto 
                    gli amministratori di allora, recependo il messaggio con la 
                    dovuta tranquillità, sollecitati dallo Studio Araldico di 
                    Genova, iniziarono il cammino verso la meta, ancora lungi 
                    a venire.<br>
                    I documenti d'archivio ci evidenziano una 
                    iniziale corrispondenza datata 1946 con la quale il sindaco 
                    comunicava al marchese Brivio che il consiglio comunale aveva 
                    deliberato di adottare, quale stemma, quello della sua Casata 
                    chiedendone l'autorizzazione.<br>
                    Il Brivio rispondeva al sindaco: &quot;Le 
                    comunico che la domanda mi è giunta gradita quale tangibile 
                    riconoscimento dell'opera compiuta dai miei Maggiori sia verso 
                    la popolazione di questo Comune che al riguardo della bonifica 
                    dei terreni&quot;. Il sindaco rispose alla disponibilità del 
                    Brivio aderendo alla sua richiesta d'incontro per accordarsi 
                    e provvedere. I documenti d'archivio lasciano un vuoto in 
                    merito al seguito del contatto con la casa Brivio.<br>
                    Si passa ad un sollecito del novembre 1948 
                    dello Studio Araldico di Genova il quale specifica l'evidente 
                    necessità di uno stemma e gonfalone comunale, adoprandosi 
                    per la ricerca storica e lo svolgimento gratuito della pratica 
                    di Concessione Ministeriale. Il sindaco con nota del 12 novembre 
                    sollecita l'evasione della pratica.<br>
                    Troviamo poi una ricevuta dell'ufficio 
                    cassa della Presidenza del Consiglio dei Ministri di lire 
                    dieci, a titolo di deposito provvedimento araldico, del 27 
                    novembre 1949. La pratica però non ebbe esito.<br>
                    Passarono gli anni, la ricerca svolta dallo 
                    studio citato allega una dichiarazione dell'allora direttore 
                    dell'Archivio di Stato di Milano datata 25 novembre 1967: 
                    &quot;In risposta alla Sua relativa all'oggetto (Stemma Comune 
                    di San Giuliano Milanese) si comunica che le ricerche effettuate 
                    in: Cremosano Mario, Galleria d'imprese, arme ed insegne dei 
                    vari regni, provincie, città e terre dello Stato di Milano, 
                    1673, hanno dato esito negativo&quot;.<br>
                    Trattasi di due volumi di grande formato, 
                    consultabili presso lo stesso archivio, di notevole bellezza 
                    per i disegni e per la vivacità dei colori, oltre ai riferimenti 
                    araldici di interesse storico.<br>
                    E' dell'aprile 1968 la documentazione ufficiale 
                    di spiegazione dello stemma, probabilmente allegata alla richiesta 
                    per l'emanazione del decreto presidenziale.
                    Il fascicolo dal titolo: &quot;Cenno storico 
                    corografico e blasonature dello stemma civico e del gonfalone 
                    municipale di San Giuliano Milanese (MI)&quot;, così lo descrive: 
                    &quot;Per quanto premesso blasoniamo: Arma: troncato d'azzurro, 
                    alla fascia in divisa d'argento sulla partizione nel I° cuspide 
                    di un campanile di pietra finestrato di tre fondatoni sulla 
                    fascia; nel II° a due spade d'argento, decussate, guarnite 
                    d'oro, con le punte all'ingiù. Segni esterni di Comune. Gonfalone: 
                    d'azzurro caricato dell'arma sopra descritta&quot;.<br>
                    Ci sembra doveroso segnalare che qualche 
                    disinformato funzionario, affidando ad un grafico l'elaborazione 
                    moderna dello stemma, abbia errato nell'approvarne l'operato: 
                    vedasi quello murato sul blocco di cemento di fianco il nuovo 
                    palazzo comunale, è molto diverso dall'originale. L'errore 
                    più eclatante stà nel disegno del campanile a forma triangolare 
                    quando invece è conico, non chè nelle finestre dello stesso.<br>
                    Rifacendosi alla torre campanaria dell'Abbazia 
                    di Viboldone, subito sotto la cuspide conica, sul fronte si 
                    notano tre finestre (trifore), sul fianco due (bifore). I 
                    segni esterni &quot;da Comune&quot;, esagerando nel tratto, 
                    non lasciano capire che l'incrocio dei rami d'alloro e di 
                    quercia, nell'originale, sono trattenuti da un fiocco tricolore.<br>
                    Oggi lo stemma ha rivisto nuovi splendori, 
                    tornando nella forma e nei colori originali.<br>
                    <br>
                    (da &quot;Il Borgo dei Giganti&quot;, Zivido, novembre 1995, 
                    di Giancarlo Cattaneo)</font></p>
