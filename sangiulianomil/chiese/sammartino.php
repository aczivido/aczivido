            <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">La 
              chiesa di Carpianello è molto antica e, come molti luoghi sacri 
              della campagna, ha subito diverse vicissitudini.<br>
              Fu visitata, verso il 1570, da San Carlo, il quale 
              annotò alcune osservazioni interessanti che tracciano una sommaria 
              descrizione: &quot;... che la chiesa se repari in questo modo, cioè 
              si faccia una Cappella bella et honetsa ove hora è la porta maggiore, 
              e la porta si faccia ove hora è la cappella. Si faccia l'altare 
              alla misura, ben ornato con la sua Ancona et altre necessità. Si 
              facciano il pavimento e la soffitta e le mura s'imbianchino e le 
              porte e fenestre si concino con le ferrate e stanghe alle finestre.<br>
              Per eseguire le cose soprascritte potransi vender gli arbori che 
              sono nel cimitero col interessamento del Vicario Foraneo e licenza 
              in iscritto del Vicario Generale...&quot;.<br>
              Questa disposizione indicò la volontà dell'arcivescovo 
              milanese di portare una modifica sostanziale alla chiesa: quella 
              di spostarne l'entrata dalla parte opposta.<br>
              Il perchè è chiaro: le chiese milanesi antiche avevano 
              sempre l'ingresso che guardava verso la città. La descrizione indica 
              poi chiaramente l'esistenza in Carpianello di un cimitero, certamente 
              sistemato attorno alla sua chiesa, di cui non se ne sapeva nulla.<br>
              E' probabile che le volontà di San Carlo non siano 
              state eseguite, poichè sappiamo che i Brivio fecero costruire nella 
              loro casa padronale una cappella votiva, dedicata a San Martino. 
              Presumibilmente l'antica chiesetta fu abbattuta ed il suo titolo 
              trasportato nella nuova chiesa.<br>
              Dalle rilevazioni di una visita pastorale fatta nel 
              1602 da Federico Borromeo abbiamo notizia dell'esistenza della nuova 
              chiesa e del particolare che essa è stata appena costruita: infatti 
              - si legge nelle relazioni - il suo campanile non è ancora terminato.<br>
              Nella visita pastorale del 1609 viene detto che la 
              nuova chiesa fu fondata da Giacomo Brivio e vi officiava prete Henrico 
              Bevilacqua; per mantenerlo i fedeli si erano impegnati a pagare 
              140 lire e i Brivio 60 lire.<br>
              Della nuova chiesa sappiamo che Francesco Brivio, il 
              4 aprile 1628 le assegnò un censo sui suoi beni di 650 lire imperiali, 
              affinchè si potesse mantenere un cappellano che officiasse tutti 
              i giorni. Nel 1659 il censo venne trasformato in diritto di juspatronato 
              dei Brivio, che assegnarono all'oratorio il reddito derivante da 
              50 pertiche di terreno, oltre al reddito ottenibile dai dazi sull'osteria, 
              ammontanti in totale, a circa 450 lire imperiali. In una descrizione 
              del 1690 si legge che questo Oratorio ha un solo altare in legno, 
              la sua sacrestia ed il campanile, mentre il cimitero non viene più 
              menzionato. E ciò in ragione del fatto che il nuovo edifici religioso 
              sorgeva all'interno del cortile della casa nobiliare dei Brivio. 
              I defunti, dopo la soppressione della primitiva chiesa, vennero 
              trasportati a S.Giuliano.<br>
              Nel 1816 la vecchia Cappella, ormai cadente, venne 
              demolita (sono ancora visibili alcuni affreschi sacri su un muro 
              di casa Brivio) ed i Recalcati fecero costruire l'attuale chiesa, 
              pagando per i lavori lire 36.000. Il nuovo edificio, costruito contemporaneamente 
              a quello di Bolgiano, egualmente per volontà dei Recalcati, fu benedetto 
              e aperto al culto il 15 aprile 1818.<br>
              Della primitiva chiesa di Carpianello conosciamo il 
              nome di due suoi cappellani: Don Cristoforo De Partinì nel 1546 
              e Don Antonio della Rocca nel 1592.<br>
              Dell'oratorio dei Brivio si conoscono i seguenti cappellani:<br>
