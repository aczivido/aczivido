              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>San 
                Giuliano Martire</u><br>
                <br>
                Quando e perchè la chiesa parrocchiale fu dedicata a San Giuliano 
                Martire non lo sappiamo con certezza neppure oggi e risulta un 
                lavoro arduo stabilire, fra i 34 martiri di nome Giuliano citati 
                nella &quot;Bibliotheca Sanctorum&quot; volume 6°, a chi riferirsi.<br>
                Per consuetudine si è creduto che il nostro Martire fosse Giuliano 
                di Anazarbo nella Cilicia ed in tal senso si sono orientati gli 
                storici locali fino ad oggi.<br>
                Come giustamente fa rilevare il sangiulianese Luigi Toletti, sul 
                Bollettino Parrocchiale, il Martire da considerare è però un altro: 
                si tratta di Giuliano nato in Istria e venerato a Rimini. Per 
                entrambia, ad ogni modo, la storia del martirio è identica.<br>
                Giuliano aveva il padre senatore e pagano mentre la madre, cristiana, 
                lo aveva educato alla nuova fede religiosa. Non facendo mistero 
                di essere cristiano, a diciotto anni venne denunciato al governatore 
                Marciano e costretto a sacrificare agli dei. Di fronte al suo 
                deciso rifiuto e dopo le consuete torture viene condotto ad Egea, 
                una località sul mare, e condannato a morte. Qui, rinchiuso in 
                un sacco insieme a serpenti velenosi, viene gettato in mare.<br>
                A questo punto le notizie di Giuliano di Anazarbo terminano mentre, 
                quasi fosse la continuazione logica, si sa che il corpo del Martire 
                venerato a Rimini, chiuso in un sarcofago, vagò per molto tempo 
                sul mare fino a fermarsi presso il litorale riminese.<br>
                Ciò sarebbe avvenuto ai tempi di Ottone I (961-963) e del Vescovo 
                di Rimini Giovanni (962-968). Il corpo del Martire venne collocato 
                nel monastero dei SS.Pietro e Paolo a Rimini, dove in seguito 
                si procedette ad una ricognizione constatando che le reliquie 
                erano intatte.<br>
                In un documento si legge che ciò avvenne il 22 giugno, già giorno 
                di festa per tutta la Diocesi riminese, e proprio il 22 giugno 
                viene celebrata a San Giuliano Milanese la festa dedicata al Martire.<br>
                Dal sec.XII il culto di questo Martire in Rimini fiorì singolarmente: 
                le sue reliquie sono conservate nella attuale S.Giuliano a Mare, 
                già chiesa dell'antico monastero dei SS.Pietro e Paolo, presso 
                il ponte d'Augusto.<br>
                Dal 1225 Giuliano è il protettore della città e monete della zecca 
                di Rimini sono contrassegnate dal motto: &quot;Sanctus Iulianus&quot; 
                e la sua festa del 22 giugno è sagra popolare e segna l'iniziare 
                o lo scadere dei patti contrattuali.<br>
                In genere San Giuliano Martire è stato raffigurato sotto le sembianze 
                di un giovanetto recante in mano una palma, segno di martirio, 
                ed un cesto colmo di serpi, simbolo delle lotta sostenuta contro 
                la falsa fede.<br>
                Sono emblemi comuni nella iconografia cristiana ed i rari dipinti 
                oggi presenti non rappresentano certo una documentazione attendibile 
                per riconoscere il Martire istriano.<br>
                Il &quot;Liber Chronicus&quot; conservato nell'archivio parrocchiale 
                dice che in occasione della visita pastorale del 1911 il prevosto 
                Don Giovanni Penuti fece ritoccare da un pittore i due quadri 
                che adornavano le pareti dell'altare maggiore. Uno rappresentava 
                la Natività, l'altro il martirio del patrono della parrocchia: 
                San Giuliano.<br>
                E' questo l'unico cenno conosciuto di una immagine del martire 
                nella parrocchiale prima di essere sostituita negli ultimi tempi 
                da una statua lignea, dono della locale sezione &quot;Unione Artigiani 
                della Provincia di Milano&quot;.<br>
                Fino al 1960 era tradizione il 22 giugno, ricorrenza del Martire, 
                celebrare una curiosa usanza.<br>
                Nella navata centrale della chiesa parrocchiale, proprio sopra 
                le balaustre, il sagrestano appendeva un pallone del diametro 
                di circa 50 cm, formato da una intelaiatura in filo di ferro e 
                ricoperto da ovatta imbevuta di alcool. Il parroco, vestito dei 
                paramenti liturgici, con un lungo bastone, sulla cui sommità era 
                stata collocata una candela accesa, dava fuoco al pallone che 
                bruciava rapidamente.<br>
                Terminata questa piccola e significativa funzione, si iniziava 
                la celebrazione della Messa in onore del Santo Martire Giuliano.</font></p>
