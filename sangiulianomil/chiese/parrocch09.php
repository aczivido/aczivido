              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>San 
                Carlo e le osterie sangiulianesi</u><br>
                <br>
                Da sempre il punto d'incontro delle comunità piccole o grandi 
                fu la piazza, intorno alla quale sorgevano gli edifici principali. 
                Prima o poi naturalmente comparvero le osterie e San Giuliano 
                ebbe la sua situata sullo stradale romano e questa felice posizione 
                fece sì che la sua importanza risultò evidente.<br>
                L'osteria chiamata &quot;San Giuliano&quot; costituì un sicuro 
                punto di riferimento per tutti i viaggiatori che andavano o venivano 
                da Milano. Non fu certamente l'unica osteria presente a San Giuliano, 
                anche se era la più antica; infatti sappiamo che nel 1629 ve n'era 
                un'altra gestita da Maffiolo Scalzati la cui ubicazione però è 
                sconosciuta.<br>
                Generalmente le osterie erano di proprietà di famiglie nobili 
                e la nostra appartenne per buona parte della sua storia ai Meravigli 
                di Milano passando in seguito ad altre casate. I Meravigli addirittura 
                ottennero da Carlo V di Spagna l'esenzione dal pagamento dei dazi 
                sia per l'osteria sia per l'annesso panificio con decreto in data 
                22 agosto 1550.<br>
                Nel 1537 era oste Cristoforo da Rippa e nel 1553 troviamo addirittura 
                l'oste Marchisoni nelle vesti di console di San Giuliano.<br>
                In un documento del 1715 vi è la descrizione, seppure sommaria, 
                dell'osteria &quot;San Giuliano&quot;.<br>
                Sul davanti un portico sorretto da due pilastri in mattoni; una 
                grande porta a due battenti dava accesso all'interno nel grande 
                locale chiamato &quot;baiana&quot; col pavimento in mattoni, mentre 
                gli altri locali erano in terra battuta.<br>
                Sotto il portico trovava posto una bottega per la vendita del 
                pane e una per la carne. Nel grande cortile interno erano situati 
                gli stallaggi e al piano superiore alcune stanze per riposare. 
                Sulla facciata faceva bella mostra di sè l'insegna in ferro battuto 
                raffigurante San Giuliano.<br>
                Questa antica osteria, della quale non vi è più traccia, si trovava 
                in via Toscani prospiciente la piazzetta ricavata dal ponte che 
                scavalca il Redefossi sul quale è la pensilina degli autobus.<br>
                All'inizio del nostro secolo era ancora in attività e vi alloggiavano 
                i viaggiatori, davanti si fermavano i postiglioni per il cambio 
                dei cavalli e al suo bancone lucido e rustico ci si dissetava 
                in compagnia.<br>
                L'insegna sulla facciata rimase anche dopo la chiusura dell'osteria, 
                ma purtroppo col tempo fu definitivamente tolta.<br>
                Per la povera gente, durante i secoli, l'osteria rappresentò l'unico 
                svago a portata di borsa e in questi locali rischiarati da fumose 
                e maleodoranti candele di sego, fra il rumore dei boccali di metallo 
                e le grida degli avventori dediti al gioco, si cercava disperatamente 
                di trovare un poco di felicità ingurgitando pinte di vino.<br>
                Gli osti facevano buoni affari chiudendo due occhi sui giochi 
                d'azzardo, sui balli e in certi casi organizzavano incontri galanti 
                con donnine compiacenti.<br>
                San Carlo ben comprese l'importanza dell'osteria per la società 
                dell'epoca e visto l'andazzo si rese conto del lato negativo che 
                sempre più risultava a danno dei costumi già fin troppo rilassati.<br>
                Nel suo mirino fu principalmente il clero milanese, in parte dimentico 
                della propria missione e frequentatore assiduo di osterie.<br>
                Nel 1570 venne a trovarsi sotto il potente torchio del Borromeo 
                un certo cappellano Giovanni Morosino, gran frequentatore dell'osteria 
                di Zivido dove teneva regolarmente banco nel gioco e nel linguaggio 
                pittoresco. A nulla valse la sua lettera di difesa inviata al 
                Santo nella quale però a denti stretti ammetteva le sue colpe; 
                fu invitato a chiedere il trasferimento e gli fu concesso immediatamente.<br>
                Una dura lettera di richiamo fu inviata da San Carlo nel 1580 
                al parroco di Sesto Ulteriano don Antonio Broncus.<br>
                Il parroco era accusato di non vigilare con serietà sulla locale 
                osteria nella quale avveniva di tutto e gli si chiedeva di riferire 
                i nomi dei suonatori e dei ballerini. Nella eventualità che l'oste 
                non si portasse a più corretto atteggiamento, il parroco di Sesto 
                Ulteriano era autorizzato dal Borromeo a far intervenire il braccio 
                secolare, ovverossia la soldataglia spagnola, come le grida del 
                tempo permettevano.<br>
                Sono luci ed ombre di tempi lontani, ma hanno il pregio di far 
                toccare con mano usi e costumi della povera gente; unico rimpianto 
                sta nella scarsità di ulteriore e minuziosa documentazione.</font></p>
