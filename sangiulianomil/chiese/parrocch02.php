              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>le 
                origini</u><br>
                <br>
                Le origini della chiesa parrocchiale di San Giuliano Milanese 
                si perdono nel tempo e fino ad oggi non ci è possibile conoscere 
                con esattezza quando iniziò la costruzione del primitivo edificio 
                sacro.<br>
                La causa principale della mancanza di notizie è da attribuirsi 
                ai numerosi eventi bellici ed incendi che distrussero parte dei 
                documenti conservati nell'archivio parrocchiale.<br>
                Anche il territorio sangiulianese, sul quale sorge la chiesa e 
                che è parte integrante della pianura padana, ha le sue radici 
                immerse nella nebbia del tempo ed è circondato di leggende.<br>
                Della pianura padana si incomincia a parlare con un certo interesse 
                solo dopo l'arrivo delle legioni romane e grazie a loro sappiamo 
                che era già abitata da popolazioni primitive. Queste notizie sono 
                suffragate da rinvenimenti archeologici numerosi pure nel nostro 
                territorio.<br>
                Nel VI secolo avanti Cristo erano giunti i Galli, gli odierni 
                francesi, e due tribù di questi, i Galli Boi e i Galli Insubri, 
                si stabilirono in quella parte della pianura padana oggi conosciuta 
                con il nome di Lodigiano e Milanese. Costoro fondarono due grossi 
                villaggi e precisamente l'odierna Milano e Lodivecchio.<br>
                Il territorio sangiulianese venne così a trovarsi fra i Galli 
                Boi di Lodivecchio ed i Galli Insubri di Milano e forse il fiume 
                Lambro stabiliva già un confine naturale fra loro.<br>
                La costruzione della strada che univa Milano a Lodivecchio da 
                parte delle legioni romane assunse subito notevole importanza, 
                tanto che ad intervalli regolari furono istituiti ben presto posti 
                per il cambio dei cavalli ed il ristoro dei viaggiatori.<br>
                Gli agrimensori romani al seguito delle legioni imperiali nel 
                frattempo si erano premurati di suddividere il nuovo territorio 
                conquistato e questa delimitazione venne accettata senza modifiche 
                pure dai Longobardi.<br>
                I cippi romani che segnavano gli incroci dei confini e dedicati 
                al dio Termine, con la conversione dei Longobardi alla religione 
                cristiana vennero sostituiti da edicole o piccole chiese: Fu questo 
                il primo passo per la nascita di numerose chiese ancora oggi esistenti, 
                fra le quali la nostra parrocchiale. Attorno ad esse poi con il 
                tempo vennero a formarsi piccoli borghi, nucleo originale degli 
                odierni paesi.<br>
                Quando le legioni romane giunsero nel nostro territorio lo trovarono 
                rigoglioso e fertile, abbondantemente ricoperto di boschi come 
                testimoniano il nome di alcune cascine locali, e ricco di acque.<br>
                Sia i fontanili naturali, numerosissimi, che le limpide acque 
                dei fiumi, avevano inoltre creato vasti acquitrini.<br>
                Secondo una leggenda vi era una grande palude poco distante da 
                noi, abitata da un drago pestifero che con il suo alito contaminava 
                la vegetazione, gli animali e le persone. Noi oggi possiamo affermare 
                che quelle esalazioni tanto terribili non provenivano altro che 
                dalle sorgenti di acque solfuree presenti presso l'attuale S. 
                Colombano, a Miradolo.<br>
                Il primo compito dei romani quindi fu di regolare il corso delle 
                acque scavando fossati e cingendo fontanili, bonificando così 
                notevoli estensioni di territorio. Furono in questo modo gettate 
                le basi di quel processo inarrestabile che portò la nostra zona, 
                insieme ad altre vicine, ad essere esempio di ingegneria idraulica 
                e di coltivazione della terra.</font></p>
