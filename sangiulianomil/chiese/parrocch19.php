            <td align="left" valign="top" width="98%">
              <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>l'altare</u><br>
                <br>
                Imponente nelle dimensioni, colpisce per l'originalità e la semplicità 
                del disegno. Il piano della mensa, lungo tre metri, è sorretto 
                da quattro pilastrini trapezoidali ed aggettanti che danno l'illusione 
                di braccia tese. L'altare è in marmo rosso di persia e rosa portogallo 
                e la preziosità di questo materiale ben si addice allo scopo per 
                cui è stato destinato. Tutto il complesso è sopraelevato rispetto 
                al piano della chiesa di 75 cm e ciò gli permette di dominare 
                l'intera navata, mentre l'illuminazione proviene dall'alto attraverso 
                una apertura circolare.<br>
                <br>
                <u>il tabernacolo</u><br>
                <br>
                Opera in bronzo dello scultore Giancarlo Marchese eseguita nel 
                1965, il tabernacolo è visto in modo dinamico come un'esplosione 
                ed irraggiamento della maestà del Cristo. Si discusse a lungo 
                sul luogo migliore per la sua collocazione ed infine predominò 
                l'idea di sistemarlo sotto il Cristo, perfettamente in asse con 
                l'altare e l'atrio della Chiesa, lasciando la mensa spoglia.<br>
                <br>
                <u>il Cristo</u><br>
                <br>
                L'artista Giancarlo Marchese ha preso in considerazione per la 
                sua opera due concetti: la morte e la resurrezione di Cristo. 
                L'autore ha inteso mostrare ai fedeli un Cristo morto in croce, 
                risorgente e glorioso. La figura ha le braccia distese quasi fosse 
                in croce, mentre ai suoi lati due lastre lisce indicano il sepolcro 
                aperto per la resurrezione resa ancor più evidente dalle profonde 
                e variegate increspature del fondo dove la luce si riflette creando 
                notevoli contrasti di chiaroscuro. La figura del Cristo non ha 
                volto per l'impossibilità di rendere contemporaneamente il dolore 
                della morte ed il trionfo della resurrezione e l'intenzione dell'artista 
                è di invitare i fedeli ad immaginarlo continuamente. Nonostante 
                la sua mole ed il suo peso di ben 10 quintali, la composizione 
                scultorea in bronzo pare sospesa sul fondo chiaro dell'abside.<br>
                <br>
                <u>l'abside</u><br>
                <br>
                Un discorso più complesso richiederebbe l'illustrazione dei rilievi 
                che completano l'abside della nuova Chiesa eseguiti pure essi 
                dal Marchese nel 1980. Premesso che il Cristo ed il tabernacolo 
                rimangano i punti focali insieme alla mensa, il rilievo, osservato 
                nel suo complesso, assolve la funzione di legare architettonicamente 
                l'abside con la navata. Si è inteso, nell'esecuzione dell'opera, 
                tradurre in forme plastiche la tensione dei fedeli oranti. Infatti 
                l'elemento base del rilievo è costituito dalle impronte, reali, 
                di mani abbinate a segni carichi di tensione nello sforzo dell'ascesa.<br>
                Il materiale scelto, nello spirito di essenzialità e rigorosità 
                che già caratterizza l'architettura dell'edificio sacro, è il 
                cemento.<br>
                Anche il portone laterale sinistro è del medesimo materiale con 
                supporti metallici e la sua decorazione richiama i bassorilievi 
                dell'abside.<br>
                <br>
                <u>le vetrate</u><br>
                <br>
                La luce naturale penetra all'interno della Chiesa filtrata da 
                vetrate eseguite sul tipo dei vetri cattedratici antichi. Attraverso 
                il gioco del rosso e del grigio, che rappresentano i due colori 
                dominanti, viene così a crearsi la giusta atmosfera ed ogni particolare 
                ottiene un dosato risalto. I vetri utilizzati sono stati soffiati 
                a fuoco e legati con lo stagno, mentre il motivo ricorrente è 
                la barca e la vela.<br>
                <br>
                <u>la Via Crucis</u><br>
                <br>
                I pannelli della Via Crucis, contrariamente al solito, sono raggruppati 
                e collocati sulla sinistra del tempio entrando. Eseguiti da uno 
                scultore di Faenza nel 1966, sono 14 lastre in rame sbalzato di 
                due metri d'altezza ed 80 cm di base. Alcuni, come la Crocefissione, 
                Deposizione e Resurrezione, sono di pregevole fattura e l'opera 
                intera denota lo sforzo dell'artista nella trattazione di un tema 
                non certo facile.<br>
                <br>
                <u>le campane</u><br>
                <br>
                Le campane sono cinque, o meglio sei, perchè bisognerebbe aggiungere 
                la famosa campanella di San Carlo, posta sopra tutte. Si chiama 
                così perchè fu collocata ai tempi di San Carlo e precisamente 
                nell'anno 1580. Le cinque campane hanno tutte una scritta particolare 
                in rilievo sulla faccia esterna. Sono frasi scultoree, invocazioni 
                al Signore, oppure annunciano il compito delle campane stesse.<br>
                Un complesso sistema elettrico le mette in movimento all'ora esatta. 
                Infatti un orologio con banda perforata posta in sacrestia trasmette 
                gli impulsi elettrici ai motorini che, ai finchi delle campane, 
                danno il moto rotatorio. Tutte, durante i lavori di costruzione 
                dell'attuale Chiesa parrocchiale, vennero tolte dalla loro sede 
                e restaurate riprendendo così l'antico splendore.<br>
                <br>
                <u>la cripta</u><br>
                <br>
                La Chiesa invernale si sviluppa sotto l'area dell'altare maggiore 
                e ad essa si accede da una scala posta all'interno della parrocchiale 
                oppure dalla rampa esterna; inoltre è in comunicazione con l'Oratorio 
                Femminile al quale serve da Cappella. Questa cripta ha una superficie 
                di 200 mq e può contenere trecento persone. Viene utilizzata nei 
                giorni feriali d'inverno perchè di facile riscaldamento e quindi 
                più economica, motivo questo da non trascurare ai giorni nostri.</font></p>
