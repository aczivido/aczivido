        <p ALIGN="JUSTIFY"><small><u><font face="Verdana, Arial, Helvetica, sans-serif" size="2"><b>Molinazzo</b></font></u></small><br>
          <font face="Verdana, Arial, Helvetica, sans-serif" size="1">Posta a 
          fianco della via Emilia, di fronte alla cascina di Santa Brera, traeva 
          l'acqua per far azionare i suoi meccanismi idraulici dalla roggia Spazzola.<br>
          Edificio molto antico, di cui non si conosce l'anno di costruzione, 
          ma di cui sappiamo che nel 1438 era di propriet&agrave; dei capra, padroni 
          di Santa Brera, i quali da tempo avevano dato in affitto il Mulino a 
          Gabriele e Gaspare Bertolla, padre e figlio; vi &egrave; poi un contratto 
          d'affitto, stipulato il 4 gennaio 1480 a favore dei coniugi Bertolla, 
          sempre per la conduzione del Mulino.<br>
          La gestione cambi&ograve; e il Mulino risulta essere affidato il 5 gennaio 
          1501 a Giovan Pietro Faino ed a suo figlio Bernardino &quot;abitanti 
          nel Mulino di Sancta Brigida di propriet&agrave; del Signor Paolo Capra&quot; 
          i quali s'impegnarono a versare 14 mogge di biade residuo dell'affitto 
          dovuto.<br>
          Il 3 agosto 1517, Andrea e Francesco Capra danno un0investitura livellaria 
          a Hestore Visconti, fu Bernab&ograve;, per tutti i beni di Santa Brera, 
          compresi: la casa da Nobile, un mulino di tre rodigini, case, cascine 
          e forno, &quot;pertinenti a detto mulino con le sue ragioni d'acque, 
          di qua e di l&agrave; del Lambro, per un fitto annuo di lire imperiali 
          2.100, pi&ugrave; gli appendizi di un animale grasso, del valore di 
          non meno di 120 lire, quattro paia di capponi, un carro di fieno maggiengo, 
          un carro di paglia&quot;. La conduzione della propriet&agrave; oggetto 
          del contratto sopra indicato fu affidata da Hestore Visconti a Giovanni 
          Antonio de Bagnolo, detto il Signore.<br>
          La vendita probabilmente non and&ograve; a buon fine perch&eacute; queste 
          propriet&agrave; risultano essere in seguito ancora dei Capra.<br>
          Nell'inventario dei beni posseduti dai Capra, eseguito nel 1532, si 
          pu&ograve; leggere: &quot;un mulino da tre ruote, mal ridotto per le 
          passate guerre&quot;. Queste poche righe sono importanti, nella loro 
          essenzialit&agrave;, perch&eacute; indicano che i fatti bellici del 
          1515 influirono pesantemente sull'economia della zona, tanto che a distanza 
          di 17 anni il mulino &egrave; ancora diroccato. L'anno dopo il mulino 
          risulta essere non solo riparato, ma addirittura trasformato in &quot;Folla&quot; 
          per tessuti.<br>
          Ci&ograve; &egrave; deducibile da due documenti, uno del 1533 e l'altro 
          del 1535. Nel primo il Giudice delle Strade di Milano asserisce che 
          dalla &quot;Folla dei Capra&quot; iniziava un canale che andava a Sancta 
          Brigida e ordinava pertanto la &quot;purgationem di decta rugia et manutentionem&quot;, 
          perch&eacute; l'acqua che conduceva ogni tanto ne usciva andando ad 
          inondare le campagne di Zivido e lo stesso stradone (la via Emilia), 
          rendendo malagevole percorrerla.<br>
          L'altro documento &egrave; l'atto di una visita ispettiva, fatta per 
          ordine del Giudice delle Strade l'9 luglio 1535, un sabato, alla roggia 
          Spazzola. Quel giorno l'ing. Tommaso Della Valle del Comune di Milano, 
          il &quot;follatore&quot; Giovan Ambrogio de Prata e il proprietario 
          della &quot;Folla prospicente la strada&quot;, magnifico dott. Francesco 
          Capra assieme all'affittuario Ippolito Faino, s'incontrarono per verificare 
          lo stato della roggia.<br>
          Queste visite vennero fatte periodicamente e nel 1539 il Capra ebbe 
          a scrivere che il tratto di roggia vicino alla &quot;Folla&quot; aveva 
          le rive &quot;sit devastata&quot; e che per questo le aveva fatte ripulire.<br>
          Nel censimento dl 1537 nel Molinazzo troviamo &quot;Ludovico Fuso, molinaio 
          nel mulino del Dottore Monsignor Francesco Capra&quot;. Altri dati indicano 
          il ritorno del fabbricato alla primitiva origine del Mulino. La Giunta 
          comunale di Milano (XII di Provvisione), vendette il 26 giugno 1545 
          a Francescolo Capra il dazio della macina del frumento prodotto nel 
          podere di Santa Brera e macinato nel suo Mulino.<br>
          Come sappiamo Francescolo Capra don&ograve; i suoi beni all'Ospedale 
          dei Poveri Vecchi di Milano e nel 1571 questi vennero verificati per 
          la divisione da farsi con gli eredi: il Mulino pass&ograve; in propriet&agrave; 
          a Giovanni Paolo Pirovano.<br>
          In seguito l'ex Mulino dei Capra venne acquistato dai Brivio e della 
          sua antica ragione sociale non resta a ricordarcelo che il nome: &quot;Molinazzo&quot;.<br>
          <br>
          (Luciano Previato &quot;San Giuliano Milanese, una storia da raccontare&quot; 
