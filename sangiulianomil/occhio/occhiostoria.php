                        <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">Troviamo 
                          notizia di Occhiò per la prima volta in un documento 
                          stilato il 3 giugno 823 nella chiesa di San Martino 
                          a Carpiano, trattante delle permute di terreni. Tra 
                          i testimoni presenti a quell&#146;atto figura un certo 
                          Odeperto de Octabo (Occhiò).<br>
                          Un&#146;altra importante segnalazione dell&#146;esistenza 
                          di un centro abitato chiamato Occhiò ci viene dall&#146;elenco 
                          scritto dal Bussero nel XIII secolo, in cui si fa menzione 
                          dell&#146;esistenza di due Oratori situati in Occhiò, 
                          dedicati ai SS. Giovanni e Paolo e a Santa Maria. Il 
                          secondo si riferisce alla Chiesa esistente presso il 
                          fabbricato della Follazza di Occhiò. Nel documento di 
                          frate Goffredo è scritto: &quot;&#133; In plebe Sancti 
                          Iuliani in loco Ucloe ecclesia Sanctorum Iohannis et 
                          Pauli, ed ancora &#133; in plebi Sancti Iuliani in loco 
                          Ucloe ecclesia Sancte Maria&quot;.<br>
                          Questo ci induce ad alcune riflessioni, prima fra tutte 
                          quella del nome: Occhiò!<br>
                          Esso deriva dal limes posto all&#146;VIII miglio della 
                          via Romana, in corrispondenza del quale, come voleva 
                          il sistema di centurizzazione latino, si dipartiva un 
                          sentiero campestre che conduceva alla località che oggi 
                          chiamiamo Occhiò. Il nome subì, nel decorso dei secoli, 
                          numerose trasformazioni, quali Veloè, Vigloè, Oggiò, 
                          fino all&#146;attuale denominazione; tutte però derivanti 
                          da un unico ceppo onomastico: Octavium.<br>
                          Il territorio fu inizialmente quasi tutto di proprietà 
                          ecclesiastica ed al riguardo sono molti i documenti; 
                          possiamo citare il pagamento fatto nel 1483 da Paolo 
                          Capra, padrone del fondo di cascina Santa Brera, del 
                          fitto livellario di alcuni beni di</font></p>
