                        <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
                          proprietà dei monaci benedettini di S. Celso. Sempre 
                          il Capra il 20 ottobre 1500 acquistò un campo detto 
                          Vigna dell&#146;Osnaga impegnandosi a pagare per dieci 
                          anni 50 lire e poi 300 lire, un totale quindi di 800 
                          lire da pagare a Francesco Pelirone.<br>
                          Tra gli Ordini religiosi proprietari in Occhiò vi fu 
                          il Monastero di Santa Maria Assunta del Lentasio, che 
                          possedeva alcune case ed un fondo di 386 pertiche circa. 
                          Inizialmente il fondo era di 164 pertiche, che, nel 
                          1394, risultavano essere in enfiteusi perpetua di Stefano 
                          Gambari, il quale pagava un livello annuo di tredici 
                          moggia di avena e segale, due moggia di frumento, una 
                          moggia di fave, nove brente di vino, (come appare da 
                          uno strumento notarile di enfiteusi, rogato da Belino 
                          da Merlino il 4 maggio 1394).<br>
                          Un discendente di Stefano Gambari, Francesco, nel 1521, 
                          cedette il suo diritto di enfiteusi a Battista Saronno; 
                          nel 1621 le monache del Lentasio mossero causa, davanti 
                          al senatore Leizaldo, presidente del Magistrato straordinario, 
                          a Giobatta Saronno, perché non pagava il fitto dovuto. 
                          Così cessò l&#146;enfiteusi perpetua.<br>
                          Il fondo nel 1625 tornò al Saronno con un contratto 
                          di fitto semplice, rinnovabile ogni tre anni; il Saronno 
                          morì nel 1632 senza aver pagato il fitto e con altri 
                          debiti verso il Monastero, cosicchè questo, oltre alla 
                          ridisponibilità immediata del fondo di proprietà, ottenne, 
                          a titolo cautelare di garanzia dei propri crediti, anche 
                          i beni di proprietà dei Saronno, che erano contigui 
                          a quelli del Monastero.<br>
                          In seguito le monache perfezionarono, tra il 1648 e 
                          il 1653, l&#146;acquisto delle terre di Lodovico Saronno, 
                          figlio di Giobatta, in modo tale che le proprietà fondiarie 
                          aumentarono a 386 pertiche.<br>
                          Le altre terre di Occhiò erano divise tra: il Luogo 
                          Pio delle Quattro Marie, proprietario di circa 500 pertiche; 
                          il Monastero di Santa Maria della Vittoria, proprietario 
                          di circa 300 pertiche, acquistate nel 1585 da Giobatta 
                          Patrocinio di Melegnano, e di una casa con cinque pertiche 
                          di terra, acquistate nel 1619 da Cesare Brivio; il Collegio 
                          di S. Barnaba, proprietario di 420 pertiche; la famiglia 
                          Brivio, proprietaria di 320 pertiche.<br>
                          Dal censimento del 1537 si rilevano i proprietari di 
                          terra dai loro massari: Francesco Bresciano per Leone 
                          De Squassi , Ambrogio Mesini per Federico Griffo, Giovannangelo 
                          De Ferrari per il marchese Della Croce, Domenico Medisoni 
                          per Ambrogio De Arnazze e Francesco De Ferrari per il 
                          Sig. Marchesoni. E&#146; pure un sarto di panno e lana 
                          nella persona di Alessandro Dal bene e un filatore in 
                          Cristoforo Montano.<br>
                          Uno dei documenti dell&#146;amministrazione civile del 
                          Forese di Milano per l&#146;anno 1662, così recita: 
                          &quot;Libro del carico della comunità di Vigloè detto 
                          Occhionno, comenciando l&#146;anno 1662 et seguita.<br>
                          Riparto fatto da me sottoscritto Carlo Sesti, antiano 
                          della Plebe di Santo Giuliano, alli homini et habitanti 
                          di Vigloè per il carico del tasso della cavalleria et 
                          altri dipendenti per l&#146;anno presente, fatto d&#146;ordine 
                          de Dionisio farè, Console di detta terra et della maggior 
                          parte delli homini et d&#146;ordine di detto Console, 
                          sonato la campana et congregato il popolo in piazza, 
                          acciò restino informati di tal riparto.<br>
                          &nbsp;&nbsp;&nbsp; Staia 13 di sale per il tasso della 
                          cavalleria lire 185,18<br>
                          &nbsp;&nbsp;&nbsp; Censo alla Regia Camera lire 27,40<br>
                          &nbsp;&nbsp;&nbsp; Provvisione al Commissario Generale 
                          lire 5,60<br>
                          &nbsp;&nbsp;&nbsp; Censo dovuto al Signor Conte Del 
                          Pozzo lire 13<br>
                          &nbsp;&nbsp;&nbsp; Censo dovuto al Signor Ippolito Brivio 
                          lire 9<br>
                          &nbsp;&nbsp;&nbsp; Alle due banche Criminali lire 2<br>
                          &nbsp;&nbsp;&nbsp; Spesa per strada maestra lire 7<br>
                          &nbsp;&nbsp;&nbsp; Antianaria, salario all&#146;antiano 
                          lire 3,50<br>
                          &nbsp;&nbsp;&nbsp; Spese per l&#146;ordinario anno corrente 
                          lire 4,20<br>
                          &nbsp;&nbsp;&nbsp; Salario per il Console lire 12<br>
                          &nbsp;&nbsp;&nbsp; Totale lire 268,88<br>
                          &nbsp;&nbsp;&nbsp; Ripartite le suddette lire 268,88 
                          sopra bocche 63 in detto comune&quot;.<br>
                          &nbsp;&nbsp;&nbsp; Si conosce inoltre il numero degli 
                          abitanti di Occhiò negli anni:<br>
                          &nbsp;&nbsp;&nbsp; 1570: persone 49<br>
                          &nbsp;&nbsp;&nbsp; 1662: persone 63<br>
                          &nbsp;&nbsp;&nbsp; 1722: persone 144<br>
                          &nbsp;&nbsp;&nbsp; 1785: persone 220<br>
                          Un esame attento sul numero degli abitanti ci fa notare 
                          il costante progresso che Occhiò conobbe nel passato; 
                          oggi, relegata l'agricoltura in posizione subalterna 
                          all'industria, si sta verificando il fenomeno inverso: 
                          infatti attualmente vi abitano meno di 50 persone.<br>
                          Altri dati su Occhiò permettono di conoscere il nome 
                          d&#146;alcuni Consoli della Comunità: Pedro Riboldo 
                          nel 1537; Tommaso de Martino nel 1648, seguito da Andrea 
                          Possente sino al 1660; poi Dionisio Farè; indi, nel 
                          1692, Giovanni Mandello; nel 1722 Francesco Fiocco e 
                          nel 1752 Piero Matto.<br>
                          Della chiesa dedicata a due martiri romani SS. Giovanni 
                          e Paolo si sa che è antichissima. Nominata nel XIII 
                          secolo, deve aver subito diversi riadattamenti ed un 
                          completo rifacimento alla fine del Seicento o nel Settecento.<br>
                          Nel libro delle ordinazioni fatte durante la visita 
                          di San Carlo Borromeo alla Pieve di San Giuliano nel 
                          1573, si trova scritto che non si dovrà più celebrare 
                          la messa nella chiesa di Occhiò se prima non verrà riattata. 
                          Dovrebbe trattarsi quindi di un edificio in non buone 
                          condizioni. Nel 1589 &quot;Li huomini de la terra di 
                          Vigloè&quot; fecero una supplica all&#146;arcivescovo 
                          affinchè i beni e le rendite della diruta chiesa di 
                          Santa Maria alla Follazza fossero devoluti alla loro. 
                          Nella visita effettuata nel 1602 dall&#146;arcivescovo 
                          Federico Borromeo è scritta una descrizione della chiesa 
                          nella quale è detto &quot;ch'essa ha un solo altare, 
                          non ha sepolture (in chiesa) né cimitero esterno. Campanile 
                          bello con campana in fondo all&#146;Oratorio; vi è un 
                          confessionale ma non vi è casa per il cappellano. La 
                          messa è mantenuta dal popolo e le chiavi le tiene il 
                          fittabile. Vi è l&#146;usanza che nella festa dei SS. 
                          il popolo di Chiaravalle venga in processione con candele 
                          alla messa, canta il Prevosto la messa e quelli restano 
                          soddisfatti ed inoltre riceve ancora li denari dello 
                          pane e cera &#133; Si benedicono micconi e se ne distribuisce 
                          un sacco. La spesa di detti micconi si fa dal popolo&#133;&quot;<br>
                          Il 26 giugno, giorno dei patroni della chiesa di Occhiò 
                          era dunque festa grande, addirittura con una processione 
                          di fedeli che veniva, ceri alla mano, sino ad Occhiò 
                          dove poi si benediva il pane e se ne dava ai poveri 
                          un sacco.<br>
                          Questa chiesa deve essere stata poi abbattuta per far 
                          posto a quella attuale; un bell&#146;edificio, solido, 
                          ancora resistente, malgrado sia in deplorevole stato 
                          di abbandono da quasi vent&#146;anni. Quando venne costruito 
                          ebbe a trovarsi di circa quattro metri sopra il livello 
                          stradale e di questo tumulo non vi è spiegazione alcuna. 
                          L&#146;unica potrebbe essere che sotto si trovino i 
                          resti dell&#146;Oratorio precedente. Da più parti si 
                          dice anche che vi siano stati sepolti molti caduti della 
                          Battaglia dei Giganti, che ebbe a svolgersi anche qui.<br>
                          Di questa chiesa si conosce un episodio curioso, accaduto 
                          nel 1722.<br>
                          In quell&#146;anno, il parroco di San Giuliano si lamentò 
                          che ad Occhiò si officiava nei giorni festivi troppo 
                          presto: mezz&#146;ora dopo il levar del sole. Egli intimò, 
                          come le leggi canoniche imponevano, che si officiasse 
                          solo dopo che la prima Messa fosse stata celebrata nella 
                          chiesa parrocchiale.<br>
                          I fittabili di Occhiò protestarono, perché, a loro dire 
                          se i contadini fossero andati a Messa dopo l&#146;orario 
                          consueto non avrebbero potuto accudire con profitto 
                          al lavoro della fabbricazione del formaggio: al riguardo 
                          si dilungarono in una serie di argomentazioni per sostenere 
                          la loro tesi. Il parroco di San Giuliano, che certo 
                          aveva compreso bene lo scopo venale dei fittabili, che 
                          era quello di non permettere che si perdesse tempo nel 
                          voler adempiere al dovere cristiano dell&#146;assistere 
                          alla Santa Messa domenicale, iniziò una prova di forza 
                          nei confronti dei padroni di Occhiò e ne finì vincitore 
                          della loro grettezza.<br>
                          Vi è da tener presente, infatti, che allora non c&#146;era 
                          l&#146;obbligo del riposo festivo ma solo quello morale 
                          dell&#146;assistere ai servizi divini, per cui era demandato 
                          all&#146;arbitrio dei datori di lavoro di lasciar liberi, 
                          la domenica per alcune ore, i propri dipendenti; tuttavia 
                          il non concedere questo permesso, avrebbe certo creato 
                          dei grattacapi con le autorità.<br>
                          Il fondo di Occhiò, 1.400 pertiche milanesi, all&#146;inizio 
                          del secolo passò in gestione a Domenico Pizzini.<br>
                          Fu lui, durante l&#146;ultima guerra, a far aprire nella 
                          frazione una scuola rurale, sistemata in un edificio 
                          opportunamente adattato. Nell&#146;anno scolastico 1944/45 
                          l&#146;insegnate fu Bianca Novellati; nell&#146;anno 
                          scolastico del 1955 erano presenti 19 alunni scesi a 
                          14, per tutte le cinque classi elementari, nel 1959.<br>
                          Dopo la guerra vi venne aperto uno spaccio cooperativo, 
                          per servire le numerose famiglie che allora vi abitavano.<br>
                          Oggi esiste ben poco del grosso borgo rurale che fu 
                          Occhiò per tanto tempo: sono scomparse le case dei Mariani, 
                          un rudere, la sua antica chiesa, la cooperativa che 
                          ha chiuso i battenti per mancanza di clienti, la scuola, 
                          lontano ricordo, chiusa nel 1959.<br>
                          Occhiò sta vivendo ora un periodo migliore e ciò per 
                          merito del nuovo proprietario, Franco Molteni, che ha 
                          salvato dall&#146;obsolenza i vari edifici, favorendo 
                          con ciò il ritorno abitativo.<br>
                          <br>
                          (da Luciano Previato &quot;San Giuliano Milanese, una 
                          storia da raccontare&quot; 1989)</font></p>
