              <td align="left" valign="top"><p>San Giuliano - L'associazione Zivido presenter&agrave; il progetto al comune<br>
                    Nascer&agrave; un museo &quot;dei Giganti&quot; per salvare i reperti del Sudmilano<br>
                    <br>
                    I due antichi coperchi di sarcofaghi risalenti all'epoca romana, insieme agli affreschi di Occhi&ograve; e ad altri reperti storici sparsi per il territorio sangiulianese, potrebbero finire in un museo inserito nel disegno per il futuro del Parco dei Giganti. &Egrave; una delle proposte dell'associazione Zivido rivolte all'amministrazione comunale, a cui entro il mese di maggio sar&agrave; presentato un innovativo piano pensato da un team di tre esperti, che si sono offerti spontaneamente per stilare un progetto pilota che verrebbe tastato per la prima volta su quei 136 mila metri quadrati. &laquo;Uno degli aspetti peculiari del lavoro in via di definizione - spiega il presidente del sodalizio Pierino Esposti -, riguarda la creazione del caratteristico ambiente lombardo, attraverso particolari combinazioni di specie arboree autoctone. Inoltre vorremmo riattivare il mulino, dove abbiamo pensato di insediare anche dei laboratori. Mentre un'altra professionista sta pensando a valorizzare i corsi d'acqua, concentrandosi sulle canalizzazioni, sulle risorgive e su vari ambiti per ripristinare una caratteristica morfologica che contraddistingue quest'area del Sudmilano&raquo;. Intanto uno studioso di storia, sta pensando insieme al sodalizio a creare un punto di riferimento dove concentrare tutto ci&ograve; che rimane del passato di San Giuliano. E se il recupero dell'ormai fatiscente chiesetta di Occhi&ograve; diventasse impossibile, l'idea sarebbe quella di salvare quanto meno gli affreschi, che verrebbero anch'essi ospitati all'interno del parco. Un'ipotesi per cui a breve verranno presi contatti con la Soprintendenza per i beni artistici. &laquo;Per la sistemazione della chiesa occorrerebbero parecchi fondi - prosegue il presidente dell'associazione -, ma a questo punto sarebbe interessante salvaguardare i dipinti, tra cui si intravede una Madonna in trono tipica del 1300: un affresco di grosse dimensioni, che da quel poco che si vede, dovrebbe mantenere ancora dei colori vivaci e ben delineati. Inoltre c'&egrave; la vecchia lapide della chiesina di San Gioacchino, e i due coperchi di sarcofaghi utilizzati nel tempo come abbeveratoi che si trovano a Zivido e a Mezzano. Puntiamo anche a farci restituire dalla Soprintendenza i reperti rinvenuti durante gli scavi a Zivido. Tutto materiale, che potrebbe essere raccolto in una struttura a nord del Parco, dove &egrave; prevista anche un'area di ristoro&raquo;. Ambiziosi progetti quindi, per l'area designata a diventare il patrimonio verde della frazione, arricchito da un ricco archivio storico, che toglierebbe dall'anonimato tanti elementi nascosti in aree ben poco frequentate di San Giuliano.<br>
                    <br>
                (di Giulia Cerboni, &quot;Il Cittadino&quot;, 24.04.2003)</p></td>
