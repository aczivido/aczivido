              <td align="left" valign="top"><p><strong>Riusciranno gli svizzeri a salvare il borgo medioevale di Zivido?</strong><br>
                    <br>
  L'Associazione Culturale Zivido sta cercando con tutti i mezzi di far conoscere e nello stesso tempo di sensibilizzare l'opinione pubblica, che il Borgo medioevale di Zivido, va salvaguardato e difeso da ogni forma di speculazione edilizia.<br>
  Sono state molte le iniziative tendenti alla valorizzazione e al rispetto di questo angolo carico di storia; il pi&ugrave; significativo &egrave; di un anno fa, allorch&egrave; il Parlamento Svizzero, tramite l'Associazione Pro Marignano, tent&ograve; di acquistare dieci chilometri quadrati di territorio sangiulianese, per creare una fasci di rispetto e di salvaguardia di quella zona che rappresenta per gli elvetici un momento saliente della loro storia. A quel tempo gli svizzeri, motivarono la loro iniziativa per impedire il continuo avanzare del cemento e salvare l'ultima fascia di territori dove si svolse la &quot;Battaglia dei Giganti&quot;.<br>
  L'Associazione, con in testa il suo presidente Pierino Esposti, interessata a far conoscere il centro storico di Zivido, sta cercando di sensibilizzare l'opinione pubblica e di far radicare in tutti quei valori culturali che lentamente stanno scomparendo. Nel Borgo sono da salvare il fatiscente vecchio cascinale del 1300, il Castello Medievale, teatro di un cruento assalto francese, una cascina ottocentesca, l'antica casa dei Bonacina, la cappella privata dei Brivio, nobile famiglia feudataria della zona e, su tutti, la famosa casa dello storico pittore, Bergognone.<br>
  La Svizzera sta cercando, attraverso vie diplomatiche, di costituire il suo 24&deg; Cantone qui in Italia e pi&ugrave; precisamente a Zivido. Ma perch&egrave; questo interessamento, si chiederanno molti. Un passo indietro &egrave; doveroso farlo.<br>
  Era il 13 settembre dell'anno 1515, alle sedici si trovarono di fronte due eserciti, quello francese comandato da Francesco I e quello svizzero, alleato di Massimiliano Sforza, duca di Milano. Al termine degli scontri, si contarono migliaia di morti da ambo le parti, 12.000 per i francesi che videro morire anche otto membri della famiglia reale e 5.000 per gli svizzeri.<br>
  Dopo questa cruenta battaglia, gli svizzeri capirono che la loro non poteva considerarsi grande potenza; inizi&ograve; quindi la vocazione alla neutralit&agrave;, che dura tutt'oggi. Quei fatti, ancora vivi nell'animo dei transalpini, spingono ogni anno molti elvetici a visitare i luoghi intorno a Zivido: Molte sono le iniziative per ricordare gli avvenimenti storici. E' programmata quest'anno, anniversario dell'erezione del monumento ai caduti svizzeri, situato nel cortile della parrocchia, una sfilata storica in costume d'epoca, con la partecipazione di autorit&agrave; italiane e straniere.<br>
  L'Associazione si sta muovendo in molte direzioni. Giorni fa &egrave; stata ospite della trasmissione radiofonica &quot;Agenda Lombarda&quot; cndotta sulla rete nazionale da Silvio Morra. Venerd&igrave; 20 gennaio alle ore 21,00 alla presenza di Andrea Cancellato, sindaco di Lodi, Maurizio Riccotti, assessore al territorio della Regione Lombardia, Egidio Gilardi, sindaco di San Giuliano Milanese e del Dr. Fritz von Ins, console generale aggiunto di Svizzera a Milano, ha organizzato nel Castello Brivio a Zivido, una conferenza sul tema: &quot;Centri storici del sud milanese - loro valorizzazione e recupero&quot;. Ci auguriamo che tutte queste iniziative a favore della valorizzazione e della giusta considerazione che si deve dare ad un luogo ricco di storia, abbia la giusta definizione e che la &quot;battaglia&quot; intrapresa dall'Associazione Culturale, dia una svolta decisiva a certe posizioni come la famosa &quot;Battaglia dei Giganti&quot; e che, finalmente, anche i Verdi e gli Ambientalisti nostrani si decidano a scendere sul campo di &quot;battaglia&quot;.<br>
  <br>
  (di Stefano Sportelli, &quot;Lodisette&quot;, 22.01.1990)</p></td>
