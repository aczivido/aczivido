            <tr>
              <td align="left" valign="top"><p>Coerentemente alle strategie del Parco Agricolo Sud Milano di valorizzazione della fruizione e del turismo si deve pensare quest&#146;area a sistema con le altre risorse e ai percorsi che le connettono. La realizzazione di tale sistema che valorizzi e renda godibile ai cittadini il patrimonio storico-ambientale pu&ograve; essere realizzato con la riqualificazione delle campestri e delle strade minori che collegano i nuclei rurali, le cascine, le abbazie, i boschi, i fiumi, i fontanili etc.<br>
                    Si tratta quindi di concretizzare a livello locale il grande progetto delle greenways, quella rete di &#147;percorsi verdi&#148; che si pongono l&#146;obiettivo di migliorare il rapporto tra uomo e territorio, contribuiscono a ridare identit&agrave; e riconoscibilit&agrave; a luoghi troppo spesso compromessi dalla crescita disordinata delle citt&agrave;, dall&#146;abbandono e dell&#146;incuria delle aree rurali. Le greenways sono un sistema di &#147;circolazione dolce&#148; integrativo alla mobilit&agrave; motorizzata attraverso tutto ci&ograve; che &egrave; apprezzabile dal punto di vista ambientale, naturalistico, paesaggistico, storico e culturale. Sono vie di movimento per pedoni, ciclisti, persone a cavallo, con funzione ricreativa e educativa e anche corridoio di movimento per la fauna. Il progetto di percorsi verdi per San Giuliano Milanese pu&ograve; contare sull&#146;appoggio di molti elementi di pregio e pu&ograve; essere connesso a una rete pi&ugrave; ampia. Si fa riferimento ad esempio alla possibilit&agrave; di connettersi al progetto del nuovo parco agricolo intercomunale della Vettabbia (135 ettari di cui 35 di foresta e 100 a parco che si estende fino alla foce della roggia Vettabbia a Melegnano, comprende al suo interno la storica abbazia di Chiaravalle e il depuratore di Nosedo, presentato dalla Regione nel dicembre 2003). Un progetto di un percorso verde per l&#146;area agricola che si trova tra la Via Emilia e il Lambro (tutelata come &#147;Bellezza paesaggistica&#148; dalla legge 1497/39, ora Testo Unico 490/99) potrebbe partire dal castello nel centro di Melegnano passando per il Bosco di Montorfano, Rocca Brivio, Cascina Santa Brera, costeggiando il Lambro, per Cascina Carlotta e per il nuovo parco di Zivido lungo l&#146;attuale strada campestre, per la chiesa della Nativit&agrave; di Maria, il borgo e il castello, per le altre cascine fino al borgo di Carpianello per riconnettersi al sistema delle piste ciclabili comunali.<br>
                Il nuovo parco assume in questa prospettiva, quale nodo portante di una greenway, un valore aggiunto che richiede di ripensare a questo sito come luogo profondamente radicato nel suo territorio e nella sua storia e nello stesso impone la ricerca di una nuova forte e riconoscibile identit&agrave; ed eccezionalit&agrave;.</p></td>
