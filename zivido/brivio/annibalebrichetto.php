    <p ALIGN="JUSTIFY">La famiglia Brivio discendente dai Signori di Airuno viceconti di
    Bergamo prese il nome dalla Signoria di Brivio di cui fu investita fino al 975 dai conti
    di Lecco e dopo tale data dai vescovi di Bergamo che erano successi nel possesso di quel
    feudo, finchè nel 1026 la signoria passò al Monastero di S. Dionigi.<br>
    Già dal 1189 possedeva beni in Paullo e nel 1251 Ambrogio di Brivio acquista beni in
    Zivido e suo figlio Todesco cittadino milanese nel 1272 acquistò il beneficio delle
    decime di Zivido già appartenente a Napoleone della Torre.<br>
    Da quel tempo Todesco, i suoi fratelli e i loro discendenti estesero i loro beni
    costeggianti il Lambro dalla Rocca Brivio (Melegnano) fino a Linate curandone la bonifica
    e l&#146;irrigazione colla costruzione di dighe su quel fiume e precisamente quelle di
    Carpianello, Bolgiano, Gabasso.<br>
    La famiglia parteggiò coi nobili milanesi fuoriusciti contro i Torriani;
    coll&#146;avvento alla signoria di Ottone Visconti nel 1277 fu ascritta per privilegio
    (essendo forese) alla matricola delle nobili famiglie milanesi che potevano avere
    l&#146;accesso all&#146;ordinariato della Metropolitana (Cardinales sanctae Ecclesiae
    Mediolanensis).<br>
    Con varie vicende fu fedele alla dominazione Viscontea e fu uno dei più validi appoggi
    della Sforzesca.<br>
    Caduto Ludovico il Moro i tre fratelli Brivio:Gian Francesco questore
    dell&#146;ordinario;Aloisio giureconsulto collegiato;Alessandro capitano ducale (figli di
    Giacomo Stefano pure questore dell&#146;ordinario) essi promossero anche accettando in
    casa una ribellione che permise la riconquista del ducato allo Sforza.<br>
    Dopo Novara Aloisio fu deportato in Francia e gli altri fuggirono.<br>
    I loro beni furono confiscati; la grazie e il condono della pena per il delitto di
    ribellione fu concessa dal Cardinale d&#146;Amboise ai fratelli Brivio coll&#146;esborso
    di 8.000 ducati d&#146;oro nel 1500.<br>
    Mentre ad Aloisio e ad Alessandro poterono ritornare in patria Gian Francesco,
    maggiormente compromesso, restò in esilio a Crema fino al 1505 e ritornò a Milano con un
    nuovo pagamento di 2.000 scudi d&#146;oro.<br>
    Gian Francesco organizzò a Milano il ritorno di Massimiliano sovvenzionando del suo con
    1.000 ducati d&#146;oro.<br>
