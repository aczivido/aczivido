    <td width="20%" align="left" valign="top"><p><strong>Gli Svizzeri avanzano...</strong></p></td>
    <td width="80%" align="left" valign="top"><p><a href="/zivido/igiganti/personaggi/schinner.php">Matteo Schinner</a>, in quei giorni di attesa e di permanente consiglio, si dichiar&ograve; senza esitazione per la guerra. Occorreva essere concordi - disse - ed agire tutti insieme. Il trattato di Gallarate era un tranello: accettarlo, significava condannare gli svizzeri a non uscir pi&ugrave; dalle loro valli.<br>
        Si discuteva, e pi&ugrave; aspramente del solito, nel primo pomeriggio del 13 settembre; quando irruppe nella sala del consiglio un cuoiaio di Zurigo, Rodolfo Han: &quot;Battaglia!&quot;, annunci&ograve;. Si combatteva sotto le mura. I francesi si erano fatti sotto.<br>
        I capi confederati accorsero: fu subito chiaro che si era trattato di un semplice incidente: una zuffa, agli avamposti, tra sentinelle e qualche cavaliere francese in ricognizione. Il nemico non era in vista... Ma il cardinale di Sion non si lasci&ograve; sfuggire quella occasione; arring&ograve; brevemente la massa eccitata dei soldati; esort&ograve; alla battaglia; e &quot;sibbene rivestito dei purpurei abiti pontificali&quot;, mont&ograve; in sella e s'avvi&ograve; verso la Porta Romana.<br>
        Lo seguirono tutti, bandiere al vento, per la strada di Lodi. Marciavano, davanti a tutti, i &quot;perduti&quot;: &quot;ed erano costoro uomini elettissimi di tutti i Cantoni, d'et&agrave; fiorita e di singolare prestanza i quali, per poter conseguire rapidamente i rari onori della milizia, con prova d'illustre valore, usavano mettersi volontariamente a tutti gli aspri e difficili uffici della guerra. Ed erano conosciuti dagli altri, solo dai candidissimi mazzi di penne che con gran pompa portavano sui cappelli...&quot;<br>
        Verso la localit&agrave; di San Donato, la massa tumultuante dei Confederati lasci&ograve; la strada ed entr&ograve; nei campi; alcuni ufficiali, che s'erano portati avanti a riconoscere il terreno, cercarono d'arrestare la marcia.<br>
        Attaccare subito - dissero - era follia. Non si combatteva dopo una corsa come quella, non si combatteva a stomaco vuoto; e se occorreva farlo, non ci si buttava avanti cos&igrave;, in disordine e proprio in quel giorno infausto, il tredicesimo del mese!... Proposero d'accamparsi, di mangiare, e d'attaccare il giorno dopo.<br>
    Ma i soldati, in tumulto, non ascoltarono; e in una sorta d'eroico ammutinamento, s'ordinarono su tre quadrati, e seguirono i &quot;perduti&quot; che s'avviavano verso il nemico. Ai comandanti non rest&ograve; che portarsi in testa ai loro reparti.<br><br>(da &quot;La Battaglia dei Giganti&quot; di Mino Milani - Storia Illustrata - Aprile 1973)</p></td>
