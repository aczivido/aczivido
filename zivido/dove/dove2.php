    <td width="20%" align="left" valign="top"><p><strong>...Arrivano i Francesi </strong></p></td>
    <td width="80%" align="left" valign="top"><p>Il 10 settembre l'esercito arriva a Melegnano.
        
     Qui per tentare di capire esattamente i movimenti del re e dei suoi, bisogna dimenticare la topografia del giorno d'oggi e ricostruire quello che era l'aspetto del paese (Melegnano) nel '500; in particolare la differenza pi&ugrave; importante &egrave; che la via Emilia (che Pasquier chiama &quot;le gran chemin&quot;) passava per il paese e l'unico ponte sul Lambro (Pasquier lo chiama &quot;Dambre&quot;) era quello che si trova al centro del paese.<br>
  All'epoca Melegnano era probabilmente tutto sulla destra del fiume e il ponte divideva il paese dai sobborghi.<br>
  Per alloggio del Re era stato scelto dai suoi l'albergo del <strong>Cappello Rosso</strong>, situato, secondo alcuni, dove oggi c'&egrave; il ristorante &quot;Al Vapore&quot; (a nostro avviso, invece, doveva essere sull'attuale via Vittorio Veneto, perch&egrave; Pasquier dice &quot; vicino alla porta, all'uscita della citt&agrave;, andando verso Milano&quot;).<br>
  Viene invece un contrordine: il Re prende alloggio nei sobborghi, oltre il ponte, vicino &quot;all'immagine di San Giovanni&quot;, un buon alloggio situato alla biforcazione di due strade, davanti al quale c&#146;&egrave; un grande olmo: noi pensiamo che il posto possa essere individuabile nell'ex <strong>cascina Pallavicina</strong>, all'incrocio tra la strada Pandina e la &quot;gran strada&quot;.<br>
  I comandanti e il Connestabile si portano invece oltre, sulla gran via, in parte all'attuale <strong>cascina Bernarda</strong> e in parte a <strong>Sordio</strong> e al <strong>Bissone</strong>.<br>
  Dunque, secondo Pasquier, il Re non si &egrave; accampato n&egrave; a Mulazzano, n&egrave; a Casalmaiocco; egli menziona questi due paesi (&quot;Mulsan&quot; e &quot;Casal&quot;) solo per dire che sono a poca distanza dall'alloggio del Re.<br><br>
  (dal testo della conversazione tenuta da <a href="/zivido/felcaro.php">Riccardo Felcaro e Giovanni Canzi</a> del &quot;Gruppo Amici della Storia Locale&quot; nella biblioteca di San Giuliano Milanese la sera del 10 settembre 1998 in occasione dell'appuntamento culturale <a href="/zivido/archeologia/zivarcheo1.php">&quot;Zivido Segreta&quot;</a> promosso dall'Associazione Culturale Zivido nell'ambito delle manifestazioni per l'VIII edizione della rievocazione storica &quot;<a href="/zivido/igiganti/gigcopertina.php">Ritornano i Giganti</a>&quot;).</p></td>
