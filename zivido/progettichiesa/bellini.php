    <td width="70%" align="left" valign="top"><p>San Giuliano Milanese si trova sulla via Emilia tra Milano e Lodi. Lo scenario di questa zona è mutato velocemente negli ultimi anni. Non esiste più un elemento di passaggio dalla &quot;bassa&quot; alla città; il tracciato della via Emilia è caratterizzato da un'urbanizzazione diluita e dalle nuove &quot;cattedrali della felicità&quot;: enormi ipermercati che diventano centri di aggregazione, con grandi piazze-parcheggio e insegne luminose fuori scala. Il nuovo centro parrocchiale si pone come punto di riferimento di una periferia che, a parte il vecchio borgo, è cresciuta, così come la maggior parte delle periferie delle grandi città, in maniera quasi informe. Una piazza circolare pavimentata, guarnita da una corona di alberi, collega idealmente tre costruzioni: una chiesetta con caratteri quattrocenteschi ormai inadatta ad assolvere la funzione di centro parrocchiale e due nuovi edifici che, disposti radialmente sulla piazza, definiscono insieme agli alberi uno spazio protetto. La grande facciata della nuova chiesa, dall'aspetto austero e allo stesso tempo permeabile, è la promessa di un interno che, nella sua semplicità, svela un'articolazione più complessa. Una corte aperta ma &quot;definita&quot; verso il cielo da una serie di capriate &#150; quasi l'ossatura di una grande basilica &#150; è racchiusa lungo i lati maggiori da corpi porticati dedicati alle funzioni complementari alla chiesa, la canonica e le aule didattiche. Sul lato di fondo, una grande e discreta vetrata con un portale intagliato al centro separa fisicamente dallo spazio aperto l'edificio di culto vero e proprio. Con questa costruzione, innovativa e, al contempo, profondamente legata alla concezione originaria dello spazio sacro, si è voluto reinterpretare il senso dell' &quot;ecclesia&quot;. La vita della chiesa non si limita, infatti, al solo momento liturgico, ma coinvolge nelle sue forme modi e usi che le appartengono da sempre e che sono spesso disorganici al suo impianto. Li coinvolge in modo da esprimere anche in senso estetico la realtà profonda di una comunità raccolta attorno alla sua chiesa.<br>
