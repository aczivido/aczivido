    <td width="70%" align="left" valign="top"><p>Non esistono, in questa zona di San Giuliano Milanese, elementi urbani capaci di orientare la chiesa: come tutte le periferie anche questa, più che orientare, disorienta. La nuova chiesa di Santa Maria deve quindi assumersi il compito di divenire un punto di riferimento riconoscibile per questa parte di &quot;non città&quot; e di &quot;non campagna&quot;. L&#146;alta e lunga nave sospesa è l&#146;elemento orientante scelto; essa si eleva sopra il sistema di volumi che ospitano le funzioni della parrocchia, rivolgendo la sua facciata a ovest, mentre gli altri edifici si dispongono paralleli ai confini del lotto. L&#146;intero complesso parrocchiale si organizza attorno a due grandi vuoti: il sagrato, racchiuso su tre lati dalla facciata della chiesa, da un portico e dalla sala parrocchiale, e la corte-chiostro su cui affacciano la navata laterale della chiesa, la scuola della dottrina cristiana e il lato lungo della sala parrocchiale. Questi due grandi vuoti, assieme a un piccolo giardino per l&#146;abside e a una chiostrina della canonica sul lato sud, intagliano il grande volume edilizio, quasi ad arretrare gli ingressi principali e alcune parti della chiesa, come l&#146;abside, in spazi protetti e appartati, in reazione all&#146;informe spazio vuoto circostante. La facciata della chiesa è sdoppiata e racchiude, oltre all&#146;ingresso principale, un percorso laterale che a sinistra conduce al piccolo battistero. Con il coro laterale e una piccola edicola della Madonna, il battistero è uno dei tre fuochi liturgici dell&#146;aula esterni al grande presbiterio, rialzato di due gradini rispetto allo spazio dei fedeli. Questo sale con una pendenza appena percettibile verso il presbiterio. La lunga navata centrale è naturalmente solo un modo di articolare lo spazio interno della chiesa; non pregiudica la disposizione corale dei fedeli attorno all&#146;altare, tutti a una distanza non eccessiva dai due fuochi liturgici centrali: l&#146;altare e l&#146;ambone. Il risultato è un compromesso formale tra la chiesa a pianta centrica e la chiesa a navata, che corrisponde allo spirito e alle indicazioni della riforma conciliare.<br>
