    <td align="left" valign="top" width="75%"> 
      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
        <b>La forza elvetica verso Marignano</b><br>
        Nel 1300 una singolare formazione emerge dalla confusione politica tedesca, 
        &quot;la Svizzera&quot;. Ai margini del dominio asburgico le regioni montuose 
        di Uri, Unterwalden e Schwitz (donde poi il nome di Svizzera), abbandonate 
        alla prepotenza dei governatori feudali, dopo avere altre volte ottenuto 
        e perduto riconoscimenti e garanzie, alla notizia della morte di Rodolfo 
        d'Asburgo, nel 1291, strinsero fra di loro un patto di reciproca difesa 
        e di vicendevole aiuto.<br>
        La prima prova dell'efficacia della solidariet&agrave; giurata svizzera 
        avvenne nella battaglia di Morgarten (1315), quando i rudi montanari sconvolsero 
        e cacciarono un superbo squadrone di cavalieri asburgici. E per tutto 
        il 1300 gli Svizzeri dovettero difendere la loro libert&agrave; dal prepotere 
        degli Asburgo.<br>
        Ma intanto la loro unione si rafforzava e si estendeva per l'adesione 
        spontanea di regioni vicine: ai tre Cantoni primitivi si unirono successivamente 
        Lucerna, Zurigo, Glarona, Zug e Berna. Questa &egrave; la Confederazione 
        degli Otto Cantoni (1353).<br>
        I loro legami, assai incerti e deboli, furono rafforzati nella tradizionale 
        lotta contro gli Asburgo, sconfitti duramente a Sempach (1386) ed a Nafels 
        (1389).<br>
        Da questo momento gli Asburgo, orientatisi verso il Danubio, cessarono 
        di interessarsi degli Svizzeri, che dovettero per&ograve; difendersi contro 
        un altro pericolo da parte dell'ambizioso duca di Borgogna, sconfitto 
        poi a Morat (1476) e successivamente nello stesso anno a Grandson. Queste 
        due battaglie, nelle quali la cavalleria borgognona di Carlo il Temerario 
        fu respinta e disfatta dalle fanterie svizzere ordinate in numerose schiere 
        ed armate di lunghe picche, segnarono il declino dell'arma della cavalleria, 
        tipicamente feudale, ed iniziarono la straordinaria fama delle fanterie 
        svizzere. <a href="amelli10.php"><font color="#0000FF">(nota 1)</font></a><br>
        Era infatti cosa del tutto insolita che le fanterie vincessero le pesanti 
        cavallerie feudali: sino allora alla cavalleria era legata l'azione offensiva 
        e l'esito della battaglia. Il successo delle fanterie svizzere segn&ograve; 
        il trapasso ad una nuova fase dell'arte militare.<br>
        Si cominci&ograve; cos&igrave; a ricercare, dagli altri Stati, l'amicizia 
        degli Svizzere, che fornivano ottimi soldati mercenari: fu il re francese 
        Luigi XI a concludere con essi il primo accordo per il reclutamento di 
        soldati svizzeri al suo servizio (1474).<br>
        Il valore e la potenza militare degli Svizzeri erano cresciuti a tal segno 
        che del ducato di Milano pareva potessero disporre a loro talento; l'Impero 
        e la Casa d'Austria ne ricercarono l'alleanza non meno della Francia; 
        ed ugualmente facevano il Papa, Venezia ed i duchi di Savoia; la vittoria 
        dipendeva dal numero dei soldati svizzeri che si potevano reclutare.<br>
        Intanto, per&ograve;, in Svizzera si indeboliva il sentimento unitario, 
        si trascurava l'agricoltura, si rovinava il commercio, perch&egrave; i 
        giovani si davano al mestiere delle armi, sia per disposizione naturale, 
        sia per le somme di denaro offerto loro per l'ingaggio. <a href="amelli10.php"><font color="#0000FF">(nota 
        2)</font></a><br>
        Nel 1500 gli Svizzeri avevano aoutato Ludovico il Moro a rioccupare il 
        ducato di Milano, e da allora essi si erano installati nella contea di 
        Bellinzona, in territorio italiano; e un trattato stipulato ad Arona nell'aprile 
        del 1503 con il re francese Luigi XII, aveva finito col riconoscerne loro 
        il possesso.<br>
        Ma gli Svizzeri, ora, pretendono di pi&ugrave;: vogliono aveve voce deliberativa 
        nei futuri destini del Milanese. Essi infatti, appoggiati alla forza delle 
        armi, pretendono che nel ducato di Milano, dopo che Ludovico il Moro con 
        tradimento fu consegnato ai Francesi, tornino di nuovo gli Sforza. Tale 
        &egrave; anhe il pensiero del papa Giulio II e dei Veneziani, i quali 
        tutti, insieme con i Cantoni svizzeri, vogliono essere riconosciuti protettori 
        di Milano, come antico feudo imperiale, e reclamano a gran voce che i 
        Milanesi prestino loro giuramento di fedelt&agrave;.<br>
        Prevale la forza degli Svizzeri: il 29 dicembre 1512 Massimiliano Sforza 
        entra in Milano, scortato da una compagnia svizzera, e riceve le chiavi 
        della citt&agrave; dai rappresentanti dei Cantoni svizzeri. I Cantoni, 
        dunque, trionfano. Anzi, essi vorrebbero per s&egrave; tutta quella parte 
        del Milanese che guarda verso i loro monti; il che rende, a qualsiasi 
        potenza europea, impossibile il dominio del Milanese. E qualcuno comincia 
        a pensare che l'unica salvezza dell'Italia contro i Tedeschi, gli Spagnoli, 
        i Francesi sia l'unione con gli Svizzeri.<br>
        Ancora nel 1513 il re di Francia, Luigi XII, desiderava ardentemente l'amicizia 
        degli Svizzeri: sapeva per esperienza che con le loro forze si otteneva 
        una sicura vittoria. Il suo progetto di riconquistare il Milanese, quale 
        discendente di Valentina Visconti che aveva sposato Luigi duca di Orl&eacute;ans, 
        diventava ogni giorno pi&ugrave; maturo e richiedeva una soluzione. Ma 
        il re di Francia ben sapeva le condizioni dell'accordo tra Massimiliano 
        Sforza e gli Svizzeri, e si accorse molto presto che la fatica di amicarsi 
        quella nazione era del tutto vana: quando infatti Carlo de la Tremouille, 
        signore di Talmont, giunse a Lucerna, a capo di unambasciata per presentarsi 
        alla Dieta svizzera, sei Cantoni avevano gi&agrave; ratificato i patti 
        con Massimiliano Sforza, tre altri Cantoni stavano per ratificare, e tre 
        ancora erano incerti.<br>
        Ma qualche tempo dopo tutti i Cantoni ratificarono gli accordi stabiliti 
        con il duca di Milano; respinsero tutte le proposte di Luigi XII; e dichiararono 
        che per la Francia vi era assoluta proibizione di reclutare soldati svizzeri, 
        sia da usarsi in Italia sia fuori d'Italia.<br>
        Il collaudo delle intenzioni elvetiche avvenne il 6 giugno 1513 a Novara, 
        assediata dai Francesi, guidati dal La Tremouille e dal Trivulzio. Gli 
        Svizzeri, esaltati dai loro capitani, uscirono dopo la mezzanotte dalle 
        mura, ed assaltarono con feroce impeto il campo francese; occuparono sanguinosamente 
        le artiglierie, e le rivoltarono contro il nemico. Poi passarono all'assalto 
        all'arma bianca. Dopo due ore di furioso combattimento, rientrarono in 
        Novara urlando vittoria e trionfanti sulle spoglie francesi. E questa 
        vittoria elev&ograve; alle stelle la loro fama: gli Svizzeri erano invincibili. 
        La loro prima ed unica sconfitta era avvenuta nel remotissimo anno 58 
        avanti Cristo, quando Giulio Cesare conduceva la campagna militare in 
        Gallia.<br>
        Lo storico Francesco guicciardini, il pi&ugrave; grande genio della storiografia 
        italiana con machiavelli, e che muore nel 1540, scrive sugli Svizzeri 
        le sue impressioni, che dovevano essere le conoscenze pi&ugrave; comuni 
        per allore: &quot;Sono i Svizzeri quegli medesm che dagli antichi si chiamavano 
        Elvezii, generazione che abita nelle montagne pi&ugrave; alte di Giura, 
        dette di san Claudio, in quelle di Briga e di san Gottardo; uomini per 
        natura feroci, rusticani, e per la sterilit&agrave; del paese pi&ugrave; 
        tosto pastori che agricoltori. Furono gi&agrave; dominati da' Duchi di 
        Austria; da' quali ribellatisi, gi&agrave; &egrave; grandissimo tempo, 
        si reggono per loro medesimi, non facendo segno alcuno di ricognizione 
        n&egrave; agl imperatori n&egrave; a altri principi. Sono divisi in tredici 
        popolazioni, essi li chiamano Cantoni; ciascuno di questi si regge con 
        magistrati, leggi e ordini proprii. Fanno ogni anno, o pi&ugrave; spesso 
        secondo che accade di bisogno, consulta delle cose universali; congregandosi 
        nel luogo il quale, ora uno ora l'altro, eleggono i deputati di ciascuno 
        Cantone: chiamano, secondo l'uso di Germania, queste congregazioni diete; 
        nelle quali si delibera sopra le guerre, le paci, le confederazioni, sopra 
        le dimande di chi fa istanza che gli sia conceduto, per decreto pubblco, 
        soldati o permesso a' volontari di andarvi, e sopra le cose attinenti 
        allo interesse di tutti. Quando per pubblico decreto concedono soldati, 
        eleggono i cantoni medesimi tra loro uno capitano generale di tutti, al 
        quale con le insegne e in nome pubblico si d&agrave; la bandiera. Ha fatto 
        grande il nome di questa gente, tanto orrida e inculta, l'unione e la 
        gloria delle armi, con le auli, per la ferocia naturale e per la disciplina 
        dell'ordinanze, non solamente hanno sempre valorosamente difeso il paese 
        loro, ma esercitando fuori del paese la milizia con somma laude: la quale 
        sarebbe stata senza comparazione maggiore se l'avessino esercitata per 
        lo imperio proprio, e non agli altri stipendii e per propugnare lo imperio 
        di altri, e se pi&ugrave; generosi fini avessino avuto innanzi agli occhi, 
        a' tempi nostri, che lo studio della pecunia; dall'amore della quale corrotti, 
        hanno perduta l'occasione di essere formidabili a tutt l'Italia, perch&egrave; 
        non uscendo dal paese loro se non come soldati mercenari, non hanno riportato 
        frutto pubblico delle vittorie, assuefandosi, per la cupidit&agrave; del 
        guadagno, a essere negli eserciti, con taglie ingorde e con nuove dimande, 
        quasi intollerabili, e oltre a questo, nel conservare e nell'ubbidire 
        a chi li paga, molto fastidiosi e contumaci&quot;. (F. Guicciardini, &quot;Storia 
        d'Italia&quot;, libro X, cap. VIII).<br>
        Non bisogna per&ograve; credere che la supremazia svizzera fosse senza 
        difetto. Anzi, il mercenario svizzero presentava parecchie lacune, derivate 
        non tanto dal temperamento o dalla razza, ma proprio dalle condizioni 
        geografiche elvetiche. <a href="amelli10.php"><font color="#0000FF">(nota 
        3)</font></a><br>
        La Svizzera del 1500 era un paese povero, e non aveva ancora saputo ricavare 
        dalla montagna il proprio benessere. La sua economia era imperniata sulla 
        pastorizia, pascoli ed allevamento di bestiame, soprattutto ovini; mancavano 
        per&ograve; gli agricoltori, nel senso vero della parola, e tanto meno 
        vi era una classe operaia. Non esistevano miniere d'oro, ferro, carbone; 
        ed ancora oggi la produzione mineraria si riduce a poco sale (Basilea, 
        Vallese), e a poca torba (Neuchatel e un po' dovunque); di conseguenza 
        la truppa svizzera mancava di artiglieria. La stessa natura montuosa (Fascia 
        Alpina, Altipiano Svizzero, Fascia del Giura) che non lascia posto ad 
        immense o vaste pianure, inmpediva un diffuso allevamento di cavalli; 
        e di conseguenza gli Svizzeri mercenari non potevano neppure disporre 
        di una robusta e massiccia cavalleria soprattutto leggera; e la cavalleria 
        era l'arma cui fin dell'antichit&agrave; era riserbato l'urto per la risoluzione 
        della battaglia. <a href="amelli10.php"><font color="#0000FF">(nota 4)</font></a><br>
        Quando nel 1474 Luigi XI di Francia chiam&ograve; gli Svizzeri al suo 
        soldo, egli disponeva anche di artiglieria e di cavalleria francese; nella 
        battaglia di Novara, il 6 giugno 1513, gli Svizzeri attaccarono di notte 
        l'accampamento nemico per non subire l'urto della cavalleria che nell'oscurit&agrave; 
        era impossibilitata a combattere, e per non essere bersaglio delle artiglierie 
        che di notte non si poteva sfruttare.<br>
        E nel 1515, contro i Francesi che disponevano di scelta cavalleria ed 
        artiglieria, 10.000 cavalli e 72 cannoni, gli Svizzeri non hanno alcuna 
        cavalleria e pochi pezzi di artiglieria.<br>
        Per&ograve; la forza elvetica, oltre che nell'audacia, prontezza, decisione, 
        disprezzo della fatica e del pericolo, era nelle armi che usavano: le 
        picche. La picca era un'arma con asta assai lunga, che appartiene alla 
        stessa categoria dell'alabarda e della partigiana: si componeva di un'asta 
        di legno, lunga da 5 a 7 metri, alla cui estremit&agrave; veniva assicurato 
        un puntale di ferro, molto robusto, lungo circa 50 cm. a forma di lingua 
        o di lancia. La lunghezza e la robustezza erano tali da frenare l'impeto 
        della cavalleria.<br>
        Per tali motivi, se si toglie che lo schieramento di guerra svizzero era 
        ordinato intelligentemente per affrontare la cavalleria e che il valore 
        del soldato elvetico era notevole, la mancanza di una cavalleria e di 
        un'artiglieria non poteva assicurare per sempre una vittoria certissima 
        degli Svizzeri: nell'esercito francese cavalleria, fanteria ed artiglieria 
        si componevano in unit&agrave; organica.<br>
        Bisogna poi aggiungere che anche la posizione dei vari Cantoni influiva 
        sulla partecipazione pi&ugrave; o meno interessata alle vicende italiane: 
        i Cantoni pi&ugrave; vicini all'Italia erano maggiormente attivi nell'intervenire; 
        quelli al Nord erano piuttosto freddi ed indifferenti. Non ci meraviglieremo 
        dunque di dover assistere ad un esercito svizzero che, poche ore prima 
        del fatto di Marignano, si presenta diviso in opinioni diverse e tumultuose 
        sulle decisioni da prendere fino all'ultimo momento: talora neppure l'autorevole 
        voce del comandante supremo del Consiglio di guerra, Matteo Schinner, 
        cardinale di Sion, poteva sedare e ricomporre le fazioni. <a href="amelli10.php"><font color="#0000FF">(nota 
        5)</font></a><br>
        Nonostante ci&ograve;, tuttavia, rimaneva altissima la fama degli Svizzeri, 
        acquistata prima contro la Casa d'Austria, poi contro la cavalleria di 
        Carlo il Temerario di Borgogna, con la nuova tattica del combattimento 
        in file serrate, armati di corte spade e, come dicemmo, di picche dal 
        lungo manico, nell'uso delle quali si dimostrarono particolarmente abili. 
        <a href="amelli10.php"><font color="#0000FF">(nota 6)</font></a><br>
        La povert&agrave; della Svizzera doveva incrociarsi logicamente con il 
        problema demografico: la popolazione aumentava in proporzioni maggiori 
        delle disponibilit&agrave; del paese. Anche per i prodotti di prima necessit&agrave; 
        (cereali, sale) bisognava ricorrere all'importazione, e per l'acquisto 
        fuori dai confini occorrevano forti somme. la richiesta di soldati mercenari 
        per le guerre in Italia rappresent&ograve; la soluzione pi&ugrave; opportuna, 
        e gli offerenti non mancarono mai: chi offriva pi&ugrave; lautamente aveva 
        la precedenza.<br>
        Lo stipendio era individuale per ogni sodato, e collettivo ai Cantoni 
        che si impegnavano a consegnare i combattenti; e diventava una specie 
        di pensione annua quando il servizio militare era stabile a lunga scadenza. 
        naturalmente, dal servizio come mercenari alla politica di conquista per 
        se stessi, il passo era breve. La guerra dunque divenne una professione 
        sociale, e la nazione svizzera diventa il popolo pi&ugrave; guerriero 
        di tutta Europa.<br>
        E' per questo che i reganti europei interessati devono cedere alle richieste 
        di natura commerciale: i Cantoni che forniscono i mercenari strappano 
        privilegi importantissimi, esenzioni primarie, immunit&agrave; mai prima 
        concesse: gratuiti erano i pedaggi, abolite le imposte; e non solo per 
        allora, ma anche in futuro. I mercanti elvetici potevano finalmente importare 
        ed esportare liberamente, e liberamente comperare e vendere dove avessero 
        voluto tutte le merci. Le voci pi&ugrave; attive nel commercio erano cavalli, 
        buoi, altro bestiame; grano e cereali in genere; pelli, lino, lana, seta, 
        legname. </font></p>
