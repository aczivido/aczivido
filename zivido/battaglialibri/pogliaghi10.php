      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Note</b><br>
        <br>
        <b>(1)</b> Domenico Contarini, in una lettera a Marin Sanudo, tomo XXI, 
        col.104, scrive: &quot;... la maestà del Re oggi ha dimostrato tal valor 
        de la persona soa, che molti Cesari haria superato et ha fatto tal fatiche 
        da heri in qua, che è cosa meravejosa a credere che la età de soa Mjestà 
        lo patisca&quot;. Il Verri ci dice che: &quot;Il Re ebbe il cavallo ferito 
        e nella persona ricevè molte contusioni e vi combattè come ogni soldato&quot;.<br>
        <br>
        <b>(2)</b> Ferrante d'Avalos (1490-1525), marchese di Pescara, sposò Vittoria 
        Colonna, figlia di Fabrizio. Vinse effettivamente la battaglia di Pavia.<br>
        <br>
        <b>(3)</b> Vittoria Colonna (1490-1547), grande poetessa, nelle sue rime 
        pianse la perdita del marito Ferrante d'Avalos, che aveva amato di un 
        amore profondo, seguendone con ansia le imprese guerresche. Michelangiolo 
        ebbe per lei un sentimento purissimo di amicizia e di ammirazione.<br>
        <br>
        <b>(4)</b> O. Buonocore: Nuptialia Isclana. Napoli 1907, pag.79. Una zia 
        di F. d'Avalos, sorella di suo padre - Beatrice - aveva sposato Gian Giacomo 
        Trivulzio, Maresciallo di Francia.<br>
        <br>
        <b>(5)</b> &quot;I generali imperiali facevano a gara la loro corte al 
        Re, che ammise anche il duca di Borbone e lo accolse come un principe 
        del suo sangue qual era. Il marchese di Pescara appena ristabilito dalle 
        ferite si presentò al Re, a differenza degli altri con abito semplice 
        e senza pompa; egli vi unì pure maniere semplici e piacque al Re sopra 
        ogni altro... Indi il Re, dice il Tegio, con molto rispetto fu condotto 
        a S.Paolo, ove il duca di Borbone gli presentò magnifiche vesti, dopo 
        essersi disarmato, ed al pranzo il Vicerè lo servì, portandogli il catino 
        da lavar le mani, il marchese del Vasto versò l'acqua, il duca di Borbone 
        l'asciugatoio. Il Borbone lasciava cader le lagrime mirando prigioniero 
        il Re&quot;.<br>
        <br>
        <b>(6)</b> e <b>(7)</b> O. Buonocore: op. già cit., pag.82 e 89. narra 
        il Burigozzo: &quot;... il signor marchese de Pescara era già infermo 
        et alli doi de novembre moritte e fu portato in San Pietro Gessate&quot;. 
        (c'è una diversità di data tra il Burigozzo, il Giovio e il Buonocore). 
        &quot;Il 12 maggio del vegnente anno gli avanzi mortali del gran capitano 
        furono portati a Napoli... e riposano nella sagrestia di San Domenico 
        Maggiore. Unica decorazione sulla bara di Ferrante posa la sua spada&quot;. 
        O.B.<br>
        Vittoria Colonna allevò come fosse suo figlio, Alfonso del Vasto, rimasto 
        orfano bambino. &quot;Non mi dite sterile - Ella amva ripetere - io pure 
        conto un figlio: il figlio del mio ingegno e del mio cuore&quot;. Alfonso, 
        figlio di Inico, fu poi il continuatore delle gesta di Ferrante. Sposò 
        Maria d'Aragona e un suo poemetto, nel quale canta epiche imprese, fu 
        stampato a Verona nel 1542. Lodovico Ariosto lo ricorda nell'Orlando furioso.</font></p>
