    <td width="75%" align="left" valign="top"> 
      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Considerazioni. 
        Il valore degli Svizzeri. La figura di Francesco I°. Dieci anni dopo</b><br>
        <br>
        Ora, torna spontanea la considerazione di quanto siano effimere e mutevoli 
        le situazioni create dagli uomini, anche quelle che sembrano più stabili 
        e durature.<br>
        Qui su questi campi, tra Melegnano, San Giuliano, Zivido, Santa Brera, 
        Occhiò, Mezzano, rivive il ricordo della eroica ritirata dei soldati Svizzeri 
        e della grande vittoria di Francesco I°.<br>
        Ecco, come descrive la ritirata degli Svizzeri, Galezzo Visconti, conte 
        di Busto Arsizio, in una lettera a Enrico VIII d'Inghilterra: &quot;Infine, 
        scemato il vigore dall'una parte e dall'altra, di nostra iniziativa e 
        senza essere costretti, ma soltanto perchè molti dei nostri francesizzanti 
        erano scappati con le bandiere, ci siamo ritirati. A passi lenti si andò 
        a Milano, ognuno con le sue insegne, con le nostre bandiere intatte e 
        con non poche di quelle strappate ai Francesi, senza essere inseguiti 
        dai nemici a cavallo o a piedi. Se ci fossimo fermati a San Giuliano, 
        o almeno a Milano, il nemico avrebbe dovuto rivolgere le spalle.&quot; 
        Nos vicimus - avremmo vinto!&quot;<br>
        Francesco I°, sempre nella famosa lettera scritta a sua madre, da Santa 
        Brigida, dice: &quot;La battaglia è stata lunga e dura da ieri ad oggi, 
        senza sapere chi l'avesse perduta o guadagnata (&quot;sans savoir qui 
        l'avoit perdue ou gagnée&quot;)&quot;.<br>
        Un Re <b>(1)</b>, che allora aveva vent'anni e che i cronisti e gli storici 
        del tempo ci descrivono bello, cavalleresco e valoroso, fu al centro dei 
        più importanti avvenimenti storici e dei più grandi fatti d'arme del suo 
        secolo. La sua personalità fu ad un tempo ecclettica e sconcertante: conquiste 
        e sconfitte, ambizioni smodate e ideali d'arte e generoso mecenatismo. 
        Leonardo fu accolto da lui e a lui si dovrà la costruzione del Louvre 
        e delle Tuilleries. Non a torto egli è chiamato il padre del rinascimento 
        francese. La sua vita, così ricca di chiari-oscuri, sollecita il raffronto, 
        richiama l'anologia con quella che sarà poi la concezione vichiana della 
        storia e della esistenza, con i suoi corsi e ricorsi, con le sue tre età 
        dell'uomo.<br>
        La battaglia di &quot;Marignano&quot; fu per Francesco I° il trionfo, 
        la gloria, il coronamento dei suoi sogni cavallereschi, ma bastarono solo 
        dieci anni e la situazione sarà capovolta, infatti, il 24 febbraio del 
        1525, a Pavia, egli subirà una grande sconfitta, combattendo contro l'esercito 
        di Carlo V comandato dal vicerè di Napoli Carlo di Lannoy e da Ferdinando 
        d'Avalos <b>(2)</b>, marito di Vittoria Colonna <b>(3)</b><br>
        Con l'Avalos vi era pure il suo giovanissimo cugino, marchese del Vasto 
        e il Connestabile di Borbone, che a Marignano aveva combattuto valorosamente 
        per il suo Re e che qui a Pavia già lo aveva tradito, passando nelle file 
        nemiche.<br>
        Scrive il Buonocore: &quot;E proprio dalla tenda del marchese di Pescara, 
        Francesco I° scriveva alla madre sua quelle parole che hanno avuto fortuna 
        nella storia - tutto è perduto fuorchè l'onore - &quot;. Mentre &quot;Ferrante 
        dando sangue da tre ferite si era battuto come un leone sino alla fine, 
        aggirando nelle spire delle sue rapide mosse il fio fiore della nobiltà 
        francese: La Tremouille, La Palisse, Saint-Pol, di Foix, Bonnivet e altri 
        ancora&quot;. <b>(4)</b><br>
        &quot;Il famoso Ferrante d'Avalos&quot; scriverà il Verri, e la sua descrizione 
        di come il Re fu fatto prigioniero, delle parole che egli pronunciò innanzi 
        al Vicerè di Napoli Carlo di Lannoy e di quanto accadde in seguito, è 
        impressionante e commovente.<br>
        Il Sepulveda, detto anche il Tito Livio spagnolo, o il Cicerone castigliano, 
        ci parla diffusamente di questa giornata così dolorosa per Francesco I°.<br>
        Sempre dal Verri apprendiamo che, dopo essere caduto prigioniero, il Re 
        fu trattato con molta umanità. <b>(5)</b><br>
        Ferrante d'Avalos morirà nello stesso anno a Milano, dopo aver preso possesso, 
        a nome dell'imperatore, della capitale lombarda. Vittoria Colonna non 
        arriverà in tempo a rivederlo: &quot;... lasciata in fretta Ischia pigliò 
        la via di Milano. Era giunta a Viterbo, quando le pervenne la notizia 
        che il 25 di quell'algido novembre, lo sposo desiderato, assistito da 
        prodi, punto solo da un vivo sentimento nostalgico, serenamente, a trentasei 
        anni, aveva chiusa la giovinezza&quot;. <b>(6)</b><br>
        I bellissimi arazzi che nel Museo Nazionale di Napoli, narrano le fasi 
        della battaglia di Pavia, appartenevano a Vittoria Colonna: &quot;...quei 
        preziosi arazzi fiamminghi decoravano una sala dello storico castello 
        di Ischia, splendido dono di Carlo V alla moglie di Ferrante&quot; <b>(7)</b><br>
        Paolo Giovio che con altri letterati del suo tempo, fu più volte ospite 
        al castello di Ischia, oltre alle &quot;Historiae del suo tempo&quot; 
        scrisse pure la vita di F. d'Avalos.<br>
        Ricordo che visitando qualche anno fa, le rovine dello storico castello 
        di Ischia, e passando tra quelle mura che furono testimoni, prima della 
        vita felice e poi del dolore di Vittoria Colonna, scrissi pensando a lei 
        queste rime: &quot;E l'eco del tuo canto / e il tuo ricordo intenerisce 
        l'aspre / pietre del tuo castello, alto nel sole. / E tu rivivi ancora 
        / così felice e lui ami, Ferrante / e poi lo piangi chè lo sai perduto 
        / lassù in quella bella / terra di Lombardia. / E questa azzurra, che 
        dal mare sale / onda di vita, è la tua stessa, o donna, / che a noi secoli 
        tornano. / Ora dormi serena, la tempesta / placata: chè portato hai nell'anima 
        / quello che Michelangiolo / per te dipinse / meraviglioso Cristo Crocefisso.</font></p>
