      <p align="JUSTIFY"> <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>Dopo 
        la vittoria, la vita quotidiana dell'occupazione. Politica d'occupazione, 
        atteggiamenti sconsiderati, ostilit&agrave;. Gli Italiani visti dagli 
        Svizzeri. Gli Svizzeri visti dagli Italiani. Finanziamento delle truppe, 
        ammontare della paga, condizioni di vita. Riciclaggio della paga e del 
        bottino con l'acquisto di articoli di lusso, effetti sulla domanda in 
        Svizzera.</u><br>
      <br>Ritorniamo all'Italia, causa di tutta questa concitazione, perch&eacute; essa era 
        diventata il campo di battaglia preferito delle grandi potenze europee. 
        La spedizione del 1512 aveva fatto del ducato di Milano, in pratica, un 
        protettorato della Confederazione. All'euforia della vittoria subentr&ograve; 
        ora la vita quotidiana dell'occupazione. Ecco la lista trionfale delle 
        conquiste: &quot;le grandi citt&agrave; si sono arrese tutte&#133;, cio&egrave; 
        Meyland (Milano), Genow (Genova), Cremona, Playsantz (Piacenza), Parma, 
        Kum (Como), Noware, Alezandry e molte altre citt&agrave; di cui ci sfugge 
        il nome&quot;. Ma ora se ne sente il peso. Se, da una parte, Milano era 
        caduta, dopo la vittoria di Novara, ancora pi&ugrave; profondamente in 
        bal&igrave;a dei confederati, per cui non scorgevano pi&ugrave; nessun 
        nemico intorno a loro (&quot;&#133; che non abbiamo e non conosciamo pi&ugrave; 
        nessun nemico&quot;), dall'altra non accennavano a diminuire le mene del 
        re di Francia e dei suoi seguaci tra i nobili milanesi.<br>
        Per i non-italiani il gioco politico delle famiglie nobili milanesi (di 
        cui i confederati diffidavano pi&ugrave; che non del &quot;poppel&quot;, 
        del popolo), era difficile da capire. Il desiderio di intervenire con 
        rabbia, e di farla finita, si manifest&ograve; nella valutazione della 
        alternativa se i confederati dovessero prendere ostaggio o meno anche 
        tra il ceto dirigente milanese. Non faceva parte di un'efficiente politica 
        di occupazione, &quot;portare nel nostro paese parecchi dei migliori (cio&egrave; 
        probabilmente dei pi&ugrave; cattivi, perch&eacute; &quot;besten&quot; 
        nell'originale pu&ograve; essere letto, in tedesco, &quot;b&ouml;sesten&quot;) 
        e pi&ugrave; diffidenti milanesi, come ha fatto anche il re (di Francia) 
        in molti periodi?&quot;. Cosa ne pensava Berna?<br>
        Rientra nel contesto di tali considerazioni un documento insolito, di 
        poco anteriore, che getta una caratteristica luce sul desiderio di capire 
        le vicende politiche di quel paese. Un semplice biglietto, del 1500 circa, 
        riporta succintamente informazioni sulla posizione scelta da alcune personalit&agrave; 
        del ducato di Milano, tra Lodovico il Moro e il re di Francia, il loro 
        supposto patrimonio, il numero dei figli: &quot;Quello di Cuom (Como) 
        si chiama Johannes Baptista, (il suo patrimonio) vale 4000 ducati e due 
        fabbricati, ha tradito il castello di Mattarella e ferito il capitano 
        nel corso di questo tradimento; e prima era stato nel Vallese e ha portato 
        con s&eacute; soldati e pagati con i propri soldi per compiere il tradimento; 
        'uxor suua fuit ducis&#133;; non habent pueros. Ambo recesserunt cum fratre 
        ducis&#133;; non habet pueros'.<br>
        Petrus Anthonius de Loys domo doctor de Cuom, con denaro &egrave; riuscito 
        nel Feltlin ad avere mercenari, e anche che il Feltlin abbandonasse il 
        re; egli scrive anche al Moro e viceversa; &egrave; stato il vero mandante 
        del tradimento; 'valent bona XIII ducatos; habet quinque pueros'.<br>
        Meiland: 'Johannes Angelus de Poris fugit prima vice cum Moro, et fecit 
        venire ligam grisam et omnia que facta sunt per pratickam fecit. Valent 
        bona quinque milia; habent III pueros&quot;.<br>
        &quot;Iacobus Mantega de Cum fecit sicut Petrus Anthonius. Valent bona 
        III; non habet pueros&quot;.<br>
        &quot;Iohannes Petrus Rusca fuit capitanius in Belitz et tradit regem 
        et Belitzen; est de Cum; non habet pueros nec uxorem. Valent bona sua 
        octa milia ducatos. Dedit fugam cum cardenali&quot;<br>
        &quot;Fridericus Galleranus von Meiland tradit regem et dedit sufragium 
        Moro, id est er hat lib und guot zuo im gesetz (egli gli ha dato vita 
        e patrimonio). Habet pueros; valent bona VI milia tugatos&quot;.<br>
        Questo biglietto sembra innocuo, ma &egrave; sospetto, come se un informatore 
        segreto volesse indicare furtivamente i punti vulnerabili: come si &egrave; 
        comportato l'uomo? Dove &egrave; ricattabile? Vale la pena prenderlo in 
        ostaggio? Quale sar&agrave; il suo &quot;valore&quot; nel caso di riscatto 
        dalla prigionia o di ritorno dal confino?<br>
        Il profilo di quella che potrebbe essere definita una politica d'occupazione 
        (stando alla lettera, si trattava solo dell'adempimento di un patto difensivo, 
        ma in effetti si andava, appunto, oltre) si pu&ograve; desumere da osservazioni 
        del tipo: aver &quot;trovato che il popolo &egrave; giusto e buono, e 
        che volentieri ci starebbe, ma che la nobilt&agrave; ha troppo peso per 
        loro ed &egrave; pi&ugrave; forte&quot;: cos&igrave; sarebbe stato ad 
        Asti, e la stessa considerazione varrebbe anche per Milano. Che si credesse, 
        trovandosi in loco, di comprendere la situazione meglio delle lontane 
        autorit&agrave; svizzere, lo dimostra gi&agrave; la raccomandazione rivolta 
        in patria &quot;di non accettare intese con i signori di Milano, prima 
        di sapere da noi, tutto per bene e veritiero, come si presenta la situazione 
        in questo paese&quot;.<br>
        Ma in sostanza ci si limitava poi a rafforzare il dominio del giovane 
        protetto Massimiliano Sforza e a sostenere la sua dura politica fiscale 
        diretta a estorcere dai tormentati sudditi ingenti somme per pagare il 
        tributo richiesto dai protettori: &quot;E' stato imposto un grosso, pesante 
        tributo a questo paese&#133;, perci&ograve; si deve provvedere affinch&egrave; 
        Lor Signori siano pagati, in quanto, altrimenti, il diavolo se lo porta 
        via&quot;, scrive Albrecht vom Stein, nel suo solito tono impertinente, 
        al Cancelliere comunale di Berna. Bastava allora una semplice lettera, 
        da parte del rappresentante bernese, al tesoriere ducale per far mettere, 
        senza tante storie, due altri bernesi sulla lista delle pensioni di Milano. 
        Sporadici casi di sfruttamento si ebbero anche l&agrave; dove la presa 
        dei confederati era diretta come a Lugano e Locarno, castelli che all'inizio 
        del 1513, dopo un assedio durato molti mesi, erano stati finalmente tolti 
        ai Francesi e sottoposti al dominio comune dei XII confederati: &quot;Sappiate 
        una volta tanto qualcosa di pi&ugrave; preciso sulla condotta di Kaspar 
        G&ouml;ldli&quot;, si sente dire il Consiglio di Berna da Sebastian vom 
        Stein, &quot;come egli tratta la povera gente&#133;; mi &egrave; stato 
        detto che finora egli ha incassato circa ottomila fiorini, e questa &egrave; 
        la minima parte delle entrate, tormentando inoltre la gente povera&quot;.<br>
        Gi&agrave; per attuare un assedio prolungato, i confederati erano poco 
        adatti, infatti durante l'assedio di Locarno, incominciato subito dopo 
        la spedizione di Pavia, il capitano supplicava disperatamente il Consiglio 
        nella lontana Berna: &quot;Illustri Signori, scrivete perch&eacute; essi 
        restino con me, e non vadano via durante il mio congedo&quot;. Altrettanto 
        malvolentieri gli Svizzeri svolgevano ora, nel castello di Milano, il 
        servizio di occupazione, e l'umore non sar&agrave; stato il migliore. 
        Notavano con irritazione alcuni accenni di ostilit&agrave; da parte italiana 
        e segni di mene francesi, si vedevano &quot;cacciati con i sassi dalla 
        porta&quot;, e per il resto si sentivano in questo castello - &quot;quando 
        volessero farci del male&quot; - in trappola. Per discolparsi, Balthasar 
        Finsternau richiamava preventivamente l'attenzione del Consiglio di Berna 
        sulle difficolt&agrave; del momento, chiedeva regole di comportamento, 
        orecchiava timorosamente in direzione della citt&agrave;; non sentendo 
        niente, si poteva essere gi&agrave; contenti: &quot;Non abbiamo ancora 
        n&eacute; visto n&eacute; sentito alcun rumore nella citt&agrave; di Milano, 
        il che ci d&agrave; tanto sollievo che i nostri stanno ancora bene, se 
        Dio vuole, perch&eacute; se a loro fosse accaduto qualcosa in qualche 
        modo, da tempo ci sarebbe stato un grande rumore a Milano&quot;.<br>
        Da osservatori pacati come Peter Falk, rappresentante della Confederazione 
        e residente a Milano, il Consiglio bernese seppe per&ograve; che gli uomini 
        della forza di occupazione, &quot;di cui parecchi hanno espresso parole 
        assai improprie contro il duca, e inoltre sono privi di tatto&quot;, non 
        erano senza colpa per l'affiorare di certe tensioni. E poi d&agrave; un 
        assaggio di tali &quot;parole improprie: il duca non &egrave; il Signore, 
        i Signori siamo noi&quot; (ci&ograve; era vero, ma non andava detto); 
        oppure: &quot;egli ha molti buoni abiti d'oro e di seta che deve ancora 
        dare in pegno e vendere&quot;. Tali grossolanit&agrave; furono per&ograve; 
        capite, &quot;perch&eacute; a corte ci sono molti milanesi, e altri, che 
        conoscono bene la lingua tedesca e riportano queste parole; hanno parlato 
        inoltre spesso in questo modo, sicch&egrave; avr&agrave; potuto sentirle 
        anche il duca in persona&quot;; sarebbe meglio &quot;mandare in licenza 
        e congedo cos&igrave; rozza e riprovevole gente&quot;. E conoscendo la 
        lingua sciolta e impertinente delle lettere di Albrecht vom Stein, si 
        pu&ograve; ben credere che questi non si sar&agrave; esentato da osservazioni 
        beffarde sul duca (il quale avrebbe, con la morte del re di Francia, &quot;perso 
        gioia e coraggio come se fosse morto il Messia vero e proprio&quot;).<br>
        Cosa la guardi svizzera pensasse (&quot;quelli&#133; che sono nella sua 
        guardia&quot;) del giovane duca, non era niente di buono, e dimostra - 
        nel ritratto, breve e cattivo, tracciato in una lettera - quanto fosse 
        lontana la mentalit&agrave; di questi semplici soldati da quella del principe 
        di una corte rinascimentale: questo Massimiliano Sforza dorme durante 
        il giorno e vive la notte, &quot;non fa altro che dormire per tutto il 
        giorno, serve poco Iddio e non ascolta nessuna messa e vive come se si 
        fosse data la vita da solo. Nella notte non fa altro che torneare e giubilare, 
        e fa come se avesse catturato degli uccelli, e tiene uno stile di vita 
        assai disordinato per un principe&quot;. Un bernese non condurrebbe una 
        vita cos&igrave;.<br>
        Se prima questi Svizzeri erano stati convinti di essere venuti in qualit&agrave; 
        di soccorritori e liberatori, ben presto dovettero accorgersi, con amarezza, 
        di ricevere &quot;nessun'altra ricompensa se non l'enorme tradimento in 
        tutto il paese&quot;. Queste animosit&agrave;, non in tempi di pace ma 
        durante le spedizioni militari come quella del 1515, sfociarono in terribili 
        rappresaglie: sulla base del semplice sospetto, poi non confermato, dell'uccisione 
        di prigionieri svizzeri, fu massacrata e ridotta in cenere un'intera citt&agrave; 
        come Chivasso. La battaglia di Novara era appena combattuta e gi&agrave; 
        si credeva di individuare i primi segni di un'attivit&agrave; sovversiva: 
        &quot;A Casale c'&egrave; un fabbro che ha fabbricato tremila punte di 
        ferro e le ha montate a Vercelli su altrettante lance; e tutto ci&ograve; 
        &egrave; avvenuto di nascosto, perci&ograve; non se ne pu&ograve; trarre 
        niente di buono&quot;.<br>
        Se i confederati fossero in grado di assicurare la protezione, come era 
        previsto dal trattato, e di difendere il ducato di Milano durevolmente 
        dalle rivendicazioni francesi, era ancora da vedere, nonostante le brillanti 
        vittorie riportate, ma il dubbio si sarebbe sciolto ben presto con la 
        loro rinuncia definitiva alla politica delle grandi potenze: nel 1513 
        a Novara sarebbero stati ancora vittoriosi, a Melegnano invece, nel 1515, 
        non pi&ugrave;.<br>
        L'immagine che gli Italiani dell'epoca si facevano di questi Svizzeri 
        - che in certi periodi erano calati ogni anno vittoriosamente nell'Italia 
        settentrionale o si erano arruolati come mercenari in diversi eserciti 
        operanti sullo scacchiere italiano - er perci&ograve; del tutto dominata 
        dal loro ruolo militare. &quot;Mai erano st&agrave; debellati da Julio 
        Cesare in qua&quot;. A non pochi Italiani sembravano quindi indispensabili 
        per vincere una guerra ed &egrave; caratteristico che in dichiarazioni 
        sulla forza numerica di un esercito, il gruppo degli Svizzeri spesso venisse 
        registrato separatamente. Suscitavano stupore, se ne rallegravano: furono 
        corteggiati a Venezia &quot;&#133; e nel corteo venivano subito dopo il 
        doge, sempre uno dei Signori di Venezia e uno di noi Confederati&quot;. 
        Tutta Roma afflu&igrave; per vederli: il Papa si sarebbe fatto portare 
        &quot;sul bastione fuori dal palazzo, per vederci e per darci la benedizione&#133;, 
        e inoltre &egrave; stata fatta gran festa con trombe, pifferi, e spari 
        dal palazzo e dal Castel Sant'Angelo; ed evidentemente per tutte le strade 
        &egrave; corso a vederci un gran numero di gente, e ci&ograve; avvenne, 
        crediamo, per la gloria della Confederazione&quot;. Sicuri di s&eacute;, 
        non volevamno marciare dietro i Fiorentini e, sebbene contro ogni protocolli, 
        il Papa per questa volta lasci&ograve; correre.<br>
        Certo, non era pura ammirazione quella che provavano gli Italiani, affluiti 
        ai bordi delle strade, per gli Svizzeri che si aggiravano per le vie di 
        Venezia &quot;con spade soto e pater nostri in mano&quot;, e per i quali 
        da Roma si erano inviati, preventivamente, &quot;veri&quot; abiti. Ammiravano 
        senz'altro la forza bellica di questa &quot;gente ferocissima&quot; che 
        si buttava alla cieca contro il nemico, ma che era anche in grado di tenere 
        una notevole disciplina (&quot;E' alozati in Verona con tanto ordine, 
        che si fossero stati frati non se ar&igrave;ano portato meglio&quot;, 
        osservava un veneziano per le truppe della spedizione di Pavia, quando 
        entrarono a Verona), ma ciononostante erano barbari, e l'immagine degli 
        Svizzeri nudi che, senza far tante storie, attraversarono il Ticino a 
        nuoto e attaccarono Pavia all'improvviso, non corrispondeva proprio all'idea 
        che gli Italiani si facevano della guerra, pittura a parte. Gli Svizzeri 
        sapevano senz'altro vincere una battaglia campale, ma sembrava impossibile 
        tenerli a bada per tutta una spedizione, anche per una sola, e la struttura 
        federalista del comando elvetico (lui stesso disponeva di una sola voce 
        nel consiglio di guerra, si lamentava il bernese Albrecht vom Stein), 
        non era proprio fatta per impressionare i militari italiani.<br>
        Gli Svizzeri non erano amati, erano anzi temuti, come alleati, quasi quanto 
        lo erano come avversari: essi conducevano la guerra in modo inutilmente 
        sanguinoso, a differenza delle truppe mercenarie italiane, che costituivano 
        per cos&igrave; dire il capitale d'esercizio &quot;dell'imprenditore di 
        guerra&quot;, e che quindi venivano risparmiate, ove era possibile. Le 
        carte contabili, conservate per caso, di una compagnie italiana di ventura 
        attiva dal 1425 al 1448, dimostrano come dei 512 uomini in totale, nell'arco 
        di un quarto di secolo, non ne morirono pi&ugrave; di 25, di cui solo 
        15 in battaglia, gli altri &quot;di propria morte, di buona morte&quot;; 
        uno addirittura &quot;aneg&ograve; in Olio in Brexana per pigliar una 
        anguilla in domenicha mattina&quot;; un altro fu perso perch&eacute; fu 
        mandato a casa &quot;dalla moglie, cum licenzia che guarisse e tornasse 
        al Signore&quot;, ma egli non fece ritorno.<br>
        Erano finiti quei tempi. Quello che facevano gli Svizzeri, per gli Italiani 
        non era guerra come arte, o, meglio, non era &quot;arte della guerra&quot; 
        nel senso vero e proprio. Passavano perci&ograve; per aggressivi, imprevedibili 
        e avidi di bottino, e nelle richieste salariali erano pi&ugrave; sfacciati 
        degli stessi Italiani, perch&eacute; potevano permetterselo, come ammettevano 
        alcuni osservatori. Francesco Guicciardini, ad esempio, che nella sua 
        &quot;storia d'Italia&quot; giudica gli Svizzeri con apparente equanimit&agrave;; 
        nelle sue lettere, invece, d&agrave; libero corso all'indignazione sulle 
        esperienze fatte, trattando con loro come rappresentante del Papa durante 
        la spedizione del 1526: &quot;E' terribile cosa negoziare con loro&quot;. 
        Scorrendo le nostre liste non si penserebbe proprio che questi uomini 
        probi fossero in grado di ridurre altolocati diplomatici italiani sull'orlo 
        di una crisi di nervi! Nel far soldi, appunto, non si mostravano cos&igrave; 
        ingenui come poteva sembrare al alcuni. Il loro calcolo era semplice, 
        da contadini, ma efficace: &quot;Sarebbe meglio avere due o pi&ugrave; 
        mucche che una sola&quot;, e mungerne anche diverse, il &quot;magro&quot; 
        imperatore, il &quot;ricco&quot; re di Francia e il &quot;grasso&quot; 
        duca di Milano.<br>
        Tipi caratteristici di soldati italiani, nei quali, come amici e nemici, 
        s'imbattevano allora questi Svizzeri, si vedono sui muri del santuario 
        di Santa Maria delle Grazie sul Mincio, non lontano dal luogo dove nel 
        1512, al primo impatto con i nemici, gli Svizzeri avevano vittoriosamente 
        passato il fiume: un'adunanza spettrale di figure di cartapesta che, come 
        immagini votive in grandezza naturale, raccontano - in armatura, con la 
        spada, con il cannone - i loro destini e i loro ferimenti, ferite provocate 
        anche dagli Svizzeri, come espressamente sottolinea un'iscrizione, circondata 
        da palle di cannone incastrate nel muro: &quot;Celta ferox, Venetus prudens, 
        Elvetius atrox&quot;.<br>
        &quot;Sguizari &egrave; bon averli&quot;, era l'indiscusso principio, 
        e quanto a quell'epoca si guardasse a essi, si desume gi&agrave; dal fatto 
        che i &quot;Diarii&quot; veneziani di Marin Sanudo nominano la Dieta federale, 
        l'organo di coordinamento dei XIII cantoni, nella seconda met&agrave; 
        del 1512, dopo la spedizione di Pavia appunto, non meno di 30 volte. Era 
        doveroso guardare alla Confederazione durante la Dieta, svoltasi a Baden 
        nel settembre 1512, perch&eacute; essa decise sul destino del ducato di 
        Milano e perch&eacute; in quell'occasione nella piccola citt&agrave; si 
        accalcarono i rappresentanti delle grandi potenze! Cosa fosse veramente 
        questa Dieta, non si riusciva per&ograve; a capirlo bene e non si voleva 
        neanche saperlo, come si pu&ograve; supporre, vista la sorprendente superficialit&agrave; 
        delle informazioni di cui disponevano i diplomatici italiani, per solito 
        osservatori molto attenti. Mancava la comprensione per l'organizzazione 
        federalistica di questo apparato politico; la Confederazione elvetica 
        appariva piuttosto anarchica, perch&eacute; non si riusciva a individuare 
        un'autorit&agrave; centrale e perci&ograve; si vedeva nella Confederazione 
        sempre e solo una riserva di mercenari e mai veramente una potenza politica, 
        anche quell'unica volta in cui si mosse, fatto inaudito, senza essere 
        pagata e si temette momentaneamente che gli Svizzeri si annettessero il 
        ducato di Milano: &quot;Voleno far Milan canton loro; &#133;vor&igrave;ano 
        Milan fusse in la soa liga, e uno di prinzipal capi e cantoni come &egrave; 
        Basilea e Costanza!&quot;.<br>
        La fame di soldi sembrava dunque essere l'unico loro movente, e si spiegava 
        tale avidit&agrave; con la loro povert&agrave;: questi contadini vendevano 
        l'unica cosa che possedevano, cio&egrave; la forza fisica; &quot;Sguizari&quot; 
        chiamava Sanudo, perci&ograve;, i nobili poveri che nel Maggior Consiglio 
        vendevano il loro voto, perch&eacute; non avevano altro da vendere se 
        non se stessi. Era dunque logico che gli Svizzeri, con la loro reputazione 
        di essere invincibili, persero per gli Italiani, nel 1515 a Marignano 
        (Melegnano), anche l'unica dote che avevano. Anzi gi&agrave; la sera della 
        sconfitta, durante il sopralluogo sul campo di battaglia, era svanito 
        il loro fascino: &quot;Vide assa' corpi di sguizari morti in terra come 
        porzi, mal vestiti e poco imo (o magari) niente armadi (armati)&quot;, 
        scrive un veneziano dopo un giro tra i cadaveri ammassati; e nei loro 
        borsellini non si trovava neanche denaro in quantit&agrave; degne di nota: 
        &quot;Dice che di 22 borse tajate (tagliate) di questi sguizari fo trovato 
        dentro varie monete non di valuta, marzeli 19, sich&egrave; sono poveri 
        e chanaja (canaglia)&quot;.<br>
        Per solito, le tasche dei mercenari svizzeri non erano cos&igrave; vuote 
        (ma forse, prima del veneziano, per il campo di battaglia erano gi&agrave; 
        passati altri che non si erano limitati a contare i soldi). Per la spedizione 
        di Pavia del 1512 &egrave; possibile stabilire l'esatto ammontare dello 
        stipendio ricevuto, perch&eacute; la relativa contabilit&agrave; fu contestata, 
        e quindi documentata in modo particolarmente preciso.<br>
        Lo stipendio mensile di un semplice fante era di 9 libr. Bernesi oppure 
        4 &frac12; fiorini renani. La distribuzione di un fiorino a Coira e di 
        un ducato a Verona (di pi&ugrave; non era possibile, perch&eacute; non 
        bastavano i 20.000 ducati che il cardinale legato, nella previsione di 
        soli 6.000 uomini, aveva portato con s&eacute;) era un acconto sulla prima 
        paga mensile. Ma complessivamente lo stipendio fu pagato correttamente: 
        tre mensilit&agrave; dalla mano del cardinale legati (con fondi pontifici 
        e con un tributo imposto al ducato di Milano dopo la conquista) e l'ultima 
        al momento del congedo avvenuto ad Alessandria a fine luglio. In aggiunta 
        fu versata una mensilit&agrave; straordinaria, con la quale Pavia, dopo 
        l'occupazione, evit&ograve; il saccheggio. Quattro (3+1) stipendi semplici 
        dunque, cio&egrave; 36 libr. Bernesi o 18 fiorini renani; di conseguenza, 
        per i mercenari a doppio stipendio (fucilieri, capisquadra, stato maggiore), 
        il doppio; e inoltre altre gratificazioni, per lo pi&ugrave; destinate 
        ai mercenari a doppio stipendio, il cosiddetto soprassoldo, che l capitano 
        poteva distribuire a 12 su 100 uomini arruolati.<br>
        Disporre di una tale truppa anche per soli tre mesi, costava caro al committente 
        italiano: per il contingente bernese inizialmente di 1446 uomini (nel 
        primo e secondo mese), poi 1069 tra coscritti e volontari, si arrivava 
        a 3961 (2x1446+1x1069) stipendi semplici, con 11 doppi stipendi a 462 
        soprassoldi, il tutto a nove lib. Bernesi rispettivamente; di soli stipendi, 
        dunque, pi&ugrave; di 40.000 libr. o 20.000 fiorini renani, cio&egrave; 
        circa 15.000 ducati veneziani (e con ci&ograve; era coperto il colo contingente 
        bernese del grande esercito!). Per nostra fortuna, il capitano Burkhard 
        von Erlach aveva presentato alla commissione d'inchiesta un carteggio 
        meticoloso e con esito soddisfacente; aveva addirittura accuratamente 
        elencato i regali ricevuti lungo la strada: la cena della prima giornata 
        gratis perch&eacute; offerta dall'abate di Thorberg; per la prima colazione 
        a Burgdorf il vino gratis&#133; ecc.<br>
        Ma rivolgiamoci ancora una volta al singolo, alle sue condizioni di vita: 
        36 libr. bernesi o 18 fiorini renani di stipendio rappresentavano per 
        un semplice fante una bella somma in contanti che a casa forse non aveva 
        mai avuto in mano (ovviamente ci&ograve; non vuol dire che ai contadini 
        dell'Oberland fosse preclusa la possibilit&agrave; di accumulare ricchezze, 
        come Valerius Anshelm dimostra citando l'esempio del &quot;ricco Jenneli&quot; 
        che, da povero con due mucche, era diventato il contadino pi&ugrave; ricco 
        tra Kander e Simme), chi portava un fucile, come per lo meno 12 dei nostri 
        Oberl&auml;nder, o chi era caposquadra come Hans Gering, Peter Z&uuml;richer, 
        Peter von Moss e altri, riceveva gi&agrave; il doppio stipendio oppure 
        (con il soprassoldo) ancora di pi&ugrave;. Chi invece riusciva a impossessarsi 
        di qualche bottino o a fare addirittura un buon prigioniero, per la libert&agrave; 
        del quale poteva essere chiesto un riscatto, racimolava ancor di pi&ugrave;: 
        nel 1500, Hans Wingarter di Berna aveva ricevuto 500 ducati per Antonius 
        de Ferrariis di Milano e 350 ducati per Nicolaus de Scaldamariis di Mantova, 
        e non furono gli unici prigionieri che lui e altri avevano fatto; vi era, 
        per cos&igrave; dire, un consorzio per i riscatti, i cui membri poi, in 
        Svizzera, si cedevano reciprocamente i diritti. E ora, nel 1512, durante 
        il lungo assedio di Lugano e dopo tre settimane senza stipendio, a un 
        &quot;povero soldato orfano&quot;, per disperazione, venne la buona idea 
        di catturare e spremere un ricco malvagio; subito per&ograve; ci misero 
        sopra le maini i capitani, come egli confessa candidamente a Lor Signori 
        di Berna in un dettagliato rapporto. A Felix Bader di Thun, al quale mancavano 
        ancora 100 delle complessive 140 libr. per pagare la sua bottega di barbiere, 
        o a Heini Sager, che non aveva ancora potuto pagare la sua segheria sull'Aare, 
        sarebbe servito un buon bottino - e probabilmente proprio per questo motivo 
        si erano arruolati nel 1495, prendendo, da Thun, la strada per Lamparten 
        (Lombardia) attraverso l'omonima porta. Per dare una certa idea del valore, 
        o del potere d'acquisto, di un tale stipendio occorre dire che, per cento 
        libr. un cittadino di Thun poteva all'epoca assicurarsi, per la vecchiaia, 
        un posto all'ospizio.<br>
        Certo, non sempre sar&agrave; stato tanto ci&ograve; che rimaneva dello 
        stipendio e del bottino da portare a casa. Gli oggetti requisiti non potevano 
        essere portati a piacimento oltre le Alpi: era l'ora degli incettatori 
        che accompagnavano ogni esercito e contribuivano, a modo loro, a tener 
        basso il ricavato e a non far uscire gli oggetti dal paese. Spesso il 
        bottino ufficialmente distribuito non era molto cospiquo, perci&ograve; 
        ci si doveva muovere da soli, come quegli Svizzeri che durante la spedizione 
        di Pavia buttarono gi&ugrave; dalla sua preziosa bara, allestita nel duomo 
        di Milano, il fresco cadavere dello - scomunicato - vincitore di Ravenna, 
        il duca di Nemours, tagliandola in pezzi maneggevoli da portare via comodamente; 
        a sua volta il capo del contingente friburghese, Peter Falk, mandava a 
        sua moglie - come aveva annunciato in una lettera al suo &quot;tesoro 
        Ennelyn&quot; - &quot;due balle con diversa roba&quot; dal campo di battaglia, 
        e tra l'altro &quot;otto o nove begli stendardi che dovresti spiegare 
        bel bello e attaccare a uno o due pali&quot;. Il bottino era, per l'appunto, 
        una questione di caso e di fortuna, nelle guerre milanesi come gi&agrave; 
        nelle guerre della Borgogna: allora alcuni, anch'essi provenienti dall'Oberland 
        bernese, erano riusciti a raccattare cos&igrave; tanto da poter vendere 
        gli oggetti, requisiti in Borgogna, perfino a Francoforte; gli altri invece 
        requisivano ben poco, solo &quot;un formaggio, che hanno mangiato&quot;.<br>
        Del resto per molti il fascino del paese sconosciuto e dei prodotti mai 
        visti era fin troppo allettante, per cui la smania di acquistare generava, 
        alla fine, una forma di riciclaggio: i tributi estorti e i ricavi tratti 
        dalla vendita degli oggetti requisiti rifluivano nella produzione di articoli 
        di lusso, portavano per cos&igrave; dire - all'interno di una stessa regione 
        per&ograve; - a una ridistribuzione tra i diversi settori dell'economia. 
        La seta milanese era irresistibile: sappiamo di bernesi che si recavano 
        (anche senza licenza) a cavallo fino a Milano per comprare seta: &quot;Quando 
        a Novara pioveva forte&#133;, egli si diresse con altri capitani verso 
        Milano senza avere il passaporto, e compr&ograve; tanta seta&quot;. Cos&igrave; 
        arrivavano, al di l&agrave; delle Alpi, colori mai visti (il giallo!), 
        a Berna la moda femminile si arricchiva di strani accessori; un capitano 
        bernese si indebitava fino al collo solo per acquistare giarrettiere in 
        gran quantit&agrave; e altre stravaganze della moda, da far vedere poi 
        nelle strade della citt&agrave;. Ma anche tra gli stessi contadini, nelle 
        loro fattorie, si diffondeva questa brama per il lusso. Intanto il cronista 
        bernese stentava a capire quello che per un Italiano dell'epoca sarebbe 
        stato ovvio gi&agrave; da tempo, cio&egrave; che, spesso, nella formazione 
        del prezzo degli oggetti di lusso, la lavorazione incide pi&ugrave; dello 
        stesso materiale, come &quot;le scarpe che appena appena si reggono sulle 
        dita e ciononostante costano il doppio di un solido calzare!&quot;.<br>
        Se invece lo stipendio serviva per mantenersi, senza poter contare su 
        circostanze particolari dovute alle entrate provenienti dal saccheggio 
        o dagli abbondanti rifornimenti veneziani, buona parte di esso gi&agrave; 
        sfumava. La spedizione del 1512 &egrave; istruttiva per comprendere questo 
        nesso. Gli uomini della truppa, che poche settimane dopo Pavia iniziarono 
        l'assedio del castello di Locarno - tra essi 50 bernesi con inizialmente 
        otto uomini dell'Oberland - ben presto protestarono per lo stipendio troppo 
        basso. &quot;Ci sono parecchi soldati che ora non hanno pi&ugrave; soldi&quot;, 
        dovette apprendere il consiglio di Berna da una lettera scritta a met&agrave; 
        agosto; date le alte spese per il vitto, essi non erano disposti a fare 
        i soldati per otto libre. E dieci giorni dopo ancora: &quot;Cos&igrave; 
        i vostri soldati non sono disposti a prestare servizio per tale stipendio, 
        perch&eacute; quattro fiorini non bastano per mantenersi, costando tutto 
        caro in strada e in campo&quot;; cos&igrave; essi chiedevano non quattro, 
        ma cinque fiorini renani, cio&egrave; dieci libr. bernesi. E poco dopo 
        compare la stessa lamentela, documentata da un biglietto con alcuni nomi: 
        &quot;Questi sono ammalati&#133;&quot; E poi: &quot;Questi non sono ammalati 
        e non vogliono prestare servizio per tale stipendio: quello di Unterseen&#133;, 
        quello di Spiez&quot;, quindi anche due dell'Oberland. Tra gli assedianti 
        vi erano uomini che avevano appena prestato servizio nella spedizione 
        di Pavia per 4 &frac12; fiorini renani (o pi&ugrave; di 3 ducati veneziani) 
        al mese e con ottime vettovaglie provenienti dal paese, perci&ograve; 
        non erano disposti a partecipare a un lungo assedio senza la prospettiva 
        di un notevole bottino; preferivano piuttosto tornare a casa e aspettare 
        occasioni migliori.</font></p>
