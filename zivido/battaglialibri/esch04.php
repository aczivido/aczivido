      <p align="JUSTIFY"> <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><u>La 
        marcia verso l'Italia. Prosopografia di una spedizione militare: rassegna 
        delle truppe, tenuta delle liste, distribuzione della paga (1512). Morti, 
        ammalati, feriti: annotazioni nelle liste e destini individuali sull'esempio 
        di alcuni uomini dell'Oberland. Frequenza delle partenze, sull'esempio 
        di uomini di Biel e di Soletta. Congetture sull'esperienza italiana: ebbrezza 
        consumistica, edifici (l'anfiteatro di Verona), architettura delle fortificazioni, 
        pittura. La rapida avanzata sulla base delle lettere dal campo. Il problema 
        della comunicazione con la patria; confronto con il sistema d'informazione 
        organizzato dall'alleato veneziano. Il potere delle chiacchiere.</u><br>
      <br>Accompagnamo ora i nostri mercenari dell'Oberland bernese, i cui nomi risultano dalle 
        liste di truppa dell'aprile 1512 e in parte gi&agrave; da precedenti mobilitazioni, 
        nella loro marcia, mantenendo la prospettiva individuale ricavata da questo 
        tipo di fonti. Non solo gli interrogatori e le lettere infatti ci permettono 
        una diretta conoscenza della situazione e dello stato d'animo dei singoli: 
        riusciamo a far parlare anche le liste di truppa, apparentemente poco 
        significative, quando si presentano, come in questo caso, in modo cos&igrave; 
        fitto e intrecciato. Inoltre possiamo seguire la truppa senza perdere 
        d'occhio il singolo, in quanto le liste erano aggiornate di continuo con 
        annotazioni riguardanti la rassegna delle truppe, il pagamento dello stipendio, 
        le qualificazioni particolari, i ferimenti, le malattia e le licenze.<br>
        Afferriamo qui il fenomeno molto lamentato dell'ingrossamento massiccio 
        degli eserciti elvetici durante la loro marcia verso l'Italia, fenomeno 
        che creava ulteriori problemi relativi al comando, all'approvvigionamento, 
        al pagamento dello stipendio. Certo, alcuni volontari mancavano gi&agrave; 
        al momento della rassegna avvenuta a Coira, come risulta chiaramente dal 
        confronto delle liste no.6 e 18. Invece si presentarono a questa rassegna 
        molti pi&ugrave; uomini prima non iscritti: nelle sole liste di truppa 
        bernesi, dove l'incremento rimase tuttavia relativamente modesto, furono 
        inseriti, a Coira, ulteriori 107 volontari, e ancora a Villafranca si 
        aggiunsero altri sette bernesi. Altri non si sottoposero neanche al minimo 
        sforzo di farsi aggiungere nelle liste, sicch&egrave; dalla valle dell'Adige 
        calarono verso il costernato cardinale legato Schinner, che non aveva 
        portato con s&eacute; tanti soldi dalle casse pontificie per poter pagare 
        tutti, &quot;perch&eacute; pensavo di assoldare con essi sei mila soldati&quot;, 
        non gli attesi 5000 svizzeri, ma per lo meno il triplo! Le stime da parte 
        degli osservatori veneziani oscillano tra 15.000 e 24.000 uomini, e gli 
        stessi elvetici non avevano cifre pi&ugrave; precise.<br>
        Tali rassegne ben presto resero necessaria una correzione delle liste 
        di truppa, perch&eacute; l'Obersimmental, per esempio, era stato confuso 
        con il Niedersimmental, oppure un coscritto era finito tra i volontari 
        e viceversa. Una certa difficolt&agrave; derivava inoltre - per chi compilava 
        le liste allora, come per lo storico oggi - dai casi di omonimia: dal 
        piccolo distretto Aeschi/Krattigen partirono addirittura - caso estremo 
        per&ograve; - due Peter Wittwer e due Hans Schercz (nomi ricorrenti d'altronde 
        ancora oggi da quelle parti). E' probabile, anche se in concreto non lo 
        si pu&ograve; provare con sicurezza, che l'omonimia riscontrabile in alcune 
        unit&agrave; fosse segno di un legame di parentela. A Thun fu reclutato 
        un Caspar K&ouml;r, mentre un Peter K&ouml;rr era presente come volontario; 
        da Frutigen si misero in marcia addirittura tre Z&uuml;richer (uno di 
        loro divent&ograve; anche uno dei due capisquadra di Frutigen), dall'Obersimmental 
        tre Jonner; al volontario Ueli M&uuml;nig infine, proveniente dall'Obersimmental, 
        si un&igrave; a Coira un certo Caspar M&uuml;nig.<br>
        Una lista a parte elenca i fanti qualificati, i fucilieri, in quanto ricevevano 
        (come anche in altri eserciti) il doppio stipendio. Anche se la lista 
        non &egrave; suddivisa esplicitamente per regioni, i nomi dell'Oberland 
        sono raggruppati in modo tale da non lasciare dubbi sulla provenienza 
        e sull'identificazione. In tutto sono 11, cio&egrave; 4 di Thun, 4 di 
        Frutigen e 3 di Simmental: i due del Niedersimmental sono del resto i 
        soli fucilieri dell'Oberland scelti tra i volontari e non tra i coscritti. 
        Complessivamente, l'Oberland non era rappresentato proporzionalmente tra 
        i fucilieri dell'esercito bernese: in tutto l'esercito un uomo su 20 disponeva 
        di un fucile (72 su 1446), ma solo uno su 26 tra quelli dell'Oberland.<br>
        Nel corso della spedizione, come lista base per i coscritti, fu scelta 
        quella no.24, come si pu&ograve; desumere dalle successive annotazioni 
        accanto ai diversi nomi: &quot;&egrave; stato congedato&quot;; &quot;&egrave; 
        stato congedato giustamente&quot;; &quot;morto&quot;; &quot;&egrave; morto 
        a Pavia&quot;; &quot;ha avuto il passaporto&quot;. Per i volontari, la 
        lista no.18 svolgeva un'analoga funzione, anch'essa corredata di annotazioni, 
        come quelle relative a Bendicht Girssy: &quot;&egrave; morto&quot;, a 
        Hans Sorga: &quot;&egrave; morto il mercoled&igrave; a Pavia&quot;, entrambi 
        dell'Obersimmental. La nota di avvenuta morte relativa a Peter Sorga di 
        Simmental &egrave; stata cancellata in seguito: il segretario si era probabilmente 
        confuso con Hans Sorga, questi effettivamente morto, mentre Peter si sar&agrave; 
        fatto vivo, al pi&ugrave; tardi, il giorno di paga per ritirare lo stipendio. 
        Oppure: &quot;&egrave; caduto a Pavia&quot;; &quot;&egrave; tornato a 
        casa&quot;; &quot;a Cremona&quot;; &quot;mancava a Cremona&quot; e altre 
        annotazioni sulle quali torneremo tra breve.<br>
        Un caso di truffa, dibattuto in seguito dal tribunale municipale di Berna, 
        illustra quanto fosse importante tenere accuratamente le liste. Uno zurighese, 
        che &quot;nell'ultima guerra italiana&quot; aveva prestato servizio agli 
        ordini di un capitano di Unterwalden e &quot;preso da questi, su raccomandazione 
        di Lor Signori di Berna, il suo stipendio&quot; ammise &quot;di essersi 
        fatto arruolare da tre diversi capitani&quot;; egli raccont&ograve; come, 
        con un suo complice, avesse tentato di ritirare, sotto diversi nomi, e 
        magari mediante terzi, lo stipendio altrui. Ad esempio &quot;lo stipendio 
        di un compagno caduto in battaglia, di cui avrebbero fatto il nome&quot;. 
        Era importantissimo dunque che nella lista paga fosse scritto chiaramente 
        &quot;morto&quot; per il corrispondente nome, e in soprappi&ugrave; per 
        quello giusto, perch&eacute; anche nel caso in questione, era riapparso 
        una volta il vero titolare e ci&ograve; era costato al capitano un bel 
        gruzzolo di soldi.<br>
        Due liste registrano persino i feriti del contingente bernese perch&eacute; 
        i feriti costavano, e tutto ci&ograve; che costa ha pi&ugrave; possibilit&agrave; 
        di essere documentato e con ci&ograve; tramandato. Tra i 13 Bernesi, feriti 
        durante l'assalto di Pavia, ritroviamo quattro uomini dell'Oberland, e 
        raccogliamo inoltre notizie pi&ugrave; precise sul loro ferimento: &quot;Albrecht 
        Kuirssiner ferito con uno sparo al braccio&quot;; &quot;Tschan an der 
        Leng &egrave; stato ferito nel primo bastione&quot;, &quot;Gering al primo 
        bastione&quot; (sono due uomini dell'Obersimmental, e tra essi il caposquadra); 
        Bendicht Berger dell'Hasli &quot;&egrave; stato ferito in citt&agrave;, 
        sul ponte&quot;. Se la rispettiva somma, dietro i nomi, &egrave; proporzionale 
        alla ferita subita, Berger dev'essere stato colpito gravemente.<br>
        Fino alla battaglia decisiva, la truppa sembra in sostanza essere rimasta 
        compatta. Le liste paga utilizzate a Walenstadt, Verona e Pavia registrano 
        di regola l'organico previsto, con queste eccezioni: per la retribuzione 
        a Pavia sono stati attribuiti a Obersimmental 39 uomini (anzich&eacute; 
        40), a Interlaken 44 (anzich&eacute; 46) e a Unterseen/Unspunnen/Ringgenberg 
        19 (anzich&eacute; 20).<br>
        Ma subito dopo la fine della battaglia di Pavia, dopo la conquista delle 
        citt&agrave; pi&ugrave; importanti, l'esercito rischiava di disgregarsi, 
        perch&eacute; in molti volevano tornare a casa: questo comportamento appare 
        come un tratto caratteristico per tutti gli eserciti elvetici, che cogliamo 
        qui dal lato dell'esperienza individuale.<br>
        A questa fase risale probabilmente la sigla &quot;kr&quot; (=krank), cio&egrave; 
        &quot;ammalato&quot;, aggiunta, con inchiostro diverso, accanto a molti 
        nomi elencati nella lista principale, e in quella dei volontari: &quot;&egrave; 
        malato&quot;; &quot;&egrave; tornato a casa ammalato&quot;; &quot;&egrave; 
        tornato a casa&quot;; &quot;&egrave; tornato a casa con licenza&quot;; 
        oppure un semplice &quot;&egrave; andato via&quot;.<br>
        In effetti molti si saranno ammalati a causa del clima, delle fatiche 
        e dell'inconsueta alimentazione. Non pochi per&ograve; avranno colto ci&ograve; 
        come pretesto per dileguarsi; infatti i capitani bernesi scrissero, con 
        tanto sarcasmo, in un altro contesto: &quot;&#133; si sono dati per malati 
        e infermi ma, appena sulla strada (del ritorno), sono diventati cos&igrave; 
        forti che i sani non riuscivano a tenere il passo: per questi dovete preparare 
        dolci perch&eacute; ne hanno gran bisogno&quot;. A ogni modo, in occasione 
        della rassegna e del pagamento dello stipendio, avvenuti ad Alessandria, 
        il capitano bernese Burkhard von Erlach, per precauzione, si fece attestare 
        dal comandante supremo dell'esercito unificato, il barone Ulrich von Hohensax, 
        che i capitani erano tenuti, su ordine del cardinale legato Schinner, 
        a corrispondere lo stipendio solo ai presenti o a quelli con regolare 
        licenza, perch&eacute; &quot;in questa campagna militare molti soldati 
        si sono ammalati e, a causa della loro malattia, sono tornati a casa in 
        licenza e con il passaporto, mentre parecchi si sono allontanati senza 
        passaporto, n&eacute; licenza, sicch&egrave; alla rassegna, effettuata 
        ad Alessandria, si &egrave; avuta una grande confusione&quot;. E ci&ograve; 
        perch&egrave; in occasione del pagamento della terza e ultima mensilit&agrave;, 
        anche questa volta rispettivamente 9 lib. Bernesi, oppure 4 &frac12; fiorini 
        per ogni soldato semplice, si erano presentati solo 1069 dei 1446 uomini 
        di cui il contingente bernese era originariamente composto: gli altri 
        facevano parte delle forze di occupazione, o si erano semplicemente dileguati.<br>
        Anche quest'ondata, vera o presunta, di malattie si riflette, con precisione, 
        nell'andamento dell'organico della truppa: tra i coscritti del Niedersimmental 
        si ammal&ograve; uno su dieci, tra i volontari addirittura uno su cinque 
        (e tra essi entrambi i fucilieri); tra i coscritti dell'Obersimmental 
        uno su cinque (9 su 44), come tra quelli dell'Interlaken (Stefan K&uuml;ngsberger 
        sparisce senza licenza: &quot;non ha passaporto&quot;), tra quelli dell'Hasli 
        6 su 33. Pi&ugrave; robusti sembrano essere gli arruolati di Thun. Non 
        esamineremo qui la questione se la maggiore presenza di casi di malattia 
        in alcune unit&agrave; fosse il risultato di un contagio di natura clinica 
        o psichica; la nostalgia e il desiderio di tornare a casa, in seguito 
        ritenute malattie tipiche dei mercenari svizzeri, apparentemente avevano 
        molteplici cause. Nel giugno 1512 bastava, forse, che si fosse al tempo 
        del raccolto.<br>
        Solo sulla base di queste liste, con le loro aggiunte, comprendiamo il 
        senso di un elenco dal titolo &quot;ultimo stipendio&quot;; da un confronto 
        risulta che i nomi in esso menzionati sono nella maggior parte - in 28 
        su 38 casi - identici ai nomi con un'annotazione di malattia. Dovrebbe 
        dunque trattarsi degli ultimi stipendi corrisposti agli ammalati in licenza 
        e ad altri congedati; rientra il fatto che un malato senza &quot;passaporto&quot;, 
        Stefan K&uuml;ngsberger di Interlaken appunto, non ricevette niente.<br>
        La vittoriosa campagna esercit&ograve; un forte richiamo quando si svolse, 
        nell'estate, l'arruolamento per un'ulteriore spedizione in Italia. Mentre 
        alla prima aveva partecipato, da Thun, un certo Urban Sutter, toccava 
        ora ad Hans Sutter; da Spiez part&igrave; prima un Peter B&uuml;rgi, ora 
        un Hans B&uuml;rgi; da Interlaken prima un Heiny Darer, ora da unterseen 
        un Ueli Darer; dall'Obersimmental prima Philipp e Bendicht Rouber, ora 
        Hans Rouber. Ma evidentemente non mancavano, dall'Oberland, quelli che 
        si arruolavano due volte; congedati nel luglio ad Alessandria, gi&agrave; 
        nell'autunno si trovavano di nuovo al di qua delle Alpi: Stefan Schneling 
        dal niedersimmental per esempio, o Caspar ze Wingarten da Thun, J&ouml;rg 
        Sparen dall'Obersimmental, Peter Schmidli da Interlaken. Due degli otto 
        uomini dell'Oberland si ammalarono presto, altri due, che non volevano 
        prestare servizio per uno stipendio mensile di soli 4 fiorini, chiesero 
        di essere sostituiti.<br>
        Procediamo nell'analisi di tali liste; esse potrebbero essere utilizzate 
        anche in altro modo, e cio&egrave; non esaminando il materiale, come abbiamo 
        fatto finora, rispettivamente nel suo insieme, ma seguendo, sulla base 
        di un approccio prosopografio, singoli nomi, singoli destini, attravesro 
        le diverse liste.<br>
        J&ouml;rg Spare dell'Obersimmental, ad esempio, prese gi&agrave; parte 
        all'arruolamento clandestino del maggio 1509, per la spedizione contro 
        Venezia, e appare addirittura tra i primi che il reclutatore Christian 
        Saler recluta nella sua valle. All'inizio dell'estate del 1512 J&ouml;rg 
        era tra i volontari, si present&ograve; alla rassegna avvenuta a Coira, 
        partecip&ograve; alla campagna militare e alla fine dello stesso anno 
        era di nuovo nella truppa intorno a Lugano.<br>
        Ancora pi&ugrave; fortemente coinvolto nell'arruolamento clandestino del 
        1509 fu il suo compaesano Philipp Rouber che, come agente del reclutatore, 
        si diresse a valle verso Boltigen per dare col&agrave; il segnale di partenza, 
        indicando Gsteig come punto di raccolta (intendevano ovviamente passare 
        per il valico del Sanetsch), invadendo cos&igrave; per&ograve; il campo 
        d'azione di altri reclutatori (&quot;e ai predetti discorsi Philipp Rouber 
        risponde a Gr&uuml;newald: &quot;No, voglio che coloro che ho portato 
        con me, vengano con me e con nessun altro&#133;&quot;). Ora, nel 1512, 
        egli part&igrave; come coscritto per la volta di Pavia e ritorn&ograve; 
        con la nota ufficiale &quot;ammalato&quot;.<br>
        Puer Tschan an der Leng, cio&egrave; di Lenk in fondo al Simmental, finisce 
        la spedizione come &quot;ammalato&quot;, ma sul suo caso siamo informati 
        pi&ugrave; dettagliatamente: fu ferito durante l'assalto al primo bastione 
        di Pavia e ricevette perci&ograve; giustamente il suo &quot;ultimo stipendio&quot;; 
        ebbe cio&egrave; salva la vita, mentre, tra i suoi compaesani, Hans Sorga 
        cadde presso Pavia, e Benedikt Kirssy e Bartlome Huge morirono durante 
        la campagna in altro modo: &quot;morti&quot;.<br>
        Di Stefan Schneling del Niedersimmental, il calzolaio Mathis disse, nel 
        1509, che era sulla strada per Sitten, per la guerra contro Venezia. Nella 
        spedizione di Pavia, egli faceva parte della truppa regolare e gi&agrave; 
        alcune settimane dopo, a fine agosto 1512, si trovava di nuovo a sud, 
        presso Lugano, dove per&ograve;, ammalatosi, fu ben presto sostituito. 
        Il suo compaesano Heini Matte o Mathee partecip&ograve; come volontario, 
        ma ricevette lo stesso un fucile e, di conseguenza, il doppio stipendio. 
        Era presente alla rassegna delle truppe a Coira e fu congedato pi&ugrave; 
        tardi come malato.<br>
        Tra gli uomini dell'Hasli, Caspar Michel sembra una persona di fiducia, 
        perch&eacute; a Walenstadt, senza essere caposquadra, ricevette dei soldi 
        da distribuire tra i componenti della sua unit&agrave;. Il suo compaesano 
        Claus Halter, congedato per malattia con ultimo stipendio nell'ottobre 
        del 1515 durante la campagna di Pavia, fece parte delle forze d'occupazione 
        di Bellinzona. Tra i 50 soldati che nell'ottobre 1515, dopo la sconfitta 
        di Melegnano, furono rapidamente mandati per presidiare le fortezze di 
        Bellinzona, troviamo, accanto a Claus Halter, altri vecchi conoscenti 
        tra gli 11 uomini dell'Oberland.<br>
        L'analisi prosopografica delle liste di coscrizione e la ricerca di alcuni 
        nomi attraverso tutte le liste, pu&ograve; portare a risultati interessanti 
        da tanti punti di vista, a condizione per&ograve; che il materiale a disposizione 
        sia sufficientemente vasto. Ci&ograve; vale, ad esempio, per le liste 
        di truppa di Soletta e Biel. Come Bruno Koch recentemente ha potuto constatare, 
        sulla base delle liste di Soletta, dei complessivi 250 uomini obbligati 
        al servizio militare, tre partirono, tra il 1510-1515, non meno di cinque 
        volte (si trattava di capitani), undici quattro volte, e 36 pur sempre 
        tre volte: costoro erano in gran parte (circa la met&agrave;) persone 
        qualificate in quanto titolari di doppio stipendio e del soprassoldo. 
        Tra essi vi erano i tre figli del macellaio Hans Hugi, i quali raramente 
        perdevano l'occasione di partecipare alle spedizioni nell'Italia settentrionale, 
        e raggiunsero, nel contingente di Soletta, posizioni notevoli.<br>
        A Biel si distingue ancora pi&ugrave; chiaramente un nucleo di uomini 
        che superavano la media di due partenze e che incontriamo ripetutamente 
        nelle campagne militari del 1511-1515: due uomini vi parteciparono sei 
        volte (cio&egrave; a tutte le spedizioni), nove cinque volte, diciannove 
        quattro volte, venticinque tre volte - per un &quot;gruppo di 30 uomini, 
        il 15% di tutti i militari&quot;, si registrano dunque 133 partenze, &quot;il 
        che corrisponde al 35% di tutti gli arruolamenti&quot;. Anche in questo 
        caso si &egrave; dimostrato molto proficuo uno studio prosopografico pi&ugrave; 
        dettagliato, capace di considerare la condizione sociale, il ruolo ufficiale, 
        l'appartenenza corporativa. Da tale studio emergono alcune famiglie che 
        hanno compiuto, attraverso il servizio militare, un'ascesa sociale. A 
        Berna, invece, il mercenariato rimaneva nelle mani della nobilt&agrave; 
        e del patriziato: il padre o il fratello maggiore facevano parte del Consiglio, 
        ai figli minori non rimaneva altro che il servizio mercenario il quale, 
        talvolta, li port&ograve; a conflitti d'interesse con la politica estera 
        del Consiglio, fin quando la Riforma e le sconfitte dei confederati nell'Italia 
        settentrionale non bandirono, in misura crescente, il mercenariato.<br>
        Per rievocare l'esperienza di guerra fatta da coloro che, attraverso le 
        liste di truppa, abbiamo conosciuto di persona, ritorniamo ancora una 
        volta al momento della loro partenza verso l'Italia, verso un paese i 
        cui nomi, nella loro bocca, prendevano un timbro curioso: &quot;Frischgast&quot; 
        per Brusasco o Bricherasio, &quot;Fischgondt&quot; per Visconti, &quot;Tschawats&quot; 
        per Chivasso, M&uuml;se&quot; per Mincio, &quot;Pre&szlig;&quot; per Brescia, 
        &quot;Paffy&quot; o &quot;Bofyg&quot; per Pavia, &quot;Polatz&quot; per 
        Piossasco; &quot;der Wisenrein&quot; per il Vice-Re, &quot;der Roggen&quot; 
        per la Rocca; e al ritorno si passava per &quot;Wersel&quot; (Vercelli), 
        &quot;Jffery&quot; (Ivrea), &quot;Ougstal&quot; (Aosta). L'una o l'altra 
        di queste parole, i nostri militi le avranno gi&agrave; sentite come nomi 
        di un lontano mercato, al quale avevano venduto - dalla Svizzera interna, 
        ma anche dall'Oberland bernese - il loro bestiame. Per recuperarlo magari, 
        adesso: come i bernesi, che si ripresero, nel 1513, un orso, regalato 
        al generale francese La Tr&eacute;mouille a Lucerna, e lo riportarono 
        per il valico del Gran San Bernardo, nel &quot;b&auml;renh&uuml;sle&quot; 
        (&quot;casetta degli orsi&quot;) di Berna.<br>
        Ci piacerebbe sapere quale fosse l'impressione prodotta sui mercenari 
        dalla vista di questo paese per loro nuovo. Sappiamo (anche ci&ograve; 
        &egrave; importante per cogliere l'atmosfera in cui s'inseriva tale esperienza) 
        di ammalati in massa, delle conseguenze provocate dal consumo di vino, 
        dell'ebbrezza consumistica vedendo, nelle strade milanesi, gli articoli 
        di lusso. Ma quale sensazione avranno suscitato, in loro, i monumenti 
        mai visti prima? Essi li vedevano appena entrati in Italia. Quando i confederati 
        occuparono Verona, il 26 maggio, si trovarono di fronte a un colossale 
        monumento, cio&egrave; l'anfiteatro romano, che ad essi doveva risultare 
        incomprensibile in tutti i suoi aspetti: nella forma, nella grandezza, 
        nella sua funzione. Dalla descrizione di un anfiteatro, per mano di uno 
        svizzero all'incirca dello stesso periodo, si pu&ograve; desumere quanto 
        fosse difficile, per un occhio non istruito, spiegare a se stesso e ad 
        altri la strana e sconvolgente vista di un tale edificio. Mentre l'umanista 
        italiano dell'epoca - come noi oggi - se ne impossessava rapidamente, 
        con l'aiuto di una specifica terminologia tecnica, questi soldati semplici, 
        privi di ogni idea dell'architettura antica, disponevano solo delle loro 
        goffe parole per descrivere, in modo tortuoso, quello che vedevano. E 
        perci&ograve; un testo come quello citato, ci offre per lo meno una vaga 
        idea sul modo in cui avevano recepito il monumento i nostri mercenari 
        dell'Oberland.<br>
        Questi vedono e non dicono: anfiteatro, capacit&agrave; circa 30.000 spettatori, 
        cavea su sostruzioni di pietra; tre piani di arcate, altezza 30 metri, 
        ma vedono: reggia di Dietrich von Bern con cortile per i tornei dei suoi 
        cavalieri; estensione: &quot;talmente grande che, se al suo interno ci 
        fosse cresciuta l'erba, un falciatore neanche in una giornata sarebbe 
        riuscito a falciarla&quot;; alla base, volte per proteggersi dal caldo 
        italiano; molti archi, poi altri archi, poi ancora archi; altezza: nessun 
        edificio da noi a Lucerna &egrave; cos&igrave; alto - grandezza smisurata 
        in ogni caso, perch&eacute; gli Svizzeri confrontavano il monumento con 
        l'architettura abitativa borghese di stile gotico, e non con l'architettura 
        delle corti del Rinascimento.<br>
        Si potrebbe del resto ricostruire quali erano gli esempi di recentissima 
        architettura in cui i nostri mercenari s'imbattevano: a Casale Monferrato 
        volgevano al termine i lavori per la chiesa di S.Domenico, che poteva 
        sembrare loro, nelle sue forme gotiche, ancora piuttosto familiare. A 
        Pavia invece era in costruzione, con il duomo, un magnifico edificio a 
        pianta centrale, alla cui progettazione avevano collaborato Bramante e 
        Leonardo da Vinci. Anche a Milano, citt&agrave; ostinatamente gotica, 
        gi&agrave; allora si potevano vedere novit&agrave; meravigliose: Santa 
        Maria delle Grazie, dove letteralmente si passava dallo stile gotico (corpo 
        anteriore) al Rinascimento (la grandiosa tribuna di Bramante). Oppure, 
        durante l'avanzata dal Mincio verso l'Adda, essi trovavano tra Crema e 
        Cremona, nell'aperta campagna, diverse notevoli chiese rinascimentali 
        appena terminate o ancora in costruzione, come S.Maria di Bressanoro, 
        la Madonna della Misericordia, S.Maria della Croce. Ma gi&agrave; a L&ouml;werz, 
        cio&egrave; a Lugano, questi Svizzeri s'imbattevano nella nuova facciata, 
        appena finita, di San Lorenzo, in uno stile rinascimentale mai visto prima.<br>
        Oppure essi vedevano l'architettura italiana delle fortificazioni: gi&agrave; 
        il primo passaggio di fiume, che l'esercito dovette aprirsi con la forza, 
        cio&egrave; il passaggio del Mincio presso Valeggio e Borghetto, avvenuto 
        il due giugno, era protetto da un imponente ponte fortificato, di dimensioni 
        mai viste prime da questi uomini, e che ancora oggi &egrave; impressionante 
        nei suoi resti. Uno Svizzero che aveva partecipato all'assalto, paragon&ograve; 
        quest'opera di difesa con la cittadella di Bellinzona, anch'essa milanese, 
        con le sue torri e cortine, e riport&ograve; la sensazione , che i mercenari 
        svizzeri avranno avuto durante la loro avanzata verso Pontevico, di ritrovarsi 
        cio&egrave; all'interno di tutto un complesso di fortificazioni: appena 
        superata una prima barriera, ci si imbatteva &quot;in un'altra, quella 
        pi&ugrave; forte&#133;, in una rocca fortissima, poi acque molto vaste 
        dopo, e dopo l'acqua ancora una rocca&quot;.<br>
        L'impressione momentanea suscitata dall'arte e dall'architettura italiana 
        sar&agrave; stata forte, ma non avr&agrave; avuto un effetto duraturo. 
        Queste campagne militari di breve durata non vi si prestavano. Neanche 
        nelle pitture e nelle opere grafiche di un Niklaus Manuel si possono individuare 
        tracce della sua permanenza in Italia come mercenario. Ci piacerebbe sapere 
        cosa provassero i semplici mercenari davanti alla grande pittura dell'epoca, 
        per esempio davanti all'Ultima Cena di Leonardo, gi&agrave; terminata, 
        a Milano. Ma non aspettiamoci troppo: con disinvoltura si apponeva il 
        proprio nome nel bel mezzo di immagini sacre (come un lanzichenecco tedesco 
        nella chiesa dei domenicani a Verona; ma anche gli Italiani lo facevano), 
        e specialmente in una citt&agrave; conquistata, come Roma durante il Sacco 
        del 1527, i lanzichenecchi tedeschi non ebbero scrupoli a incidere sugli 
        affreschi di Raffaello il nome di Lutero o altri graffiti provocatori.<br>
        Le effettive esperienze di questa breve, impetuosa, spedizione erano il 
        combattimento, l'ebbrezza della vittoria facile e il ricco bottino raccolto. 
        E - dato che disponiamo di fonti adatte - si tratta qui di queste sole 
        esperienze personali, non (per ribadirlo una volta ancora) dello svolgersi 
        degli avvenimenti e dell'organizzazione militare. In questo contesto i 
        confederati erano certissimi di agire per una giusta causa (&quot;Iddio 
        il Signore sar&agrave; il protettore di tutti noi, perch&eacute; combattiamo 
        per la sua Santa Chiesa&quot;), erano dominati inoltre dal desiderio, 
        di difficile comprensione per gli eserciti mercenari italiani, di battersi: 
        &quot;tutti gli uomini sono bramosi di venire alle mani con i francesi&quot;.<br>
        Gi&agrave; il primo successo militare, l'espugnazione della fortezza di 
        Valeggio presso il Mincio (ricordata, con entusiasmo, anche da Zwingli, 
        il futuro riformatore della Svizzera, nella sua lettera a Vadian), causava 
        euforia, invitava a raccogliere il bottino, a ubriacarsi.&quot;Questi 
        sguizari (Svizzeri) hanno sachizato tutto el castello, el quale era pieno 
        di vino, formazzo, farine e robe de' contadini e altro&quot;, scrive un 
        osservatore veneziano. E per chi pensa che, forse, si sia trattato solo 
        di un'affermazione diffamatoria da parte dell'alleato italiano, sappia 
        che a questo proposito il Consiglio bernese era stato informato, da una 
        lettera non sospetta, di ben altri dettagli: in quel castello sarebbero 
        state trovate &quot;indicibili quantit&agrave; di vino, del quale i soldati 
        ne hanno bevuta talmente tanto che hanno picchiato e pestato&quot;.<br>
        Furono ubriacature dopo una vittoria inaspettatamente facile (&quot;&#133;non 
        sappiamo di nessun confederato caduto&quot;), e dopo l'eccitante spettacolo 
        di un fuoco d'artiglieria (&quot;di massimo piacere&quot;). Si sentivano, 
        ora, irresistibili: si spingevano in avanti, sempre in avanti (&quot;per 
        non coricarsi la notte l&agrave; dove ci si era coricati nella precedente&quot;), 
        e assaporavano l'effetto da loro prodotto sul nemico: &quot;Essi si sono 
        spaventati in sommo grado come abbiamo potuto leggere in una lettera che 
        il signore de La Palisse, ora gran maestro, aveva scritto al Re&quot;.<br>
        Ora tutto cadeva nelle loro mani, essi erano i padroni di un ricco paese. 
        Erano invece partiti, questi uomini dell'Oberland, con pochi soldi, talmente 
        pochi che, subito dopo la partenza, i capitani, allarmati, avevano scritto 
        a Berna di spedirne immediatamente altri, in caso contrario &quot;avremo 
        molta gente disubbidiente&#133;: e in particolare si lamentano quelli 
        di Niedersimmental, e quelli di Thun&quot;. Pi&ugrave; forte di tutti, 
        sembra, avessero gridato quelli dell'Obersimmental per essere pagati prontamente, 
        altrimenti &quot;volevano tornare a casa!&quot; Ora invece &quot;i soldati 
        sono pieni di soldi&quot;, raccolgono un ricco bottino, tanto pi&ugrave; 
        che la popolazione, che non si era aspettata un'avanzata cos&igrave; rapida, 
        non aveva portato via i suoi averi. Lo stesso cardinale legato li aveva 
        esortati espressamente a rifarsi prima di tutto sul paese: &quot;e quello 
        che conquistate, tenetelo per voi&quot;. Ed essi non se lo erano fatto 
        ripetere due volte.<br>
        Dopo lo scarso approvvigionamento di cibo durante il passaggio delle Alpi 
        con le sue grandi fatiche (i due cannoni bernesi dovevano essere portati 
        per i passi &quot;con gran lavoro e costi, quando la strada &egrave; aspra 
        e dura&quot;, e nello stesso momento vi fu &quot;una gran penuria di cibo&quot;, 
        come lamenta un'altra lettera, anzi: gli uomini di Biel, che si erano 
        uniti ai Bernesi, ritenevano di non aver mai mangiato cos&igrave; male), 
        ora ne disponevano in abbondanza. &quot;Cos&igrave; i veneziani ci hanno 
        fornito di una tale quantit&agrave; di pane, vino, pesce, carne e di tutto 
        ci&ograve; di cui gli uomini hanno bisogno per alimentarsi, anche di buon 
        vino di malvasia e in grande abbondanza, sicch&egrave; vi fu dappertutto 
        un mercato come se ci fossimo fermati in una buona citt&agrave;&quot;.<br>
        E sempre compare il Malvasia (o ci&ograve; che fu ritenuto tale): &quot;&#133;a 
        sufficienza tutto il necessario, e in aggiunta il Malvasia&quot;. E quello 
        che non arrivava con i rifornimenti, lo si prelevava dal apese: &quot;in 
        tal modo il paese &egrave; stato saccheggiato gravemente di cibo e di 
        altre cose&#133;&quot;; in breve: &quot;quello che l'uomo desidera, lo 
        trova a sufficienza&quot;.<br>
        L'esercito avanz&ograve; rapidamente. Di soli cannoni se ne conquistarono 
        pi&ugrave; di quanti ne servissero accanto all'ammirata artiglieria veneziana 
        (non sarebbe dunque stato necessario per i Bernesi portare con s&eacute;, 
        con tanta fatica, due cannoni sulle Alpi!). E soprattutto si conquistarono 
        pi&ugrave; cannoni rispetto al numero di cannonieri a disposizione, ricorrendo 
        perci&ograve;, all'occorrenza, per la seconda spedizione, piuttosto alle 
        antiquate catapulte: &quot;anche cos&igrave; ci mancano cannoni, perci&ograve; 
        abbiamo un maestro che costruisce un &quot;bliden&quot; (catapulta) con 
        il quale vuole abbattere il castello&quot;. A causa dell'impetuosa avanzata, 
        crollarono dopo pochi giorni le posizioni tenute dai Francesi nel ducato 
        di Milano. Trionfante, e con l'alta autoconsiderazione, allora tipica, 
        dei confederati, Obwalden scrisse all'Hasli (che pass&ograve; la lettera 
        a Berna) che essi, semplici contadini, eseguivano la volont&agrave; di 
        Dio, se ogni tanto tenevano d'occhio anche i signori. Infatti &quot;per 
        l'eternit&agrave; mai sar&agrave; dimenticato che con l'aiuto di Dio abbiamo 
        sollevato e liberato i grandi capi dalle loro grandi preoccupazioni e 
        pene&quot;, ringraziando il Creatore per tanto onore, tanta fortuna e 
        salvezza: a queste categorie terrestri si trova aggiunta, al margine della 
        lettera, anche una categoria religiosa: &quot;gnad&quot;, la grazia.<br>
        La notizia pi&ugrave; importante di tutta la spedizione invece, l'espugnazione 
        di Pavia, avvenuta il 15 giugno, non arriv&ograve; subito a Berna, perch&eacute; 
        il corriere fu intercettato. Vi &egrave; un problema da tenere assolutamente 
        presente quando si esaminano imprese come quella della spedizione di Pavia, 
        il problema cio&egrave; della trasmissione di notizie, del collegamento 
        tra il mercenario e la patria: &quot;Nicola, che &egrave; in campo, vive 
        ancora, come egli stesso ha appreso&quot;, solo questo un contadino di 
        Thun sa di suo figlio. Ma il problema pi&ugrave; grave &egrave; quello 
        delle informazioni a disposizione del governo per le sue decisioni.<br>
        Per decidere, il Consiglio di Berna o la Dieta federale avevano bisogno 
        di quelle lettere il cui esterno, sporco e unto, spesso mostrava i segni 
        della lunga strada fata attraverso le Alpi. La frequenza con la quale 
        i capitani scrivevano dal campo al loro Consiglio a Berna, non si pu&ograve; 
        desumere, con sicurezza, dal materiale documentario residuale tramandatoci 
        dal destinatario, come appunto quel fondo &quot;Unn&uuml;tze Papiere&quot; 
        conservato nell'Archivio di Stato a Berna. In tutta evidenza essa risulta 
        invece, in relazione alla spedizione di Pavia, per Friburgo, perch&eacute; 
        il capitano di questa citt&agrave;, Peter Falk, numerava le sue lettere 
        per sicurezza: &quot;La prima da Berna, l'altra da Coira, la terza da 
        Trento e la quarta da Dietrchs Bern (Verona)&quot;; poi &quot;la quinta 
        lettera scritta a Castion de le Strivere&quot;; la settima lettera il 
        18 giugno da Pavia; e anche l'ottava da Pavia il 26 giugno. Ben otto lettere 
        nel corso di circa sette settimane, di cui quattro conservate nel fondo 
        bernese, perch&eacute; alcune lettere di Falk furono mandate per conoscenza 
        anche al Consiglio di Berna. Nello stesso modo Berna da parte sua ebbe 
        cura di far circolare le informazioni tra le citt&agrave; elvetiche (soprattutto 
        Friburgo e Soletta): ci&ograve; si pu&ograve; dimostrare dettagliatamente 
        gi&agrave; per il periodo delle guerre contro Carlo il Temerario, perch&eacute; 
        le copie, necessarie a tale scopo, producevano costi che venivano registrati 
        come spese della cancelleria di Stato. Che Berna prendesse sul serio questa 
        funzione di smistare le informazioni, e non badasse a spese per svolgerla, 
        vorrei sottolinearlo esplicitamente, perch&eacute; si vedr&agrave; che 
        anche con tutti questi sforzi non si riusciva a uguagliare il sistema 
        di informazione sviluppato dalle potenze italiane, anche se Berna o la 
        Dieta federale, non meno di Venezia, avevano bisogno di un rapido e fitto 
        flusso di informazioni.<br>
        Berna invece era tagliata fuori, talvolta per intere giornate, da ogni 
        notizia sulle operazioni in Italia (&quot;Noi siamo un poco irrequieti 
        e siamo oltremodo meravigliati che non ci arrivi nessun messaggio da voi, 
        perch&eacute; dopo l'arrivo del corriere Streler non abbiamo avuto nessuna 
        notizia&quot;, si lamentava Berna in un'altra occasione), alle potenze 
        con un assetto pi&ugrave; moderno non ne mancavano. Per Venezia, l'alleato 
        pi&ugrave; dinamico dei confederati durante la spedizione del 1512, lo 
        si desume in dettaglio dai &quot;Diarii&quot; di Marin Sanudo. E' difficile 
        immaginarsi la massa di notizie che arrivavano ogni giorno, anzi pi&ugrave; 
        volte al giorno, a Venezia. Le pagine dei &quot;Diarii&quot; sembrano 
        semplicemente dei fogli strappati, per cos&igrave; dire, da una sorta 
        di telescrivente, e, dopo aver aggiunto giorno e ora, messi insieme per 
        presentarli poi come storia o come la materia prima di essa: la &quot;historia&quot; 
        potrebbe essere scritta anche in forma pi&ugrave; breve, &quot;ma in la 
        diaria bisogna scrivere il tuto&quot; e cos&igrave; gli avvenimenti saltellano, 
        come momenti isolati, davanti agli occhi del lettore, perch&eacute; vengono 
        presentati troppo da vicino, e ancora senza contorni.<br>
        Bastino due esempi, tratti dalla spedizione del 1512, per illustrare, 
        rispetto al rivoletto che giunse a Berna, la marea di informazioni che 
        sommerse Venezia: l'arrivo dell'esercito elvetico a Treviso il 26 maggio 
        e il passaggio del Mincio il 2 giugno. Il mattino del 26 maggio il Consiglio 
        di Venezia apprende, direttamente dalla bocca di un inviato, alcuni dettagli 
        sull'esercito in arrivo, e contemporaneamente, da una lettera spedita 
        la sera prima da Vicenza, dati sulla sua forza numerica e sulla sua avanzata. 
        Dell'arrivo a Verona (con ulteriori particolari sulla forza numerica, 
        sui problemi relativi agli stipendi e ai rifornimenti, sul viavai degli 
        inviati) informano il 27 maggio una lettera del &quot;provedadr general&quot; 
        spedita il giorno prima da Cologna, il 28 maggio una missiva dello stesso, 
        e inoltre una lettera dell'inviato spagnolo a Verona e una di un inviato 
        elvetico; il mattino del 29 maggio una lettera di un inviato del cardinale 
        legato, il pomeriggio una del medesimo &quot;provedador&quot;, e un'altra 
        spedita la sera prima, come pure una lettera del cardinale legato; il 
        30 maggio in mattinata una lettera proveniente da Verona; infine nel corso 
        del solo pomeriggio tre lettere scritte da Cologna, che accennano gi&agrave; 
        alla successiva tappa di Villafranca.<br>
        Identiche considerazioni valgono per il passaggio del Mincio nei pressi 
        di Valeggio. Gioved&igrave; mattina, 3 giugno, il Consiglio di Venezia 
        riceve una lettera spedita marted&igrave; notte dall'accampamento, secondo 
        cui l'esercito elvetico intendeva attaccare Valeggio, e un'altra lettera 
        del luned&igrave; sulle possibilit&agrave; di difendere Valeggio - entrambe 
        con molti dettagli, utilizzando anche alcune lettere intercettate. Nel 
        corso dello stesso pomeriggio una lettera, scritta il giorno prima &quot;hore 
        17&quot; da Albar&egrave;, informava per esteso sull'avanzata e sul primo 
        contatto con il nemico tra Villafranca e Valeggio, con una prima sconcertata 
        osservazione: gli Svizzeri non fanno prigionieri, &quot;dicono non voler 
        far presoni&quot; (un atteggiamento ritenuto poco italiano), ma per il 
        resto piena di lodi: &quot;&#133; con bellissimo ordine, mai vide la pi&ugrave; 
        bella zovent&ugrave;&quot;; erano 20.000-22.000 uomini (ma le cifre divergono); 
        e infine si informa dell'espugnazione di Valeggio sotto gli occhi del 
        cardinale, anch'egli armato. Poi un'altra lettera della stessa ora, con 
        la notizia che Valeggio &egrave; stata presa. Il successivo mattino, un 
        venerd&igrave;, giunse come terzo messaggio una lettera di marted&igrave;, 
        scritta la sera tardi, &quot;de 2, a hore 22&quot;, dall'accampamento 
        presso Valeggio con l'informazione che il luogo &egrave; stato occupato 
        e che era riuscito il passaggio del Mincio. Poi, dallo stesso posto e 
        nella stessa ora, la lettera di un medico veneziano che descriveva, riservando 
        entusiastiche parole alle truppe svizzere, il passaggio del fiume e la 
        direzione dell'ulteriore avanzata. Un flusso di informazioni ben funzionante 
        dunque, che offr&igrave; al Consiglio veneziano, per prendere le sue decisioni, 
        una base di cui le lontane citt&agrave; svizzere non disponevano affatto; 
        esse aspettavano a lungo per veder arrivare il successivo corriere all'imbocco 
        della vallata! Tale situazione risultava quindi fatale, se era inesatta 
        la prima provvisoria notizia su un avvenimento, come quello della battaglia 
        di Melegnano. La sera della prima e pi&ugrave; fortunata giornata di combattimento 
        fu inviata una lettera: Berna lo seppe da Zurigo, Zurigo da Uri con lettera 
        del 16 settembre notte, Uri l'aveva appena appreso da Bellinzona e a Bellinzona 
        girava voce che gli Helvetici avevano, il 13 settembre, &quot;riportato 
        la vittoria sui francesi e ammazzato 14.000 lanzichenecchi&#133; e trovato 
        un prezioso cavallo insanguinato, sperando che fosse appartenuto al Re 
        o al duca di Borgogna&quot;. Certo, come fosse finito l secondo assalto, 
        &quot;Lor Signori non hanno saputo dirmelo fino adesso&quot;, ma proprio 
        questo era il punto: la battaglia non era ancora finita!<br>
        Quanto pi&ugrave; scarse erano le notizie e quanto pi&ugrave; lunghe le 
        distanze, tanto pi&ugrave; frequenti erano le vociferazioni. Le chiacchiere 
        che circolavano allora nelle trattorie, drammatizzavano ancora, e probabilmente 
        non di poco, gli avvenimenti in Italia. In questi anni il Consiglio bernese 
        reagiva, come ogni autorit&agrave;, con estrema suscettibilit&agrave; 
        a ogni voce, riguardante la guerra, che rischiasse di disorientare i sudditi 
        e di spingerli in preda al panico. Un uomo, ad esempio, avendo raccontato 
        che i confederati avevano perso una battaglia nei pressi di Milano, era 
        stato graziato dalla pena di morte per essere messo alla gogna con un 
        cartello secondo cui egli aveva affermato che &quot;gli elvetici avevano 
        combattuto ora una battaglia a Milano e avevano perso, da parte loro, 
        seimila uomini; e se i clementi Lor Signori di Berna non avessero dimostrato 
        tanta grazia, egli sarebbe stato spedito, come aveva meritato, dalla vita 
        alla morte&quot;.<br>
        Come risulta da alcuni atti giudiziari bernesi, l'autorit&agrave; interveniva 
        con decisione anche contro i cupi vaticinii di guerra di cui era pieno 
        il primo Cinquecento. Nell'archivio del piccolo centro di La Neuveville, 
