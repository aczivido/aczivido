      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>L'impresa 
        di Pavia, 1512</b><br>
        Prima della grande spedizione di Pavia, i Confederati avevano iniziato 
        due campagne che riuscirono disgraziate e vergognose; baster&agrave; farne 
        qui un rapido cenno. Nell'alleanza conclusa tra gli Svizzeri e la Santa 
        Sede, auspice il vescovo di Sion, i primi s'erano impegnati a fornire 
        seimila uomini a Giulio II&deg;, onde combattere i nemici della Chiesa, 
        identificati in quel momento nei Veneziani. Nel 1510 si ebbe la rottura 
        aperta tra il Pontefice e il Re di Francia, mentre Venezia veniva guadagnata 
        alla causa di Roma, e quando l'attacco generale contro i Francesi fu pronto, 
        lo Schiner domand&ograve; i seimila soldati previsti dalla capitolazione. 
        Cominci&ograve; allora in ogni Cantone il pi&ugrave; intenso lavorio diplomatico, 
        svolto dal Re di Francia e dall'Imperatore, ma in modo particolare dal 
        primo e con straordinaria abilit&agrave;; fu una strategia di corruzione 
        e di esaurimento, cui corrispondeva l'impreparazione militare e finanziaria 
        del partito papale, che ebbe come prima conseguenza di ritardare di due 
        mesi l'arrivo degli Svizzeri sui campi di Lombardia. Lo svolgimento della 
        guerra nell'alta Italia riusc&igrave; pertanto vantaggioso a Luigi XII&deg; 
        che stava rioccupando la Lombardia, e gli Svizzeri presero la via del 
        ritorno (settembre 1510); questa infelice spedizione si chiam&ograve; 
        &quot;di Chiasso&quot;. Da ci&ograve;, sdegno del Papa, irritazione della 
        Dieta contro Giulio II&deg;, risentimento di tutti contro lo Schiner, 
        rivolta della &quot;dec&agrave;nie&quot; superiori del Vallese, ribelli 
        al loro Principe-vescovo, e fuga dello Schiner a Roma, dove gli riusc&igrave; 
        di calmare il Pontefice, ottenendo il cappello cardinalizio e l'incarico 
        di nuove trattative con gli Svizzeri.<br>
        Il Re di Francia soffiava nel fuoco, ch&egrave; l'alleanza con gli Svizzeri 
        avrebbe potuto consentirgli la conquista dell'intera Europa; dice con 
        la consueta sua intuizione il Guicciardini: &quot;&#133; Ma niuna cosa 
        pi&ugrave; premeva al Re di Francia che il desiderio di riconciliarsi 
        gli Svizzeri, conoscendo da questo dipendere la vittoria certissima, per 
        l'autorit&agrave; grandissima che aveva allora quella nazione, per il 
        terrore delle loro armi, e perch&eacute; pareva che avessino cominciato 
        a reggersi non pi&ugrave; come soldati mercenari n&eacute; come pastori, 
        ma vigilando, come in repubblica bene ordinata, e come uomini uniti nell'amministrazione 
        degli Stati &#133;&quot; (&quot;Storia d'Italia&quot;, vol. III, libro 
        VII). Le trattative di Luigi fallirono tuttavia un'altra volta e ci&ograve; 
        sopra tutto per l'irriducibile opposizione dei Cantoni primitivi. Questa 
        opposizione si andava estendendo anche ai Confederati del nord e dell'occidente, 
        con una sorda irritazione del popolo verso ci&ograve; che fosse francese 
        e lombardo, e crebbe in modo inaspettato quando si seppe dell'uccisione, 
        avvenuta per opera della guarnigione francese di Lugano, dei messaggeri 
        di Friburgo, Berna e Svitto. Cos&igrave;, verso la met&agrave; del 1511, 
        la Confederazione fu di nuovo in guerra contro la Francia. Si tratta della 
        &quot;fredda campagna invernale&quot; - novembre e dicembre - che culmin&ograve; 
        in un disastro. I Francesi usarono con astuzia la loro strategia di spossamento, 
        attirarono gli Svizzeri nella pianura per rifugiarsi poi nelle piazzeforti; 
        gli alleati papali e veneziani non si lasciarono vedere; i viveri cominciarono 
        a mancare, e allora i Confederati si abbandonarono a rapine, incendi e 
        violenze d'ogni sorta; finalmente, fu la ritirata disordinata e furibonda.<br>
        Ora, tornava alla ribalta Matteo Schiner; malgrado la vittoria di Gastone 
        de Foix, il luned&igrave; di pasqua del 1512, gli Svizzeri rafforzarono 
        l'alleanza col Papa. Per la prima volta, in molti anni, in tutto il territorio 
        della Confederazione ci fu unit&agrave; di propositi, dal governo delle 
        Citt&agrave; ai montanari delle pi&ugrave; remote vallate, in ogni ambiente 
        e in ogni classe; se ne trova un'eco in una missiva del cappellano glaronese 
        Ulrico Zwingli: &quot;I Confederati ritengono che non &egrave; lecito 
        a ogni tiranno furioso di assalire impunemente la Madre comune dei cristiani. 
        Essi &#133; intendono ristabilire per il meglio le cose della Chiesa e 
        dell'Italia&quot;. Diciottomila soldati furono arruolati in breve tempo, 
        dai passi dei Grigioni condotti in Italia e concentrati a Verona, consenziente 
        la Repubblica di Venezia che s'era alleata agli Svizzeri.<br>
        Da Verona part&igrave; dunque la spedizione di Pavia che in poco pi&ugrave; 
        di tre settimane spazz&ograve; via i Francesi dall'Italia. Mai s'era visto 
        un pi&ugrave; grande esercito di Svizzeri, n&eacute; mai tanto animoso 
        e concorde; protetto alle spalle dai Veneziani, si spinse deciso verso 
        occidente; dove, come sull'Adda, mancavano i ponti, i soldati si gettarono 
        a nuoto reggendo sul capo l'alabarda e cacciando i nemici che tentavano 
        d'impedire la formazione di teste di ponte. In tutte le contrade di Lombardia 
        risuon&ograve; il grido di gioia della liberazione: &quot;Giulio! Gli 
        Svizzeri son vincitori!&quot;. Ulrico Zwingli scriveva esultando: &quot;Grazie 
        agli Svizzeri, tutta l'Italia, le coste liguri e la Lombardia sono ora 
        libere&quot;; Niccol&ograve; Machiavelli, che stava in quel momento componendo 
        &quot;Il Principe&quot;, definiva gli Svizzeri &quot;armatissimi e liberissimi&quot;; 
        letterati di Lombardia scrivevano: &quot;Nelle come nelle borgate e nei 
        villaggi echeggiano le trombe, squillano le campane&quot;. Dotti, religiosi 
        e predicatori esclamano dai pulpiti: &quot;Voi siete il popolo di Dio, 
        voi avete umiliato i nemici della sposa di Cristo!&quot;; il Papa saluta 
        i Confederati quali &quot;Difensori della libert&agrave; della Chiesa&quot; 
        e manda loro quelle preziose bandiere che ancora oggi si possono ammirare 
        nei musei della patria; al barone Ulrico di Hohensax, che quale pupillo 
        di Hans Waldmann era anche borghese di Zurigo, invi&ograve; in dono una 
        preziosa spada gemmata.</font></p>
