          <td><p>... Storia di uomini, e di ambiente, soprattutto, di uomini e di terra, di fiumi, di acque di risorgiva, di canali artificiali, storie che si intrecciano, che si sovrappongono, che si obliterano, che comunque si stratificano proprio sul suolo, che sa rivelare, a chi lo sappia interpretare, le chiavi di lettura delle fasi storiche salienti, le tracce, le persistenze di questo secolare divenire...<br>
              ... Il libro &egrave; stato suddiviso in tre parti che corrispondono ai &quot;momenti&quot; che a nostro avviso sono stati quelli determinanti e &quot;formativi&quot; per la storia della località. La prima riguarda la fase di vita più antica del territorio zividese: impossibile infatti, al di là del titolo del libro che intende &quot;fissare&quot; nella memoria collettiva i parametri del ciclo vitale di Zivido, ignorare non solo le radici delle denominazione, ma anche le radici dell'insediamento che alla denominazione risultano strettamente connesse; impossibile dunque, affrontare lo studio delle premesse al nucleo medievale senza inquadrarle in quell'ambito territoriale, politico e cronologico, che è in grado di giustificarle, di chiarirle. Ecco che allora la storia di Zivido si è fusa, per alcune pagine, con quella di Milano di età romana e con quella della Padania tutta, per riemergere, ad intervalli, vivificata e sicuramente arricchita...<br>
              ... La seconda parte prendendo l'avvio da quanto precedentemente è stato detto, si è incentrata sulle vicende politiche ed amministrative di età alto medievale, vicende che sono state seguite sulla scorta di documenti d'archivio, nei loro riflessi economici e sociali, fino alle soglie dell'età moderna, oggetto della terza parte del libro, età in cui Zivido ha dato di sè, così parrebbe dalla documentazione presa in considerazione, quelle che sono da leggersi come le &quot;ultime&quot; espressioni di vitale presenza: architettura, pittura, arte in genere. Volendo tralasciare il meno possibile della storia del borgo, sono state poi destinate alcune pagine a taluni aspetti relativi alla battaglia di Marignano o dei Giganti (1515) ed alla figura di Francesco I, considerati dal punto di vista degli storici francesi, ed a quelli che potremmo definire gli esiti &quot;letterari&quot; della grande battaglia combattutasi a Zivido: la &quot;chanson di maitre Janequin&quot; rivisitata da Maria Teresa Bisiachi, pur rientrando nel filone della produzione vernacola, cionondimeno pare essere la risposta della gente comune ad un fatto d'arme vissuto molto intensamente dalla Francia di Francesco I in un periodo di quasi totale &quot;vuoto letterario&quot;.<br>
              ... A questo scopo, oltre che per favorire coloro che possano sentire l'esigenza di verificare e di approfondire, sono state inserite nel libro, a mo' di censimento, delle schede di materiali, quelli che si è riuscito a recuperare anche con il prezioso lavoro di ricerca dell'Associazione Culturale Zivido e che potrebbero subire in futuro sorti differenti. Di essi si forniscono, unitamente ai dati essenziali, alla documentazione fotografica ed ai rilievi, il luogo di conservazione e l'indicazione dello stato giuridico...<br>
              ... C'è infatti la &quot;storia&quot; e ci sono le &quot;storie&quot;: riteniamo che chi cerca le &quot;storie&quot; debba rivolgersi ad altro tipo di letture. Quello che in sostanza si vuol dire è che non si deve commettere l'errore di aspettarsi un libro per lo svago, il passatempo, un libro pieno di fantasiosi ricami su un passato pieno di gloria. L'importante è che esso contenga la verità storica e, come tale, che serva come base per quelle ricerche che in futuro saranno rese possibili probabilmente dal recupero di altri documenti ignoti.<br>
(Mariavittoria Antico Gallina)</p></td>
