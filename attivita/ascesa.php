              <br>
          Scultrice milanese di chiara fama, Nicoletta Frigerio è l’artista che ha realizzato l’opera dal titolo «Ascesa» e diverse altre esposte allo SpazioCultura di San Giuliano Milanese nel settembre 2005 in occasione della rievocazione storica «Ritornano i Giganti».<br>
          L’opera, dal profondo significato simbolico, nel settembre del 2006 campeggiava al centro della sala consiliare del palazzo comunale, attorniata dai principali protagonisti dell’epica «Battaglia dei Giganti», nel corso della tradizionale cerimonia alla presenza delle autorità cittadine, delle autorità straniere e di un folto pubblico.<br>
          In tale occasione il presidente dell’Associazione Culturale Zivido, Pierino Esposti, lanciava la proposta di collocarla, con proporzioni adeguate, all’ingresso dello storico borgo di Zivido con il titolo «La Porta dei Giganti».<br>
          L’iniziativa era esaurientemente illustrata nel fascicoletto distribuito ai presenti. Nessun riscontro o accenno ne è conseguito.<br>
          Nell’agosto scorso l’opera è stata acquistata dal presidente dell’Associazione Culturale Zivido e collocata nello spazio antistante la sede associativa.<br>
          Alla presenza della scultrice, Nicoletta Frigerio; delle ospiti francesi del Comitato di Gemellaggio di Bussy Saint-Georges, Prof.ssa Anne-Marie Trinh e Prof.ssa Marie-Claude Phan; della studiosa e scrittrice di storia milanese, Dr.ssa Giuliana Fantoni; del Dr. Domenico Piraina; del presidente dell’Associazione Culturale Zivido, Pierino Esposti; di alcuni rappresentanti della stampa; di un gruppo di cittadini, si è svolta, sabato 15 settembre 2007 alle ore 10,30, la semplice cerimonia di inaugurazione.<br>
          Con questa iniziativa un privato cittadino, a sue spese, ha voluto arricchire San Giuliano Milanese di un’opera scultorea dedicata all’evento storico più importante della città</p></td>
