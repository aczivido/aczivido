        <b>Il massimo delle solennit&agrave;</b><br>
        L'ultimo giorno della sua permanenza - nel pomeriggio rientrer&agrave; 
        a casa - Ulrich Fuchs ripensa alle cinque giornate trascorse a Bressanone, 
        riflette sulle impressioni ed esperienze avute e le fissa per iscritto 
        nel suo diario. Colloquiando con l'oste che lo ospita arriva alla conclusione 
        che torner&agrave; ancora nella citt&agrave; vescovile sull'Isarco almeno 
        due volte l'anno. La sua prossima venuta sar&agrave; per la processione 
        di San Cassiano.<br>
        A Bressanone infatti la festa per eccellenza, il massimo delle solennit&agrave;, 
        &egrave; la &quot;Giornata di San Cassiano&quot; che cade la seconda domenica 
        dopo Pasqua. Terminato il solenne pontificale in Duomo per le vie cittadine 
        si snoda la grande processione cui partecipano migliaia di fedeli provenienti 
        da tutta la diocesi; una testimonianza di profonda fede religiosa, un 
        segno tangibile di ritrovata unit&agrave; fra i credenti dei tre diversi 
        gruppi linguistici conviventi in Alto Adige. La processione esce anche 
        dal centro storico percorrendo solenne e decorosa le vie che costeggiano 
        i resti delle antiche mura urbane.<br>
        Che San Cassiano, il patrono della diocesi, sia stato un predicatore della 
        fede cristiana in questa &quot;Terra fra i monti&quot; - cos&igrave; si 
        chiamava il territorio prima che nel 13&deg; secolo nascesse la contea 
        del Tirolo - e il primo vescovo di Sabiona &egrave; una leggenda nata 
        in tempi remoti e sfatata da diversi decenni. In realt&agrave; era un 
        semplice maestro di stenografia nella sua citt&agrave; natale di Imola 
        dove, nel corso delle persecuzioni, venne martirizzato dai propri alunni 
        a colpi di stiletto per aver segretamente diffuso la nuova religione cui 
        si era convertito. Pi&ugrave; tardi il suo culto giunse a Sabiona attraverso 
        Aquileia e la profonda venerazione tributatagli dalle popolazioni locali 
        e da quelle dell'Italia settentrionale lo fece entrare nella leggenda. 
        Sembra che la prima reliquia del martire sia giunta da Imola a Bressanone 
        gi&agrave; nel 14&deg; secolo. Certo &egrave; che il vescovo Paulinus 
        Mayr ne ricevette una nel 1685 e per accoglierla degnamente organizz&ograve; 
        una processione solenne con partenza presso la chiesa della Clarisse. 
        Nel 1704 il principe-vescovo Kaspar Ignaz conte Kunigl ottenne da Imola 
        una reliquia &quot;insigne&quot; - un braccio del santo - e a ricordo 
        di questa traslazione come pure in ringraziamento per il felice esito 
        della guerra del 1703 contro i franco-bavaresi, la curia dispose che ogni 
        anno si tenesse la processione di San Cassiano. Dal 1734 essa coincide 
        con la seconda domenica dopo Pasqua. Vescovo o no, l'importante &egrave; 
        che al patrono diocesano - raffigurato in abiti pontificali assiame ai 
        santi Ingenuino e Alboino sopra il portone principale del Duomo - sia 
        dedicata una giornata che sinora non ha perso nulla della sua secolare 
        tradizione; anzi, si &egrave; evoluta in un'autentica festa della fede 
        che non trova riscontro nelle analoghe manifestazioni dell'intero Tirolo.<br>
        Nella sua seconda visita alla citt&agrave; il nostro turista si dedicher&agrave; 
        ad un avvenimento mondano; meglio, alla ricerca di qualche soddisfazione 
        strettamente personale quale potrebbe essere la partecipazione ad un &quot;Torggelen&quot; 
        autunnale. Il termine deriva dal latino &quot;torculum&quot; che equivale 
        al nostro &quot;torchio&quot; per pressare l'uva. Si tratta di una scampagnata 
        a piccoli gruppi che porta ad un incontro conviviale nei masi del vicinato. 
        In origine la giornata inaugurale di questo &quot;rito&quot;, divenuto 
        ormai tradizione, era il pomeriggio della festa d'Ognissanti. Amici di 
        citt&agrave; e conoscenti dei dintorni intraprendevano una breve passeggiata 
        &quot;fuori le mura&quot; per ritrovarsi in un maso del circondario e 
        degustare il vino nuovo, quello bianco e quello rosso. Il succo dell'uva 
        appena fermentato piace e seduce; la compagnia si accomoda in una &quot;stube&quot; 
        dalle pareti rivestite in legno e il contadino con moglie e figli porta 
        in tavola i prodotti della casa: speck e formaggio, uva, noci e caldarroste, 
        pere e mele, forse anche una zuppa d'orzo, una salciccia e delle costine 
        affumicate di maiale; insomma, una golosit&agrave; rurale, tutto il ben 
        di Dio che il contadino ricava dal lavoro dei campi e della stalla. Questo 
        era il &quot;Torggelen&quot; di una volta; purtroppo al giorno d'oggi 
        &egrave; tutt'altra cosa: la stagione precoce del &quot;Torggelen&quot; 
        ha inizio ancor prima che la brina e il gelo scoloriscano il giardino 
        e nel bosco i fiori e le foglie variopinte; e non soltanto nei dintorni 
        di Bressanone, dove &egrave; nato. Infatti questa usanza locale nel frattempo 
        si &egrave; trasformata in un'attrazione turistica che coinvolge tutto 
        l'Alto Adige; non importa se la scampagnata si svolge laddove non cresce 
        l'uva, se il vino non &egrave; proprio quello prodotto in loco, e se le 
        specialit&agrave; culinarie non provengono dalla cucina del maso. Ormai 
        siamo giunti addirittura al &quot;Torggelen&quot; con tanto di men&ugrave;, 
        di tavoli riservati, di liste d'attesa per la consueta calca all'ingresso 
        del ristorante e un servizio di routine, sbrigativo e senza calore; tanto 
        l'autobus del &quot;tutto compreso&quot; parte fra un'ora.<br>
        E' proprio questo degrado che il nostro turista non vuol vedere quando 
        torner&agrave; nell'autunno prossimo. L'oste gli spiega che qua e l&agrave; 
        ce ne sono ancora di piccoli masi, poco appariscenti, dove non ha ancor 
        preso piede lo sfruttamento turistico o dove il contadino vi si &egrave; 
        dignitosamente opposto. Si tratta di piccoli ambienti familiari sopra 
        Novacella, o nei pressi di Velturno, o in genere nei dintorni un po' fuori 
        mano; l&igrave; il &quot;Torggelen&quot; &egrave; ancora quello di un 
        tempo, l&igrave; la tradizione &egrave; ancora tradizione, e la semplice 
        scampagnata autunnale rimane a lungo un piacevole ricordo.<br>
        Nel corso di queste tranquille passeggiate in tersi pomeriggi autunnali 
        ci sta bene anche una visita ai vigneti carichi di uve, alla cantina del 
        maso dove dalle botti di quercia si spande &quot;l'aspro odor dei vini&quot;, 
        del mosto in fermentazione. Ecco, proprio in uno splendido pomeriggio 
        di sole il nostro Ulrich vuole andare alla scoperta dei dintorni di Bressanone. 
        E' sicuro che in una dimessa &quot;stube&quot; di contadini potr&agrave; 
        incontrare la compagnia buona con cui gustare un &quot;Torggelen&quot; 
        come si deve. Pu&ograve; darsi che ci sia qualcuno che canta accompagnato 
        da una cetra, da una fisarmonica o da una chitarra. Non importa se poi, 
        a tarda ora, le gambe sono un tantino pesanti, il passo si fa incerto 
        e la lingua si scioglie in amene chiacchiere. Tutto sommato, sono piccole 
        &quot;manifestazioni di contorno&quot; che testimoniano come la scampagnata 
        sia ben riuscita. Ma questo non accadr&agrave; prima di Ognissanti, perch&eacute; 
        appena dopo questa festa si esaurisce per breve tempo il flusso turistico 
        nei dintorni di Bressanone.<br>
