            <p ALIGN="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Tracciato</b><br>
              La testimonianza precisa della strada da &quot;Mediolanum&quot; 
              a &quot;Placentia&quot; si trova nei medesimi documenti itinerari 
              che abbiamo considerato per il tratto Piacenza-Bologna della via 
              Emilia. La lunghezza complessiva era certo di 40 miglia, perch&egrave; 
              da &quot;Mediolanum&quot; a &quot;Laus&quot; si deve accettare la 
              cifra 16 data da due Itinerari, anzi da tre, se si apporta a quello 
              Burdigalense un'ovvia correzione, e da &quot;Laus&quot; a &quot;Placentia&quot; 
              la cifra 24, indicata pure da due fonti e contro la quale non pu&ograve; 
              essere in alcun modo sostenuta l'altra di 20 miglia data dalla &quot;Tabula 
              Peutingeriana&quot; inferiore grandemente anche alla distanza in 
              linea d'aria fra i due estremi.<br>
              Per le localit&agrave; intermedie l'Itinerario Burdigalense &egrave; 
              il pi&ugrave; completo. Vi figurano &quot;Ad Nonum&quot; 7 miglia 
              dopo &quot;Mediolanum&quot; (ma la cifra va sostituita con 9, perch&egrave; 
              il nome stesso della stazione la dichiara situata a 9 miglia da 
              &quot;Mediolanum&quot;: ecco la correzione che, come si &egrave; 
              detto sopra, fa concordare anche questa fonte con le altre), &quot;Laus&quot; 
              a 7 miglia da &quot;Ad Nonum&quot;, &quot;Tribus Tabernis&quot; 
              a 8 miglia da &quot;Laus&quot; e &quot;Ad Rotas&quot; a 5 miglia 
              da &quot;Tribus Tabernis&quot;. Da &quot;Ad Rotas&quot; a &quot;Placentia&quot; 
              vi sono 11 miglia.<br>
              Per la ricostruzione schematica della via manca purtroppo l'elemento 
              che in questi casi offre il pi&ugrave; valido aiuto, cio&egrave; 
              non ci si pu&ograve; basare sui miliari, dato che i pochissimi attribuibili 
              alla Milano-Piacenza sono di ubicazione incerta. Resta tuttavia 
              possibile riconoscere sul terreno le orme di una strada il cui tracciato 
              abbia lo sviluppo richiesto dagli Itinerari e la cui antichit&agrave; 
              trovi conferma nei documenti medievali, nella toponomastica e in 
              rinvenimenti vari. Ci&ograve; &egrave; merito di vari specialisti 
              e particolarmente del Fraccaro, che ricercando la romana &quot;Placentia-Mediolanum&quot; 
              ha dato un esempio di come lo studio delle nostre carte topografiche 
              possa far identificare gli elementi che risalgono all'antica viabilit&agrave;.<br>
              Punto di partenza a Milano &egrave; la porta romana della cinta 
              imperiale, con l'attuale corso di porta Romana. Viene segnata una 
              direzione che la via seguiva fino a Rogoredo; qui piegava leggermente 
              ma ritornava subito diritta fino a Melegnano. Poco prima di Melegnano 
              era l'antica &quot;mutatio ad Nonum&quot;. Dopo la cittadina, al 
              passaggio del Lambro, il percorso non &egrave; pi&ugrave; riconoscibile: 
              se ne ritrovano le tracce 4 km dopo in una serie di strade e di 
              rogge che va diritta a Lodi Vecchio. Nel passare da Lodi Vecchio 
              il rettilineo della Milano-Piacenza subiva una spezzatura: &quot;caratteristica 
              - spiega Fraccaro - delle strade romane quando attraversano una 
              citt&agrave; lungo un asse che non &egrave; nella stessa direzione 
              della strada&quot;.<br>
              Sorgeva qui dunque un abitato importante, il maggiore che la via 
              incontrasse lungo il suo cammino. In epoca romana si chiam&ograve; 
              &quot;Laus Pompeia&quot;, molto probabilmente per celebrare la &quot;gloria&quot; 
              di Pompeo Strabone, il console dell'89 a.C. Autore di una legge 
              per concedere un diritto di cittadinanza minore ai Transpadani, 
              legge che fece rifiorire l'intera zona. Ma era un centro che gi&agrave; 
              esisteva sotto i Galli e che doveva ben aver raggiunto una certa 
              consistenza quando fu costruita la strada, se questa fu costretta 
              ad adattarsi alla sua topografia.<br>
              Dopo Lodi Vecchio l'antico tracciato &egrave; segnato da tronchi 
              di strada pi&ugrave; o meno importanti, limiti e rogge allineati 
              da San Bassiano a Pieve de' Guazzi: e prolungando la linea si giunge 
              a S. Maria del Toro, il cui nome antico di S. maria &quot;in strata&quot; 
              o &quot;in via lata&quot; ha il valore di una conferma. Dopo S. 
              Maria si trova ancora, quasi in asse col precedente, una serie di 
              vie campestri o carrozzabile che, con una sola interruzione e passando 
              per Monastirolo di Borghetto, l'antica stazione &quot;Tres Tabernae&quot;, 
              porta nei pressi di Livraga, quindi a Cascina Nuova. A sud di questo 
              punto il Fraccaro non nota altre tracce della strada romana, ma 
              crede possibile affermare su elementi dati dall'Agnelli che essa 
              passava accanto a Cascina Griona e fra Ospedaletto e Cascina Mandella. 
              Di fatto Cascna Griona &egrave; congiunta a Cascina Nuova da una 
              roggia che ha proprio la direzione dell'antica strada; lungo di 
              essa corre anche il limite di propriet&agrave;. Alla Griona il Fraccaro 
              pone la &quot;mutatio Ad Rotas&quot; che era a 11 miglia da Piacenza 
              e a 5 da &quot;Tres Tabernae&quot;. Egli si arresta qui, ritenendo 
              vano investigare un terreno tanto alterato dai cambiamenti di letto 
              di due fiumi, lambro e Po, quale &egrave; quello tra Ospedaletto 
              e Piacenza. Il Caretta indica oltre la Griona alcuni tronchi che 
              possono ancora segnare l'antico tracciato fino a Mirabello.<br>
              Caratteristiche positive del tracciato cos&igrave; restituito sono 
              ch'esso congiunge, oltre a Melegnano e Lodi Vecchio, le localit&agrave; 
              che pi&ugrave; frequentemente resultano adiacenti alla strada romana 
              nei documenti medievali quali Pieve de' Guazzi, Mongiardino, S.Maria 
              in Strada, Livraga, Cascina Griona, ecc.., e che i tratti tuttora 
              in uso corrispondenti alla via romana hanno spessissimo un evidente 
              carattere di residui, poco legati alla viabilit&agrave; attuale, 
              cosicch&egrave; non di rado bruscamente muoiono all'incontro di 
              una roggia o di una strada pi&ugrave; recente.<br>
              Ma soprattutto tornano le distanze. Infatti completando le lacune 
              con delle rette il tracciato misura da Milano alla C. Griona &quot;Ad 
              Rotas&quot; poco pi&ugrave; delle 29 miglia richieste. Dalla Griona 
              a Piacenza restano, secondo gl'Itinerari, 11 m. precise. Si u&ograve; 
              ritenere scarsa quest'ultima cifra perch&egrave; 11 miglia corrispondono 
              alla distanza in linea d'aria fra le due localit&agrave; e sarebbero 
              riempite solo da una strada assolutamente rettilinea, problematica 
              essendoci di mezzo l'attraversamento di un grande fiume. Ma con 
              tutta probabilit&agrave; nel dato degli Itinerari non &egrave; compreso 
              il tratto fra le due rive del Po, il che permette di supporre che 
              la strada fosse pi&ugrave; lunga, si scostasse dalla linea retta 
              e magari passasse per Guardamiglio, come sembra ritenere il Fraccaro. 
              Il Po era superato infatti con un traghetto (data l'importanza della 
              strada specie dopo l'istituzione del regolare &quot;cursus publicus&quot; 
              bisogna supporre che tale servizio fosse sempre assicurato), in 
              circostanze particolari i Romani possono aver costruito anche ponti 
              di barche, ma non risulta che essi abbiano mai intrapreso la costruzione 
              di un ponte in pietra.<br>
              Non esistono oggi, come si &egrave; detto, miliari di cui sia dimostrata 
              l'appartenenza a questa via; ma si ritiene giustamente che lungo 
              di essa sorgessero i tre conservati nel museo di Lodi. Sono del 
              medesimo marmo rosso e, bench&egrave; due appaiano frammentari, 
              si vede che dovettero avere tutti eguali proporzioni; l'unico intero 
              reca due iscrizioni, la pi&ugrave; breve delle quali &egrave; leggibile, 
              almeno in parte, anche sugli altri. La identit&agrave; del materiale 
              e delle epigrafi indica che le pietre sono di una stessa strada 
              e poich&eacute; la via complessivamente pi&ugrave; vicina alle localit&agrave; 
              dalle quali provengono - Salerano, Pieve Fissiraga e Massalengo 
              - resulta l'antica strada Milano-Piacenza, ipotesi pi&ugrave; che 
              fondata &egrave; che abbiano appartenuto a quest'ultima. Vedremo 
              pi&ugrave; avanti quali indicazioni, ammesso ci&ograve; per certo, 
              essi possano dare sulle vicende della strada.</font></p>
