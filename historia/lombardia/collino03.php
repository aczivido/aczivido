      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><br>
        <b>Il convento e l'esterno della chiesa</b><br>
        Le abbazie cistercensi sono costruite secondo un piano costante. Un chiostro 
        quadrato raduna intorno a s&eacute; tutti gli edifizi conventuali. Tre 
        lati sono formati dalla foresteria, dal dormitorio, dall'aula capitolare, 
        dal refettorio, ect. Al quarto lato sta la chiesa, tre navate, la centrale 
        pi&ugrave; alta, con campate quadre nella navata di mezzo e, come &egrave; 
        logico, rettangolari in quelle laterali: il transetto &egrave; pure quadrato, 
        come presbiterio. Sull'incrocio delle navate si alza il tiburio con larghe 
        finestre. Ridotta, secondo la regola instaurata da San Roberto e da San 
        Bernardo, &egrave; la decorazione. All'esterno saldi speroni a scarpa 
        si addossano agli spigoli dell'edifizio. La facciata semplicissima ha 
        un rosone sulla porta centrale. Il tiburio sostituisce la torre campanaria.<br>
        L'abbazia di Fossanova e quella in rovina di San Galgano ci conservano 
        in Italia le caratteristiche pi&ugrave; pure dell'architettura cistercense, 
        ma anche quella di Morimondo e fedele, di massima, alle norme costruttive 
        dell'Ordine, e presenta un notevole interesse artistico. Essa sorge sulla 
        strada che da Abbiategrasso conduce a Pavia, ed &egrave; assai vasta e 
        ben conservata malgrado le inevitabili devastazioni.<br>
        La strada che porta a Morimondo, appena giunta ai primi casolari del borgo 
        si restringe sotto un grande arco in cotto di stile romanico-lombardo, 
        gi&agrave; tendente all'archiacuto gotico francese. E' formato di due 
        archi sovrapposti, quello superiore cordonato a dente e quello inferiore 
        rientrato assai con cunei di arenaria. Al di sopra dell'arco si nota un 
        ampio foro sbrecciato, l&agrave; dove esisteva fino a pochi anni or sono 
        un rosone in cotto di ottima fattura. Questo arco d'ingresso alla Badia 
        appare evidentemente opera del secolo XIII, ed &egrave; da notarsi il 
        pieno contrasto fra la tendenza all'acuto della ghiera superiore e l'allargarsi 
        a tutto sesto di quella dell'arco sottostante. Si dibatte, insomma, lo 
        stile fra il romanico di buon sapore lombardo e il gotico importato di 
        Francia colla severa regola cistercense; &egrave; stile di transizione, 
        perch&egrave; le nuove forme mai soffocarono o sostituirono del tutto 
        le preesistenti, e perch&egrave; il gotico in Italia non serv&igrave; 
        che a segnare il trapasso al Rinascimento.<br>
        Il senso di gelosa conservazione dello stile romanico-lombardo, malgrado 
        gli assalti del gotico oltremontano, si rivela possente nella bella facciata 
        del tempio, salda ed uniforme, ad onta delle molte deturpazioni. La parte 
        superiore di essa conserva per fortuna la sua primitiva struttura in mattoni. 
        Il coronamento &egrave; ricco di un cornicione parzialmente ancora rivestito 
        di piastrelle a forma di losanga; &egrave; adorno inferiormente di archetti 
        pensili verticali, e lo sormontano tre pinnacoli. Al centro della facciata 
        si apre il caratteristico rosone delle chiese romaniche, leggermente cordonato 
        di archetti e sormontato da una bifora centinata, che rivela l'influsso 
        del gotico francese, come del resto lo rivelano pure due finestre ad arco 
        acuto, immediatamente laterali al rosone centrale. Pi&ugrave; verso i 
        lati vi sono, sui tetti delle navate minori, due finestre a pieno centro 
        puramente ornamentali, mentre infine due altri finestroni sottostanti 
        al rosone danno luce alla navata centrale.<br>
        La parte inferiore della facciata &egrave; purtroppo malamente intonacata 
        e un rozzo protiro, o meglio portichetto, con lesene e colonne sostituisce 
        forse uno scomparso pi&ugrave; vasto nartece. Due finestre ai lati della 
        porta, non situate alla stessa altezza, una rettangolare e l'altra a sesto 
        acuto, finiscono di deturpare senza remissione la parte bassa di questa 
        facciata, che pare viceversa acquistare un maggior respiro a mano a mano 
        che si alza verso il cielo. L'abbondanza delle finestre, aperte senza 
        criterio e in varie epoche, prova la contaminazione dei vari stili.<br>
        Pi&ugrave; uniforme, regolare e ben conservato &egrave; il fianco settentrionale 
        della chiesa, che &egrave; anche il solo dei due visibili, poich&eacute; 
        l'altro &egrave; addossato al Chiostro. Ha degli archi rampanti rettangolari 
        che sporgono ed emergono dal tetto della navata minore; la quale risulta 
        cos&igrave; divisa in otto campi. Una cordonatura semplice e diritta corre 
        qui sotto la grondaia, mentre la navata superiore &egrave; lateralmente 
        percorsa da un leggero ricamo di archetti pensili. Il fianco del transetto 
        sovrasta alla navata minora, pareggiando quella maggiore, ed &egrave; 
        pi&ugrave; accentuatamente coronato di graziosi archetti perpendicolari. 
        Un rosone cordonato in cotto, con vetrate quattrocentesche, si apre nel 
        centro per dar luce alla navata trasversale. Al centro del transetto, 
        e perci&ograve; nel punto d'incrocio dei due bracci della croce latina, 
        sorge il tiburio, che ha una finestra a pieno centro per ognuno dei quattro 
        lati rispondenti alla croce, e piccole aperture tonde nelle quattro smussature 
        dell'ottagono. Questa piccola torre campanaria &egrave; adorna di paraste, 
        di archetti e, secondo la regola cistercense, conteneva una sola campana, 
        la cui corda pendeva attraverso un foro della volta nell'antico coro dei 
        monaci, al centro della chiesa.<br>
        Il tempio volge la sua facciata verso ponente, e il chiostro si estende 
        sul lato destro di esso. Il portico del chiostro &egrave; sostenuto da 
        colonne in sarizzo o in marmo bianco, a base elegante, sormontate da capitelli 
        di varia foggia. Sulla parete che guarda a ponente si apre con una porta 
        e due finestre la Sala del Capitolo. La porta &egrave; a tutto sesto. 
        In cotto, a strombo leggero, con nervatura in marmo appoggiata su due 
        esili ed aggraziate colonne. Le finestre affacciate dalla Sala del Capitolo 
        sul portico claustrale sono trifore con colonnine binate, arco a tutto 
        centro e breve strombatura. Il chiostro di Morimondo arieggia assai, nella 
        sua parte meglio conservata, a quello di Staffarda, che &egrave; del 1300, 
        ricco esso pure di colonnine binate, di finestre trifore, e di capitelli 
        decorati a fogliami diversi.<br>
        Fra la navata destra della chiesa, colla quale &egrave; in comunicazione, 
        e la Sala del Capitolo &egrave; posta secondo il rito la sagrestia, che 
        prende luce da una finestra a levante ed ha il soffitto a volta. Le pareti 
        sono coperte da alcuni forti armadii ad intarsio, contemporanei forse 
        agli stalli del coro, e in uno di essi si conserva il bello stendardo 
        della Madonna, opera preziosa del secolo XVII.<br>
        La sagrestia comunicava col Penitenziario ovverossia luogo di punizione 
        per i monaci indisciplinati del convento, che pare fosse pure il deposito 
        dei feretri degli Abati.<br>
        Adiacente al Penitenziario &egrave; la Sala del Capitolo, oggi deturpata 
        dalle trasformazioni, ma che ancora rivela l'agile architettura archiacuta 
        di origine francese, assai diffusa in Italia nei primi decenni del secolo 
        XIV. E' quadrata, con volta a crociera e colonne a fascio sormontate da 
        capitelli a cubo. Fu costruita certo assai dopo la chiesa, come dimostra 
        l'eleganza gi&agrave; progredita dell'architettura, ed &egrave; orientata 
        da levante a ponente. Risulta contemporanea alla Sala del Capitolo dell'Abbazia 
        di Staffarda che ha pure la volta a crociera poggiata su colonne, e s'illumina 
        attraverso graziose finestre trifore. Sopra questa sala era la biblioteca 
        del convento di Morimondo, di cui per&ograve; non rimane pi&ugrave; traccia.<br>
        Attorno al cortile centrale con portici a tutti quattro i lati erano disposti 
        il locutorium, la sala di riunione, la cucina ed il refettorio dei monaci 
        con volta a crociera e bei pilastri quadrilobati. Pi&ugrave; ampi sono 
        i portici che costeggiano il lato meridionale della chiesa, con robuste 
        nervature negli archi a tutto sesto e dipinti alle pareti. Qui si apre 
        pure l'antica porta laterale della chiesa, sormontata da un forte architrave 
        e circondata da bei disegni geometrici ornamentali che gi&agrave; sentono 
        assai il Rinascimento. Sui capitelli delle colonne e sui muri occhieggiano 
        targhe e stemmi, fra cui notevole quello del convento con la mitra abbaziale 
        e le iniziali M.O., stemma che d'altronde ritorna ancora vicino alla porta 
        del Convento, presso l'altare maggiore e nel bel camino marmoreo secentesco 
        di una delle sale del chiostro.<br>
        Altri locali del Monastero, che ancora &egrave; possibile individuare 
        a seconda della regola cistercense, sono la foresteria, l'ospedale con 
        annessa farmacia, la Sala del Pretorio, la lavanderia, le molte cantine, 
        ed una curiosa ghiacciaia, a forma di torre ellittica sorgente dietro 
        l'abside e fabbricata, come risulta da una lapide, nell'anno di grazia 
        1676.<br>
        Pure adattate nel Seicento appaiono infine le vaste stanze che servivano 
        di abitazione all'Abate e Conte di Morimondo. Sorgono esse al piano superiore, 
        e vi si accedeva da un grande scalone ancor conservato in parte; sono 
        sette vasti locali aprentisi, con molte finestre ed un terrazzo, sulla 
        maggiore facciata del chiostro orientata a ponente, e, da quanto ne rimane, 
        risulta che fossero decorati con fasto a grandi riquadrature di gusto 
        gi&agrave; pesante, che contrasta colla semplicit&agrave; dello stile 
        prevalente nell'antica Abbazia.<br>
        La rapida rassegna delle principali costruzioni di Morimondo basta a rivelarci, 
        malgrado le molte e spesso volute omissioni, l'importanza di questa Congregazione 
        cistercense, che per quattro secoli prosper&ograve;, tramutandosi poi, 
        destino comune a tutte le potenti Abbazie, in ricca e fastosa Commenda. 
        E l'arte vi sub&igrave; intanto tutte le trasformazioni: dal semplice 
        e pesante stile romanico-lombardo a quello gotico-francese pi&ugrave; 
        agile ed acuto; dalla pura bellezza rinascimentale del coro di Francesco 
        Giramo, della porta laterale della chiesa, dei dipinti del Luini e del 
        Rizzo all'impacciata pesantezza barocca dei restauri voluti dall'Abate 
        e Conte Libanorio. Pi&ugrave; tardi poi la decadenza senz'arte, gl'intonachi 
        che tutto coprono, i muri che tappano le belle finestre gotiche, e la 
        rapace mano del moderno saccomanno che strappa dall'arco d'ingresso all'Abbazia, 
        solo pochi anni fa, il bel rosone in cotto dall'aggraziato ricamo di sagome 
        ed archetti.<br>
        &quot;Quod non fecerunt barbari...&quot;<br>
