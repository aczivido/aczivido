      <p ALIGN="JUSTIFY"><br>
        <b>L&#146;alternarsi delle dominazioni nei primi decenni del Cinquecento</b><br>
        Ludovico il Moro, costretto una prima volta alla fuga nell&#146;estate 
        del 1499, dopo un effimero tentativo di riconquistare lo Stato, fu definitivamente 
        sconfitto nell&#146;aprile del 1500. Il dominio di Luigi XII, accolto 
        dapprima con relativo favore, grazie anche alla luogotenenza di Gian Giacomo 
        Trivulzio, suscitò via via malcontento e ostilità crescenti, soprattutto 
        per le gravose imposizioni fiscali. La sconfitta francese a Ravenna, ad 
        opera della cosiddetta Lega Santa promossa da Giulio II, consentì la restaurazione 
        del dominio degli Sforza, nella persona di Massimiliano, figlio di Ludovico 
        il Moro. Egli fece un'entrata trionfale il 20 dicembre 1512: ma l'ambasciatore 
        veneziano pochi giorni dopo scriveva alla Serenissima essere il &quot;duchetto&quot; 
        nulla più che un&#146;insegna (&quot;tamquam signum&quot;), e che Milano 
        si poteva dire essere governata piuttosto &quot;da Tedeschi, da Sguizari 
        e Spagnoli&quot;. Pochi anni dopo i Francesi, con la vittoria di Marignano, 
        poterono riconquistare Milano (1515). Ma la città e il Ducato continuavano 
        a essere al centro della grande contesa che in tutta Europa vedeva contrapposti 
        gli Asburgo d&#146;Austria e di Spagna alla Francia. Il prevalere delle 
        armi spagnole, nel 1521, consentì nuovamente il ritorno di uno Sforza, 
        Francesco II, fratello di Massimiliano: la sua principale forza consisteva 
        tuttavia nell&#146;appoggio dell&#146;imperatore Carlo V. Dopo una nuova 
        riconquista francese nell&#146;inverno fra il 1524 e il 1525 &#150; operazione 
        conclusasi con la famosa sconfitta subita a Pavia da Francesco I di Francia 
        &#150; lo Sforza fu rimesso sul trono, ma ormai sempre più sotto il controllo 
        e la tutela di Carlo V, che anzi per alcuni anni (dal 1525 al 1529), in 
        seguito alla congiura di Gerolamo Morone, governò direttamente, attraverso 
        suoi luogotenenti.<br>
        Il quadro della vita milanese nei primi decenni del secolo presenta dunque 
        gravi segni di crisi. Da un lato risultava evidente il tramonto di quello 
        che era stato uno dei più grandi Stati italiani: non solo per la diminuzione 
        dei territori (l&#146;odierno Canton Ticino, la Valtellina e la Val Chiavenna, 
        Piacenza, Parma, Genova: tanto che l&#146;estensione complessiva si riduceva 
        a circa 17.000 chilometri quadrati), per la grave crisi finanziaria (da 
        un bilancio di forse 800.000 ducati alla fine del Quattrocento si scese 
        ai forse 200.000 ducati del tempo di Francesco II), per la poca autorevolezza 
        dei regimi che si succedevano. Soprattutto si capiva bene, ormai, che 
        il Ducato di Milano era una semplice pedina nel gioco delle grandi potenze 
        in lotta per l&#146;egemonia europea, e non appariva più plausibile la 
        possibilità di una stabile restaurazione di un governo milanese, o di 
        una dinastia indigena come gli Sforza. nelle cancellerie e nelle corti 
        d&#146;Europa, nelle città italiane, nelle filastrocche dei cantimbanchi 
        sulle piazze, il gran discorrere che si faceva sulle sorti future dello 
        Stato di Milano prescindeva sempre di più dalla capacità di durata della 
        dinastia sforzesca (&quot;Guerre d&#146;Italia&quot;, 1989, II e III). 
        Per altro verso le lunghe guerre guerreggiate, i saccheggi, le distruzioni, 
        i taglieggiamenti delle soldatesche (dei Francesi come degli Svizzeri, 
        degli Spagnoli come dei lanzichenecchi), il malgoverno degli occupanti, 
        il susseguirsi di carestie colpivano duramente la popolazione. Le pagine 
        dei cronisti &#150; il Burigozzo, Giovanni Andrea da Prato, Ambrogio da 
        Paullo &#150; descrivono questi anni come un periodo fra i più difficili 
        e drammatici della storia della città, e danno vivo il senso di un disagio 
        che si esprime anche in una diffusa inquietudine culturale e religiosa 
        (Martini, 1957).<br>
        E tuttavia con questo quadro, di un potere statale in disfacimento e delle 
        difficoltà provocate dalla guerra, contrastavano altri aspetti della città 
        e della vita della società milanese, dei diversi ceti, &quot;corpi&quot; 
        e gruppi sociali che la componevano. La crisi non impediva di vedere anche 
        segni positivi di vitalità e di consistenza. La città, innanzitutto: &quot;il 
        povero Milano&quot; come scrivevano tanti cronisti dell&#146;epoca. Nonostante 
        i guasti i le distruzioni delle guerre, non tutta l&#146;antica floridezza 
        economica si era perduta. L&#146;inviato veneziano Gian Jacopo Caroldo 
        intorno al 1520 offriva un quadro in cui alle zone d&#146;ombra si contrapponevano 
        segni di vitalità e di ripresa. &quot;La cità de Milano è grande, e la 
        più populosa de Italia. Sono molti poveri, e si consuma pan de mestura 
        (cioè di segale e miglio). Sono etiam gran quantità de gentilomini, che 
        hano grossa intrata fino a ducati 8 in 10.000. Fanno gran spese in fameglia: 
        cavali, vestiti e viver ed etiam in elimosine; adeo che in capo de l&#146;anno 
        vengono ad intacar la intrata de l&#146;altro. Sono grandissimo numero 
        de artefici più che in (ogni altra) città de cristiani, li quali fano 
        ogni sorta lavori e merce, che vanno per tutto el mondo, come armature, 
        briglie, selle, ecc.; e però ditta città sempre vorìa guerra, per dar 
        spazamento a le robe sue. Sono etiam molti mercadanti, qual fanno per 
        Venezia, Puglia, Lion, Spagna ed Alemagna: lavorano assai panni de seda, 
        se fano boni veluti &#133; Sono etiam molte boteghe de lana: se fa gran 
        numero de panni e de barete, se manda per ogni fiera de Lion in bona summa 
        &#133;, e mandano etiam in Linguadoca; fanno etiam fostagni e bombasine 
        in gran quantità&#133;&quot;.<br>
        Ma non solo la città appare capace di reagire nelle sue attività economiche. 
        Molti grandi casati &#150; i Visconti, i Trivulzio, i Borromeo, i Castiglioni 
        &#150; mostrano di avere risorse e ricchezze per partecipare attivamente 
        al gioco politico, in una schermaglia di scontri e di alleanze fra i diversi 
        partiti e le forze esterne al Ducato: guelfi e ghibellini, francesi, imperiali, 
        sforzeschi &#133; Come ha ben messo in luce Giampiero Bognetti (1957), 
        questi decenni rappresentano una fase di rinnovata attività politica dei 
        grandi casati milanesi. Soprattutto, caratteristica importante è ancora 
        l&#146;emergere, con rilievo e consistenza maggiori di quanto sappiamo 
        per l&#146;età sforzesca, di strutture di aggregazione cittadine e municipali 
        che si formano intorno a parrocchie, vicinie, quartieri (le &quot;porte&quot;), 
        nella forma di consigli, di assemblee, di deputazioni: organismi che compaiono 
        con vivacità non trascurabile nelle testimonianze cronachistiche dell&#146;epoca, 
        spesso come espressione di umori e orientamenti popolari. Mercanti e giurisperiti, 
        artigiani e notai, per parte loro, sembrano consolidare le loro vecchie 
        strutture di aggregazione &#150; collegi, mestieri, confraternite &#150; 
        e danno vita a forme di azione collettiva. Rimangono attivi e vivaci gli 
        enti elemosinieri, gli ospedali, le istituzioni della Chiesa cittadina, 
        ora più liberi di muoversi, senza il controllo di una forte signoria. 
        Si delinea come un processo di riorganizzazione dal basso delle forze 
        e delle istituzioni urbane, in un lavorio nuovo di ricostruzione del tessuto 
        municipale. da tutto questo, come si è detto, non emergono forti progetti 
        politici: ma si intravede un orientamento non del tutto passivo, volto 
        a cercar spazio, se non per un grande stato territoriale, per condizioni 
        di autonomia che i ceti e la città si sentono abilitati a rivendicare, 
        nell&#146;ambito delle diverse dominazioni possibili.<br>
        Ci si chiedeva quale potesse essere il &quot;padrone&quot; migliore. La 
        Francia, ben vista in un primo tempo, e sostenuta soprattutto dai &quot;gentiluomini&quot;, 
        appariva in discredito, per le cattive prove già date e per il suo progressivo 
        indebolirsi nello scontro con gli Asburgo. Si parlava anche della Repubblica 
        di Venezia, ma senza entusiasmo, nel timore di un governo poco amico: 
        come riferiva ancora l&#146;ambasciatore Gian Jacopo Caroldo, i gentiluomini 
        aborrivano la sua superbia; &quot;li mercadanti et artefici etiam dubitano 
        che, essendo milano subiecto a Venezia, se veniriano a minuir le facende 
        con devedarli el comprar&quot;; i chierici temevano una forte subordinazione 
        delle istituzioni ecclesiastiche, secondo la prassi introdotta nella Terraferma 
        (&quot;quasi tutti li benefici seriano de&#146; veneziani, adeo che milanesi 
        non averiano cosa alcuna&quot;). Maggiori simpatie suscitavano altri potentati. 
        Circolava con insistenza, ad esempio, l&#146;idea di una Milano &quot;cantone 
        svizzero&quot;, anche se essa non si manifestava in progetti precisi: 
        un&#146;idea del resto accreditata e resa plausibile anche dalla forte 
        e continua pressione espansiva dei confederati che premevano sui confini 
        o che, chiamati dai contendenti, scorazzavano per il Ducato. Simpatie 
        continuava a suscitare anche una soluzione imperiale, come risulta dai 
        molti segni di disponibilità che si manifestavano fra i ghibellini, via 
        via che il prestigio dell&#146;Impero si rafforzava; disponibilità che 
        avrebbe trovato espressione poi, alla morte dell&#146;ultimo Sforza, nella 
        richiesta della città di restare &quot;perpetuamente nel poter et regimine 
        imperiale de Sua maestà (Carlo V) et Sacro Romano Imperio&quot;. E fu 
        questo l&#146;esito, alla morte di Francesco II; esito peraltro distorto 
        nei suoi sviluppi, per l&#146;assegnazione che Carlo V fece di milano 
        al ramo spagnolo degli Asburgo.<br>
        Ma nel frattempo, mentre si dibatteva sulle sorti del Ducati, nei primi 
        decenni del Cinquecento si poterono anche introdurre e sperimentare nuove 
        istituzioni, capaci di rappresentare la cittadinanza milanese e le sue 
        principali componenti di fronte ai governi che si succedevano. Proprio 
        i frequenti mutamenti di regime che travagliarono il Ducato ebbero come 
        effetto il rafforzamento di quelle magistrature e pratiche in cui potevano 
        trovare espressione e riconoscimento istituzionale le locali forze municipali. 
        In primo luogo va ricordata la costituzione del Senato, o, se si vuole, 
        il forte aumento di autorità che esso ebbe a registrare dopo gli antecedenti 
        viscontei e sforzeschi. Di fatto questa magistratura &#150; in cui si 
        fondevano gli antichi due Consigli sforzeschi, Consiglio Segreto e Consiglio 
        di Giustizia &#150; veniva ad assumere nel periodo francese un carattere 
        nuovo e diverso: ora la lontananza del re (sostituito a Milano da un rappresentante, 
        la cui autorità, per quanto alta, non era certamente paragonabile a quella 
        di un sovrano presente) conferiva a questa magistratura un rilievo e un'autonomia 
        che prima non aveva. Di fatto essa avrebbe visto notevolmente ampliate 
        le sue prerogative; inoltre, costituita com&#146;era da esponenti del 
        ceto dirigente milanese e lombardo, finiva per porsi, di fronte a principi 
        deboli o lontani, come organismo autorevole di rappresentanza, e anche 
        di governo della città e dello Stato. E accanto ai senatori &#150; quei 
        senatori &quot;sotto la ragione de&#146; quali tutto il ducato di Milano 
        era regulato&quot; -, e in stretta connessione con essi, operava il Collegio 
        dei Giureconsulti. Se vi era stato da parte dei Visconti e degli Sforza 
        il tentativo di fare dei dottori del Ducato un ceto di giurisperiti al 
        servizio del principe e del suo governo, la caduta stessa della dinastia 
        sforzesca aveva sanzionato il tramonto definitivo di quel disegno (e la 
        scomparsa stessa del Consiglio di Giustizia). Aveva lasciato viceversa 
        riemergere l&#146;antica connotazione urbana e patrizia di quel ceto: 
        una connotazione che si sarebbe accentuata con la riorganizzazione dei 
        locali Collegi dei dottori nei primi anni dell&#146;età spagnola. inoltre 
        si costituì un Consiglio di governo proprio della città. Milano aveva 
        avvertito da tempo l&#146;aspirazione &#150; varie volte espressa nel 
        periodo del governo visconteo-sforzesco, e poi con l&#146;avvento dei 
        Francesi &#150; ad avere organi propri di governo municipale, eletti dai 
        cittadini: un diritto che, come si ricordava nel 1502 a Luigi XII, possedevano 
        &quot;fere omnes Italiae civitates&quot;, autorizzate a creare &quot;ex 
        civibus suis presidentes qui publice utilitati preessent et providerent&quot;, 
        un&#146;aspirazione che nella città ambrosiana non aveva mai potuto realizzarsi.<br>
        Nel secondo decennio del Cinquecento si costituì un consiglio di sessanta 
        membri (dieci per ognuna delle sei porte della città, secondo la riforma 
        del Lautrec del 1518) che assunse poi il nome di Consiglio generale dei 
        sessanta nobili Decurioni, e che sarebbe divenuto l&#146;espressione organica 
        e il luogo di formalizzazione del patriziato cittadino. Sotto il segno 
        prevalente del patriziato, infatti, si esprimeva la rivendicazione dell&#146;autonomia 
        urbana, come anche più oltre vedremo, in consonanza del resto con linee 
        di tendenza comuni a gran parte della società italiana. I decenni a cavallo 
        tra la fine del Quattrocento e gli inizi del Cinquecento, dunque, se furono 
        testimonianza del fallimento, o dell&#146;impossibilità della restaurazione 
        di uno stato sovrano, permisero anche di vedere l&#146;organizzarsi della 
        società politica urbana in quelle istituzioni che sarebbero state protagoniste 
        della storia di Milano per tutta l&#146;età spagnola (Chittolini, 1996).<br>
