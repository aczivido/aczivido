      <p align="left"><font face="Verdana, Arial, Helvetica, sans-serif" size="2"><b><br>
        Presentazione</b><br>
        <font size="1">Il 19 maggio 1956 il Presidente della Repubblica Giovanni 
        Gronchi dava unizio ai lavori di quella &quot;Autostrada del Sole&quot; 
        che sar&agrave; una delle maggiori opere dell'Italia risorta.<br>
        Oggi, mentre il Presidente si appresta ad inaugurare il primo tronco funzionale 
        - da Milano a Bologna - mentre ferve il lavoro in tutti i cantieri - da 
        Bologna a Firenze e da Roma a Napoli - la Societ&agrave; Concessioni e 
        Costruzioni Autostrade ritiene doverosa una rievocazione storica dei collegamenti 
        stradali fra Milano e Napoli.<br>
        A ci&ograve; basterebbe una descrizione di tracciati, se l'impianto di 
        una via con definito disegno fosse solo un punto di arrivo che corona 
        l'azione di certi fattori geografici e storici, e non anche l'inizio di 
        un nuovo ciclo di rapporti umani, cio&egrave; un positivo contributo allo 
        sviluppo delle et&agrave; successive. La vita di una strada, in realt&agrave;, 
        si esplica proprio nel configurare quella dei centri che collega e delle 
        regioni che attraversa.<br>
        Richiamando qui sommariamente i collegamenti tra Milano e Napoli, si evocano 
        per lo pi&ugrave; episodi e figure di larghissima fama: ci&ograve; conferma 
        quanto essi si inseriscano nella storia d'Italia e quanto giustificato 
        sia lo sforzo di assicurare la permanenza e lo sviluppo, nelle forme che 
        il progresso tecnico consente.<br>
        <br>
        Fedele Cova<br>
        Amminisratore Delegato e Direttore Generale della Soc. Concessioni e Costruzioni 
        Autostrade p.a.</font></font></p>
