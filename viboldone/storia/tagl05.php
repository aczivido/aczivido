        <br>
        <b>Dedizione di famiglie a Viboldone: il caso dei Polvale</b><br>
        La grande forza di attrazione esercitata a lungo dalle comunit&agrave; 
        umiliate sulle aspettative sociai e religiose di ampi strati della popolazione 
        si coglie assai bene nella dediczione a esse di intere famiglie, che per 
        quanto riguarda Viboldone trova un'esplicita conferma nel caso dei Polvale, 
        una famiglia di Torrevecchia, nel Pavese. Offrendo se stesso e i suoi 
        beni a Viboldone, Ambrogio Polvale, nel 1276, portava con s&egrave; la 
        moglie Contisia, i tre figli, Pasino, Pierino e Miranetto, i nipoti Albertino, 
        Martino e Zanino, figli di suo fratello Porro, defunto, e un abbiatico, 
        Ambrogino, anch'egli orfano del padre Giovanni.<br>
        L'entrata dei genitori nella comunit&agrave; come professi non comportava 
        l'automatica adesione dei sette minori, ai quali era data la possibilit&agrave; 
        di una scelta definitiva con la maggiore et&agrave;: potevano cio&egrave; 
        scelgliere se dedicarsi anch'essi alla vita religiosa o se ritornare al 
        secolo, portando in tal caso, con s&egrave; parte del capitale guadagnato 
        con il loro lavoro.<br>
        La scelta religiosa venne effettuata, per certo, da Ambrogio, il figlio 
        del defunto Giovanni: il 15 aprile 1287, infatti, fece atto di donazione 
        alla &quot;canonica&quot; di Viboldone di tutti i beni ereditati nei territori 
        di Torrevecchia, Landriano e S.Ambrogio in Zibido, alla condizione di 
        ricevere sostentamento dalla casa, al cui servizio si proponeva di rimanere 
        insieme alla madre, donna Belfiore, per il resto della vita.<br>
        Anche Pasino rimase a Viboldone, quale fratello laico professo: in tale 
        veste lo segnalano documenti del 1321-22, e ancor prima un atto di procura 
        del 5 agosto 1302, a seguito del quale tre giorni dopo sovrintese alla 
        vendita di un sedime con casa nella citt&agrave; di Lodi, pervenuto a 
        Viboldone mediante l'unione della casa umiliata di Rocole Lodigiano e 
        venduto per far fronte alle difficolt&agrave; economiche in cui versava, 
        in quel momento, Viboldone.<br>
        Se da una parte l'esempio dei Polvale ci indica a quale inesauribile serbatoio 
        di forze sempre fresche e nuove attingesse la comunit&agrave; per il proprio 
        irrobustimento, dall'altra il fenomeno delle dedizioni pu&ograve; senz'altro 
        ritenersi significativo dell'incidenza esercitata sulla societ&agrave; 
        dal cosiddetto &quot;nuovo monachesimo&quot; (che non era necessariamente 
        soltanto quello cistercense) nei secoli centrali del medioevo.<br>
