      <p ALIGN="JUSTIFY"><b><font face="Verdana, Arial, Helvetica, sans-serif" size="2">Gli 
        Umiliati a Viboldone</font></b><br>
        <font face="Verdana, Arial, Helvetica, sans-serif" size="1"> di Mauro 
        Tagliabue<br>
        <br>
        <b>Una Comunit&agrave; di &quot;Fratres et Sorores</b>&quot;<br>
        La coesistenza di frati e di suore nella stessa &quot;domus&quot;, vietata 
        in via generale dai canoni, ma ammessa presso gli Umiliati, pur con le 
        opportune precauzioni, &egrave; documentata anche per Viboldone, bench&egrave; 
        tardivamente, a partire dalla seconda met&agrave; del Duecento. E' del 
        1276 un documento, sul quale ritorneremo, che vede un intero nucleo familiare, 
        quello dei Polvale, con padre, madre, figli e nipoti, fare un atto di 
        dedizione alla &quot;ecclesia seu canonica seu domus humiliatorum&quot; 
        di Viboldone, per condurvi la propria vita &quot;sicut fratres et sorores 
        illius domus&quot;. Un decennio dopo, nel 1287, sar&agrave; ancora uno 
        di costoro a ricordare la propria madre, Belfiore, come &quot;devota&quot; 
        vivente nella casa di Viboldone. Pi&ugrave; tardi, nel 1310, &egrave; 
        addirittura la nipote di un maestro generale, Benvenuta, &quot;professa 
        dicte domus&quot;, a venir ricordata dallo zio, fra Guidotto Riboldi, 
        che dimessosi dal generalato si era ritirato a Viboldone e, prossimo verosimilmente 
        a morire, volle disporre della rendita derivantegli da un mulino sul Lambro 
        a Melegnano in favore della comunit&agrave;, oltre che della prediletta 
        nipote: della porzione a lei riservata, &quot;soror&quot; Benvenuta avrebbe 
        potuto disporre con piena libert&agrave; per l'acquisto di vestiti o per 
        cure in caso di malattia, previa licenza tuttavia del preposito. Del permanere 
        di &quot;sorores humiliatae&quot; a Viboldone si ha notizia ancora dopo 
        che, nel 1327, Giovanni XXII non solo viet&ograve; l'ulteriore convivenza 
        di frati e di suore nello stesso edificio, ma persino la contiguit&agrave; 
        di monasteri maschili e femminili. L'ultima indicazione ci viene da un 
        elenco di case umiliate risalenti al 1344: nella &quot;domus de Vicoboldono&quot; 
        dimoravano 28 &quot;fratres&quot;, 7 &quot;sorores&quot;, 8 &quot;famuli&quot;. 
        Poi pi&ugrave; nulla. Presumibilmente, anche questa comunit&agrave; femminile, 
        al pari di quelle esistenti in altri luoghi, venne estinguendosi nel corso 
        del secolo XIV; il Tiraboschi, infatti, ci attesta che all'inizio del 
        Quattrocento non esistevano pi&ugrave; conventi comuni di frati e di suore.<br>
        Sull'organizzazione e il tenore di vita della comunit&agrave; femminile 
        di Viboldone siamo assai poco informati. Si pu&ograve; tuttavia presumere, 
        in analogia ad altre comunit&agrave; doppie, che a essa fosse preposta 
        una &quot;ministra o magistra&quot; e vi si praticasse una vita di preghiera 
        scandita dalle ore canoniche in alternanza con il lavoro manuale, &quot;pro 
        sustentatione vitae suae&quot;. L'&quot;exercitium manuale&quot; di maggior 
        assorbimento dovette essere senz'altro quello della lavorazione della 
        lana, specialmente nelle sue fasi pi&ugrave; delicate. A suore dedite 
        all'opus lanae, dalla pettinatura alla filatura, alla tessitura, rimanda 
        tra l'altro la preziosa serie di quadretti, stupendi nella lore semplicit&agrave; 
        espressiva, che istoriano nei codici ambrosiani la conaca minore di fra 
        Giovanni di Brera del 1421. In una parola, delle &quot;sorores&quot; di 
        Viboldone potremmo ripetere quanto scrive Umberto di Romans delle Umiliate 
        in generale: &quot;De lana et lino operantur assidue et fusum manibus 
        apprehendunt&quot;. I pannilani inviati alla comunit&agrave; cistercense 
        di Morimondo, distrutte e saccheggiata dai Pavesi nel 1237, sono un'ulteriore 
        conferma dell'impianto di un lanificio anche tra gli Umiliati e le Umiliate 
        di Viboldone.<br>
        Si sa per certo che all'elezione del preposto partecipavano anche le &quot;sorores&quot;, 
        almeno fino al 1247, insieme alla componente maschile della comunit&agrave;, 
        chierici e fratelli laici. Questi ultimi, esclusi dagli atti capitolari 
        soltanto a partire dal 1374, costituiscono un gruppo assai consistente 
        entro la &quot;domus&quot; di Viboldone. Nel 1258 se ne contano almeno 
        35; a essi vanno aggiunti 12 chierici (di cui sei sacerdoti, due diaconi 
        e quattro suddiaconi) per non avere che un quadro comunitario ancora lontano 
        - pensiamo - dalla sua completezza. La rivela, invece, un capitolo del 
        1302, al quale intervennero. &quot;preter novem&quot;, tutti ifrati professi 
        con diritto di voto: siccome i presenti risultano 29, a 38 membri doveva 
        ascendere l'intero nucleo comunitario. E di altri 29 frati, &quot;omnes 
        professi&quot;, costituenti &quot;plus quam due partes&quot;, ossia i 
        due terzi del capitolo conventuale, risulta formata la comunit&agrave; 
        nel 1310. Mentre,verso la met&agrave; del Trecento, il numero di &quot;fratres&quot; 
        dove essersi stabilizzato, come si &egrave; visto, intorno alle 28 unit&agrave; 
        di essi, almeno 16 dovevano essere chierici, secondo quanto nel frattempo 
        erano venute stabilendo le costituzioni dell'Ordine.<br>
        Tra le prerogative per essere ammessi allo stato chiericale vi era quella 
        di saper leggere e cantare. Ma a seguito delle disposizioni costituzionali 
        emanate dal maestro generale Beltramo (1309-17), l'accesso al chiericato 
        da parte dei fratelli laici non venne pi&ugrave; consentito e, di riflesso, 
        negato il passaggio a fratello laico professo da parte dei comnversi. 
        Quest'ultima categoria &egrave; documentata anche per Viboldone: sono 
        tali il frate Monte e il frate Ambrogio, entrambi conversi, che l'8 settembre 
        1276 intervengono in rappresentanza della &quot;domus de Vicoboldono&quot; 
        a un contratto di permuta con il monastero di Chiaravalle, dando in cambio 
        del diritto di decima sulle terre di Civesi e Rancate un bosco nel territorio 
        di Sestogallo (l'attuale Cascina Sestogallo), dove Chiaravalle intendeva 
        portare un acquedotto e dove voleva trasformare le terre in prato. Accanto 
        ai conversi spuntano i &quot;famuli&quot; o &quot;familiares&quot; ad 
        arricchire ulterirmente il gi&agrave; variegato quadro della comunit&agrave; 
        che, pur obbedendo a una concezione verticistica della scala sociale, 
        integrava al suo interno personalit&agrave;, categorie, mentalit&agrave; 
        e culture tanto diverse, riunendo comunque tutti nel momento cardine dell'elezione 
        del preposito, anche - almeno nel periodo iniziale - le componenti femminili.<br>
        Di questa microsociet&agrave; potremmo tranquillamente ripetere, senza 
        timore di errare, quanto papa Alessandro IV, il 23 novembre 1258, scriveva 
        al re di Francia per indurlo ad accogliere anche nel suo regno gli Umiliati, 
        &quot;in provincia Lombardie potissimum dilatati&quot;; si guadagnano 
        da vivere con il lavoro delle proprie mani, distribuiscono le elemosine, 
        praticano l'ospitalit&agrave;, predicano la parola di Dio.<br>
        <br>
        (da &quot;L'Abbazia di Viboldone&quot;, 1990 Banca Agricola Milanese)</font> 
