        <font face="Verdana, Arial, Helvetica, sans-serif" size="1">Mons. Pasquale Galbiati<br>
        <br>
       <b>l'ultima pagina degli Umiliati. Abyssus abyssum invocat. S.carlo 
        protettore dell'Ordine. Tentata fusione degli Umiliati coi Branabiti. 
        Attentato alla vita di S.Carlo. Bolla di soppressione.</b><br>
        Siamo ormai all'ultima pagina degli Umiliati e purtroppo vicini all'estrema 
        rovina e alla estinzione dell'Ordine. Nel 1570 le Case degli Umiliati 
        erano 97, di cui 39 date in commenda. tra queste v'era la nostra Casa 
        di Viboldone commendata ad Antonello Arcimboldi, e l'abitavano sei sacerdoti 
        Umiliati, due chierici e alcuni conversi.<br>
        Le case commendate fruttavano il reddito di quasi 32.000 scudi d'oro e 
        contenevano circa 60 frati, mantenuti dai commendatari, mentre altre erano 
        vuote.<br>
        Così le altre 58, rette da Preposti dell'Ordine, rendevano 26.000 scudi 
        d'oro. Anche di queste molte erano prive di frati, i quali in tutto l'Ordine 
        sommavano a 162.<br>
        I costumi poi erano un disastro. Il Padre Pizzo lamentasi così del suo 
        Ordine:<br>
        &quot;De' buon costumi persa è la semenza,<br>
        &quot;che non gli è più timor, più erubescenza.<br>
        &quot;.....................................................<br>
        &quot;Troppo è il dolor, Iddio, che mi circonda<br>
        &quot;Veder tanta insolenzia! ....<br>
        E continua in una lunga geremiade a narrare i disordini. In quel tempo 
        Protettore dell'Ordine era il Card. Morone, e poi nel 1562 il Card. Carlo 
        Borromeo, che con infinite cure, con coraggio indomito, in mezzo a mille 
        difficoltà si studiò di ridurre l'Ordine all'antica disciplina.<br>
        Vedendo riuscir vani i suoi santi sforzi, il santo cardinale, in un secondo 
        tempo, caldeggiò anche l'unione degli Umiliati col giovane Ordine dei 
        Barnabiti, nell'interesse dei due Ordini stessi, l'uno dei quali possedeva 
        immense ricchezze, ma era ormai senza spirito religioso, laddove l'altro 
        abbondava di questo spirito e non aveva che il necessario alla vita. Così 
        si sarebbe provveduto a un benessere vicendevole.<br>
        La tentazione era grave: ricevere in dono 94 Case religiose e la vistosa 
        rendita di 30.000 scudi d'oro!<br>
        Ma ci volle l'avvedutezza di Alessandro Sauli per rifiutare d'un colpo 
        quel dono. Il Santo, che era allora Generale dei barnabiti e gli Anziani 
        di S. Barnaba travidero in questa mescolanza una causa d'infezione, che 
        poteva alterare l'indole del giovane Istituto di Antonio Zaccaria. Quindi 
        non se ne fece nulla: e lo stesso S. Carlo scrisse al suo procuratore 
        di Roma che lasciasse andare l'idea dell'unione, per aver egli approvate 
        le ragioni contrarie dei Barnabiti.<br>
        Frutto purtroppo di tante sante fatiche da parte del santo Arcivescovo 
        per la salvezza dell'Ordine fu la congiura di ucciderlo ordita principalmente 
        da tre Preposti dell'Ordine, cioè Gerolamo Legnani, preposto di S. Cristoforo 
        in Vercelli, Lorenzo Campagna, preposto di S. Bartolomeo di Levata a Verona 
        e Clemente Mirisio di Caravaggio, preposto di S. Maria di Fornovo.<br>
        La sacrilega congiura fu attuata il 26 ottobre 1569 da un frate dello 
        stesso Ordine, che era diacono, Gerolamo Donato, soprannominato il Farina, 
        nativo di Nave.<br>
        Scampato miracolosamente S. Carlo dalla schioppettata, si diede ancor 
        più a riformare l'Ordine, ma il pontefice Pio V fu inamovibile nel suo 
        divisamento di sopprimere l'Ordine; ciò che fece con Bolla del 7 febbraio 
        1571.<br>
        Vari tentativi si fecero negli anni appresso da parte dei religiosi superstiti 
        per far rivivere il loro Ordine; ma sempre invano; finchè papa Gregorio 
        XIII, con concessione del 1 agosto 1580, permetteva loro di deporre l'abito 
        della regolare osservanza, di vivere liberi e vestire l'abito dei sacerdoti 
        secolari. Molti allora uscirono, altri furono assegnati a monasteri extradiocesani, 
        altri ancora rimasero in conventi della diocesi milanese.<br>
        Tra questi l'Oltrocchi ricorda due frati che furono trasferiti a Viboldone. 
        Il Preposto Generale, Luigi Bescapè, come degno di premio, fu richiamato 
        a Roma e quivi insignito della Prefettura di S. Agata.<br>
        La distribuzione delle Commende, che il Papa s'era riservato a sè, servì 
        per aiuto a varie opere buone, specialmente ai Seminari, Collegi e altre 
        Opere pie.<br>
        &quot;Onde, commenta il Giussani, quei luoghi ed entrate, che prima per 
        la mala vita di quei cattivi Religiosi, servivano a cose profane, si impiegarono 
        poscia, per opera di questo Servo di Dio (S. Carlo) in opere santissime 
        e di molto utile e frutto delle anime&quot;.</font></p>
