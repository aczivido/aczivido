      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1">La 
        Madonna in trono, nata per campeggiare isolata, precede probabilmente, 
        di alcuni anni, la grandiosa scena del Giudizio che si articola solenne 
        sulle tre pareti restanti dell'ultima campata.<br>
        Sulla parete di fronte alla Madonna in trono siede il Cristo Giudice, 
        circondato da angeli, con a lato le schiere degli eletti e dei dannati. 
        La scena si prolunga e si completa nelle due pareti restanti che presentano 
        a destra la Vergine seguita da Sant'Anna e dallo stuolo delle Sante coronate 
        di rose, a sinistra San Giovanni battista, seguito dalla schiera dei Santi. 
        Nei registri sottostanti delle medesime pareti, il consesso degli Apostoli, 
        sei per ogni lato su ciascuna parete, e i Dottori della Chiesa, due per 
        ogni parete. Infine, nei quattro pennacchi, figure d'angeli con cartigli.<br>
        L'unità della rappresentazione si coglie così, nonostante la distribuzione 
        sulle tre pareti, grazie a questa solenne esatta spartizione ritmica e 
        rivela un'interpretazione meditata e altamente religiosa della Scrittura: 
        qualità questa, che, da sola, basterebbe a indicare nell'autore il fiorentino 
        Giusto de' menabuoi, attivo in Lombardia forse già dal 1346 e poi trasferitosi 
        sino alla morte a Padova, dove confermerà questa sua predilezione per 
        i temi di ardua trascendenza religiosa, interpretati con una sottigliezza 
        speculativa unica nel Trecento.<br>
        La mandorla luminosa circoscrive esattamente la figura del Cristo Giudice 
        e in essa la forma acquista un'imponenza grandiosa, non però bloccata 
        e perentoria, alla maniera di Giotto a Padova (questo Giudizio è infatti 
        esemplato fedelmente su quello di Giotto nella Cappella degli Scrovegni): 
        qui la linea e il chiaroscuro plastico sono come disciolti e assorbiti 
        in una luminosità del colore che dilata e alleggerisce la figura senza 
        nulla toglierle della sua imponenza. Nella gamma dei colori teneri, accanto 
        ai rosa e ai verdi pallidi trionfa il bianco, quasi luce immateriale. 
        Nel viso, dall'epidermide chiara, si incidono lievi i tratti di una bellezza 
        tutta umana, ma di un'imanità eletta e per sempre trasfigurata; il gesto 
        pacato delle braccia lascia intravvedere sulle mani le ferite dei chiodi 
        e la tunica s'apre sulla ferita del costato, a ricordare che Cristo è 
        giudice, perchè prima è stato Redentore.<br>
        Lungo l'alone della mandorla si spartiscono gli angeli in posizioni perfettamente 
        simmetriche: suonano le trombe del giudizio, reggono il flagello, la corce, 
        la colonna, la canna, la corona di spine, già sacre reliquie senza peso 
        della Passione, non più oggetti, anche se scorciati perfettamente nello 
        spazio. Si flettono questi angeli in volo, lievi e quasi senza peso, con 
        una dolcezza grave e muta nei visi, eppure costruiti con un modulo ampio 
        e ancora romanico nel panneggio delle tuniche di un rosa continuamente 
        trascolorante in bianco al sommo delle pieghe.<br>
        Mentre, in alto, due arcangeli arrotolano &quot;il padiglione del cielo&quot;, 
        svelando &quot;il nuovo cielo e la nuova terra&quot;, aurei e gemmati, 
        in basso i castighi infernali sono già in atto, con tutto il repertorio 
        tipico della tradizione medievale; ma nuova è la tecnica pittorica, di 
        una straordinaria libertà di pennellate scoperte, dense di un colore rossastro.<br>
        Acsendono a sinistra sulle nubi gli eletti, stipati in file composte, 
        protesi con le mani, con gli occhi verso il Cristo, unificati, ancor più 
        che dai gesti e dall'atteggiamento, dalla raggiante letizia degli sguardi; 
        un'immagine che ricorda &quot;tutto il convento delle bianche stole&quot; 
        cantato da Dante nel Paradiso.<br>
        Sulla parete destra un'altra processione avanza, preceduta dalla Vergine, 
        inginocchiata a mani giunte sulla spessa coltre di nubi: ampia e lieve 
        nella tunica di un rosa freschissimo, essa è immagine di una spirituale 
        lievità e purezza. Dietro a lei, guidate da Sant'Anna, avanzano le Vergini 
        coronate di rose e protette dalla schiera compatta della milizia celeste. 
        Tutto il gruppo sembra esprimere una realtà estaticamente contemplata 
        più che razionalmente pensata, cosicchè questa sublime &quot;monotonia&quot; 
        assume quasi il tono di una salmodia pittorica.<br>
        Sulla parete di sinistra si ripete puntualmente lo stesso schema, nella 
        processione dei Santi e dei Patriarchi preceduti dal Battista, che tiene 
        il volto levato al Cristo, affilato da morbide penombre, pieno di fuoco 
        interiore. Scandiscono gli spazi sottostanti sulle due pareti gli Apostoli 
        seduti a giudicare le dodici tribù di Israele; le figure monumentali e 
        massicce si susseguono compatte in un ritmo grave, che definisce l'ampia 
        curva dello spazio. La sensibilità per lo spazio si fa più attenta nei 
        Dottori della Chiesa seduti in cattedra, intenti a penetrare il senso 
        recondito delle Scritture e a illustrare il senso della visione apocalittica. 
        Qui Giusto de' Menabuoi profonde per la prima volta particolari di meticolosa 
        precisione, come il grande manoscritto aperto sul leggìo e scritto a grossi 
        caratteri, i volumi negli scaffali, o il gesto di affilare la cannuccia.<br>
        Come è stato giustamente sottolineato da M.Gregori, questa raffigurazione 
        di &quot;studi&quot; medioevali rappresenta il massimo risultato di ordinata 
        e razionale spazialità raggiunto da Giusto a Viboldone: le quinte scorciate 
        delle cattedre a pancale, i leggii girevoli &quot;visti quasi come giochi 
        quattrocenteschi di solidi geometrici&quot;, gli sportelli aperti degli 
        scaffali rivelano la volontà di definire un ambiente interno, sottomettendo 
        il gusto padano della descrizione realistica al dominio di un'intellettuale 
        ricerca spaziale.<br>
        Gli &quot;studi&quot; di giusto rappresentano un primo esempio di quelle 
        rappresentazioni di &quot;interni&quot; destinate ad avere importanti 
        sbocchi nella tematica religiosa e profana della pittura padana della 
        seconda metà del Trecento, eloquente testimonianza del crescente processo 
        di fusione tra cultura toscana e cultura settentrionale.<br>
        Ignota è la data di esecuzione delle tre pareti del Giudizio e la difficoltà 
        di circoscriverla è dovuta alla incertezza del &quot;curriculum&quot; 
        delle opere di Giusto prima del suo soggiorno a Padova. mentre la maggior 
        parte degli studiosi (Coletti, marabottini e Salmi) tendono a datarli 
        tra il 1363 e il 1365, il Longhi propone una data non lontana dal 1349, 
        assegnando dunque questi affreschi alla prima maturità dell'artista <b>(*)</b><br>
        <br>
        <b>(*)</b> un'importante conferma di questa ipotesi si è avuta dal recente 
        ricupero (1963) di affreschi nella antica chiesa di Santa Maria di Brera, 
        chiesa madre degli Umiliati, terminata nel 1347. Nei sottarchi busti di 
        Santi attribuiti a Giusto (Russoli) si affiancano ad altri dipinti dalo 
        stesso pittore della Madonna votiva del 1349. Gli stessi maestri dovettero, 
        dunque, essere impegnati negli stessi anni nelle due chiese degli Umiliati.</font></p>
