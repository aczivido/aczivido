        <br>
        <b>Gli affreschi del tiburio: la Madonna in trono - il Giudizio</b><br>
        Le quattro pareti del tiburio sono intieramente affrescate: sul lato antistante 
        il portale è raffigurata la madonna in trono tra i Santi Michele, Giovanni 
        Battista, Niccolò (?) e Bernardo. Sulla parete di fronte è dipinto il 
        Giudizio universale, mentre le pareti laterali, idelamente legate al Giudizio, 
        rappresentano la duplice processione delle Sante e dei Santi; nei registri 
        sottostanti delle medesime pareti si individuano ancora i dodici Apostoli, 
        sei per ogni lato, e quattro Dottori della Chiesa; infine nei quattro 
        pennacchi figure di angeli con cartigli.<br>
        L'unità ideale della rappresentazione, che si configura come una solenne 
        teofania religiosa, s'accompagna alla sostanziale contemporaneità stilistica 
        di tutto il complesso, anche se dovettero intercorrere alcuni anni tra 
        l'esecuzione della Madonna in trono e le scene del Giudizio universale.<br>
        L'affresco della Madonna in trono ha un carattere chiaramente votivo e 
        commemorativo, sia per la data in cui fu affrescato, il 1349, visibile 
        sul bordo inferiore dell'affresco, sia per la presenza di quel devoto 
        inginocchiato a sinistra, nel quale è stato facile riconoscere il priore 
        Guglielmo Villa, promotore del completamento della chiesa, il quale, terminata 
        felicemente l'opera sua nell'anno precedente, ne rende ora grazie ai piedi 
        della Madonna. Come è stato notato da M.Gregori, la composizione dell'affresco 
        rivela l'intento di richiamare, per chi entra in chiesa, la stessa disposizione 
        delle statue della facciata - la Madonna col Bambino e quattro santi - 
        creando così una sorta di unità tra architettura, scultura e pittura che 
        dovette essere tenuta presente nella prima fase dei lavori di trasformazione 
        della chiesa.<br>
        La non comune sorte di possedere la data certa dell'affresco non ha facilitato 
        agli studiosi il compito dell'identificazione del pittore che lo eseguì, 
        nè è valsa ad accordarli sull'origine stessa di questi: da alcuni è ritenuto 
        un lombardo (Wittgens, Bettini, Marabottini), da altri un toscano (Longhi, 
        Coletti, Gregori e, dubitativamente, Toesca).<br>
        Invero, sin dall'ampio, solenne aprirsi della composizione, sembra risuonare 
        nell'affresco un timbro inconfondibilmente toscano; la nuda platea su 
        cui poggiano i Santi acquista ampiezza sonora e vastità di suggerimento 
        spaziale poichè le figure vi si collocano su piani diversi, convergendo 
        in diagonale con un ritmo unitario di gesti, verso il trono arretrato 
        della Madonna. Quel trono è anch'esso di marca prettamente toscana, ed 
        anzi fiorentina, candido e lieve nelle cuspidi ordinate e negli archi 
        laterali dell'edicola, ma impeccabilmente geometrico nel massiccio sedile 
        e nelle decorazioni ad intarsio del gradino sfacettato. Saldamente inserita 
        al volume del trono è la figura della Madonna, che in manto, scendendo 
        lieve dal capo, dilata verso il basso; la sua fermezza tutta toscana è 
        temperata dal fuso impasto del colore, dalla preziosa tunica e dal gesto 
        di presentare il Bambino. Ignuno dei personaggi si presenta con una sua 
        intima individualità: San Bernardo focoso e appassionato nel viso dagli 
        zigomi robusti, San giovanni Battista mite e solenne, San Niccolò grave 
        e patriarcale, San Michele giovinetto, che schiaccia con giovanile baldanza 
        il drago sotto le gambe divaricate e presenta alla Vergine la lama accuminata 
        con gesto cavalleresco. Le figure sono dipinte con una profilatura nitida 
        e marcata, che stacca e rileva sottilmente i contorni, mentre il colore 
        penetra anch'esso sottilmente la forma. Questo modo di intendere il colore 
        e, al tempo stesso, questa concisa e pur penetrante individuazione di 
        personaggi e infine la sensibilità per uno spazio aperto e reale confermano 
        l'appartenenza dell'ignoto pittore a quel gruppo di fiorentini che aveva 
        fatto proprie le conquiste giottesche, arricchendole di una qualità sottilmente 
        cromatica e di un tono sentimentalmente sfumato.</font></p>
