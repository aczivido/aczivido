      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="2"><b>Le 
        Storie di Cristo<br>
        </b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">(autore 
        sconosciuto) <br>
        <br>
        La grande Crocefissione occupa l'intera parete che separa la penultima 
        dall'ultima campata e a sua volta divide, come momento centrale e culminante, 
        i misteri della passione di Cristo da quelli della sua resurrezione che 
        occupano rispettivamente la parete destra e la sinistra della campata. 
        Ma essa appare in certo modo, concepita isolatamente e, per la presenza 
        di un frate in ginocchio ai piedi della croce, assume il significato di 
        sacra rappresentazione votiva.<br>
        Lautore ignoto si rivela a prima vista un lombardo, il primo che incontriamo 
        all'opera a Viboldone dopo i toscani, quei toscani che egli ha certamente 
        visto e meditato.<br>
        Gi&agrave; nella composizione della scena vi &egrave; pi&ugrave; di un'eco 
        della affollata Crocefissione in San Gottardo: essa appare creata per 
        sequenze di gruppi e nuclei compositivi, dove accanto alle figure della 
        Vergine e delle pie donne, di Giovanni e della Maddalena, trovano largo 
        posto figure minori e comparse: dal centurione che si toglie il copricapo 
        e, battendosi il petto, confessa il Cristo, al soldato con la spugna imbevuta 
        d'aceto, ai gran sacerdoti, ai guerrieri di cui uno occhieggia dietro 
        la spalla del compagno, come un comprimario distratto dal pubblico. Ma 
        mentre nella Crocefissione di San Gottardo ogni richiamo alla realt&agrave; 
        della scena veniva trasposto su un piano di verit&agrave; sentimentale, 
        qui diventa per il pittore lombardo puntiglioso esercizio descrittivo 
        di costume: le cotte attillate, gli elmi, le scarpe di ferro appuntite, 
        i mantelli. A guardar meglio, per&ograve;, il pittore &egrave; intento 
        a una &quot;ricostruzione storica&quot; di un avvenimento religioso e 
        il tono dominante resta quello dolente e drammatico. A cominciare dal 
        Cristo, il cui strazio della carne si trafigura in quella linea di un'eleganza 
        estenuata che s'allunga a contornare il corpo esangue, sottile; e se qua 
        e l&agrave;, nei ginocchi, nei piedi, il chiaroscuro s'addensa ancora 
        in un'anatomia marcata, a esprimere acutamente il dolore, secondo una 
        tradizione antica in Lombardia, pi&ugrave; sovente esso si stempera in 
        un'ombra soffice, che accarezza teneramente il corpo, il colto minuto 
        ed emaciato. Il patetismo lombardo ha qui attinto nuove possibilit&agrave; 
        espressive grazie alla gotica raffinatezza della linea e alla dolcezza 
        sfumata delle luci e delle ombre; al di l&agrave; di ogni contatto pi&ugrave; 
        diretto, il pittore ha utilizzato la nuova maniera di colorire dei &quot;dissidenti 
        giotteschi&quot;, che qui si trasforma in una qualit&agrave; cromatica 
        nuova, goticamente squisita e gi&agrave; tipicamente lombarda.<br>
        Anche il tono sentimentale dei personaggi &egrave;, per un lombardo, nuovo: 
        quel San Giovanni per esempio, dagli occhi gonfi di pianto e le mani incrocicchiate, 
        che fissa muto il terreno, pare una versione pi&ugrave; domestica e meno 
        vibrante del San Giovanni di Stefano nella Crocefissione di San Francesco 
        d'Assisi; ed &egrave; nuovo anche il modulo monumentale della figura, 
        l'ampio, morbido panneggiare del mantello. Ombre cineree e tocchi rosati 
        emergono dal rovinio attuale del colore, rivelando una tecnica sottile 
        del modellato.<br>
        O si guardi il gruppo di Maria sorretta dalle pie donne. L'afflosciarsi 
        di quel corpo svenuto &egrave; forse quanto di pi&ugrave; sentimentalemnte 
        vero possiamo trovare sin qui in Lombardia: quella mano inerte sulla spalla 
        della soccorritrice, l'altra rilasciata sul fianco sono di una soprprendente 
        verit&agrave;; cos&igrave; come il volgersi improvviso di una delle donne 
        a seguire con gli occhi il gesto concitato della compagna, mentre l'onda 
        piena dei manti allaccia tutte e tre in un'unica sequenza ritmica.<br>
        Questa Crocefissione &egrave; con tutta probabilit&agrave; la prima di 
        un gruppo di Crocefissioni che si susseguono negli anni immediatamente 
        successivi dal 1367 al '70 circa: quella pi&ugrave; rada e statica di 
        Solaro ne rappresenta sostanzialmente una ripetizione, quella di Lentate 
        una versione dilatata, fantasiosa, pittoresca, quella di Mocchirolo, ora 
        a Brera, invece, sfrondata, concentrata, drammatica.<br>
        Nell'intradosso dell'arco che separa la penultima dall'ultima campata 
        l'artista dispone busti di profeti dai volti miti, pazienti, dai gesti 
        meditabondi. La soffice stesura del colore perlaceo negli incarnati rivela 
        la conoscenza della pittura di Giovanni da Milano e ripropone cos&igrave; 
        il modo stringente l'ipotesi di una presenza di questo pittore in Lombardia, 
        provvisoriamente rimpatriato dalla Toscana, forse accanto ai toscani. 
        Che il fenomeno innovatore dei pittori toscani e di Giovanni avesse fruttificato 
        in Lombardia, cooperando al sorgere di una pittura pi&ugrave; colta e 
        ormai gotica, lo si vede nel tondo bellissimo di Eva, posta a riscontro 
        di Adamo, sotto la Crocefissione, a ricordare la &quot;felix culpa&quot; 
        che permise la redenzione della Croce. Il contorno esile della figura 
        vista di schiena, quel profilo diafano, l'eleganza del panneggiare, quella 
        balenante chioma biondo fulva, fanno di questa Eva la prima di quelle 
        creature aristocratiche ddel mondo gotico che s'appresta a farsi cortese 
        e internazionale in Lombardia. E il raffinato passaggio delle tinte tenuissime 
        si accorda con l'eleganza mondana che accomuna e livella il sacro e il 
        profano. Facile &egrave; accostare questa Eva con la Santa Caterina, altera 
        nobildonna in ermellino, inginocchiata ai piedi del Cristo Bambino negli 
        affreschi di Mochirolo. Progenitrici entrambe delle &quot;svaporate&quot; 
        ame di casa Borromei e non estranee, nella dolcezza delle carni madreperlacee, 
        alle creature masoliniane di Castiglione Olona.<br>
        Nelle volte ove si svolgono l'Annunciazione, l'Adorazione dei Magi, la 
        Presentazione al Tempio e il Battesimo di Cristo, sembra accentuarsi la 
        stlizzazione gotica della linea che &egrave; sembrata al Marabottini farsi 
        addirittura &quot;disperata&quot;, mentre il Toesca riteneva questi affreschi 
        esemplati su modelli toscani affini ai minori seguaci di un Agnolo Gaddi. 
        Non si &egrave; forse tenuto conto che la necessit&agrave; di colmare 
        gli alti spicchi della volta accentua, qui come altrove, lo stilizzato 
        allungamento delle figure. In realt&agrave;, troppi particolari stilistici 
        rivelano la stessa mano lombarda che dipinse tutti gli altri affreschi 
        delle storie di Cristo, mentre talora il ricordo di Giovanni da Milano 
        si faaddirittura imperioso: nella scena dell'Annunciazione, ad esempio, 
        ambientata in un'ariosa stanzetta dove vi &egrave; pi&ugrave; di un richiamo 
        all'Annunciazione di Pisa e a quella della predella del Polittico di Prato; 
        oppure negli angeli del Battesimo dalle sontuose, araldiche ali, o nel 
        sacerdote della Presentazione. E inoltre, &egrave; inequivocabilmente 
        lombardo il gusto novellistico che annida nell'angolo dell'Adorazione, 
        i destrieri stanchi e affamati, il famulo che atttende di sfamarli, e 
        veste di lusso orientale uno dei re.<br>
        Sulla parete di destra si allineano gli episodi dell'Ultima Cena, della 
        Preghiera nell'Orto, della Cattura, della Flagellazione e della Salita 
        al Calvario. l'elevazione relativamente modesta delle pareti costringe 
        le scene in proporzioni quasi esigue, ma non per questo il pittore rinuncia 
        ad affollarle, ordinando le composizioni pi&ugrave; in orizzontale che 
        in profondit&agrave;, con un gusto quasi da predelle, caro a Giovanni, 
        e con evidente disinteresse per le ambientazioni spaziali. Nella Cena 
        la prospettiva del tavolo &egrave; incerta e approssimata e gli apostoli 
        sono scalati pi&ugrave; su misura della cornice che dello spazio interno; 
        l'attenzione &egrave; tesa all'evidenza del racconto, alla mimica dei 
        gesti, alla &quot;natura morta&quot; del tavolo, ad alternare puntualmente 
        zazzere lunghe e corte, capelli fulvi o candidi e barbe di diversa forma. 
        ma l'influsso del Giusto &egrave; evidente nella massiccia squadratura 
        degli apostoli visti di schiena, nelle larghe e rade pieghe delle loro 
        tuniche; influsso inscindibile da quello di Giovanni da Milano, come si 
        vede nei tipi fisionomici e in particolare in quel Cristo benedicente, 
        ieratico e dal viso soffuso di soffice chiaroscuro. Ancora giustesco &egrave; 
        il modulo delle figure nella Preghiera dell'orto, come &egrave; giustesco 
        lo sbiancar del colore alla sommit&agrave; delle pieghe, mentre nel nobile 
        viso dell'apostolo giovane che par quasi meditare pi&ugrave; che dormire, 
        riaffiora ancora una volta il ricordo di Giovanni.<br>
        Il nostro artista non &egrave; peraltro un mediocre ripetitore di formule 
        appena assimilate. Egli mostra piuttosto d'aver colto le due indicazioni 
        pittoriche pi&ugrave; importanti che in quegli anni erano apparse sull'orizzonte 
        lombardo, quella di Giusto appunto e di Giovanni da Milano.<br>
        Sotto questa duplice spinta, a Viboldone, la pittura lombarda si disancora 
        dalle sue tradizionali posizioni di realismo popolare e vagamente nordico, 
        da quel linearismo pesante e un po' artigianele di sapore romanico. Un 
        altro realismo va ora nascendo, che &egrave; piuttosto attenzione all'intima 
        qualit&agrave; delle cose, ai mille aspetti di una vita quotidiana che 
        si va facendo in quegli anni via via pi&ugrave; lussuosa. Giovanni doveva 
        essere stato certamente il primo ad operare questo arricchimento della 
        pittura lombarda ritornando provvisoriamente, come si &egrave; detto, 
        dalla Toascana in Lombardia; e gi&agrave; prima di recarsi in Toscana, 
        egli aveva probabilmente nutrito la propria pittura di succhi pi&ugrave; 
        raffinati e continentali. Giovanni dovette contribuire, cio&egrave;, alla 
        frmazione di una nuova tradizione lombarda direttamente e di presenza, 
        n&egrave; questa avrebbe potuto nascere, come altri pensa, quasi per affinit&agrave;, 
        mentre egli era lontano in Toscana, sicch&egrave; egli l'avrebbe poi ritrovata 
        gi&agrave; fatta, al suo definitivo rientro in Lombardia dopo il 1370. 
        L'affollato scomparto con la cattura di Cristo &egrave; una precisa testimonianza 
        del &quot;nuovo corso&quot; della pittura lombarda. la narrazione scivola 
        continuamente nell'episodio marginale e di costume: i sacerdoti che parlottano 
        in disparte, i guerrieri racchiusi da capo a piedi nelle attillate maglie 
        di ferro, negli elmi ovoidali, o quel tipo a sinistra col berrettone di 
        panno e il nodoso randello: una parata da non sfigurare su un codice miniato 
        come quello cavalleresco del Lancelot o del Giron. ma il ricco svariere 
        dei colori teneri che risplendono luminosi, il fluire delicatamente e 
        elegantemente gotico dei panneggi riscatta l'affresco da ogni pericolo 
        cronachistico, cui non sfuggono, invece, soprattutto in alcune parti, 
        n&egrave; gli affreschi di Lentate, n&egrave; quelli di Albizzate e neppure 
        quelli, pi&ugrave; alti, di Solaro.<br>
        Le storie di Cristo rivelano un approfondimento e un'unit&agrave; di stile 
        che assegna l'intero ciclo alla stessa mano nonosttante le inevitabili 
        oscillazioni. Queste oscillazioni sembrano accentuarsi nelle due scena 
        seguenti: la Flagellazione e la Salita al Calvario; modi pi&ugrave; ingenui 
        e di derivazione strettamente miniaturistica vi affiorano, non solo e 
        non tanto nelle fisionomie - quel Cristo infantilmente dolente, con la 
        barba &quot;a forfecchino&quot; che sale il Calvario, quelle donne lacrimose 
        che la soldataglia tien dietro a forza - quanto nella profilatura pi&ugrave; 
        greve e nel marcar pi&ugrave; netto del colore, gustato pi&ugrave; come 
        timbro che come tono, dove i rossi predominano. Non per niente i protagonisti 
        della Salita al calvario sono quei due cavalieri azzimati, vestiti alla 
        nuova moda che circola gi&agrave; dalla met&agrave; del secolo ma che 
        a partire circa dal 1365 si fa pi&ugrave; spinta: gonnellino corto e ben 
        segnato alla vita per accentuare la convessit&agrave; del petto, lunghe 
        calze a punta che inguaiano la gamba, e il corto spadino. Nella Flagellazione, 
        poi, dallo sfondo di architettura di cartapesta, torna quel viso infantile 
        del Cristo, il modulo decorativo del corpo dalle piaghe regolari, mentre 
        il tipo biondo di alemanno flagellatore e il ceffo bruno sembrano aver 
        poi ispirato uno dei miniatori del &quot;Taccuinum sanitatis&quot; di 
        Parigi.<br>
        La parete di sinistra ci appare nel complesso. stilisticamente pi&ugrave; 
        alta, tanto che vorremmo annoverare le scene che la compongono tra i pi&ugrave; 
        belli affreschi lombardi del trecento a noi pervenuti.<br>
        La grande lunetta della Piet&agrave;, in alto, rinnova con accenti ancora 
        pi&ugrave; serrati la commozione della Crocefissione. la composizione 
        ha un suo sobrio ritmo decorativo che dispone in linea ascendente le rocce 
        rossastre e le figure raggiungendo un suo climax psicologico al centro, 
        nelle figura del Cristo e della madre. Lo scheletro fragile del Cristo 
        ha un suo patetismo intenso affidato soprattutto alla linea che discende 
        stremandosi lungo il corpo esile fino alle gambe smagrite e alle sottilissime 
        mani. E il compianto ha note di tenerezza umana acute: quel modo trepido 
        e vero di Maria di reggere il capo del figlio, o la disperata piet&agrave; 
        con cui le donne toccano quelle mani e quei piedi insanguinati quasi fossero 
        gi&agrave; sacre reliquie. Poi la catarsi degli affetti nella silenziosa 
        adorazione dei personaggi maschili inginocchiati, nel dolore muto di Giovanni.<br>
        In questa Piet&agrave; vi &egrave; pi&ugrave; di un riflesso di una cultura 
        francesizzante, continentale, quella stessa che, abbiamo detto, doveva 
        aver presieduto alla formazione di Giovanni e alla quale Giovanni stesso 
        torner&agrave; in un'opera tarda come la Piet&agrave; Martin Le Roy. Ne 
        fa fede, oltre l'estenuata eleganza della linea e la raffinata qualit&agrave; 
        del sentimento, il lirismo delicato, quel fluire pi&ugrave; decorativo 
        ed elegante che altrove dei panneggi delle Marie. La delicatezza dei colori, 
        che raggiunge un suo vertice luminoso nel drappo bianco sulla spalla di 
        Nicodemo reca un ricordo di Giusto, coa&igrave; come giustesca &egrave; 
        la donna protesa ai piedi del Cristo.<br>
        Pi&ugrave; avare ancora su questa parete le indicazioni ambientali: qui 
        il solo e nudo avello aperto a destra; nelle altre scene basta la sommaria 
        indicazione del monte nell'Ascensione, delle stelle nell'Incredulit&agrave; 
        o l'accenno d'una tettoietta nella Pentecoste a indicar il luogo e l'ora 
        dell'avvenimento. In queste tre scene le figure gremiscono letteralmente 
        lo spazio, muovendosi tuttavia con agio e, secondo uno schema quasi identico 
        nelle tre scene, si scindono in due gruppi ai lati. Nei tre riquadri, 
        e particolarmente nell'Incredulit&agrave; di San Tommaso &egrave; stata 
        pi&ugrave; volte notata l'evidente rassomiglianza con le tavolette di 
        Giovanni da Milano della coll. Bacri di Parigi, una delle quali rappresentante 
        appunto l'Incredulit&agrave;. Vogliamo specificare che ricorda Giovanni, 
        soprattutto quel disporsi in ralice delle figure che fa emergere verso 
        il centro le figure pi&ugrave; lontane, il ritmo ondoso delle pieghe arcuate, 
        la mimica, il modulo fisionomico dell'ampia fronte e dai lineamenti minuti.</font></p>
