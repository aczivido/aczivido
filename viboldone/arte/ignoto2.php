      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="2"><b>La 
        Madonna in trono e Santi<br>
        </b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">(autore 
        sconosciuto) <br>
        <br>
        Simile a una vasta ancona affrescata, si leva alta sull'ultima arcata 
        della nave centrale l'immagine della Madonna in trono co Bimbo tra i santi 
        Michele, Giovanni Battista, Niccol&ograve; e Bernardo. E' un affresco 
        chiaramente votivo, anzi memorativo per quel devoto presentato alla Vergine 
        da san Michele, nel quale &egrave; stato facile riconoscere il priore 
        Villa che, terminata felicemente l'anno precedente la facciata, ne rende 
        ora grazie ai piedi della Vergine.<br>
        La non comune sorte di possedere la certa data dell'opera, l'anno 1349, 
        scritta sul bordo inferiore dell'affresco, non ha facilitato agli studiosi 
        l'identificazione dell'autore n&egrave; &egrave; alsa ad accordarli sulla 
        sua origine. Fin dal 1906 il Suida lo segnalava come opera di Giovanni 
        da Milano e nel 1912 il Toesca scriveva che l'affresco attestava &quot;un 
        modo... che non conosciamo essere stato di nessuno dei pittori fiorentini 
        della prima met&agrave; del '300&quot;. Di un pittore lombardo ritennero 
        l'affresco successivamente la Wittgens, il Bettini, e il Marabottini, 
        mentre nel 1951 il Toesca modificava in parte la precedente opinione, 
        vedendo in esso &quot;la personalit&agrave; di un fiorentino oppure di 
        un lombardo tra i discepoli di Giotto&quot;. D'altro canto il Longhi non 
        dubitava mai dell'origine fiorentina del pittore, assegnandolo in un primo 
        tempo (1940) a un &quot;maestro... non inferiore a ci&ograve; che di meglio 
        si produce a Firenze al tempo di Daddi anziano&quot;; anche il Coletti 
        riteneva si trattasse di un fiorentino orcagnesco e il Salmi &quot;fatica 
        di un epigono di Maso di Banco&quot;.<br>
        E invero, sin gi&agrave; nell'ampio, solenne aprirsi della composizione, 
        risuone un timbro inconfondibilemnte toscano. la nuda platea su cui poggiano 
        i personaggi acquista una sua grandiosa sonorit&agrave; prospettica per 
        la solenne esedra di figure che vi si collocano, comvergenti nel ritmo 
        unitario dei gesti e dei moti verso il trono pi&ugrave; arretrato della 
        Madonna. Vi si rivela una sensibilit&agrave; acuta dello spazio che si 
        ritrova poi sin nei minimi particolari, dal libro di San Bernardo, ale 
        ali di San Michele che scorciano impeccabilmente al pari delle ante del 
        trono. Qualit&agrave; gi&agrave; questa, riteniamo, impossibile in tale 
        grado di forza e sottigliezza a un pittore lombardo del 1349, ancorch&egrave; 
        aggiornato alle novit&agrave; che Giotto e i seguaci avevano recato in 
        Lombardia nel decennio precedente. Che se poi si passa a considerare i 
        personaggi della scena, il linguaggio toscano si rivela ancor pi&ugrave; 
        preciso. A cominciare da quella Madonna, di grave e possente cubatura, 
        memore chiaramente di maso. Il manto, scendendo lieve dal capo, sottolinea 
        il dilatarsi della figura verso il basso e quetsa, addentrandosi nel profondo 
        bancale del trono, si salda spazialmente ad esso e ne rieccheggia le proporzioni. 
        Ma l'epica fermezza delle creature masesche &egrave; qui temperata e addolcita 
        dalla nitida profilatura che non racchiude la figura con geometrico rigore, 
        ma rileva sottilmente le variazioni della forma. E questa, poi, &egrave; 
        continuamente indagata per via di colore: nel viso della madonna il modellato 
        &egrave; tenerissimo nel fuso impsto del colore; la preziosa tunica rosa 
        ricamata aderisce lieve alla forma e l&agrave; dove si tende ne rivela, 
        sbiancandosi, la pienezza; il velo sul capo lascia intravedere in trasparenza 
        la massa soffice dei capelli. Anche questo modo di intendere il colore 
        pu&ograve; essere solo di un pittore che abbia fatta propria, e con intelligenza, 
        la lezione del &quot;dipingere unito&quot; di Stefano. E questa dolcezza 
        del colore mitiga ancor pi&ugrave; la rigorosa impostazione masesca. Rieccheggia 
        pure moduli di maso il bambino, nel piccolo torso classicamante nudo, 
        appena sfiorato dalla tunichetta che scivola sulle spalle; e benedice 
        un poco solenne ma con umana tenerezza.<br>
        Anche il trono &egrave; di marca prettamente toscana, anzi, fiorentina. 
        L'edicola candida e live, svuotata dagli archi trilobi, s'addentra sicura 
        nello spazio, rsa preziosa da quei gotici frastagli che l'architettura 
        fiorentina aveva in quegli anni accettato pur confinandoli nei portali, 
        nelle finestre, nelle edicole, senza mai rinunciare a disciplinare gli 
        spazi in una misura certa, gi&agrave; presaga del prossimo Rinascimento. 
        Appunto un geometrico rigore prospettico scorcia le cuspidi, bilancia 
        le verticali con le orizzontali, squadra il massiccio sedile e trionfa, 
        infine, nel gioco prospettico di incassi e sporgenze della pedana, esaltato 
        dall'intarsio geometrico arnolfiano che, senza una pecca, digrada nel 
        semicerchio centrale del gradino.<br>
        Nei santi che circondano la madonna, la presentazione primeggia indubbiamente 
        sull'azione. Il pittore s'addentra a indagare la realt&agrave; vivente 
        del personaggio dimostrando un interesse naturalistico acuto che si spinge 
        talvolta sino al ritratto: come in quel focoso San Bernardo dal viso robusto 
        e qusi contadino sul collo massiccio, dagli zigomi e dalla mascella forti, 
        la pelle arrossata e plasticata da luci vivide; o quel mite e quasi dolente 
        Giovanni battista; o, invece, l'adolescente San Michele che nella tunichetta 
        guerriera, schiaccia con giovanile baldanza sotto le gambe divaricate 
        il mostro e presenta alla Vergine la lama acuminata, ruotando il polso, 
        il braccio discosto dal busto, in un gesto cavalleresco e cortese.<br>
        Per questa indagine il pittore si serve sempre di una profilatura nitida 
        e marcata, non mai per&ograve; aggravata e dura, che stacca e rileva sottilmente 
        i contorni; ma resta pur sempre al colore il ruolo preminente, quello 
        di penetrarela verit&agrave; sottile delle &quot;cose&quot;: per esempio, 
        di suggerire la morbida lanosit&agrave; del saio di San Bernardo, di un 
        bianco che si fa giallino nel cavo delle pieghe che ricadono morbide ai 
        piedi, s'infittiscono nei maniconi, trattenute nel cavo della mano l&agrave; 
        dove s'appoggia il libro, o scivolanti nel braccio levato; o lo spesso 
        broccato cangiante dal rosa al verde del mantello episcopale di San Nicol&ograve;, 
        o il panneggiare spesso e bellissimo della tunica violacea del Battista, 
        quasi classicheggiante. Un colore talora vivido e rossastro nelle epidermidi, 
        lieve e luminoso, unito a una delicatezza lineare impalpabile nei capelli 
        e nelle barbe, liquido e traslucido nei bianchi.Giunti a questo punto, 
        ci sembra che non si possa pi&ugrave; davvero dubitare che il pittore 
        dell'affresco memorativo sia un toscano, come pure ci sembra siano emersi 
        molti indizi che lo collocano, come primo orientamento, in rapporto ai 
        due grandi aspetti della pittura fiorentina di quegli anni, vogliamo dire 
        la pittura di Maso e quella di Stefano, ma in posizione, verso entrambe, 
        di discepolo ottimo e personale.<br>
        Ci si chiede allora se le indicazioni stilistiche che siamo andati rilevando, 
        non facciano capo, quasi puntualmente, alla sfera poetica di Giottino, 
        figlio di stefano, allora, nel 1349, presumibilmente poco pi&ugrave; che 
        ventenne; al di l&agrave; dei persistenti legami con Maso e con Stefano, 
        suoi ascendenti diretti, legami ovviamente pi&ugrave; sensibili in un'opera 
        che sarebbe cos&igrave; giovanile, sembrano farsi gi&agrave; evidenti 
        in questo affresco taluni aspetti del suo personalissimo stile. La modellazione 
        finissima del colore accompagnata dalla nitida marcatura grafica come 
        pure l'attenzione alla realt&agrave; sentimentale del personaggio gi&agrave; 
        appartengono, ci sembra, al linguaggio di Giottino.<br>
        Per quanto riguarda la possibilit&agrave; di una presenza sua in Laombardia 
        intorno a quegli anni, ricordiamo che gi&agrave; il Coletti ha proposto 
        il nome di Giottino per la bella e purtroppo rovinatissima Crocefissione 
        di San Gottardo, che dovrebbe essere di quegli anni, e con la quale alcuni 
        dati di questo affresco sembrano coincidere. Qui il tema non si presta 
        a quell'onda vigorosa del racconto che trascorre da un capo all'altro 
        della folta composizione di San Gottardo, preludio a quel segreto ritmo 
        affettivo e ai rari sentimenti della patetica Piet&agrave; degli Uffizi, 
        pi&ugrave; tarda, sembra, almeno un decennio. Ma si ritrova qui quel nudo 
        piano di posa, apparentemente inerte su cui posano i personaggi, quella 
        morbida preparazione rossastra delle epidermidi, la profilaturasottile, 
        quell'indagine del reale che si far&agrave; poi icastica verit&agrave; 
        naturalistica nella piccola Cocefissione del louvre, per la quale pure 
        &egrave; stato proposto il nome di Giottino. E a voler spingersi oltre 
        in raffronti pi&ugrave; puntuali, si pu&ograve; forse anche scoprire qualche 
        vicinanza pi&ugrave; stretta, tra quelle guance accaldate di Niccol&ograve; 
        sulla candida bara o la morbida scioltezza del saio di San Bernardo con 
        quelle rispettivamente del santo Vescovo e del santo monaco della Piet&agrave; 
        di San Remigio agli Uffizi. Esperienze analoghe e ricerche tangenti a 
        queste di Giottino s'andavano del resto rivelando anche altrove in quegli 
        anni, ad esempio nella Assunta di Chiaravalle.<br>
        L'ipotesi di un soggiorno, magari ripetuto, di Giottino in Lombardia varrebbe 
        infine a spiegare anche quel tono &quot;padano&quot; pi&ugrave; volte 
        rilevato delle sue opere; un tono che forse la sola amicizia con Giovanni 
        da Milano non basterebbe a giustificare e che si ritrova, puntualmente, 
        nell'altra opera di Giottino - il Tabernacolo di Via del Leone a Firenze 
        - con il quale si esaurisce, per ora, o quasi, il &quot;curriculum&quot; 
        tuttora scarno di Giottino.<br>
        E vien fatto persino di chiedersi, allora, se lo Stefano che il Vasari 
        ricorda intento a numerose opera in Lombardia non potesse invece, per 
        uno scambio non nuovo, essere proprio Giottino, figlio di Stefano.</font></p>
