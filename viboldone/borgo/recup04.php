      <p align="JUSTIFY"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><b>Dalla 
        chiesa nello scenario urbano al progetto del Borgo di Viboldone: una posizione 
        moderna</b> (Cesare Macchi Cassia)<br>
        Mi &egrave; sembrata molto bella la relazione di Chiara Mauri! Io credo 
        che se Carlo Borromeo non avesse distrutto con violenza gli Umiliati, 
        noi lombardi saremmo oggi dei riformati, dei cristiani protestanti e non 
        dei cristiani cattolici. Con il loro modo di lavorare e di pregare, gli 
        Umiliati rappresentavano la parte pi&ugrave; moderna della societ&agrave; 
        in cui vivevano. Carlo Borromeo nella sua lotta alla Riforma non poteva 
        sopportare che la Chiesa rappresentasse attraverso gli uomini e le donne 
        riuniti in questo Ordine la modernit&agrave; della societ&agrave;, perch&egrave; 
        ci&ograve; andava a saldarsi con la modernit&agrave; costituita in Europa 
        dalla Riforma.<br>
        Il fatto che gli Umiliati, nel 1569, siano arrivati a tentare di uccidere 
        Carlo Borromeo denuncia la durezza della lotta in corso tra modernit&agrave; 
        e tradizione, tra Riforma e Controriforma. Un episodio tra i tanti. Basti 
        pensare ai livelli di orrore cui giunse questa lotta in Valtellina, alla 
        frontiera tra il mondo latino e il mondo germanico, in una terra italiana 
        per geografia e delle 3 Leghe Grigie per possesso, e contemporaneamente 
        ricordare che a Poschiavo, a sud del passo del Bernina, la Riforma fu 
        portata dai milanesi, come dice la lapide nella chiesa protestante. Io 
        credo che qualcosa dell'anima di quella modernit&agrave; sia comunque 
        rimasta nelle pietre dell'abbazia di Viboldone, se intorno ad essa &egrave; 
        stato possibile negli anni '60 del secolo scorso uno dei rarissimi interventi 
        di attualit&agrave; stilistica, di modernit&agrave; architettonica realizzati 
        in una risorsa storica religiosa; il convento di Gigi Caccia &egrave; 
        un esempio di coraggio del committente e di valore del progettista. Come 
        gli Umiliati vivendo la loro modernit&agrave; sceglievano i loro architetti 
        nel modo che Chiara Mauri ci ha ricordato, cos&igrave; ancora una volta 
        &egrave; stato possibile in tempi a noi vicini. Alla luce della situazione 
        di oggi quegli atteggiamenti ci sembrano, come apparir&agrave; chiaro, 
        gi&agrave; lontanissimi.<br>
        Io vi parler&ograve; dell'occasione che ho avuto come architetto docente 
        alla Facolt&agrave; di Architettura di Milano di rispondere a una richiesta 
        del Comune di San Giuliano quando, a seguito delle critiche che le scelte 
        del Piano Regolatore aveva raccolto, agli amministratori apparve utile 
        una riflessione globale sul borgo di Viboldone. In quella occasione io 
        sviluppai una interpretazione progettuale di questo luogo-problema, che 
        serv&igrave; ad aprire una discussione. Una discussione che non giunse 
        mai ad una chiusura. Eravamo nel 1994. La posizione disciplinare che stava 
        alla base della propsta riteneva sostanzialmente diverso un atteggiamento 
        archeologico e uno progettuale di fronte alla conservazione. Se noi rimnunciamo 
        alla liceit&agrave; della modificazione in realt&agrave; rinunciamo alla 
        conservazione. Non esiste conservazione senza trasformazione, per una 
        risorsa che non sia archeologica. Le ragioni che oggi, qui come altrove 
        in Italia, impediscono di giudicare la qualit&agrave; di una proposta 
        progettuale, le ragioni che lo hanno impedito in questi sei anni rendendo 
        questo progetto inutile, caratterizzano negativamente il nostro paese 
        e la sua cultura di fronte alle altre culture europee.<br>
        La risorsa Viboldone &egrave; costituita insieme dalla presenza storica 
        e monumentale di una delle fondamentali Abbazie del territorio milanese, 
        dall'architettura moderna del convento, dalla forma del nucleo urbano 
        nel quale essi sono inseriti, dai manufatti residenziali e agricoli, dal 
        paesaggio della produzione agricola che tutto ancora circonda. Un insiemo 
        molto articolato ed eterogeneo al suo interno e, ci&ograve; nondimeno, 
        composto da elementi fortemente solidali tra di loro, legati entro un 
        unico e ben leggibile principio insediativo. Un esempio di chiesa/citt&agrave;. 
        Il processo di degrado di questo insieme &egrave; culturalmente fortissimo. 
        Da esso si salvano parzialmente i manufatti religiosi. Ed &egrave; dunque 
        urgente reinserire il borgo di Viboldone entro un ciclo vitale del territorio 
        che lo ospita nel Sud Milano, per ridare ad esso un significato. E' ci&ograve; 
        che il progetto propone, dal punto di vista funzionale, gestionale e morfologico, 
        mischiando usi, integrando forme e figure, rafforzando il ruolo ordinativo 
        della chiesa sul disegno degli spazi collettivi, fornendo una guida colta 
        alla modificazione.<br>
        Il punto di partenza del progetto &egrave; la considerazione che la chiesa 
        sia un edificio urbano, in quanto edificio collettivo. Che di conseguenza 
        essa viva nel tempo e rappresenti nelle sue forme la modificazione nei 
        modi d'uso della citt&agrave; da parte di una societ&agrave; in trasformazione. 
        Il punto di vista sulla citt&agrave; da cui io parto &egrave; un punto 
        di vista classico. esso ritiene la citt&agrave;, anche la citt&agrave; 
        contemporanea, rappresentazione dei suoi costruttori e necessitante di 
        scenari per il suo futuro da aprte dell'architetto. Contro una visione 
        che vede la citt&agrave; come qualcosa non pi&ugrave; interpretabile attraverso 
        il principio organizzatore della forma, che ritiene non pi&ugrave; utile 
        per essa un disegno fisico essendole sufficienti le sole regole derivanti 
        da altri disegni. Oggi viviamo sul territorio e nella societ&agrave; urbana 
        una radicale rottura. Da un lato la continuit&agrave; espressa dall'assoluto 
        della citt&agrave; storica, della unitariet&agrave; della citt&agrave; 
        consolidata e dalla diversit&agrave; per parti dei progetti della citt&agrave; 
        moderna, dall'altro la diversit&agrave; elementarista della citt&agrave; 
        contemporanea. La rottura &egrave; perfettamente rappresentabile dalla 
        perdita di valore dello spazio collettivo, sia esso rappresentato da luoghi 
        aperti o da manufatti, dello spazio ordinativo di quella continuit&agrave; 
        urbana, dello spazio individuato da architetture che avevano come ruolo 
        quello di costruire la citt&agrave; proprio attraverso la definizione 
        dei luoghi pubblici. L'edificio religioso, in ogni sua espressione e tipologia, 
        ha rappresentato una delle pi&ugrave; forti occasioni per mostrare il 
        ruolo di costruttrice della citt&agrave; da parte dell'Architettura, e 
        in questo senso il suo progetto &egrave; stato sempre, nel passato, un 
        prgetto ordinativo dello spazio urbano. Esso ha spartito questo ruolo 
        con pochissimi altri manufatti. Per assoluta continuit&agrave; nel tempo, 
        solo con il mercato. Non con il teatro, non con il foro, non con il tribunale. 
        Appare fondamentale che nel passato la chiesa abbia rappresentato la citt&ograve;, 
        cio&egrave; la modernit&agrave;, anche al di fuori del suo tessuto, anche 
        per gli uomini che non vivevano la citt&agrave;, che non costruivano la 
        modernit&agrave;. L'ha rappresentata sul territorio, alla scala geografica 
        come luogo dello scambio, dell'incontro. Contro l'isolamento e la staticit&agrave;. 
        L'ha certamente rappresentata, con gli Umiliati, a Viboldone. Tutto ci&ograve; 
        spiega da un lato la ricchezza di ideazione e di materia concentrata nell'edificio 
        religioso, e dall'altro il valore strutturante il paesaggio che esso sempre 
        conquistava: la intellettuale chiesa rinascimentale a pianta centrale 
        nelle campagne toscane, la chiesa barocca - la ricca e confortevole sala 
        di tutti, espressione della Controriforma - sulle colline della Baviera, 
        la agricola abbazia medievale nella pianura lombarda: Viboldone.<br>
        La citt&agrave; contemporanea &egrave; una citt&agrave; inversa. Essa 
        vive degli spazi tra le cose, cos&igrave; come del movimento: ma di spazi 
        utilizzati per connettere i suoi elementi costitutivi, non per definire 
        intenzioni. Vive del vuoto inteso come valore in assoluto, cos' come delle 
        azioni dei singoli. Di tempi brevi, cos&igrave; come della democratizzazione. 
        Nella citt&agrave; contemporanea la condizione urbana &egrave; esclusiva, 
        senza alternative, ma definita dai modi di abitare pi&ugrave; che dalle 
        configurazioni spaziali che da sempre hanno visto tra le pi&ugrave; importanti 
        per l'immagine della citt&agrave; quelle relative agli spazi pubblici. 
        Modi di abitare che rappresentano la faticosa conquista di un ampliamento 
        di condizioni di vita ottimali sul territorio che fu tra le istanze sociali 
        e politiche dell'urbanistica. Configurazioni spaziali che denunciano l'assenza 
        di contributo richiesto all'architettura per la loro definizione, ma anche 
        di riflessione intellettuale sulle motivazioni delle forme innovate che 
        le contraddistinguono. Nella citt&agrave; antica gli spazi collettivi 
        non erano specializzati cos&igrave; come i progetti della citt&agrave; 
        moderna li resero nell'Ottocento, fossero essi spazi aperti o edificati. 
        Un divieto scoraggiava senza riuscirci l'uso del transetto del Duomo di 
        Milano come percorso urbano al coperto per uomini ed animali e come luogo 
        di sosta e di incontro tra una parte meridionale e una settentrionale 
        della citt&agrave;. La monofunzionalit&agrave; ha svuotato di vita lo 
        spazio pubblico e lo ha reso estraneo alla natura stessa della citt&agrave;: 
        una natura basata infatta sul mix funzionale, sulla sovrapposizione creativa 
        di funzioni nello stesso spazio. La crisi dello spazio collettivo &egrave; 
        dunque presente ben all'interno della storia della citt&agrave; moderna, 
        ma &egrave; solo la rivoluzione dei modi di vita urbani e di uso della 
        citt&agrave; contemporanea che degrada l'importanza di questi luoghi nella 
        prassi e nell'immaginario collettivo. Essi non sono dunque pi&ugrave; 
        usabili come perni di un progetto urbano strutturante il tessuto, mentre 
        ne hanno costituito da sempre la radice.<br>
        Il ritorno ad un uso sovrapposto di spazi non privatizati sembra oggi 
        tornare a caratterizzare in forme del tutto innovate le citt&agrave;. 
        Finora senza un prgetto cosciente. Una societ&agrave; di individui ritrova 
        oggi i propri spazi comuni al di fuori di forme dedicate, via via in luoghi 
        e momenti diversi, con gradi di libert&agrave; pari a quelli utilizzati 
        per ogni altra parte della citt&agrave; che essa si &egrave; costruita 
        senza il nostro aiuto, a aprtire da quella residenziale. Sono i centri 
        commerciali, le stazioni di servizio, i luoghi di interscambio nei sistemi 
        di trasporto, a essere usati e poi abbandonati, mutati, riscoperti come 
        spazi multiuso, come spazi di tutti, come luoghi di incontro, a una scala 
        locale, soprattutto dalle generazioni pi&ugrave; giovani. Sono le strade 
        mercato, i parchi regionali, i percorsi fluviali, ad una scala geografica. 
        Le uniche due scale riconosciute dalla citt&agrave; contemporanea. Ma 
        sono i centri storici delle citt&agrave;, sonno i luoghi urbani storici 
        che costituiscono le uniche risorse che ambedue le scale, a tutte le scale, 
        giocano fortemente un ruolo popolare, un ruolo sentito come pubblico. 
        Pensatea Bergamo Alta alla scala del territorio urbano nato nella pianura 
        che si estende fino a Treviglio; pensate a Firenze o a Venezia alla scala 
        europea; pensate a VIboldone alla scala dell'area metropolitana milanese. 
        Noi possiamo, noi dobbiamo ancora tentare un progetto per la nostra citt&agrave; 
        che, ci piaccio o meno, &egrave; la citt&agrave; contemporanea. lavorando 
        inizialmente alle due scale possibili, quella degli elementi costitutivi 
        e quella geografica, ma indagando quella intermedia per riflettere sulle 
        motivazioni che hanno portato alla affermazione nei fatti della sua inutilit&agrave; 
        a fronte di altri disegni che da sempre hanno visto nella scala tipica 
        degli spazi pubblici la loro qualit&agrave;. E se il nostro progetto si 
        occupa di un centro storico, di un luogo urbano storico, noi abbiamo l'occasione 
        di lavorare automaticamente ad un scala intermedia. E' un affascinante 
        ricerca progettuale quella relativa al tentativo di risignificazione per 
        gli elementi costitutivi la citt&agrave; contemporanea. Tra essi i luoghi 
        religiosi. In ogni caso un progetto per la citt&agrave;, quindi per tutti. 
        Il progetto di uno spazio multifunzionale, non specialistico. Un progetto 
        a pi&ugrave; scale, che contemporaneamente si occupi, ai due estremi, 
        di un sistema e di uno stile per il territorio urbano. Non un progetto 
        per un solo committente, che rappresenti solo un tema, sia esso pure la 
        religiosit&agrave;, e un solo scopo, se stesso. Se l'edificio religioso 
        &egrave; stato da sempre una occasione profondamente urbana, in quanto 
        fabbrica collettiva quanti altri mai, se esso &egrave; vissuto nel tempo 
        e ha rappresentato nelle sue forme la modificazione nei modi d'uso della 
        citt&agrave; da parte di una societ&agrave; in trasformazione, abbiamo 
        bisogno oggi per esso di un progetto che in realt&agrave; usi se stesso 
        per capire la citt&agrave; e la societ&agrave; contemporanee. Dando cos&igrave; 
        nuovamente un apporto fondamentale alla modernit&agrave;, riaffermando 
        cos&igrave; un valore civile alla religiosit&agrave;. Recuperando i valori 
        della Riforma.<br>
        In situazioni a rischio come quelle di Viboldone, appare urgente reinserire 
        il nucleo abitato e confermare gli edifici religiosi, come parte essenziale 
        del territorio di San Giuliano e del sud Milano, ovviamente tenendo conto 
        in senso positivo delle caratteristiche peculiari che esso presenta. A 
        tal fine al progetto sembra necessario arricchire il ventaglio delle attivit&agrave; 
        ospitate, con l'inserimento di funzioni pregiate di interesse collettivo 
        relative ad una scala pi&ugrave; vasta d quella locale, diversificare 
        l'offerta residenziale, concentrare l'attivit&agrave; agricola negli spazi 
        necessari. Il progetto propone l'avviamento di alcuni processi di modificazione: 
        conferma della struttura insediativa del nucleo, creazione di nuovi ambienti 
        e riqualificazione di quelli esistenti, ideazione di un nuovo spazio abitabile 
        nel tessuto del nucleo, potenziamento della funzione residenziale entro 
        una prospettiva di divesrificazione sociale dei residenti quale strumento 
        della urbanit&agrave;, inserimento di funzioni e spazi ad uso collettivo, 
        proposizione di nuovi significati per l'uso dello spazio aperto, avvio 
        di tre differenti processi di modificazione, fra loro integrati nel tempo: 
        restauro, riuso, nuova edificazione. A tali scopi l'assetto del progetto 
        si basa su una sequenza di spazi ad uso collettivo entro un'unica struttura 
        insediativa, agendo soprattutto sulla fascia urbanizzata a sud di via 
        Folli. <br>
        L'organizzazione e la partizione dello spazio aperto sono fondamentalmente 
        strutturate da due edifici che integrano, sottolineandola, la forma del 
        nucleo, definendo nel contempo nuovi ambienti che si aggiungono agli esistenti: 
        un manufatto isolato come nuovo segnale a occidente e un manufatto residenziale 
        ad &quot;L&quot; posto secondo un principio che conferma le regole insediative 
        lungo la via Folli contribuendo, insieme al complesso occidentale della 
        Corte Grande, a definire lo spazio del giardino pubblico. La Corte Grande 
        viene dedicata a nuove funzioni di ricerca, rappresentanza, convegnistiche, 
        museali. Un percorso pedonale congiunge le due estremit&agrave;, dal nuovo 
        segnale a occidente allo storico segnale ad oriente: l'abbazia di Viboldone.</font></p>
